<h3>TM CountDown</h3>

<p>Ten moduł umożliwia tworzenie liczników odliczania. Dostępne są dwa rodzaje takich liczników, numeryczny oraz graficzny.
</p>
<p>Numeryczny:</p>

<figure class="img-polaroid">
    <img src="img/tm_countdown.png" alt=""/>
</figure>

<p>Graficzny:</p>

<figure class="img-polaroid">
    <img src="img/tm_countdown_circle.png" alt=""/>
</figure>

<p>Aby zmienić rodzaj licznika na graficzny, należy wybrać opcję "Graficzny" w polu "Typ". W tym przypadku staną się dostępne dodatkowe ustawienia graficznego czasomierza:</p>
<figure class="img-polaroid">
    <img src="img/tm_countdown_type.png" alt=""/>
</figure>

