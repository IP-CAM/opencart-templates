<h3>TM Twitter Box</h3>

<p>This module allows you to display posts from Twitter. It helps your site visitors to be aware of the latest
    updates and messages in Twitter social network.</p>

<figure class="img-polaroid">
    <img src="img/tm-twitter-view.jpg" alt=""/>
</figure>

<p>When the module is installed, you can configure it on the module configuration page.</p>

<h5>Settings:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>the name of the module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Twitter Page URL</dt>
            <dd>Twitter account link;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Widget ID</dt>
            <dd>you can get the Widget ID
                <a href="https://twitter.com/settings/widgets" target="_blank">here</a>
                ;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Tweet Limit</dt>
            <dd>number of tweets;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Color Scheme</dt>
            <dd>color scheme (light, dark);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>block width (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>block height (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>module status (enabled/disabled).</dd>
        </dl>
    </li>
</ol>

