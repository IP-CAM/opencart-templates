</ol>

<h4>TM Blog Articles</h4>

<p>Данный модуль выводит статьи блога по разным критериям.</p>

<figure class="img-polaroid">
    <img src="img/tm-blog-articles-view.jpg" alt="">
</figure>

<p>После установки модуля, Вы можете настроить его на странице настроек модуля.</p>

<h5>Настройки:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd> статус модуля (включен/отключен).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd> название модуля;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Article limit</dt>
            <dd> количество выводимых статей;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Readmore Button Show</dt>
            <dd> включает/отключает кнопку Read more;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Date Show</dt>
            <dd> включает/отключает показ даты;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Author Show</dt>
            <dd> включает/отключает показ автора статьи;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Comments Show</dt>
            <dd> включает/отключает показ количества комментариев к статье;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Image Show</dt>
            <dd> включает/отключает показ картинки;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Image Width</dt>
            <dd> ширина картинки;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Image Height</dt>
            <dd> высота картинки;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description Symbol Limit</dt>
            <dd> количество символов в описании статьи;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Category</dt>
            <dd> выбор категории статей для вывода. Также, имеется возможность вывести самые популярные статьи либо
                последние добавленные.
            </dd>
        </dl>
    </li>
</ol>

