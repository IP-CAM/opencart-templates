<h3>Multilingual SEO URL’s</h3>

<p align="justify">OpenCart 3.0 supports the multilingual SEO URLs! Whatever is the main language of your store, you can use it for the website links.
</p>
<p>SEO Keyword is now moved to the SEO tab in the category editing field:</p>



<figure class="img-polaroid">
    <img src="img/seourl1.jpg" alt=""/>
</figure>

<p align="justify">
That’s the keyword that will be used for SEO URL addresses.

</p>
<p>Same way, in products the SEO keyword is moved to a separate SEO tab.</p>
<figure class="img-polaroid">
    <img src="img/seourl2.png" alt=""/>
</figure>
<p>You can assign SEO links to products, just like to the categories.
</p>