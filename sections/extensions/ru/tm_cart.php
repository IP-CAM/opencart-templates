<h3>TemplateMonster Cart</h3>
<p> <strong>Cart Module </strong>— это модуль, который позволяет добавить корзину на ваш сайт. </p>
<figure class="img-polaroid"> <img src="img/cart.jpg" alt=""/> </figure>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong>Модули / Расширения</strong> (Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/> </figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/> </figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите модуль<strong> TemplateMonster Cart</strong>, нажмите кнопку &quot;редактировать&quot; и вы перейдете в окно настроек модуля.</p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/cart-1.jpg" alt=""/> </figure>
</p>
<p> Вы увидите следующие окно настроек:</p>
<figure class="img-polaroid"> <img src="img/cart-2.jpg" alt=""/></figure>
<p>Выберите <strong>Disable</strong> в раскрывающемся списки опции <strong>Status</strong>, если хотите выключить модуль.</p>
<p>
  <figure class="img-polaroid"> <img src="img/cart-3.jpg" alt=""/> </figure>
</p>