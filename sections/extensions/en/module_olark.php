<h3><a href="//special.olark.com/cDqdW">Olark Live Chat</a></h3>

<p>Olark Live Chat module for OpenCart.</p>
<figure class="img-polaroid">
    <img src="img/olark-view.jpg" alt=""/>
</figure>

<p align="justify">When the module is installed, you can configure it on the module configuration page. </p>
<p align="justify" class="alert alert-info">Feel free to check more details at the <a href="https://www.olark.com/help/guide" target="_blank" rel="nofollow">official website</a>.</p>

<h4 align="justify">How to manage Olark Live Chat module</h4>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p>Find the <strong>Olark Live Chat </strong>section. Press an <strong>edit</strong> button to configure the existing module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
  <figure class="img-polaroid"> <img src="img/olark-view-2.jpg" alt=""/></figure>
</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid">
  <img src="img/olark-view-3.jpg" alt=""/>
</figure>
<h5>Available settings:</h5>
<ul class="marked-list">
  <li> <strong>Module Name</strong> — the name of the module;</li>
  <li><strong>Your Site ID</strong> — website ID that can be found on the
  <a href='//www.olark.com/install' rel="nofollow" target="_blank">https://www.olark.com/install</a> page after you register and fill in the required fields;</li>
  <li> <strong>Status</strong> — define the module status (enabled/disabled).
    
  </li>
</ul>
<h4 align="justify">How to add a new Olark Live Chat module</h4>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">Find the <strong>Olark Live Chat</strong> section. Press the<strong> Add New </strong>button to create a new module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
  <figure class="img-polaroid"> <img src="img/olark-view-4.jpg" alt=""/></figure>
</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid"> <img src="img/olark-view-3.jpg" alt=""/></figure>
<p>Fill in the required fields:</p>
<ul class="marked-list">
  <li> <strong>Module Name</strong> — enter the name of the module;</li>
  <li><strong>Your Site ID</strong> — enter the website ID that can be found on the
  <a href='//www.olark.com/install' rel="nofollow" target="_blank">https://www.olark.com/install</a> page after you register and fill in the required fields;</li>
  <li> <strong>Status</strong> — define the module status (enabled/disabled).
    
  </li>
</ul>
