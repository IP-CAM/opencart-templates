<h3>Instalación sobre una tienda existente</h3>
<p>Si usted ya tiene una tienda OpenCart existente y sólo quiere cambiar su apariencia con el tema, utilice el siguiente
    método de instalación.</p>
<p class="alert alert-warning">Asegúrese de que su versión de OpenCart respeta las necesidades de plantilla. Todas las
    versiones de OpenCart están disponibles en la
    <a href=" http://www.OpenCart.com/index.php?route=download/download" target="_blank" class="spec_link">página
        oficial de OpenCart</a>
    .
</p>
<ol class="index-list">
    <li>Para instalar una plantilla de OpenCart, debe cargar los archivos al servidor. Usted puede encontrar el archivo
        <strong> &quot;themeXXX(update).zip&quot;</strong> en la carpeta OpenCart, debe de ser descomprimido a su
        ordenador.
    </li>

    <li>Una vez hecho esto, suba los archivos descomprimidos de la carpeta a la raíz de la instalación de OpenCart.
        <br/> Se puede ver un video con instrucciones detalladas
        <a href="/help/how-upload-files-server-2.html" target="_blank">cómo cargar archivos en el servidor</a>
        . <br/>
        <strong>los archivos de la plantilla no afectan en ningún modo al contenido de su tienda;</strong>
    </li>
    <li>Abra su página Web en un navegador y añada /install al final de la URL de su sitio en la barra de direcciones (
        <i>por ejemplo, http://your_domain_name/OpenCart/install</i>
        )
    </li>

    <li>Haga clic en el botón <strong>Continuar</strong>. Esto puede tomar algo de tiempo. </li>

    <li>Inicie sesión en su panel de administración, vaya a <strong>Extensions ->
            Extension
            Installer </strong> y cargar los ficheros del archivo
        <strong>themeXXX(extensions)</strong>. Cuando todos los archivos se hayan subido, vaya a <strong>Extensions ->
            Modifications</strong> y haga clic en el boton
        <strong>refresh</strong> en la parte superior derecha de la pantalla; </li>

    <li>El siguiente paso que hay que hacer es ir a
        <strong>System->Settings</strong>, vaya a la pestaña Store y seleccione la plantilla
        <strong>themeXXX</strong> en el campo Template. </li>
</ol>
