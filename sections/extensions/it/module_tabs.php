<h3>TemplateMonster Module Tabs</h3>

<p>Questo modulo consente di visualizzare 4 componenti principali nelle schede (Bestsellers, Featured, Specials, Latest)
    in tabs.</p>
<figure class="img-polaroid">
    <img src="img/tm-moduletabs-view.jpg" alt=""/>
</figure>

<p>Dopo aver installato il modulo, sarà possibile configurarlo nell'apposita pagina di configurazione.</p>

<h5>Impostazioni:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>il nome del modulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Specials</dt>
            <dd>per abilitare/disabilitare i prodotti speciali;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Bestsellers</dt>
            <dd>per abilitare/disabilitare i prodotti più venduti;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Latest</dt>
            <dd>per abilitare/disabilitare gli ultimi prodotti;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Featured</dt>
            <dd>per abilitare/disabilitare i prodotti in evidenza;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Products</dt>
            <dd>per selezionare i prodotti delle scheda Featured;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd>per impostare il limite di prodotti da visualizzare;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>l'ampiezza dell'immagine del prodotto;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>l'altezza dell'immagine del prodotto;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>lo stato del modulo (abilitato/disabilitato).</dd>
        </dl>
    </li>
</ol>
