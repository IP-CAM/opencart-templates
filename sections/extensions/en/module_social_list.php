<h3>TemplateMonster Social List</h3>

<p>This module allows you to display the list of the popular social network links. </p>
<figure class="img-polaroid">
    <img src="img/social.jpg" alt=""/>
</figure>

<p align="justify" class="alert alert-danger"> In case, you install "update" theme version, template sample content (the list of the social
    network links) will not be available. In order to configure the module properly, make sure to fill in the required
    information.</p>

<p align="justify">When the module is installed, you can configure it on the module configuration page. If you leave the field
    empty, the element will not show up in the list. </p>
<h4>How to manage TemplateMonster Social List module</h4>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p>Find the<strong> TemplateMonster Social List</strong> section. Press <strong>edit</strong> button to configure the existing module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
  <figure class="img-polaroid"> <img src="img/instagram-1.jpg" alt=""/></figure>
</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid"> <img src="img/instagram-14.jpg" alt=""/></figure>
<h5>Available settings:</h5>
<ol class="marked-list">
  <li><strong>Module Name</strong> — the name of the module;</li>
  <li><strong> Title</strong> — enter the module title. The one  displayed on the frontend;</li>
  <li>
    <div align="justify"><strong>Description </strong>— module description, the main text which will be displayed within the module;</div>
  </li>
  <li><strong>Name</strong> — enter the name the Social Icon.</li>
  <li> <strong>Link</strong> — specify the page link;</li>
  <li><strong>Css</strong> —  	CSS class name for the Icon to use for styling of the one;</li>
  <li><strong>Status </strong>— module status (enabled/disabled). </li>
</ol>
