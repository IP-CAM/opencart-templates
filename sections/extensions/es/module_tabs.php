<h3>TM Module Tabs</h3>

<p>Este módulo mostrará cuatro módulos principales (Bestsellers, Featured, Specials, Latest) en forma de pestañas .</p>
<figure class="img-polaroid">
    <img src="img/tm-moduletabs-view.jpg" alt=""/>
</figure>

<p>Después de instalar el módulo, se puede configurar en la página de configuración del módulo .</p>

<h5>Parámetros:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>el nombre del módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Specials</dt>
            <dd>activar/desactivar el módulo Specials;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Bestsellers</dt>
            <dd>activar/desactivar el módulo Bestsellers;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Latest</dt>
            <dd>activar/desactivar el módulo Latest;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Featured</dt>
            <dd>activar/desactivar el módulo Featured;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Products</dt>
            <dd>elegir la lista de productos para la ficha Featured;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd>establecer un límite de productos mostrados;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>la anchura de la imagen del producto;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>la altura de la imagen del producto;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>el estado del módulo (activado/desactivado).</dd>
        </dl>
    </li>
</ol>

