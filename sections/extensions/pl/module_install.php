<h3>Instalacja modułu</h3>

<p>Proces instalacj modułu składa się z kilku etapów. Przejdź do swojego panelu administratora (dodając słowo /admin w
    pasku adresu przeglądarki - www.yoursite.com/admin). Przejdź do <strong>Extensions (Rozszerzenia) </strong> i w
    rozwijanym menu wybierz <strong>Modules (Moduły)</strong> (Img.5). Zobaczysz listę zainstalowanych i gotowych do
    instalacji modułów. Jeśli zobaczysz przycisk <strong>Unisntall (Odinstaluj)</strong> koło jakiegokolwiek modułu, to
    znaczy, że ten moduł został zainstalowany i można w nim zmieniać ustawienia, nacisnąwszy <strong>Edit
        (Edytuj)</strong>. Jeśli naciśniesz przycisk <strong>Install (Instaluj)</strong>, zacznie się proces instalacji
    modułu. Aby usunąć moduł, trzeba nacisnąć przycisk <strong>Uninstall (Odinstaluj)</strong>. </p>

<p class="alert alert-warning">Należy pamiętać, że po odinstalowaniu modułu wszystkie dodane wcześniej ustawienia
    zostaną usunięte i nie można będzie ich przywrócić.</p>

<figure class="img-polaroid">
    <img src="img/install_1.jpg" alt=""/>
</figure>

<p>Po zakończeniu instalacji trzeba skonfigurować moduł. Ustawienia mogą się różnić w zależności od specyfiki modułów,
    ale pole <strong>Statusu</strong> jest takie same dla wszystkich modułów - ono pozwala włączać/wyłączać moduł.</p>

<figure class="img-polaroid">
    <img src="img/edit-module_1.jpg" alt=""/>
</figure>

<p>Aby dodać moduł na określoną stronę, trzeba przejść do listy stron. To można wykonać w dwa sposoby:</p>

<ol class="index-list">
    <li>
        <p>W bocznym menu przejdź do <strong>System -&gt; Design (Projekt) -&gt; Layout (Układ)</strong>;</p>

        <figure class="img-polaroid">
            <img src="img/page-selecting-way.jpg" alt=""/>
        </figure>
    </li>
    <li>
        <p>W sekcji modułu kliknij link <strong>tutaj</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/page-selecting-way-2.jpg" alt=""/>
        </figure>
    </li>
</ol>

<p>W liście stron wybierz stronę, na której chcesz umieścić moduł, i kliknij <strong>Edytuj</strong></p>

<figure class="img-polaroid">
    <img src="img/list-of-pages.jpg" alt=""/>
</figure>

<p>Możesz zobaczyć listę modułów oraz ich pozycji, które są już dodane na stronę </p>

<figure class="img-polaroid">
    <img src="img/layout.jpg" alt=""/>
</figure>

<p>Dostępne są następujące pozycje (odpowiednio do kolejności wyświetlania):</p>

<ol class="index-list">
    <li>Header Top (po sekcji Header);</li>

    <li>Column-left (lewy panel boczny);</li>

    <li>Column-right (prawy panel boczny);</li>

    <li>Content-top;</li>

    <li>Content-bottom;</li>

    <li>Footer.</li>
</ol>
<p>Pole <strong>Sort Order (Typ numeracji)</strong> pokazuje kolejność wyświetlania modułów przyporządkowanych do tych
    samych pozycji. Aby dodać moduł na stronę, kliknij <strong>Add Module (Dodaj moduł)</strong>. Wybierz potrzebny
    moduł oraz jego pozyjcę i kliknij <strong>Save (Zapisz)</strong>. Instalacja modułu została zakończona. </p>