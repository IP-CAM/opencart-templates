<h4>Não consegue carregar Google Fonts: </h4>
<ol class="index-list">
    <li>Abra o arquivo <strong>catalog\view\theme\themeXXX\stylesheet\stylesheet.css</strong>.</li>

    <li><p>Ache a linha: </p>
        <code>//fonts.googleapis.com/css?family=[---your_web_font_name---]</code>
    </li>
    <li><p>Mude para: </p>
        <code>//fonts.useso.com/css?family=[---your_web_font_name---]</code>
    </li>
</ol>

<h4>Validação</h4>

<p class="alert alert-info">Ambas a marcação HTML e estilos CSS usados nos nossos templates estão semânticamente
    corretos e válidos. No entanto alguns erros W3C ainda podem ocorrer. Criar código 100% válido para W3C elimina o uso
    de tecnologias modernas para websites como funcionalidades do CSS3 e marcação HTML5. Nosso objetivo é entregar uma
    experiência de usuário rica com templates de alta qualidade e, para isso, às vezes temos que quebrar algumas
    regras.</p>