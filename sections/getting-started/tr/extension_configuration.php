<h3>Var olan mağazanın üstüne kurulum</h3>

<p>Eğer hali hazırda bir OpenCart mağazanız varsa ve sadece tema desteğiyle görüntüsünü değiştirmek istiyorsanız, bir
    sonraki metodu kullanın.</p>
<p class="alert alert-warning">Elinizdeki OpenCart versiyonunun şablon gereksinimlerini karşıladığına emin olunuz. Tüm
    OpenCart versiyonlarına
    <a href=" http://www.OpenCart.com/index.php?route=download/download" target="_blank" class="spec_link">resmi
        OpenCart websitesi</a>
    nden ulaşabilirsiniz.
</p><br/>
<ol class="index-list">
    <li>OpenCart şablonunu kurmak için, sunucuya dosyaları yüklemeniz gerekir.
        <strong> &quot;themeXXX(update).zip&quot;</strong> arşivini OpenCart klasöründe bulabilirsiniz. Bilgisayarınızda
        zipli şekilde olmalı.
    </li>

    <li>Ardından, bu zipli dosyaları OpenCart'ı kuracağınız ana dizine yükleyin. <br/> Detaylı anlatımını
        <a href="/help/how-upload-files-server-2.html" target="_blank">sunucuya dosya yüklemek</a>
        videosunda bulabilirsiniz. <br/> <strong>şablon dosyaları mağaza içeriğinizi etkilemez;</strong>
    </li>
    <li>Herhangi bir tarayıcıdan websitenizi açın ve adresin sonuna /install ekleyin. (
        <i>örnek: http://site_adresiniz/OpenCart/install</i>
        )
    </li>

    <li>Daha sonra <strong>Continue</strong> (Devam et) düğmesine basın. Bir süre bekleyebilirsiniz.</li>

    <li>Yönetici paneline girin ve <strong>Extensions ->
            Extension
            Installer </strong> (Bileşenler -> Yeni Yükle) kısmından
        <strong>themeXXX(extensions)</strong> arşiv dosyalarını yükleyin. Yükleme işlemi bittikten sonra <strong>Extensions
            ->
            Modifications</strong> (Bileşenler -> Düzenlemeler) bölümüne girip, ekranın sağ üst kısmındaki
        <strong>refresh</strong> (Yenile)ye basın.; </li>

    <li>Bir sonraki adımda
        <strong>System->Settings</strong> (Sistem->Ayarlar) a girin, Mağaza sekmesindeki Şablon alanından
        <strong>themeXXX</strong> i seçin.</li>
</ol>

<p class="alert alert-info">Detaylı bilgiyi:
    <a target="_blank"
       href="/help/OpenCart-2-x-how-to-install-a-template-over-the-existing-store-using-update-package.html">OpenCart.
        Güncelleme paketi kullanarak var olan mağazanın üstüne şablon yüklemek</a>
    konusunda bulabilirsiniz.
</p>