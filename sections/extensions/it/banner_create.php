<h3>Creazione di banner</h3>

<h4>Modifica TM Description per banner</h4>

<p>La modifica consente di aggiungere descrizioni ai banner. Per disabilitare o rimuovere la modifica, vai su <strong>Extensions
        -> Modifications</strong>, trova l'estensione "TM Description for banners" e fai clic su
    <strong>Disable</strong> o <strong>Delete</strong>.</p>

<p>Alcuni moduli includono un campo per selezionare un banner. Vai sulla sezione <strong>System -> Design ->
        Banner</strong></p>

<p>La pagina permette di visualizzare l'elenco dei banner creati. Per creare un nuovo banner, fare clic su <strong>Add
        New</strong>. Per modificare un banner esistente, fare clic su <strong>Edit</strong>.</p>

<figure class="img-polaroid">
    <img src="img/banner-list.jpg" alt=""/>
</figure>

<p>Specificare nome e stato del banner e fare clic su <strong>Add Banner</strong>. Vedrai una tabella in cui dovrai
    inserire i seguenti valori: </p>

<ol class="index-list">
    <li>Nome banner</li>
    <li>Description - il testo che appare sul banner (supporta l'html-markup);</li>
    <li>Link - specifica un link relativo all'URL del sito (ad esempio, index.php?route=product/product&product_id=47),
        questo campo non è obbligatorio;
    </li>
    <li>Image;</li>
    <li>Banner order - possono esserci più banner, il campo è obbligatorio.</li>
</ol>

<p>Per eliminare il banner, fare clic su <strong>Remove</strong>. Dopo aver creato il banner sarà possibile usarlo per
    diversi moduli.</p>

