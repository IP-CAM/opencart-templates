<h3>TM Newsletter</h3>

<p>Este módulo añade el bloque con un formulario de suscripción a boletines de noticias.</p>
<figure class="img-polaroid">
    <img src="img/tm-newsletter-view.jpg" alt=""/>
</figure>

<p>Después de instalar el módulo, se puede configurar en la página de configuración del módulo.</p>

<h5>Parámetros:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>el nombre del módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>el texto que aparece justo antes de completar el formulario de suscripción;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>el estado del módulo (activado/desactivado).</dd>
        </dl>
    </li>
</ol>

