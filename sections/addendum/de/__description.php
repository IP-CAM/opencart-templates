<h4>Sie können Google Fonts nicht hochladen:</h4>
<ol class="index-list">
    <li>Öffnen Sie <strong>catalog\view\theme\themeXXX\stylesheet\stylesheet.css</strong> Datei.</li>

    <li><p>Finden Sie die folgende Zeile: </p>
        <code>//fonts.googleapis.com/css?family=[---your_web_font_name---]</code>
    </li>
    <li><p>Ersetzen Sie sie durch: </p>
        <code>//fonts.useso.com/css?family=[---your_web_font_name---]</code>
    </li>
</ol>

<h4>Bestätigung</h4>

<p class="alert alert-info">In unseren Vorlage befolgen wir den semantischen Regeln beim Schreiben des Codes von
    HTML-Markup und CSS. Aber es können einige Fehler sowieso bei der W3C-Validierung auftreten. Das Erstellen eines
    Codes, der hundertprozentig ist, beschränkt die Verwendung von modernen Technologien der Webseitenerstellung wie:
    Spezifikation CSS3 und HTML5-Markup (entsprechend den Standards des W3C). Das Ziel unseres Unternehmens besteht
    darin, dass wir umfassende Benutzererfahrung und hochwertige Vorlagen zur Verfügung stellen und manchmal müssen wir
    einige Regeln brechen.</p>