<h4>Software de edición</h4>

<p>Para que sea cómodo trabajar con su plantilla OpenCart, se recomienda descargar todas las aplicaciones necesarias.
    Usted puede ver la lista de software necesarios en la página de Live Demo de la plantilla. Los requisitos pueden
    variar dependiendo de la plantilla, le diremos aquí lo que se requiere en general:</p>

<ol class="index-list">
    <li>Es posible que necesite la aplicación de Adobe Photoshop. Se utiliza para editar los .PSD archivos de origen, lo
        cual es necesario si desea editar las imágenes y los gráficos de la plantilla. </li>

    <li>Para editar los archivos de código fuente de la plantilla, necesitará editor de código como Adobe Dreamweaver,
        Notepad++, Sublime Text, etc.</li>

    <li>Para cargar los archivos en el servidor de alojamiento, es posible que usted necesite el Gestor FTP tal como
        Total Commander, FileZilla, CuteFTP, etc. </li>

    <li>Por favor, consulte la siguiente guía para descomprimir el archivo, utilizando
        <a href="/help/extract-archived-files-windows-os.html" target="_blank"> WinZip(PC)</a>
        <a href="/help/extract-archived-files-mac-os.html" target="_blank"> StuffitExpander(MAC)</a>
        .
    </li>
</ol><h4>Hosting</h4>

<p>Tal como OpenCart es una aplicación basada en PHP/MySQL, es necesario preparar un entorno de alojamiento. </p>

<p>Si usted tiene un hosting en línea, asegúrese de que cumple con los
    <a href="http://docs.opencart.com/requirements/" target="_blank"> Requisitos del sistema de
        OpenCart</a>
    y está listo para ser utilizado en las páginas web de OpenCart.
</p>

<p>De lo contrario, puede ejecutar OpenCart localmente en su ordenador utilizando un servidor local. Para crear un
    hosting servidor local, utilice aplicaciones como localhost
    <b>WAMP</b>, <b>AppServ</b>,
    <b>MAMP</b>, etc. Cualquiera de ellos puede ser instalado fácilmente como cualquier otro programa y se utiliza para
    ejecutar OpenCart.
</p>

