<h3>TM Social List</h3>

<p>Das jeweilige Modul zeigt die Liste von Links zu Seiten der beliebten sozialen Netzwerken. </p>

<p class="alert alert-danger"> Wenn Sie die "update" Version vom Theme installieren, werden die Demodaten der Vorlage (die
    Liste von Links zu sozialen Netzwerken) nicht verfügbar sein. Füllen Sie die jeweiligen (erforderlichen) Felder aus,
    damit dieses Modul korrekt fuktionieren kann.</p>

<p>Nachdem Sie das Modul installiert haben, können Sie es auf der Konfigurationsseite einstellen. Wenn Sie
    dieses Feld leer lassen, wird das Listenelement nicht angezeigt.</p>

