<h3>TM Product Slideshow</h3>

<p>Данный модуль выводит выбранные продукты в виде карусели. </p>
<figure class="img-polaroid">
    <img src="img/tm-product-slideshow-view.jpg" alt=""/>
</figure>

<p>После установки модуля, Вы можете настроить его на странице настроек модуля.</p>

<h5>Настройки:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>название модуля;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Products</dt>
            <dd>выбор списка продуктов;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd>ограничение на количество отображаемых продуктов;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>ширина изображения продукта;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>высота изображения продукта;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>статус модуля (включен/отключен).</dd>
        </dl>
    </li>
</ol>

