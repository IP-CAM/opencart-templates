<h3>TM Single Category Products</h3>

<p>Este módulo permite exibir 4 módulos principais ("Bestsellers", "Featured", "Specials" e "Latest") com os produtos de determinada categoria.</p>

<figure class="img-polaroid">
    <img src="img/tm-single-category-product-view.jpg" alt="">
</figure>

<p>Após instalado, o módulo pode ser configurado na página de configurações do módulo.</p>

<h5>Configurações:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd> o nome do módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Category</dt>
            <dd>permite definir que categoria existente será usada;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Tabs</dt>
            <dd> ativa/desativa a visão em abas. Se estiver desativada, existe uma opção para escolher o tipo de visualização;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Product Type</dt>
            <dd>permite escolher o tipo de módulo. Disponível quando a opção "Tabs" está desativada.</dd>
        </dl>

        O seguintes valore estão disponíveis:

        <ul class="marked-list">
            <li>Specials;</li>
            <li>Bestsellers;</li>
            <li>Featured;</li>
            <li>Latest.</li>
        </ul>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Specials</dt>
            <dd> ativa/desativa o módulo Specials;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Bestsellers</dt>
            <dd> ativa/desativa o módulo Bestsellers;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Featured</dt>
            <dd> ativa/desativa o módulo Featured;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Latest</dt>
            <dd> ativa/desativa o módulo Latest;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Featured Products</dt>
            <dd> permite definir que produtos vão ficar em destaque no módulo "Featured". Disponíveis quando o módulo "Featured" está ativado ou o tipo de produto está definido como "Featured";
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd> a quantidade de produtos para exibir;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd> a largura da imagem do produto;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd> a altura da imagem do produto;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd> a situação do módulo (ativado/desativado).</dd>
        </dl>
    </li>
</ol>

