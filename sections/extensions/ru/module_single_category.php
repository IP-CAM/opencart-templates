<h3>TM Single Category Products</h3>

<p>Данный модуль выводит 4 основных модуля (Bestsellers, Featured, Specials, Latest) продуктов определенной категории в
    виде вкладок.</p>

<figure class="img-polaroid">
    <img src="img/tm-single-category-product-view.jpg" alt="">
</figure>

<p>После установки модуля Вы можете настроить его на странице настроек модуля.</p>

<h5>Настройки:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd> название модуля;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Category</dt>
            <dd> выбор существующей категории продуктов;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Tabs</dt>
            <dd> включает/отключает вывод в виде вкладок. При отключенном варианте предлагается выбрать тип выводимого
                модуля;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Product Type</dt>
            <dd> выбор типа выводимого модуля. Поле доступно при отключенной опции Tabs.</dd>
        </dl>

        Доступны следующие значения:

        <ul class="marked-list">
            <li>Specials;</li>
            <li>Bestsellers;</li>
            <li>Featured;</li>
            <li>Latest.</li>
        </ul>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Specials</dt>
            <dd> включает/отключает отображение модуля Specials;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Bestsellers</dt>
            <dd> включает/отключает отображение модуля Bestsellers;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Featured</dt>
            <dd> включает/отключает отображение модуля Featured;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Latest</dt>
            <dd> включает/отключает отображение модуля Latest;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Featured Products</dt>
            <dd> выбор списка продуктов для модуля Featured. Поле доступно при включенной опции Featured или, если опция Product type равна Featured;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd> устанавливает ограничение на количество отображаемых продуктов;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd> ширина изображения продукта;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd> высота изображения продукта;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd> статус модуля (включен/отключен).</dd>
        </dl>
    </li>
</ol>

