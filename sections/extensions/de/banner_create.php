<h3>Erstellen von Bannern</h3>

<h4>Modifikation TM Description for banners</h4>

<p>Die Modifikation wird gebraucht, um die Beschreibungen von Bannern hinzuzufügen.
    Um die Modifikation zu entfernen oder zu deaktivieren, öffnen Sie <strong>Erweiterungen
        -> Modifikation (Extensions -> Modifications)</strong> finden Sie in der Liste
    "TM Description fo banners" Erweiterung und drücken Sie auf den Button <strong>Deaktivieren
        (Disable)</strong> oder <strong>Entfernen (Delete)</strong>. </p>

<p>Die Einstellungen einiger Module haben das Feld zur Auswahl vom Banner, das gebraucht
    wird. Finden Sie den Bereich <strong> System -> Design -> Banner (System -> Design
        -> Banner)</strong></p>

<p>Auf der jeweiligen Seite ist die Liste von erstellten Bannern angezeigt. Um ein neues
    Banner zu erstellen, drücken Sie auf den Button <strong>Neu hinzufügen (Add
        New)</strong>, um das aktuelle Banner zu bearbeiten, drücken Sie auf den Button
    <strong>Bearbeiten (Edit)</strong>.</p>

<figure class="img-polaroid">
    <img src="img/banner-list.jpg" alt=""/>
</figure>

<p>Geben Sie jetzt Banner Name, Status an und drücken Sie auf den Button <strong>Banner
        hinzufügen (Add Banner)</strong>. In der Tabelle, die Sie gerade sehen, legen Sie
    das Folgende fest: </p>

<ol class="index-list">
    <li>Bannername;</li>
    <li>Beschreibung - der Text, der auf dem Banner angezeigt wird (html-Markup wird
        unterstützt);
    </li>
    <li>Link - legen Sie den relativen Link fest. Es ist der Link relativ zur
        Website-Adresse (zum Beispiel,
        index.php?route=product/product&amp;product_id=47) die jeweilige Option ist
        nicht obligatorisch;
    </li>
    <li>Bild;</li>
    <li>Reihenfolge des Banners. Sie können mehr als ein Banner haben, deswegen muss
        dieses Feld unbedingt ausgefüllt werden.
    </li>
</ol>

<p>Um das Banner zu entfernen, drücken Sie auf den Button <strong>Entfernen
        (Remove)</strong>. Nachdem Sie das Banner erstellt haben, können Sie es für andere
    Module nutzen.</p>

