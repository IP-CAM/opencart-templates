<h3>TM ABC Customers</h3>

<p>Ce module sert à la mise en place des réductions pour les différents groupes de clients. L'ordre de la quantité est calculé, lorsque la commande est placée, avec le pourcentage de réduction fixé pour un groupe spécifique de clients.</p>

<h4>Paramètres des groupes de clients:</h4>

<p>Il est nécessaire de créer et de configurer des groupes de clients.</p>

