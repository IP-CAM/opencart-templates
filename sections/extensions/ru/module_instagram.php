<h5>Доступные параметры настроек модуля:</h5>
<ul class="marked-list">
<li><strong>Module Name</strong> — наименование модуля;</li>
  <li>
    <div align="justify"><strong>Select Get</strong> — выбор, каким образом получать изображения. Доступны 2 значения (user,tagged), что соответственно
      означает, получать изображения либо конкретного пользователя, либо по хештегу; </div>
  </li>
  <li>
    <div align="justify"><strong>Entry TagName</strong> — тег, по которому будут выводиться изображения (используется, если опция "Select get" имеет значение
      tagged); </div>
  </li>
