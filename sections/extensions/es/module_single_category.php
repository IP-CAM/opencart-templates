<h3>TemplateMonster Single Category Products</h3>

<p>Este módulo le permite mostrar 4 módulos principales (Los Más vendidos, Destacados, Especiales, Los Últimos) con los productos de cierta categoría en vista de la ficha.</p>

<figure class="img-polaroid">
    <img src="img/tm-single-category-product-view.jpg" alt="">
</figure>

<p>Una vez instalado el módulo se puede configurar en la página de ajustes del módulo.</p>

<h5>Ajustes:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Nombre del Módulo</dt>
            <dd> el nombre del módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Categoría</dt>
            <dd>permite seleccionar la categoría de producto existente;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Pestañas</dt>
            <dd> activa/desactiva la vista de la pestaña. Si está desactivada, hay una opción para seleccionar el tipo de vista del módulo;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Tipo de producto</dt>
            <dd>permite seleccionar el tipo del módulo. Disponible cuando la opción Tabs está desactivada.</dd>
        </dl>

        Los siguientes valores están disponibles:

        <ul class="marked-list">
            <li>Especiales;</li>
            <li>Los más vendidos;</li>
            <li>Destacados;</li>
            <li>Recientes.</li>
        </ul>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Especiales</dt>
            <dd> activa/desactiva el módulo Especiales;</dd>
        </dl
    </li>
    <li>
        <dl class="inline-term">
            <dt>Los más vendidos</dt>
            <dd> activa/desactiva el módulo Más vendidos;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Destacados</dt>
            <dd> activa/desactiva el módulo Destacados;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Recientes</dt>
            <dd> activa/desactiva el módulo Recientes;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Productos Destacados</dt>
            <dd> permite seleccionar la lista de productos para el módulo Destacados. Disponible cuando el módulo Destacados está activado o la opción Tipo de producto está establecida en Destacados;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Límite</dt>
            <dd> el número de productos para mostrar;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Anchura</dt>
            <dd> ancho de la imagen del producto;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Altura</dt>
            <dd> altura de la imagen del producto;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Estado</dt>
            <dd> estado del módulo (activado/desactivado).</dd>
        </dl>
    </li>
</ol>

