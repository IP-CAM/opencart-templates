<h5>Find your Twitter widget ID</h5>
<p align="justify">If a Twitter widget you are attempting to enable asks for a Twitter widget ID, you will need to create a new widget at your <a href="https://twitter.com/settings/widgets" onclick="__gaTracker('send', 'event', 'outbound-article', 'https://twitter.com/settings/widgets', 'Twitter widget settings');">Twitter widget settings</a> page.</p>
<p align="justify">To access the Twitter widget settings, log into the Twitter account that you want to use with your widget. Then click the gear in the top right-hand corner and click <strong>Settings</strong>. From the settings' sidebar, pick the <strong>Widgets</strong> tab at the very bottom.</p>
<p align="justify">Locate the <strong>Create new</strong> button on this page and press it to create a new widget. From here, you will be offered configuration options and a preview of the widget. You can always change the widget's configuration options later.</p>
<p align="justify">After you've created the widget, the <strong>Create new</strong> button will be replaced by a <strong>Save changes</strong> button. Now that your widget is created, you can grab the widget ID from the URL. Look at your browser's address bar. It should have a URL that looks something like this:</p>
<blockquote><p>https://twitter.com/settings/widgets/<strong>512253083833733120</strong>/edit?focus_textarea=1&amp;notice=WIDGET_CREATED</p> </blockquote>
<p align="justify">Your widget ID is the 18 digit number. In this example it's <strong>512253083833733120</strong>. Remember to keep this number private, so your widget will not be used by others without your permission.</p>
<p>Copy your ID and paste it to the text field that asks for your widget ID. After saving the settings, your new Twitter widget should be functional.</p>
