<h3>TM Megamenu</h3>

<p>Este módulo le permite crear menús complejos. El módulo ofrece la posibilidad de mostrar un solo elemento (sin submenús), un elemento con un submenú y un elemento con el bloque de submenús de varias filas y columnas múltiples. Este módulo puede ser representado en varias posiciones, pero con el mismo contenido.
</p>

<h5>Información general</h5>

<ol class="index-list">
    <li>
        <p>Activación del menú</p>

        <p>Para permitir activar megamenu usted debe navegar a la página <strong>Extensiones -> Módulos -> TM Megamenu (Extensions -> Modules -> TM Megamenu)</strong> , pulse el botón <strong>(Edit) Editar</strong> junto al elemento correspondiente y seleccione la opción <strong>Activar (Enabled)</strong> en la nueva ventana.
        </p>
    </li>
</ol>

