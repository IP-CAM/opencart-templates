<h3>TM Facebook Box</h3>

<p>Il modulo consente di inserire una casella per il Mi piace di Facebook che mostra le persone a cui piace il tuo
    sito.</p>

<figure class="img-polaroid">
    <img src="img/tm-facebook-view.jpg" alt=""/>
</figure>

<p>Dopo aver installato il modulo, sarà possibile configurarlo nell'apposita pagina di delle impostazioni.</p>

<h5>Impostazioni:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>il nome del modulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Facebook Page URL</dt>
            <dd>il link della pagina Facebook;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Color Scheme</dt>
            <dd>lo schema colore (luminoso, scuro);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Faces</dt>
            <dd>mostra i volti degli utenti (sì, no);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Stream</dt>
            <dd>mostra post recenti (sì, no);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Header</dt>
            <dd>mostra i titoli dei blocchi (sì, no);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Header</dt>
            <dd>mostra i bordi dei blocchi (sì, no);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>ampiezza blocco (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>altezza blocco (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>lo stato del modulo (abilitato/disabilitato).</dd>
        </dl>
    </li>
</ol>

