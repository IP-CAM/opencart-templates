<h3>TM Instagram</h3>

<p>Este módulo muestra las fotos de la red social Instagram incluyendo el número de "likes" en la lista .</p>
<figure class="img-polaroid">
    <img src="img/tm-instagram-view.jpg" alt=""/>
</figure>

<p>Después de instalar el módulo, se puede configurar en la página de configuración del módulo.</p>

<h5>Parámetros:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>el nombre del módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Select Get</dt>
            <dd>cómo obtener las imágenes. Hay 2 valores disponibles (user and tagged) se puede obtener las imágenes del
                usuario o utilizar el hashtag;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry TagName</dt>
            <dd>la etiqueta que se utiliza para mostrar las imágenes (se utiliza si se ha seleccionado previamente
                "tagged" en el campo "Select get");
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry ClientID</dt>
            <dd>su API ID de instagram.com (se utiliza si se ha seleccionado previamente "tagged" en el campo "Select
                get");
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry UserId</dt>
            <dd>ID de usuario (se utiliza si se ha seleccionado previamente "user" en el campo "Select get");</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry accessToken</dt>
            <dd>una ficha para mostrar una imagen personalizada (se utiliza si se ha seleccionado previamente "user" en
                el campo "Select get");
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry limit</dt>
            <dd>limita el número de entradas de muestra;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>el estado del módulo (activado/desactivado).</dd>
        </dl>
    </li>
</ol>

