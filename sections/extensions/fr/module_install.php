<h3>Installation du module</h3>
<p>Le processus d'installation de module comprend plusieurs étapes. Connectez-vous à votre panneau d'administration (en
    ajoutant /admin dans la barre d'adresse de votre navigateur - www.votresite.com/admin). Allez à
    <strong>Extensions </strong> et dans le menu déroulant, sélectionnez
    <strong>Modules</strong> Vous verrez la liste des modules installés et prêts-à-être installés. Si vous
    voyez le bouton
    <strong>Uninstall</strong> à côté du module, cela signifie que le module est déjà installé et vous pouvez modifier
    les paramètres du module en cliquant
    <strong>Edit</strong>. En cliquant sur le bouton
    <strong>Install</strong> , vous lancerez le processus d'installation de module. Pour enlever le module, vous devez
    cliquer sur le bouton
    <strong>Uninstall</strong> , le module sera désinstallé. </p>
<p class="alert alert-warning">Veuillez noter que la désinstallation du module signifie que tous les paramètres ajoutés
    précédemment seront perdus et ne pourront pas être récupérés.</p>
<figure class="img-polaroid">
    <img src="img/install_1.jpg" alt=""/>
</figure>


<p>Une fois l'installation terminée, vous devez configurer le module. Les paramètres peuvent varier en fonction du
    module spécifique, mais le champ
    <strong>Status</strong> est le même pour tous - le champ permet d'activer ou de désactiver le module.</p>
<figure class="img-polaroid">
    <img src="img/edit-module_1.jpg" alt=""/>
</figure>


<p>Pour ajouter le module à une certaine page, vous devez aller à la liste des pages. Cela peut être fait de 2 façons:
</p>
<ol class="index-list">
    <li>
        <p>Dans le menu latéral allez à <strong>System -> Design -> Layout</strong>;</p>

        <figure class="img-polaroid">
            <img src="img/page-selecting-way.jpg" alt=""/>
        </figure>
    </li>

    <li>
        <p>Dans la section du module cliquez sur le lien <strong>here</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/page-selecting-way-2.jpg" alt=""/>
        </figure>
    </li>
</ol>


<p>Dans la liste des pages, sélectionnez la page sur laquelle vous souhaitez placer le module et cliquez sur
    <strong>Edit</strong></p>
<figure class="img-polaroid">
    <img src="img/list-of-pages.jpg" alt=""/>
</figure>


<p>Vous pouvez voir une liste des modules et les positions de modules qui sont déjà ajoutés à la page </p>


<p>Les positions suivantes sont disponibles (selon l'ordre d'affichage):</p>
<ol class="index-list">
    <li>Header Top (après la section de Header);</li>

    <li>Column-left (panneau latéral gauche);</li>

    <li>Column-right (panneau latéral droit);</li>

    <li>Content-top;</li>

    <li>Content-bottom;</li>

    <li>Footer.</li>
</ol>
<p>Le champ
    <strong>Sort Order</strong> montre l'ordre d'affichage des modules assignés à la même position. Pour ajouter un
    module à la page, cliquez
    <strong>Add Module</strong>. Sélectionnez le module nécessaire, la position du module et cliquez
    <strong>Save</strong>. L'installation du module est terminée. </p>

