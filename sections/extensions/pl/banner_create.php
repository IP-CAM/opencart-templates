<h3>Tworzenie banerów</h3>

<h4>Opis TM do modyfikacji banerów</h4>
<p>Modyfikacja pozwala dodawać opis banerów. Żeby wyłączyć modyfikację albo usunąć jej, przejdź do <strong>Extensions
        (Rozszerzenia) -&gt; Modifications (Modyfikacje)</strong>, Znajdź rozszerzenie "Opis TM do banerów" i naciśnij
    <strong>Disable (Wyłącz)</strong> albo <strong>Delete (Usuń)</strong>.</p>
<p>Niektóre moduły mają pole do wybierania banera. Przejdź do sekcji <strong>System -&gt; Design -&gt; Banner</strong>.
</p>

<p>Strona wyświetla listę utworzonych banerów. Żeby utworzyć nowy baner, kliknij <strong>Add New (Dodaj nowy)</strong>.
    Żeby edytować istniejący baner, kliknij <strong>Edit (Edytuj)</strong>.</p>

<figure class="img-polaroid">
    <img src="img/banner-list.jpg" alt=""/>
</figure>

<p>Podaj nazwę i status banera i kliknij <strong>Add Banner (Dodaj baner)</strong>. Zobaczysz tabelę, gdzie trzeba
    wprowadzić następne parametry: </p>

<ol class="index-list">
    <li>Banner Name (Nazwa banera);</li>

    <li>Description (Opis) - tekst, który pojawia się w banerze (podtrzymuje znacznik html);</li>

    <li>Link (Link) - podaje się link, który odsyła do adresu URL witryny (na przykład, index.php?route=product/product&amp;product_id=47),
        to pole nie jest wymagane;
    </li>

    <li>Image (Obrazek);</li>

    <li>Kolejność banerów: może być więcej, niż jeden baner; pole trzeba wypełnić.</li>
</ol>

<p>Aby usunąć baner, kliknij <strong>Remove (Usuń)</strong>. Utworzony baner można wykorzystywać dla różnych modułów.
</p>

