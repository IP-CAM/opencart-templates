<h3>TM Newsletter Popup</h3>

<p>Sitenizi gezen ziyaretçilere, email ile haber bültenine üye olmaları için açılır pencere aracılığıyla teklif sunan
    bir modüldür. Bu pencere, bültene zaten üye olan ve siteye giriş yapmış kayıtlı kullanıcılara açılmaz.</p>
<figure class="img-polaroid">
    <img src="img/tm-newsletter-popup-view.jpg" alt=""/>
</figure>

<p>Modül kurulduktan sonra, tüm düzenlemeleri modül ayar sayfasından yapabilirsiniz.</p>

<h5>Ayarlar:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>modülün ismi;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background Image</dt>
            <dd>açılır pencerenin arkaplan görseli;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background image width</dt>
            <dd>arkaplan görselinin genişliği;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background image height</dt>
            <dd>arkaplan görselinin uzunluğu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Cookie Storage Time</dt>
            <dd>çerez saklama süresi;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Heading Title</dt>
            <dd>açılır penceredeki manşet;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>açılır pencere açıklaması;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>modülün durumu (aktif/pasif).</dd>
        </dl>
    </li>
</ol>
