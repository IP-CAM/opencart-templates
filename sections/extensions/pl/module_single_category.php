<h3>TM Produkty jednej kategorii </h3>

<p>Ten moduł pozwala pokazywać 4 główne moduły (Bestsellery, Produkty polecane (Featured), Blok promocji (Specials), Nowe produkty) z produktami pewnej kateorii from certain category pry otkryciu zakładki.</p>

<figure class="img-polaroid">
    <img src="img/tm-single-category-product-view.jpg" alt="">
</figure>

<p>Po zainstalowaniu modułu można go skonfigurować na stronie ustawień.</p>

<h5>Ustawienia:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Nazwa modułu</dt>
            <dd> Nazwanie modułu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Kategoria</dt>
            <dd>pozwala wybierać isteniejącą kategorię produktu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Wkładki</dt>
            <dd> włącza/wyłącza tab view. Jeżeli są wyłączone pojawia się opcja dla wybrania typu widoku modułu;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Typ produktu</dt>
            <dd>pozwala wybierać typ modułu. Dostępny kiedy opcja Wkładek jest wyłączona.</dd>
        </dl>

        Poniższe wartości są dostępne:

        <ul class="marked-list">
            <li>Promocje;</li>
            <li>Bestsellery;</li>
            <li>Produkty polecane (Featured);</li>
            <li>Nowe produkty.</li>
        </ul>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Promocje</dt>
            <dd> enables/disables Blok promocji;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Bestsellers</dt>
            <dd> włącza/wyłącza moduł Bestsellerów;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Produkty polecane</dt>
            <dd>włącza/wyłącza moduł Productów polecanych;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Nowe produkty</dt>
            <dd> włącza/wyłącza moduł Nowych produktów;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Produkty polecane</dt>
            <dd> pozwala wybrać listę produktów dla modułu Productów polecanych. Dostępny kiedy moduł Productów polecanych jest włączony albo opcja typu produktu jest ustawiona na Produkty polecane;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd>liczba produktów do wyświetlania;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Szerokość</dt>
            <dd> szerokość zdjęcia produktu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Wysokość</dt>
            <dd>wysokość zdjęcia produktu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd> Status modułu(włączony/wyłączony).</dd>
        </dl>
    </li>
</ol>

