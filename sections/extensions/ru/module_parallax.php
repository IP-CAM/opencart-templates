<h3>TM Parallax</h3>

<p>Данный модуль предназначен для создания параллакс-эффекта. В модуле предусмотрена возможность вывода изображений, видео и других модулей. Модуль поддерживает неограниченное количество слоев, каждый из которых может двигаться в разных направлениях и с разной скоростью.</p>

<figure class="img-polaroid">
    <img src="img/tm_parallax.png" alt=""/>
</figure>

<h5>Настройка модуля TM Parallax:</h5>
<h6>Базовые настройки:</h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>название модуля;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>статус модуля (включен/отключен);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Image</dt>
            <dd>фоновое (основное) параллакс-изображение;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>ширина фонового изображения;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>высота фонового изображения;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Blur</dt>
            <dd>размытие фонового изображения в случае, если его размер (разрешение) слишком маленький для качественного отображения в параллакс-секции;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Direction</dt>
            <dd>направление движения параллакс-эффекта;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Speed</dt>
            <dd>скорость движения параллакс-эффекта;</dd>
        </dl>
    </li>
</ol>

<h6>Дополнительные настройки модуля:</h6>
<ol class="index-list">
    <li>
        <p>Для добавления дополнительного слоя необходимо нажать кнопку <strong>"Add Layer"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/tm_parallax_edit-1.png" alt=""/>
        </figure>

        <p>После добавления слоя необходимо указать следующие настройки:</p>

        <h5>Настройки слоя:</h5>

        <ol class="index-list">
            <li>
                <dl class="inline-term">
                    <dt>Type</dt>
                    <dd>тип слоя;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Description</dt>
                    <dd>описание слоя;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Image</dt>
                    <dd>изображение слоя;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Width</dt>
                    <dd>ширина изображения слоя;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Height</dt>
                    <dd>высота изображения слоя;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Speed</dt>
                    <dd>скорость движения слоя;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Fade</dt>
                    <dd>слой будет постепенно проявляться из полной прозрачности в полную непрозрачность в зависимости от позиции прокрутки слоя;
                    </dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Blur</dt>
                    <dd>размытие изображения слоя в случае, если его размер (разрешение) слишком маленький для качественного отображения в параллакс-секции. Применяется только к слоям с типом "Media";
                    </dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Direction</dt>
                    <dd>направление движения слоя;</dd>
                </dl>
            </li>
        </ol>
    </li>
    <li>
        <p>Для отображения модуля в слое необходимо нажать кнопку <strong>"Add Module"</strong> в соответствующем слое.</p>
        <figure class="img-polaroid">
            <img src="img/tm_parallax_edit-2.png" alt=""/>
        </figure>
        <p>После нажатия кнопки <strong>"Add Module"</strong> вы увидите поле <strong>"Added Module name"</strong>, в котором необходимо выбрать модуль, который будет отображаться  в данном слое.</p>
    </li>
</ol>

