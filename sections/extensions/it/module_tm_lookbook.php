<h3>TM Lookbook</h3>

<p>Il lookbook è una serie di foto, in cui è possibile contrassegnare qualsiasi parte dell'immagine. Questo modulo serve per visualizzare i prodotti di una o delle diverse marche.</p>
<p>I moduli ha due modalità di visualizzazione per la visualizzazione nella posizione negozio: in forma di banner (1) o in forma di una raccolta (2).</p>


