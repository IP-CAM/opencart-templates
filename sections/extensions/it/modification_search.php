<h3>TemplateMonster Live search</h3>

<p> La modifica TemplateMonster Live Search consente di inserire prodotti ricercati senza ricaricare la pagina. Per
    rimuoverlo o disabilitarlo, vai su
    <strong>Extensions -> Modifications</strong>, seleziona la modifica dall'elenco e fai clic su
    <strong>Disable</strong> o <strong>Delete</strong>.</p>
