<h3>TemplateMonster Google Map</h3>

<p>Ce module vous permet d'ajouter une carte Google avec un nombre illimité de marqueurs à votre site Web. </p>

<p>Le centre de la carte et l'un des marqueurs sont définis dans les paramètres de la boutique -
    <strong>System->Settings</strong>, dans l'onglet <strong>General</strong>, le champ
    <strong>Geocode</strong>. Après avoir cliqué sur un marqueur, vous verrez la fenêtre pop-up avec la barre
    <strong>Address</strong>.</p>
<figure class="img-polaroid">
    <img src="img/tm-googlemap-view.jpg" alt=""/>
</figure>

<p>Une fois le module installé, vous pouvez le configurer sur la page de configuration de module. </p>

<h5>Paramètres:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>le nom du module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Google Map API key</dt>
            <dd>la clé API de Google Map. Vous pouvez trouver plus de détails sur le
                <a target="_blank" href="https://developers.google.com/maps/documentation/javascript/tutorial#api_key">site
                    officiel</a>
                ;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>le statut du module (activé/désactivé);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Map Type</dt>
            <dd>le type de carte. Vous disposez de quatre opt</dd>
        </dl>
        ions:
        <ul class="marked-list">
            <li>
                <dl class="inline-term">
                    <dt>Roadmap</dt>
                    <dd>la vue carte routière par défaut;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Terrain (Landscape)</dt>
                    <dd>la carte topographique, une représentation du terrain;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Hybrid</dt>
                    <dd>une combinaison des vues carte routière et satellite;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Satellite</dt>
                    <dd>les images de la Terre prises par un satellite;</dd>
                </dl>
            </li>
        </ul>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Map Zoom</dt>
            <dd>la valeur numérique. Le niveau de zoom initial utilisé lors de l'affichage de la carte. Le niveau de
                zoom le plus bas : 0. Vous pouvez augmenter le niveau de zoom en augmentant cette valeur;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Sensor</dt>
            <dd>indique si cette application utilise détecteur pour déterminer la position de l'utilisateur.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Map Width</dt>
            <dd>la largeur de la carte. Vous devez spécifier l'unité de mesure (% ou px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Map Height</dt>
            <dd>la hauteur de la carte. Vous devez spécifier l'unité de mesure (% ou px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Map Styles</dt>
            <dd>le style de la carte. La variété des styles disponibles:
                <a href='https://snazzymaps.com/' rel="nofollow" target="_blank">https://snazzymaps.com/</a>
                ;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Disable UI</dt>
            <dd>activer/désactiver l'interface utilisateur par défaut;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Scrollwheel</dt>
            <dd>activer/désactiver la modification de l'échelle de la carte à l'aide de la molette de la souris;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Draggable</dt>
            <dd>activer/désactiver la possibilité de déplacer la carte;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Marker Image</dt>
            <dd>définir l'image du marqueur. Si vous laissez le champ vide, le marqueur par défaut sera utilisé;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Active Marker Image</dt>
            <dd>définir l'image lorsque vous cliquez sur le marqueur;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Marker Image Width</dt>
            <dd>définir la largeur de l'image du marqueur;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Marker Image Height</dt>
            <dd>définir la hauteur de l'image du marqueur.</dd>
        </dl>
    </li>
</ol>
<p>Pour ajouter un marqueur, cliquez sur <strong>Add New</strong>. Dans le champ
    <strong>Geocode</strong> spécifiez les coordonnées de marqueur (latitude, longitude - Example: 40.6700 , -73.9400 )
    et dans le champ
    <strong>Address</strong> entrez les informations qui seront affichées lorsque vous cliquez sur un marqueur. Vous
    pouvez trouver les coordonnées nécessaires sur
    <a href='http://www.mapcoordinates.net' rel="nofollow" target="_blank">http://www.mapcoordinates.net</a>
    . Pour supprimer le marqueur, cliquez sur <strong>Delete Marker</strong>.
</p>


