<h3>TemplateMonster Social List</h3>

<p>Este módulo permite exibir uma lista de endereços para redes sociais populares. </p>

<p class="alert alert-danger"> Se você instalar a versão "update" do tema, o conteúdo de amostra não estará disponível.
    Para configurar o módulo, certifique-se de preencher as informações necessárias.</p>

<p>Ao instalar o módulo, você pode alterar suas configurações na página de configurações do módulo. Se você
    deixar o campo vazio, o módulo não será mostrado. </p>

