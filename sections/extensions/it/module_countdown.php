<h3>TM CountDown</h3>

<p>Questo modulo permette di creare i contatori conto alla rovescia. Ci sono due tipi disponibili, numerici e radiali timer.
</p>
<p>Numerico:</p>

<figure class="img-polaroid">
    <img src="img/tm_countdown.png" alt=""/>
</figure>

<p>Radiale:</p>

<figure class="img-polaroid">
