<h3>TemplateMonster Newsletter Popup</h3>

<p>This module allows you to add pop-up window with an offer to subscribe to the email newsletter when someone is viewing
    your site. The window is not showing up for authorized (logged in) users and those who has already
    subscribed.</p>

<figure class="img-polaroid">
    <img src="img/pop.jpg" alt=""/>
</figure>

<p>When the module is installed, you can configure it on the module configuration page.</p>

<h4>How to manage TemplateMonster Newsletter module</h4>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p>Find the<strong> TemplateMonster Newsletter</strong> section. Press <strong>edit</strong> button to configure the existing module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
  <figure class="img-polaroid"> <img src="img/pop-15.jpg" alt=""/></figure>
</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid"><img src="img/pop-18.jpg" alt=""/></figure>
<h5>Available settings:</h5>
<ol class="marked-list">
  <li><strong>Module Name</strong> — the name of the module;</li>
  <li><strong>Background Image</strong> — background image set for the popup;</li>
  <li><strong>Background image width</strong> — set the background image width;</li>
  <li><strong> Background image height</strong> — set the background image height; </li>
  <li><strong>Cookie Storage Time</strong> — specify the time  before the window  pops up again;</li>
  <li><strong>Heading Title</strong> — enter the popup heading;</li>
  <li>
    <div align="justify"><strong>Description </strong>— popup description, the main text which will be displayed within the module;</div>
  </li>
  <li><strong>Status </strong>— module status (enabled/disabled). </li>
</ol>
