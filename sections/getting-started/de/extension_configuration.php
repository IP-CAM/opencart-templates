<h3>Die Installation auf bestehenden Online-Shop</h3>
<p>Folgen Sie dieser Installationsart, wenn Sie schon eine OpenCart Website haben und ihr Aussehen ändern
    möchten, indem Sie ein neues Theme installieren.</p>
<p class="alert alert-warning">Überzeugen Sie sich davon, dass die Version Ihrer OpenCart Engine allen Anforderungen
    entspricht. Alle Versionen OpenCart sind auf der <a
        href=" http://www.opencart.com/index.php?route=download/download"
        target="_blank" class="spec_link"> offiziellen Webseite OpenCart verfügbar</a>.
</p>
<ol class="index-list">
    <li>Um die OpenCart Vorlage zu installieren, müssen Sie ihre Dateien auf den Hosting-Server hochladen. Im Ordner
        Ihrer OpenCart Vorlage finden Sie das Archiv <strong> &quot;themeXXX(update).zip&quot;</strong> und entpacken
        Sie es auf Ihrem Computer.
    </li>
    <li>Nachdem laden Sie die entpackten Dateien in das Stammverzeichnis der installierten OpenCart Vorlage. <br/>
        Sie können auch die ausführliche Video-Anleitung nutzen <a href="/help/how-upload-files-server-2.html"
                                                                   target="_blank"> Wie man die Dateien auf das Hosting
            hochlädt</a>. <br/>
        <span class="info-box">Die Vorlagendateien schaden den Online-Shop in keinster Weise;</span>
    </li>
    <li>Danach öffnen Sie Ihre Website im Browser und fügen Sie /install am Ende der Adresszeile hinzu (
        <i>zum Beispiel,
            http://your_domain_name/opencart/install
        </i>
        )
    </li>
    <li>Auf der Seite, die auf dem Bildschirm angezeigt ist, drücken Sie auf den Button <strong>Continue
            (Weiter)</strong>. Aktualisierung vom Theme kann einige Zeit dauern.
    </li>
    <li>Loggen Sie sich ins Control Panel Ihrer Website ein, finden Sie Menü <strong>Erweiterungen ->
            Erweiterungen-Installateur (Extensions -> Extension Installer)</strong> und laden Sie die Dateien aus dem
        Archiv <strong>themeXXX(extensions)</strong> hoch. Nachdem Sie alle Archivdateien hochgeladen haben, finden Sie
        Menü <strong>Erweiterungen -> Modifikationen (Extensions -> Modifications)</strong> und drücken Sie auf den
        Button <strong>Aktualisieren(refresh)</strong> in der oberen rechten Ecke des Bildschirmes;
    </li>
    <li>Danach finden Sie die Einstellungen des Online-Shops <strong>System -> Einstellungen (System ->
            Settings)</strong>, und im Tab Shop (Store) im Feld Vorlage (Template) wählen Sie das Theme -
        <strong>themeXXX</strong>
        aus.
    </li>
</ol>
<p class="alert alert-info">Sie können auch die Anleitung zur Installation nutzen: <a target="_blank"
                                                                              href="/help/opencart-2-x-how-to-install-a-template-over-the-existing-store-using-update-package.html">
        OpenCart. Wie man die Vorlage auf bestehenden Online-Shop (mit Hilfe von dem Archiv
        “update”) installiert</a>. </p>