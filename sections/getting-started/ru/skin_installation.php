<p>
Шаблон Storeflex  - многофункциональный шаблон, внешний вид и тематику которого Вы можете настраивать по своему выбору. Шаблон состоит из ряда скинов. 
Чтобы установить новый скин, разархивируйте выбранный архив и  скопируйте все содержимое папки со скином в папку с фулпаком. Далее перейдите на страницу установки и нажмите на кнопку “Continue”.
