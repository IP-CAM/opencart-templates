<h3>TemplateMonster Cart</h3>
<p> <strong>Cart Module</strong> is the extension that allows you to display the shopping cart on your website. </p>
<figure class="img-polaroid"> <img src="img/cart.jpg" alt=""/> </figure>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/> </figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/> </figure>
</p>
<p>Find the<strong> TemplateMonster Cart</strong> module. Press <strong>edit</strong> button to configure the  module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
  <figure class="img-polaroid"> <img src="img/cart-1.jpg" alt=""/> </figure>
</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid"> <img src="img/cart-2.jpg" alt=""/></figure>
<p>Select  <strong>Disable</strong> from the <strong>Status</strong> option dropdown to turn off the module.</p>
<p>
  <figure class="img-polaroid"> <img src="img/cart-3.jpg" alt=""/> </figure>
</p>
