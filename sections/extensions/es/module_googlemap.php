<h3>TemplateMonster Google Map</h3>

<p>Este módulo le permite añadir un Google mapa con un número ilimitado de etiquetas a su sitio web. </p>
<figure class="img-polaroid">
    <img src="img/tm-googlemap-view.jpg" alt=""/>
</figure>

<p>El centro del mapa y uno de los marcadores se definen en los parámetros de la tienda -
    <strong>System->Settings</strong>, en la pestaña <strong>General</strong> , el campo
    <strong>Geocode</strong>. Una vez que haga clic en un marcador, verá la ventana pop-up con la barra de
    <strong>Address</strong>.</p>

<p>Después de instalar el módulo, se puede configurar en la página de configuración del módulo. </p>

<h5>Parámetros:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>el nombre del módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Google Map API key</dt>
            <dd>API key para Google Map. Puede encontrar más detalles sobre el en
                <a target="_blank" href="https://developers.google.com/maps/documentation/javascript/tutorial#api_key">official
                    website;</a>
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>estado del módulo (activado/desactivado);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Map Type</dt>
            <dd>tipo del mapa. Usted dispone de cuatro opci</dd>
        </dl>
        ones:
        <ul class="marked-list">
            <li>
                <dl class="inline-term">
                    <dt>Roadmap</dt>
                    <dd>la vista del mapa por defecto;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Terrain (Landscape)</dt>
                    <dd>el mapa topográfico, una representación del terreno;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Hybrid</dt>
                    <dd>una combinación de mapa y satélite visitas;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Satellite</dt>
                    <dd>las imágenes de la Tierra tomadas por un satélite;</dd>
                </dl>
            </li>
        </ul>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Map Zoom</dt>
            <dd>el valor númerico. El nivel de zoom inicial utilizado en la visualización del mapa. El nivel de zoom más
                bajo: 0. Se puede aumentar el nivel de zoom mediante el aumento de este valor;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Sensor</dt>
            <dd>indica si esta aplicación utiliza detector para determinar la posición del usuario.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Map Width</dt>
            <dd>la anchura del mapa. Debe especificar la unidad de medida (% o px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Map Height</dt>
            <dd>la altura del mapa. Debe especificar la unidad de medida (% o px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Map Styles</dt>
            <dd>la asignación de estilos. La variedad de estilos disponibles:
                <a href='https://snazzymaps.com/' rel="nofollow" target="_blank">https://snazzymaps.com/</a>
                ;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Disable UI</dt>
            <dd>activar/desactivar la interfaz de usuario por defecto;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Scrollwheel</dt>
            <dd>activar/desactivar la modificación de la escala del mapa mediante el uso de la rueda del ratón;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Draggable</dt>
            <dd>activar/desactivar la posibilidad de mover el mapa;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Marker Image</dt>
            <dd>definir la imagen del marcador. Si deja el campo en blanco, se utilizará la etiqueta predeterminada;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Active Marker Image</dt>
            <dd>establecer la imagen al hacer clic en el marcador;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Marker Image Width</dt>
            <dd>definir la anchura de la imagen del marcador;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Marker Image Height</dt>
            <dd>definir la altura de la imagen del marcador.</dd>
        </dl>
    </li>
</ol>
<p>Para agregar un marcador, haga clic en <strong>Add New</strong>. En el campo
    <strong>Geocode</strong> especificar las coordenadas de marcador (latitud, longitud - Ejemplo: 40.6700, -73.9400) y
    en el campo
    <strong>Address</strong> introduzca la información que se mostrará cuando se hace clic en un marcador. Usted puede
    encontrar las coordenadas necesarias en
    <a href='http://www.mapcoordinates.net' rel="nofollow" target="_blank">http://www.mapcoordinates.net</a>
    . Para quitar el marcador, haga clic en <strong>Delete Marker</strong>.
</p>

