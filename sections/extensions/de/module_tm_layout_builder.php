<h3>TM Layout Builder</h3>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-edit.jpg" alt=""/>
</figure>
<p>Dieses Modul ermöglicht eine schnelle, einfache und praktische Erstellung von verschiedenen Varianten der Shop Hauptseite.</p>

<h5>Modul Grundeinstellungen :</h5>
<ol class="index-list">
	<li>
		<dl class="inline-term">
			<dt>Status</dt>
			<dd>Modulstatus (aktiviert/deaktiviert);</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Modulname</dt>
			<dd>Der Name des Moduls;</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Modul Klasse</dt>
			<dd>Die Klasse des Moduls;</dd>
		</dl>
	</li>
	<li>
		<dl class="inline-term">
			<dt>Modul ID</dt>
			<dd>ID des Moduls.</dd>
		</dl>
	</li>
</ol>
<p>Um ein neues Layout zu erstellen, drücken Sie den <strong>New Layout View</strong> Knopf, und dann den <strong>Add Row
		(1)</strong> Knopf. Nachdem Sie eine Reihe hinzugefügt haben, fügen Sie Spalten hinzu. Um Spalten hinzuzufügen, drücken Sie den <strong>Add
		Column (2)</strong> Knopf.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-column.jpg" alt=""/>
</figure>

<p>Die Anzahl der Spalten in einer Reihe ist unbegrenzt, Sie können so viele Spalte hinzufügen, wie Sie wollen. Die Breite der Spalten, kann direkt nach dem Hinzufügen, mit Hilfe von der Drag and drop Funktion eingestellt werden. Ziehen Sie dazu den rechten Rand der Spalte. </p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-drag-drop.jpg" alt=""/>
</figure>

<p>Wenn Sie die Spalten hinzugefügt haben, sollten Sie diese mit dem Inhalt ausfüllen (Module). Um ein Modul hinzuzufügen, klicken Sie auf die <strong>Add Widget</strong> taste und wählen Sie eins aus dem Drop Down.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-widget.jpg" alt=""/>
</figure>

<p>Danach können Sie es bearbeiten oder löschen,</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change.jpg" alt=""/>
</figure>

<p>und auch in eine andere Spalte verschieben.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change-position.jpg" alt=""/>
</figure>

<p>Nachdem sie alle Reihen, Spalten und Module eingestellt haben, speichern Sie das Builder Modul und zeigen Sie es in einer Ihrer Position der Startseite an.</p>