<h3>TM ABC Customers</h3>

<p>Questo modulo serve per la creazione di sconti per i diversi gruppi di clienti. L'importo dell'ordine viene ricalcolato, quando l'ordine è posto, con la percentuale di sconto fissato per uno specifico gruppo di clienti.</p>

