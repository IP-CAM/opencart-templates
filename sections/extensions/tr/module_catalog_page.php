<h3>Katalog Sayfasý</h3>

<p>Tüm ürün ve ürün detaylarýnýn bulunduðu sayfa. Sayfanýn sol tarafýnda yer alan sütunda 6 adet (sayý deðiþtirilebilir) ürün gösterilir. Sað tarafta yer alan sütunda ise, ilk ürüne ait açýklama bulunur. Kullanýcý, fareyle ikinci ürünün üstüne geldiðinde, sað taraftaki sütunda 2. ürünün bilgileri gösterilir. Kullanýcý son (6) ürüne kadar indikçe, ayný iþlem devam eder. Daha sonra 'ürünleri göster' düðmesine týklayarak geri kalan diðer ürünlere (limitsiz) geçebilir.</p>

