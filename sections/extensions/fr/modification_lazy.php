<h3>TM Lazy load</h3>

<p>La modification permet de charger des images de produits au moment où vous faites défiler le site. Cette extension permet d'optimiser les performances de votre site. Pour changer l'image de preloader, vous devez remplacer le fichier
    <strong>lazy_loader.gif</strong> dans le dossier <strong>catalog/view/theme/themeXXX/image/</strong>.</p>
