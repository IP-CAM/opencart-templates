<h4>No se puede cargar Google Fonts: </h4>
<ol class="index-list">
    <li>Abra el archivo <strong>catalog\view\theme\themeXXX\stylesheet\stylesheet.css</strong></li>

    <li><p>Busque la línea:</p>
        <code>//fonts.googleapis.com/css?family=[---your_web_font_name---]</code>
    </li>
    <li><p>Sustituya por la siguiente:</p>
        <code>//fonts.useso.com/css?family=[---your_web_font_name---]</code>
    </li>
</ol>

<h4>Validación</h4>
<p class="alert alert-info">Ambos códigos del formato HTML (Langage hypertexte) y estilos CSS utilizados en nuestros
    modelos son semánticamente correctos y válidos. Sin embargo, todavía pueden producirse algunos errores del W3C.
    Haciendo código 100% W3C válido elimina el uso de las modernas tecnologías de sitio web como características de CSS3
    y del marcador HTML5. Nuestro objetivo es entregar una rica experiencia al usuario con plantillas de alta calidad y,
    a veces tenemos que romper algunas reglas.</p>