<h3>Installazione modulo</h3>
<p>Installazione/disinstallazione modulo. Vai sul pannello di Amministrazione (aggiungendo /admin all'indirizzo:
    www.tuosito.com/admin). Vai su
    <strong>Extensions </strong> e nel menu a discesa scegli
    <strong>Modules</strong> Ora vedrai un elenco di moduli installati e pronti da installare. Se vedi il
    pulsante
    <strong>Uninstall</strong> accanto a un modulo, vuol dire che il modulo è già installato e che è possibile
    modificare le impostazioni del modulo facendo clic su
    <strong>Edit</strong>. Facendo clic sul pulsante
    <strong>Install</strong>, si avvierà la procedura di installazione del modulo. Per rimuovere il modulo, fare clic
    sul pulsante
    <strong>Uninstall</strong>. </p>
<p class="alert alert-warning">Nota che disinstallando il modulo, tutte le impostazioni aggiunte in precedenza saranno
    cancellate e non potranno essere ripristinate.</p>
<figure class="img-polaroid">
    <img src="img/install_1.jpg" alt=""/>
</figure>


<p>Una volta terminata l'installazione, sarà necessario configurare il modulo. Le impostazioni possono essere diverse a
    seconda del modulo, ma il campo
    <strong>Status</strong> è lo stesso per tutti - il campo consente di attivare e disattivare il modulo.</p>
<figure class="img-polaroid">
    <img src="img/edit-module_1.jpg" alt=""/>
</figure>


<p>Per aggiungere il modulo a una certa pagina, è necessario andare sull'elenco delle pagine. È possibile farlo in due
    modi: </p>
<ol class="index-list">
    <li>
        <p>Nel menu laterale, vai su <strong>System -> Design -> Layout</strong>;</p>

        <figure class="img-polaroid">
            <img src="img/page-selecting-way.jpg" alt=""/>
        </figure>
    </li>

    <li>
        <p>Nella sezione modulo, clicca sul link <strong>qui</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/page-selecting-way-2.jpg" alt=""/>
        </figure>
    </li>
</ol>


<p>Nell'elenco delle pagine, selezionare la pagina in cui si vuole aggiungere il modulo e fare clic su
    <strong>Edit</strong></p>
<figure class="img-polaroid">
    <img src="img/list-of-pages.jpg" alt=""/>
</figure>


<p>Puoi vedere un elenco di moduli e posizionamenti moduli già aggiunti alla pagina . </p>


<p>Sono disponibili i seguenti posizionamenti per i moduli (in ordine di posizione):</p>
<ol class="index-list">
    <li>Intestazione in alto (dopo la sezione intestazione);</li>

    <li>Colonna sinistra (pannello lato sinistro);</li>

    <li>Colonna destra (pannello lato destro);</li>

    <li>Contenuto-alto;</li>

    <li>Contenuto-basso;</li>

    <li>Piè di pagina.</li>
</ol><p>Il campo
    <strong>Sort Order</strong> mostra l'ordine dei moduli assegnati alla stessa posizione. Per aggiungere un modulo a
    una pagina, fare clic su
    <strong>Add Module</strong>. Selezionare i moduli e i posizionamenti modulo desiderati e fare clic su
    <strong>Save</strong>. L'installazione del modulo è ora completa. </p>

