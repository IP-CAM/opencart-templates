<h3>La creación de banners</h3>

<h4>Modificación de TemplateMonster Description for banners</h4>

<p>La modificación permite añadir descripción del banner. Para desactivar o eliminar el cambio, vaya a
    <strong>Extensions -> Modifications</strong>, encuentre una extensión "TemplateMonster Description for banners" y haga clic en
    <strong>Disable</strong> o <strong>Delete</strong>.</p>

<p>Algunos módulos incluyen un campo para seleccionar el banner. Vaya a la sección
    <strong>System -> Design -> Banner</strong></p>

<p>La página muestra la lista de banners creados. Para crear un nuevo banner, haga clic en
    <strong>Add New</strong>. Para editar banner existente, haga clic en <strong>Edit</strong>.</p>

<figure class="img-polaroid">
    <img src="img/banner-list.jpg" alt=""/>
</figure>


<p>Indique Banner Name, Status y haga clic
    <strong>Add Banner</strong>. Verá la mesa donde debe introducir los siguientes valores: </p>

<ol class="index-list">
    <li>Banner Name;</li>

    <li>Descripción - el texto que aparece en el banner (soporta html-markup);</li>

    <li>Link - especifique el enlace relativo a la URL del sitio web (por ejemplo, index.php?route=product/product&amp;product_id=47),
        este campo no es obligatorio;
    </li>

    <li>Image;</li>

    <li>Banner order - puede haber un solo banner, el campo es obligatorio de rellenar.</li>
</ol>

<p>Para retirar el banner, haga clic en
    <strong>Remove</strong>. Una vez que el banner se ha creado se puede utilizar para diferentes módulos.</p>

