<h3>TM Pinterest</h3>

<p>Moduł TM Pinterest pozwala na wyświetlenie galerii Pinterest w twoim sklepie. </p>

<figure class="img-polaroid">
    <img src="img/tm-pinterest-view.jpg" alt=""/>
</figure>

<p>Kiedy moduł został zainstalowany, możesz konfigurować go na stonie konfiguracji modułu. </p>

<h5>Ustawienia:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>nazwa modułu;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Pinterest Page URL</dt>
            <dd>pełna ścieżka do strony, wyświetlającej galerię; </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>szerokość bloku (px);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>wysokość bloku (px); </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>status modułu (włączony/wyłączony).</dd>
        </dl>
    </li>
</ol>

