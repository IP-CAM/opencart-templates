<h4>TM Blog Artikel</h4>

<p>Dieses Modul ermöglicht es die Blog-Artikel nach Kategorien anzuzeigen.</p>

<figure class="img-polaroid">
    <img src="img/tm-blog-articles-view.jpg" alt="">
</figure>

<p>Nachdem das Modul installiert ist können Sie es auf der Moduleinstellungen Seite konfigurieren.</p>

<h5>Einstellungen:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd> Modulstatus (aktiviert / deaktiviert).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Modulname</dt>
            <dd> Name des Moduls;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Artikel Limit</dt>
            <dd> Anzahl der angezeigten Artikel;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Anzeigen des Readmore Buttons</dt>
            <dd> aktiviert\deaktiviert Read more button;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Anzeigen des Datums</dt>
            <dd> aktiviert\deaktiviert das Datum;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Anzeigen des Autors</dt>
            <dd> aktiviert\deaktiviert den Artikelautor;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Anzeigen der Kommentare</dt>
            <dd> aktiviert\deaktiviert die Anzahl der Kommentare per Artikel;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Anzeigen des Bildes</dt>
            <dd> aktiviert\deaktiviert Artikelbild;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Bildbreite</dt>
            <dd> Bildbreite;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Bildhöhe</dt>
            <dd> Bildhöhe;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Beschreibungssymbol Grenzwert</dt>
            <dd> Anzahl der Symbole in der Artikelbeschreibung;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Kategorie</dt>
            <dd> Sie können die Kategorie einstellen, von welcher die Artikel angezeigt werden. Es gibt auch eine Möglichkeit, den beliebtesten Artikeln oder zuletzt hinzugefügten Artikel anzuzeigen. 
            </dd>
        </dl>
    </li>
</ol>

