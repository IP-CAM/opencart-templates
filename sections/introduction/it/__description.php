<h2 class="item1"> Introduzione</h2>

<h5>Grazie per aver acquistato un template OpenCart. Questa documentazione è suddivisa in diverse sezioni e illustra
    l'intera procedura per configurare e amministrare un Web store OpenCart a partire da zero. Abbiamo fatto del nostro
    meglio per rendere questo manuale il più chiaro possibile.</h5>

<article id="whatisopencart">
    <h3>Cos'è OpenCart?</h3>

    <p>OpenCart è un'applicazione Open Source per l'ecommerce. OpenCart offre facili soluzioni di personalizzazione per
        il
        tuo negozio online. La comunità di OpenCart è composta da utenti di vario tipo, dallo sviluppatore Web esperto
        in
        cerca di un'interfaccia semplice da usare, fino al proprietario di un negozio che si affaccia per la prima volta
        nel
        mondo del business online. OpenCart offre un gran numero di funzioni che ti permetteranno di avere pieno
        controllo
        sul tuo negozio. Grazie agli strumenti di OpenCart potrai facilmente sfruttare a pieno tutto il potenziale del
        tuo
        negozio.
        <a href="http://docs.OpenCart.com" target="_blank">Altri dettagli</a>
    </p>
</article>

