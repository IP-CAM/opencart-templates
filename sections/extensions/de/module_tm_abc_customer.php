<h3>TM ABC Customers</h3>

<p>Dieses Modul hilft Rabatte für unterschiedliche Kundengruppen einzustellen. Der Auftragswert wird neu berechnet, wenn der Auftrag mit dem Rabattsatz für eine bestimmte Gruppe von Kunden eingestellt wird.</p>

