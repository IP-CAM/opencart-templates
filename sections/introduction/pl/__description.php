<h2 class="item1">Wprowadzenie</h2>

<h5>Dziękujemy za zakup szablonu OpenCart. Dokumentacja ta składa się z kilku części i pokazuje cały proces
    konfigurowania i administrowania sklepu internetowego OpenCart od podstaw. Dołożyliśmy wszelkich starań, aby zrobić
    ten podręcznik jaknajbardziej jasnym i łatwym do naśladowania.</h5>

<h3>Co to jest OpenCart?</h3>

<p>OpenCart - to aplikacja e-commerce Open Source. OpenCart zapewnia łatwe ustawienia sklepu internetowego. Społeczność
    OpenCart obejmuje liczbę użytkowników, zaczynając od doświadczonych programistów, szukających przyjaznego dla
    klientów interfejsu, kończąc właścicielami sklepów, którzy dopiero rozpoczynają swój biznes online. OpenCart
    zapewnia ogromną liczbę funkcji, które pozwalają kontrolować twój sklep. Korzystając z narzędzi OpenCart można łatwo
    odkryć potencjał sklepu. <a href="//docs.OpenCart.com" target="_blank">Więcej szczegółów</a></p>

