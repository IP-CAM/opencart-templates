<h3>TemplateMonster Facebook Box</h3>

<p>El módulo permite mostrar Facebook como la caja que muestra a los fans que aman su página web y su marca.</p>

<figure class="img-polaroid">
    <img src="img/tm-facebook-view.jpg" alt=""/>
</figure>

<p>Una vez instalado el módulo, se puede configurar sobre la página de parámetros del módulo.</p>

<h5>Parámetros:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>el nombre del módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Facebook Page URL</dt>
            <dd>el enlace a la página de Facebook;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Color Scheme</dt>
            <dd>la paleta de colores (claro, oscuro);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Faces</dt>
            <dd>mostrar caras de usuarios (si, no);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Stream</dt>
            <dd>mostar los artículos recientes (si, no);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Header</dt>
            <dd>mostrar los títulos de bloques (si, no);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Border</dt>
            <dd>mostrar el borde de bloque (si, no);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>la longitud del bloque (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>la anchura del bloque (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>estado del módulo (activado/desactivado).</dd>
        </dl>
    </li>
</ol>

