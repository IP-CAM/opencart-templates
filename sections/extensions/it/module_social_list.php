<h3>TM Social List</h3>

<p>Questo modulo consente di visualizzare i link dei social network più popolari. </p>

<p class="alert alert-danger"> Installando la versione aggiornata del tema i contentui campione del template (quindi
    l'elenco di link di social network disponibili) non sarà disponibile. Per configurare correttamente il modulo,
    assicurati di aver inserito tutte le informazioni necessarie.</p>

<p>Dopo aver installato il modulo, sarà possibile configurarlo nell'apposita pagina di configurazione.
    Lasciando vuoto questo campo, l'elemento non comparirà sull'elenco. </p>

