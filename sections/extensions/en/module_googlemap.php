<h4>How to find the Area Coordinates</h4>
<p>You can get the  Area Coordinates <a href='http://htmlcolorcodes.com/' rel="nofollow" target="_blank">here</a>.
</p>

<p>Select the <strong>Polygon</strong> from the option dropdown.</p>
<figure class="img-polaroid">
  <img src="img/polygon-1.jpg" alt=""/>
</figure>
<p>Select the <strong>Javascript</strong> from the option dropdown.</p>
<figure class="img-polaroid">
  <img src="img/polygone.jpg" alt=""/>
</figure>
<p>Mark the desired area on the map.</p>
<p>
  <figure class="img-polaroid">
    <img src="img/polygon-2.jpg" alt=""/>
  </figure>
</p>
<p>Copy the area coordinates.</p>
<p>
  <figure class="img-polaroid">
    <img src="img/polygon-3.jpg" alt=""/>
  </figure>
</p>
<p>You'll  obtain data as: <code>42.363281,63.074866,0.0  37.792969,61.270233,0.0  40.957031,58.631217,0.0  47.109375,59.712097,0.0  45.878906,62.754726,0.0  45.527344,62.915233,0.0  43.066406,63.074866,0.0  42.714844,63.074866,0.0  42.363281,63.074866,0.0</code>.</p>
<p>You need to make the data as:</p>
<p> <code>[42.363281,-63.074866], [37.792969,-61.270233], [40.957031,-58.631217],  [47.109375,-59.712097],  [45.878906,-62.754726],  [45.527344,-62.915233],  [43.066406,-63.074866],  [42.714844,-63.074866],  [42.363281,-63.074866]</code></p>
<p>Insert the  final data in the <strong>Area Coordinates</strong> option field.</p>
<p>When complete, tab<strong> Save</strong>.</p>

