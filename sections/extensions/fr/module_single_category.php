<h3>TemplateMonster Single Category Products</h3>

<p>Ce module vous permet d'afficher 4 modules principaux (Best-sellers, En vedette, Promotions, Les plus récents) avec les produits de la catégorie en vue d'onglet.</p>

<figure class="img-polaroid">
    <img src="img/tm-single-category-product-view.jpg" alt="">
</figure>

<p>Une fois le module installé, vous pouvez le configurer sur la page des paramètres du module.</p>

<h5>Réglages:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Nom du module</dt>
            <dd> le nom du module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Catégorie</dt>
            <dd>permet de sélectionner la catégorie de produit existante;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Onglets</dt>
            <dd> activer/désactiver les onglets. Si désactivé, il y a une option pour sélectionner le type d'affichage du module;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Type de produit</dt>
            <dd>permet de sélectionner le type de module. Disponible lorsque l'option Onglets est désactivée.</dd>
        </dl>

        Les valeurs suivantes sont disponibles:

        <ul class="marked-list">
            <li>Promotions;</li>
            <li>Populaires;</li>
            <li>Meilleures ventes;</li>
            <li>Derniers.</li>
        </ul>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Promotions</dt>
            <dd> activer/désactiver le module Promotions;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Populaires</dt>
            <dd> activer/désactiver le module Populaires;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Meilleures ventes</dt>
            <dd> activer/désactiver le module Meilleures ventes;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Derniers</dt>
            <dd> activer/désactiver le module Derniers;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Produits en vedette </dt>
            <dd> permet de sélectionner la liste des produits pour le module Meilleures ventes. Disponible lorsque le module Meilleures ventes est activé ou le type de produit choisi est Meilleures ventes;;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limite</dt>
            <dd> le nombre de produits à afficher;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Largeur</dt>
            <dd> largeur de l'image du produit;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Hauteur</dt>
            <dd> hauteur de l'image du produit;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Statut</dt>
            <dd> état du module (activé/désactivé).</dd>
        </dl>
    </li>
</ol>

