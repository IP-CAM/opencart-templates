<h2 class="item2">Prepare (Przygotowania)</h2>

<h6>Przed przystąpieniem do konfigurowania witryny internetowej OpenCart sprawdź, czy jesteś w pełni przygotowany. </h6>

<h4>Editing software (Edytowanie oprogramowania)</h4>

<p>Dla komfortowej pracy z szablonem OpenCart zalecamy pobrać wszystkie potrzebne aplikacje. Możesz zobaczyć listę
    wymaganego oprogramowania na stronie demonstracji szablonu na żywo. Wymagania mogą się różnić do różnych szablonów,
    dlatego najpierw przeliczymy, co jest potrzebne w ogóle:</p>

<ol class="index-list">
    <li> Może być potrzebna aplikacja Adobe Photoshop. Jej się używa do edytowania plików źródłowych .PSD, i ona jest
        niezbędna, jeśli chcesz redagować grafikę i obrazki szablonu.
    </li>
    <li>Dla wniesienia zmian do plików źródłowego kodu szablonu jest potrzebny jakiś edytor kodu, taki jak Adobe
        Dreamweaver, Notepad ++, Sublime Text itp.
    </li>
    <li>Dla przesłania plików na serwer hostingowy może być potrzebny program FTP Manager, taki jak Total Commander,
        FileZilla, CuteFTP itp.
    </li>
    <li>Postępuj zgodnie z następującym przewodnikiem, żeby rozpakować archiwum za pomocą programów
        <a href="/help/extract-archived-files-windows-os.html" target="_blank"> WinZip(PC) </a> i
        <a href="/help/extract-archived-files-mac-os.html" target="_blank"> StuffitExpander(MAC)</a>.
    </li>
</ol>

<h4>Hosting</h4>

<p>Ponieważ silnik OpenCart jest aplikacją opartą na PHP/MySQL, trzeba przygotować środowisko hostingowe. </p>
<p>Jeśli masz live hosting, prosimy upewnić się, że on spełnia
 <a href="http://docs.OpenCart.com/requirements" target="_blank">wymagania systemowe OpenCart</a>
 i jest gotowy do użycia
    przez witryny internetowe OpenCart.</p>

<p>Ewentualnie można uruchomić OpenCart lokalnie na swoim komputerze za pomocą serwera lokalnego. Aby utworzyć lokalny
    serwer hostingowy, należy użyć aplikacji hostingu lokalnego, takich jak <b>WAMP</b>, <b>AppServ</b>, <b>MAMP</b>
    itp. Każdą z nich można łatwo zainstalować, jak wszystkie inne programy, i wykorzystać do uruchomienia OpenCart.</p>

