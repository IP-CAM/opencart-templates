<h3>Module installation</h3>
<p align="justify">Module installation process includes several steps. Navigate to your administration panel (by adding /admin in the
    browser address bar — www.yoursite.com/admin). <strong> </strong></p>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">You will see the list of installed and ready-to-install modules. If you see the
    <strong>Uninstall</strong> button next to any module, it means that the module is already installed. </p>
<p>
  <figure class="img-polaroid"> <img src="img/install-2.jpg" alt=""/></figure>
</p>
<p align="justify">You can
  modify module settings by clicking <strong>Edit</strong>. </p>
<p>
  <figure class="img-polaroid"> <img src="img/install-3.jpg" alt=""/></figure>
</p>
<p align="justify">By clicking the <strong>Install</strong> button, you will start module installation process. </p>
<p>
  <figure class="img-polaroid"> <img src="img/install-4.jpg" alt=""/></figure>
</p>
<p align="justify">In order to remove the module, you have
  to click the <strong>Uninstall</strong> button, so the module will be uninstalled. </p>
  <p>
  <figure class="img-polaroid"> <img src="img/install-5.jpg" alt=""/></figure>
</p>
<p class="alert alert-warning">Note that uninstalling the module means all the settings added earlier will be erased and
    cannot be reverted back.</p>


<p align="justify">When the installation is finished, you need to configure the module. The settings can differ depending on the
    specific module, but the
    <strong>Status</strong> field is the same for all — the field allows you to turn the module on/off.</p>

<figure class="img-polaroid">
    <img src="img/edit-module_1.jpg" alt=""/>
</figure>

<p>In order to add the module to a certain page, you need to go to the list of pages.</p>


        <p>In the side menu navigate to <strong>Design &gt; Layout</strong>).</p>

        <figure class="img-polaroid">
         <img src="img/edit-parallax-15.jpg" alt=""/>   
        </figure>
        
  

<p>In the page list select the page where you want to place the module and click <strong>Edit</strong>.</p>

<figure class="img-polaroid">
    <img src="img/edit-parallax-17.jpg" alt=""/>
</figure>
<p>You can see a list of the modules and module positions that are already added to the page.</p>
<figure class="img-polaroid">
    <img src="img/lay.jpg" alt=""/>
</figure>

<p>The following module positions are available (respectively to the display order):</p>

<ol class="index-list">
    <li>Header Top (after the Header section);</li>

    <li>Column-left (left side panel);</li>

    <li>Column-right (right side panel);</li>

    <li>Content-top;</li>

    <li>Content-bottom;</li>

    <li>Footer.</li>
</ol>
<p align="justify">The <strong>Sort Order</strong> field shows the display order of the modules assigned to the same position. In order
    to
    add a module to a page, click
    <strong>Add Module</strong>. Select the required module and module position and click
    <strong>Save</strong>. The module installation is completed. </p>
    <figure class="img-polaroid">
    <img src="img/layout.jpg" alt=""/>
</figure>