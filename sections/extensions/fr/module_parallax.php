<h3>TM Parallax</h3>

<p>Ce module vous permet de créer des zones avec l'effet de parallaxe. Le module offre la possibilité d'afficher des images, des vidéos et d'autres modules. Le module prend en charge le nombre illimité de couches, chacun peut se déplacer dans des directions différentes et avec la vitesses différentes.</p>

<figure class="img-polaroid">
    <img src="img/tm_parallax.png" alt=""/>
</figure>

<h5>Configuration du module TM Parallax:</h5>
<h6>Réglages généraux::</h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Nom du module (Module Name)</dt>
            <dd>nom du module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Statut (Status)</dt>
            <dd>état du module (activé/désactivé);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Image (Image)</dt>
            <dd>image de parallaxe;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Largeur (Width)</dt>
            <dd>largeur de l'image de fond;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Hauteur (Height)</dt>
            <dd>hauteur de l'image de fond;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Flou (Blur)</dt>
            <dd>image de fond flou dans le cas où sa taille (dimensions) est trop faible pour un affichage de haute qualité dans le domaine de parallaxea;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Direction (Direction)</dt>
            <dd>direction du mouvement de l'effet de parallaxe;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Vitesse (Speed)</dt>
            <dd>vitesse de déplacement de l'effet de parallaxe;</dd>
        </dl>
    </li>
</ol>

<h6>Paramètres supplémentaires:</h6>
<ol class="index-list">
    <li>
        <p>Afin de créer une nouvelle couche, vous devez appuyer sur le bouton <strong> "Ajouter une couche"("Add Layer")</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/tm_parallax_edit-1.png" alt=""/>
        </figure>

        <p>Après l'ajout de la couche spécifiez les paramètres suivants:</p>

        <h5>Configuration de couche:</h5>

        <ol class="index-list">
            <li>
                <dl class="inline-term">
                    <dt>Type (Type)</dt>
                    <dd>type de couche;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Description (Description)</dt>
                    <dd>description de la couche;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Image (Image)</dt>
                    <dd>image de couche;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Largeur (Width)</dt>
                    <dd>largeur de l'image de couche;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Hauteur (Height)</dt>
                    <dd>hauteur de l'image de couche;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Vitesse (Speed)</dt>
                    <dd>vitesse de déplacement de couche;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Disparition graduelle (Fade)</dt>
                    <dd>la couche va progressivement devenir visible de la pleine transparence à l'opacité complète en fonction de la position de défilement de la couche;
                    </dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Flou (Blur)</dt>
                    <dd>image de fond flou dans le cas où sa taille (dimensions) est trop faible pour un affichage de haute qualité dans le domaine de parallaxe. Il est applicable seulement aux couches de type "media";
                    </dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Direction (Direction)</dt>
                    <dd>direction du mouvement de la couche;</dd>
                </dl>
            </li>
        </ol>
    </li>
    <li>
        <p>Pour afficher le module dans la couche, vous devez appuyer sur le bouton <strong>"Ajouter un module" ("Add Module")</strong> dans la couche appropriée.</p>
        <figure class="img-polaroid">
            <img src="img/tm_parallax_edit-2.png" alt=""/>
        </figure>
        <p>Après avoir appuyé sur le bouton <strong>"Ajouter un module" ("Add Module")</strong>, le champ <strong>"Nom du module ajouté" ("Added Module name")</strong> sera disponible, vous devez sélectionner le module ici, pour l'afficher dans la couche actuelle.</p>
    </li>
</ol>

