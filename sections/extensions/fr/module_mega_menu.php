<h3>TM Megamenu</h3>

<p>Ce module vous permet de créer des menus complexes. Le module offre la possibilité d'afficher un seul élément (sans sous-menu), un élément de menu avec un sous-menu et un élément de menu avec sous-menu par un bloc multi-lignes et multi-colonnes. Le module peut être utilisé dans différentes positions, mais il affichera le même contenu.
</p>

<h5>Informations générales</h5>

<ol class="index-list">
    <li>
        <p>Activation du menu</p>

        <p>Pour activer mégamenu vous devez aller dans <strong>Extensions (Extensions) -> Modules (Modules) -> TM Megamenu (TM Megamenu)</strong>, appuyez sur le bouton <strong>(Modifier) Edit</strong> à côté de l'élément approprié et choisissez l'option <strong>Activer (Enabled)</strong> dans la nouvelle fenêtre.
        </p>
    </li>
</ol>

