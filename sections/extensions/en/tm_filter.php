<h3>TemplateMonster Filter</h3>
<p align="justify">This module enables customers to filter the currently viewed products by their attributes. </p>
<figure class="img-polaroid">
    <img src="img/filter.jpg" alt=""/>
</figure>
<h4 align="justify">How to manage TemplateMonster Filter module</h4>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p>Find the<strong> TemplateMonster Filter </strong>module. Press the <strong>edit</strong> button to configure the  module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
  <figure class="img-polaroid"> <img src="img/filter-2.jpg" alt=""/></figure>
</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid">
  <img src="img/filter-3.jpg" alt=""/>
</figure>
<h5>Basic module settings:</h5>
<ul class="marked-list">
  <li> <strong>Module Name</strong> — the name of the module;</li>
  <li><strong>Title</strong> — the title which will be displayed on the frontend;  </li>
  <li><strong>Store</strong> —  choose the store where you want to display the module;</li>
  <li><strong>Status</strong> — define the module status (enabled/disabled);</li>
  <li><div align="justify"><strong>Sort Order</strong> — this sort order determines the position of the module on the frontend. A sorting order of 1 would be displayed before a sorting order of 2, 3, and so on;</div></li>
  <li><strong>Position</strong> — you may select a module position from the list of pre-defined positions (Column Left\Fixed);</li>
  <li><strong>Layout</strong> — choose the layout where you want to display the module. If empty its available for all;</li>
</ul>