<h3> TM Footer Links</h3>
<h4 align="justify">Как управлять модулем  TM Footer Links</h4>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong>Модули / Расширения</strong> (Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p>Find the <strong>TM Footer Link</strong> section. Press the <strong>edit</strong> button to configure the existing module .В таблице с перечнем всех модулей, найдите раздел <strong>TM Footer Link</strong>. Нажав кнопку &quot;редактировать&quot; (edit), вы перейдете к окну настроек существующего модуля.</p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/footer-2.jpg" alt=""/></figure>
</p>
<p>Вы перейдете к следующему окну настроек:</p>
<figure class="img-polaroid">
  <img src="img/footer-3.jpg" alt=""/>
</figure>
<ul class="marked-list">
  <li><strong>Module Name</strong> — название модуля;</li>
  <li><strong>Links Group</strong> — выберите группу ссылок, которую планируете отображать в футере;</li>
  <li> <strong>Status</strong> — cтатус модуля (вкл./выкл.). </li>
</ul>
<h5>Frontend:</h5>
<p>
  <figure class="img-polaroid">
    <img src="img/footer-4.jpg" alt=""/>
  </figure>
</p>
<ul class="marked-list">
  <li><strong>Module Name</strong> — TM Footer Links Information;</li>
  <li><strong>Links Group</strong> — Information;</li>
  <li> <strong>Status</strong> — enabled (активирован). </li>
</ul>
