<h3>TemplateMonster Newsletter</h3>

<p>This module allows to add newsletter block with the subscription form.</p>
<figure class="img-polaroid">
    <img src="img/tm-newsletter-view.jpg" alt=""/>
</figure>

<p>When the module is installed, you can configure it on the module configuration page.</p>

<h5>Settings:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>the name of the module;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>text that will be seen just before filling in the subscription form;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>module status (enabled/disabled).</dd>
        </dl>
    </li>
</ol>
