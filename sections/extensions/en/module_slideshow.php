<h3>TemplateMonster Slideshow</h3>

<p>This module allows you to display Camera slideshow on your website. </p>

<p>When the module is installed, you can configure it on the module configuration page.</p>

<h5>Settings:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>the name of the module;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Banner</dt>
            <dd>should be selected from the earlier created
                <a href='#banner_create'>banner</a>
                list;
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>module image width;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>module image height;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>module status (enabled/disabled).</dd>
        </dl>
    </li>
</ol>

