<h3>TemplateMonster Lazy load</h3>

<p>Die Modifikation, die die Produktbilder währden der Zeit hochlädt, wann sie in den
    Anwendungsbereich beim Scrollen der Website fallen. Die jeweilige Erweiterung wird
    benutzt, um die Geschwiendigkeit der Website zu erhöhen. Um das Preloader-Bild zu
    ändern, ersetzen Sie das Bild <strong>lazy_loader.gif</strong>in dem Ordner <strong>catalog/view/theme/themeXXX/image/.</strong>
</p>