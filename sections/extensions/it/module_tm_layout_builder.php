<h3>TM Layout Builder</h3>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-edit.jpg" alt=""/>
</figure>
<p>Questo modulo serve per una veloce e comoda creazione delle variazioni per il nostro principale negozio.</p>

<h5>Impostazioni principali del modulo:</h5>
<ol class="index-list">
	<li>
		<dl class="inline-term">
			<dt>Stato</dt>
			<dd>Stato modulo (Abilitato/Disabilitato);</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Nome Modulo</dt>
			<dd>Il nome del modulo;</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Classe Modulo</dt>
			<dd>La classe del modulo;</dd>
		</dl>
	</li>
	<li>
		<dl class="inline-term">
			<dt>Modulo ID</dt>
			<dd>ID del modulo.</dd>
		</dl>
	</li>
</ol>
<p>Per creare il nuovo stile, premere <strong>Visualizzazione Nuovo Stile (New Layout View)</strong> pulsante, dopo premere <strong> Aggingere Riga (1) (Add Row
		(1))</strong> pulsante. Dopo aggiungere riga, e aggiungere colonne. Aggiungere colonne, e dopo premere <strong>Aggiungere Colonna (2) (Add
		Column (2))</strong> pulsante.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-column.jpg" alt=""/>
</figure>

<p>Il numero delle colonne nella riga è illimitato, ed è possibile aggiungere il numero di colonne di cui hai bisogno. La larghezza delle colonne può essere impostata dopo, aggiungendoli con l'aiuto della funzione drag and drop trascinando compreso il bordo destro della colonna. </p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-drag-drop.jpg" alt=""/>
</figure>

<p>Dopo l'aggiunta di colonne, si devono riempire di contenuti (moduli). Dopo aver aggiunto il modulo, premere <strong>Aggiungere Widget (Add Widget)</strong> pulsante e selezionare quello necessario dall'elenco a discesa.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-widget.jpg" alt=""/>
</figure>

<p>Dopo l'aggiunta di un modulo, è possibile modificarlo o eliminarlo,</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change.jpg" alt=""/>
</figure>

<p>oppure muoverlo in un'altra colonna.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change-position.jpg" alt=""/>
</figure>

<p>Dopo aver impostato tutte le righe, colonne e moduli, salvare il modulo Builder e visualizzarlo in una delle posizioni nella pagina principale del negozio che hai impostato.</p>