<h3>TemplateMonster Lazy load</h3>

<p>Website içeriği kaydırılırken, ürün görsellerinin yüklenmesini sağlayan araçtır. Bu şekilde sitenizin performansı hızlanır. Önyükleme görselini değiştirmek için
    <strong>catalog/view/theme/themeXXX/image/</strong> klasöründeki
    <strong>lazy_loader.gif</strong> dosyasını değiştirmeniz gerekir.</p>
