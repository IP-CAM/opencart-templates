<h3>TemplateMonster Pinterest</h3>

<p align="justify"><strong>TemplateMonster Pinterest</strong> module allows displaying Pinterest gallery on your store. This module can help you to display Pinterest Profile Pins or Board Pins. Width and Height are scalable.</p>

<figure class="img-polaroid">
    <img src="img/tm-pinterest-view.jpg" alt=""/>
</figure>

<p>When the module is installed, you can configure it on the module configuration page.</p>
<h4 align="justify">How to manage TemplateMonster Pinterest module</h4>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p>Find the <strong>TemplateMonster Pinterest</strong><strong> </strong>section. Press the <strong>edit</strong> button to configure the existing module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-pinterest-edit-2.jpg" alt=""/></figure>
</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid">
  <img src="img/tm-pinterest-edit.jpg" alt=""/>
</figure>
<h5>Available settings:</h5>
<ul class="marked-list">
  <li> <strong>Module Name</strong> — the name of the module;</li>
  <li><strong>Pinterest Page URL </strong>— full path to the page that displays the gallery;</li>
  <li> <strong>Width</strong> — the pixel width of the widget;
    
  </li>
  <li> <strong>Height</strong> — the pixel height of the widget; </li>
  <li> <strong>Status</strong> — module status (enabled/disabled).
    
  </li>
</ul>
<h4 align="justify">How to add a new TemplateMonster Pinterest module</h4>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">Find the <strong>TemplateMonster Pinterest </strong>section. Press the<strong> Add New </strong>button to create a new module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-pinterest-edit-3.jpg" alt=""/></figure>
</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid"> <img src="img/tm-pinterest-edit-4.jpg" alt=""/></figure>
<p>Fill in the required fields:</p>

<ul class="marked-list">
  <li> <strong>Module Name</strong> — enter the name of the module;</li>
  <li><strong>Pinterest Page URL </strong>— specify the full path to the page that displays the gallery;</li>
  <li> <strong>Width</strong> — set the pixel width of the widget;
    
  </li>
  <li> <strong>Height</strong> — set the pixel height of the widget; </li>
  <li> <strong>Status</strong> — define the module status (enabled/disabled).
    
  </li>
</ul>
<h4 align="justify">How to assign the new TemplateMonster Pinterest module to the Home Page (via Layout Builder)</h4>
<p align="justify">Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p>Find the <strong>Template Monster Layout Builder</strong> section. Navigate to the Home Page module and press the edit button.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-7.jpg" alt=""/></figure>
</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>You will be brought to the settings window:</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-8.jpg" alt=""/></figure>
</p>
<p>Choose the position where you want to place the <strong>New TemplateMonster Pinterest</strong> module. You can add a row or a column.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-9.jpg" alt=""/></figure>
</p>
<p>Press the <strong>Add Widget</strong> button.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-11.jpg" alt=""/></figure>
</p>
<p> In the popup window choose the <strong>TemplateMonster Pinterest </strong>module to add. </p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-pinterest-edit-5.jpg" alt=""/></figure>
</p>
<p>We have added the <strong>New  TemplateMonster Pinterest</strong> module.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-pinterest-edit-6.jpg" alt=""/></figure>
</p>
<p>Scroll the page up and click the <strong>Save</strong> button at the top right of the screen to apply the changes:
  <figure class="img-polaroid"> <img src="img/edit-parallax-14.jpg" alt=""/></figure>
</p>
<p align="justify" class="alert alert-info">You can use such method to assign any module type to the home page.</p>
<h4>How to assign the new TemplateMonster Pinterest module to any layout (via Layouts)</h4>
<p>Navigate to <strong>Design</strong> &gt; <strong>Layouts</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-15.jpg" alt=""/></figure>
</p>
<p align="justify">Choose the page (layout) you want the <strong>New  Pinterest</strong> module place on and press the edit button in the corresponding field in the action column.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-17.jpg" alt=""/></figure>
</p>
<p>Move to the <strong>Choose the position of the modules</strong> section.</p>
<p>Select the module position you want to place the module.</p>
<p>Find the required module in the dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-pinterest-edit-8.jpg" alt=""/></figure>
</p>
<p>Press the &quot;Add Module&quot; button.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-pinterest-edit-9.jpg" alt=""/></figure>
</p>
<p>The module was added successfully.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-pinterest-edit-10.jpg" alt=""/></figure>
</p>
<p>Now you can remove and edit the module. Simply press the appropriate button.</p>
<p>Scroll the page up and click the <strong>Save</strong> button at the top right of the screen to apply the changes:
  <figure class="img-polaroid"> <img src="img/edit-parallax-21.jpg" alt=""/></figure>
</p>
<p align="justify" class="alert alert-info">You can use this method to assign any module type to any layout of your online store.</p>