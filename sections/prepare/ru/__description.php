<h4>Хостинг</h4>

<p>Поскольку OpenCart базируется на приложениях PHP/MySQL, необходимо подготовить
    хостинг сервер.</p>
<p align="justify">При наличии уже активного сервера, необходимо проверить его на соответствие 
<a href="http://docs.opencart.com/requirements/" target="_blank"> системным требованиям</a>, а также на совместимость с платформой OpenCart.</p>
<p align="justify">Установить OpenCart на компьютер можно с помощью локального сервера. Для создания локального хостинг сервера необходимо использовать программное обеспечение <strong>WAMP</strong>, <strong>AppServ</strong>, <strong>MAMP</strong> и т.п. Любое, из которых, устанавливается через мастер-установки и совместимо с OpenCart. </p>

