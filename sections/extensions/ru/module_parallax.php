  <li> 
    <div align="justify"><strong>Размытие </strong> —  	вы можете активировать эффект размытия слишком маленьких изображений [относительно размера контейнера]; </div>
  </li>
  <li> 
    <div align="justify"><strong>Направление </strong> — укажите направления движения parallax. Normal — в противоположную сторону относительно движения прокрутки, Inverse - по направлению движения прокрутки; </div>
  </li>
  <li>
    <div align="justify"><strong>Скорость</strong> —  	задайте значение скорости эффекта parallax относительно скорости прокрутки. Значение должно быть в пределах от 0 до 2; </div>
  </li>
  <li> <strong>Статус</strong> — укажите состояние модуля (включен/отключен).
    </dt>
  </li>
</ul>
<p>Не забудьте сохранить настройки модуля, нажав кнопку &quot;<strong>Сохранить</strong>&quot; в правом верхнем углу:
  <figure class="img-polaroid"> <img src="img/edit-parallax-6.jpg" alt=""/></figure>
