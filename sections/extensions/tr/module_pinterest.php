<p>Modül kurulduktan sonra, tüm düzenlemeleri modül ayar sayfasından yapabilirsiniz.</p>

<h5>Ayarlar:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>modülün ismi;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Pinterest Page URL</dt>
            <dd>görüntülenecek olan galerinin tam bağlantı adresi;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>kutu genişliği (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>kutu uzunluğu (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>modülün durumu (aktif/pasif).</dd>
        </dl>
    </li>
</ol>

