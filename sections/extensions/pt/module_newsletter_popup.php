<h3>TemplateMonster Newsletter Popup</h3>

<p>Este módulo permite incluir uma janela de pop-up oferecendo a assinatura da lista de emails quando estiverem
    visitando seu site. Esse janela não aparece para usuários já cadastrados ou que já se inscreveram.</p>
<figure class="img-polaroid">
    <img src="img/tm-newsletter-popup-view.jpg" alt=""/>
</figure>

<p>Ao instalar o módulo, você pode alterar suas configurações na página de configurações do módulo.</p>

<h5>Configurações:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>o nome do módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background Image</dt>
            <dd>imagem de fundo do popup;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background image width</dt>
            <dd>largura da imagem de fundo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background image height</dt>
            <dd>altura da imagem de fundo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Cookie Storage Time</dt>
            <dd>duração do armazenamento do cookie;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Heading Title</dt>
            <dd>cabeçalho do popup;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>descrição do popup;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>situação do módulo (ativado/desativado).</dd>
        </dl>
    </li>
</ol>
