<h3>Installation sur une boutique existante</h3>
<p>Dans le cas où vous avez déjà une boutique OpenCart existante et vous voulez juste changer son apparence à l'aide du
    thème, utilisez la méthode d'installation suivante.</p>
<p class="alert alert-warning">Assurez-vous que votre version d'OpenCart respecte les besoins du template. Toutes les
    versions d'OpenCart sont disponibles sur le
    <a href=" http://www.OpenCart.com/index.php?route=download/download" target="_blank" class="spec_link">site officiel
        d'OpenCart</a>
    .
</p><br/>
<ol class="index-list">
    <li>Pour installer un template d'OpenCart, vous devez uploader les fichiers sur le serveur. Vous pouvez trouver
        l'archive
        <strong> &quot;themeXXX(update).zip&quot;</strong> dans le dossier d'OpenCart, elle doit être décompressée à
        votre ordinateur.
    </li>

    <li>Une fois que c'est fait, uploadez les fichiers décompressés dans le dossier racine de l'installation d'OpenCart.
        <br/> Vous pouvez consulter les instructions vidéo détaillées sur
        <a href="/help/how-upload-files-server-2.html" target="_blank">comment uploader des fichiers sur le serveur</a>
        . <br/>
        <strong>les fichiers de template n'affectent, en aucune manière, le contenu de votre boutique;</strong>
    </li>
    <li>Ouvrez votre site Web dans un navigateur et ajoutez /install à la fin de l'URL de votre site dans la barre
        d'adresse (
        <i>par exemple, http://votre_nom_de_domaine/OpenCart/install</i>
        )
    </li>

    <li>Cliquez sur le bouton <strong>Continue</strong>. Cela peut prendre un certain temps. </li>

    <li>Connectez-vous à votre panneau d'administration, allez à <strong>Extensions ->
            Extension
            Installer </strong> et uploadez les fichiers de l'archive
        <strong>themeXXX(extensions)</strong>. Une fois que tous les fichiers sont téléchargés, allez à <strong>Extensions
            ->
            Modifications</strong> et cliquez
        <strong>refresh</strong> en haut à droite de l'écran; </li>

    <li>Sur l'étape suivante allez à
        <strong>System->Settings</strong>, allez à l'onglet Store et sélectionnez le template
        <strong>themeXXX</strong> dans le champ Template. </li>
</ol>
<p class="alert alert-info">N'hésitez pas à consulter le guide d'installation détaillé:
