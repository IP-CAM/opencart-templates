<h3>Installation over existing store</h3>

<p class="alert alert-info custom">If you don't want to spend your time installing the template, don't hesitate to <span class="label label-warning opener" onclick="showChat()">order</span> installation service using our live-chat.</p>

<p align="justify">In case, you already have a ready OpenCart store and you just need to change its appearance with the help
    of the theme, use the following installation method.</p>

<p align="justify" class="alert alert-warning">Make sure your OpenCart version is corresponding to the template requirements. All the
    OpenCart version are available at the <a
        href=" http://www.opencart.com/index.php?route=download/download"
        target="_blank" class="spec_link">official OpenCart website</a>.
</p>
<ol class="index-list">
<li>
  <div align="justify">In order to install an OpenCart template, you need to upload the files to the server. You can find the <strong>
            &quot;themeXXX(update).zip&quot;</strong> archive in the OpenCart folder, it should be unzipped to your
        computer.
    </div>
</li>
    <li>
      <div align="justify">After that, upload the unzipped files to the root directory of the OpenCart installation. <p></p>
        You can check the detailed video instructions on <a href="/help/how-upload-files-server-2.html" target="_blank">how
            to upload files to the server</a>.</div></li>
          <p align="justify" class="alert alert-danger"><strong>The template files will not affect your store content in any way.</strong></p>
      
    
    <li>
      <div align="justify">Open your website with any browser and add /install at the end of your site URL in the browser address bar (<i>for
            example,<strong> http://your_domain_name/opencart/install</strong>
      </i>
        )
      </div>
    </li>
    <li>
      <div align="justify">Click the <strong>Continue</strong> button. It may take some time.</div>
    </li>
    <li>
      <div align="justify">Enter the administration panel, navigate to <strong>Extensions >
            Extension
            Installer </strong> and upload the <strong>themeXXX(extensions)</strong> archive files. When all the files
        have been uploaded, navigate to <strong>Extensions >
            Modifications</strong> and press <strong>refresh</strong> in the top right side of the screen;
      </div>
    </li>
    <li>
      <div align="justify">On the next step navigate to <strong>System > Settings</strong>, go to the Store tab and select
        <strong>themeXXX</strong> template in the Template field.
      </div>
    </li>
</ol>

