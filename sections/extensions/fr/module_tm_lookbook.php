<h3>TM Lookbook</h3>

<p>The lookbook est un ensemble de photos, où vous pouvez marquer une partie de l'image. Ce module permet de visualiser les produits d'une ou de plusieurs marques.</p>
<p>Le module a deux modes d'affichage pour l'affichage dans un magasin: sous la forme d'une bannière (1) ou sous forme d'une collection (2).</p>


