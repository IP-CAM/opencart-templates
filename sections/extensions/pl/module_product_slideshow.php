<h3>TM Product Slideshow</h3>

<p>Ten moduł pozwala wyświetlać wybrane produkty za pomocą funkcji kazureli.</p>

<figure class="img-polaroid">
    <img src="img/tm-product-slideshow-view.jpg" alt=""/>
</figure>

<p>Kiedy moduł został zainstalowany, możesz konfigurować go na stonie konfiguracji
    modułu </p>

<h5>Ustawienia:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd> nazwa modułu;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Products</dt>
            <dd>wybór listy produktów;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd> liczba wyświetlanych produktów;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd> szerokość obrazka produktu;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>wysokość obrazka produktu;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd> status modułu (włączony/wyłączony).</dd>
        </dl>
    </li>
</ol>

