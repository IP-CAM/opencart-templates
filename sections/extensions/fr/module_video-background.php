<h3>TemplateMonster Video Background</h3>

<p>Ce module permet d'ajouter un bloc avec la vidéo d'arrière-plan (vous pouvez uploader la vidéo depuis votre site web
    ou depuis YouTube) .</p>

<p>Une fois le module installé, vous pouvez le configurer sur la page de configuration de module.</p>

<h5>Paramètres:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>le nom du module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>le statut du module (activé/désactivé)</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Video Source</dt>
            <dd>la source vidéo. Vous disposez de deux options:</dd>
        </dl>
        <ul class="marked-list">
            <li>
                <dl class="inline-term">
                    <dt>local</dt>
                    <dd>le chargement depuis votre site web;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>youtube</dt>
                    <dd>le chargement depuis YouTube.</dd>
                </dl>
            </li>
        </ul>
    </li>
    <li>
        <dl class="inline-term">
            <dt>YouTube url</dt>
            <dd>le lien d'une vidéo YouTube;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mute</dt>
            <dd>désactiver le volume;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile</dt>
            <dd>activer/désactiver la vidéo sur un appareil mobile. Si 'Yes', la vidéo sera affichée sur les appareils
                mobiles;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Start Video</dt>
            <dd>le moment précis où la vidéo démarre;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile Image</dt>
            <dd>une image qui apparait au lieu de la vidéo d'arrière-plan si la vidéo est désactivée pour les appareils
                mobiles;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile Image Width</dt>
            <dd>la largeur de l'image d'arrière-plan;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile Image Height</dt>
            <dd>la hauteur de l'image d'arrière-plan;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>html-balisage qui sera affiché devant la vidéo.</dd>
        </dl>
    </li>
</ol><p>Si vous choisissez
    <strong>Video Source - Local Video</strong>, veuillez définir le format ".jpg" pour une image (une bannière) qui
    sera utilisée pour les appareils mobiles. Le dossier qui contient l'image d'arrière-plan devrait également contenir
    trois fichiers vidéo avec le même nom et dans les formats suivants:
</p>

<ul class="marked-list">
    <li>*.ogv</li>

    <li>*.mp4</li>

    <li>*.webm</li>
</ul>


