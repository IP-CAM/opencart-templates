<h3>TemplateMonster Instagram</h3>

<p>Данный модуль отображает фотографии из сети Instagram в виде списка картинок с отображением количества лайков</p>
<figure class="img-polaroid">
    <img src="img/tm-instagram-view.jpg" alt=""/>
</figure>

<p>После установки модуля, Вы можете настроить его на странице настроек модуля.</p>

<h5>Настройки:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>наименование модуля;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Select Get</dt>
            <dd>выбор, каким образом получать изображения. Доступны 2 значения (user,tagged), что соответственно
                означает, получать изображения либо конкретного пользователя, либо по хештегу;
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry TagName</dt>
            <dd>тег, по которому будут выводиться изображения (используется, если опция "Select get" имеет значение
                tagged);
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry ClientID</dt>
            <dd>Ваш API идентификатор из instagram.com (используется, если опция "Select get" имеет значение tagged);
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry UserId</dt>
            <dd>уникальный идентификатор пользователя (используется, если опция "Select get" имеет значение user);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry accessToken</dt>
            <dd>маркер доступа для отображения пользовательских изображений (используется, если опция "Select get" имеет
                значение user);
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry limit</dt>
            <dd>устанавливает ограничение на количество отображаемых изображений;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>статус модуля (включен/отключен).</dd>
        </dl>
    </li>
</ol>
