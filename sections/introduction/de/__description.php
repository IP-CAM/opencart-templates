<h3>Was ist eigentlich OpenCart?</h3>

<p>OpenCart ist eine kostenlose E-Commerce-Plattform mit offenliegendem Quellcode. OpenCart ermöglicht es
    professionelle
    und sichere Basis zu erstellen, um einen erlogreichen Online-Shop aufzubauen. Zur Opencart Gemeinschaft gehören
    mehrere Benutzer wie: erfahrene Web-Entwickler, die bequemes Interface suchen, Inhaber eines Online-Shops, die
    zum
    ersten Mal eigenes Online Business starten. OpenCart stellt Ihnen eine Menge Funktionen zur Verfügung, damit Sie
    Ihren Online-Shop völlig verwalten können. Dank der Werkzeuge, die Ihnen OpenCart anbietet, ist es möglich
    solchen
    Online-Shop zu erstellen, der allen Ihren Zwecken entsprechen wird.
    <a href="http://docs.opencart.com" target="_blank">Mehr</a>
</p>


