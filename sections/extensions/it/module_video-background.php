<h3>TemplateMonster Video Background</h3>

<p>Questo modulo consente di aggiungere un blocco con video di sfondo (caricato dal tuo sito Web o condiviso da
    YouTube).</p>

<p>Dopo aver installato il modulo, sarà possibile configurarlo nell'apposita pagina di configurazione.</p>

<h5>Impostazioni:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>il nome del modulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>lo stato del modulo (abilitato/disabilitato)</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Video Source</dt>
            <dd>fonte video. Sono disponibili 2 opzioni:</dd>
        </dl>
        <ul class="marked-list">
            <li>
                <dl class="inline-term">
                    <dt>local </dt>
                    <dd>per caricare il video dal tuo Sito Web;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>youtube</dt>
                    <dd>per caricare il video da YouTube.</dd>
                </dl>
            </li>
        </ul>
    </li>
    <li>
        <dl class="inline-term">
            <dt>YouTube url</dt>
            <dd>link video YouTube;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mute</dt>
            <dd>per disabilitare l'audio;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile</dt>
            <dd>per abilitare/disabilitare il Video sui dispositivi mobili. Selezionando 'Yes', il video apparirà anche
                sulla versione del sito per dispositivi mobili;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Start Video</dt>
            <dd>il punto esatto da cui far iniziare la riproduzione del video;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile Image</dt>
            <dd>l'immagine che comparirà sui dispositivi mobili nel caso in cui il video fosse disabilitato;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile Image Width</dt>
            <dd>l'ampiezza dell'immagine di sfondo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile Image Height</dt>
            <dd>l'altezza dell'immagine di sfondo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>html-markup che apparirà dal video.</dd>
        </dl>
    </li>
</ol>
<p>Se si seleziona<strong>Video Source - Local Video</strong>, imposta un'immagine (banner) in formato ".jpg", da usare
    per i dispositivi mobili. La cartella contenente le immagini di sfondo dovrebbe anche includere 3 file video con lo
    stesso nome e nei seguenti formati:
</p>

<ul class="marked-list">
    <li>*.ogv</li>

    <li>*.mp4</li>

    <li>*.webm</li>
</ul>


