<h3>TM Lazy load</h3>

<p>Modificação que permite carregar as imagens de produto apenas quando o usuário puder visualizá-los ao navegar pelo site. Essa extensão permite melhorar a performance e a velocidade do site. Para mudar a imagem de carregamento, você deve trocar o arquivo
    <strong>lazy_loader.gif</strong> na pasta <strong>catalog/view/theme/themeXXX/image/</strong>.</p>