<h3>Comments Plugin</h3>

<p align="justify">The comments plugin lets people comment on content on your site using their Facebook account. People can choose to share their comment activity with their friends (and friends of their friends) on Facebook as well. The comments plugin also includes built-in moderation tools and social relevance ranking.</p>
<figure class="img-polaroid">
    <img src="img/comment.png" alt=""/>
</figure>
<h4>How configure the Comments Plugin</h4>
<p>Navigate to <strong>Catalog</strong> &gt;<strong> Information</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/comment-1.jpg" alt=""/></figure>
</p>
<p>Press the<strong> edit </strong>button next to the <strong>About</strong> information list.</p>
<p>
  <figure class="img-polaroid"> <img src="img/comment-2.jpg" alt=""/></figure>
</p>

<p>You will be brought to the settings window:</p>
<p>
  <figure class="img-polaroid"><img src="img/comment-3.jpg" alt=""/></figure>
</p>
<p>Press  the<strong> Code View</strong> button in the  editor toolbar.</p>
<p>
  <figure class="img-polaroid"><img src="img/comment-4.jpg" alt=""/></figure>
</p>
<p>Insert the <strong>page URL </strong>you want people to comment.</p>
<p>You need to insert the URL after the &lt;<strong>div class=&quot;fb-comments&quot; data-href=&quot;</strong> code part.</p>
<p>
  <figure class="img-polaroid"><img src="img/comment-5.jpg" alt=""/></figure>
</p>
<p>When complete, tab<strong> Save</strong>.</p>

