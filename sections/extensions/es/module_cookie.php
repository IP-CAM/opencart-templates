<h3>TemplateMonster Cookie Policy</h3>

<p>En este módulo informa a los visitantes que este sitio web utiliza cookies. Bajo los términos de la Ley de
    las cookies, debe agregar los módulos adecuados a su sitio web que va a mostrar un mensaje de advertencia acerca de
    las cookies utilizadas en el sitio.</p>

<p>Las cookies son pequeños archivos de texto que los sitios web colocan en los ordenadores de los visitantes. Se
    utilizan generalmente para identificar a los visitantes y ofrecerles la mejor experiencia. Las cookies son una parte
    crucial de varias funciones básicas de las páginas web.</p>

<p>Por ejemplo, es generalmente las cookies que permiten a las tiendas online a recordar los elementos que tiene en su
    carrito. También se utilizan para mantenerle conectado a una página web o para proporcionar importantes estadísticas
    e información sobre el uso de los propietarios del sitio web.</p>

<p>Si usted tiene un sitio web es seguro que está utilizando cookies. Su objetivo principal es controlar el número de
    visitantes y comportamientos a través de herramientas como Google Analytics. También se puede utilizar para mostrar
    anuncios relevantes a los visitantes, o - si usted vende en online - para resaltar los elementos clave de su sistema
    de compra en línea.</p>

<p>26th May 2012 - El ICO a traído un cambio en la aplicación de "consentimiento implícito" está permitido, siempre y
    cuando quede claro dónde está su política de privacidad/cookies.</p>
