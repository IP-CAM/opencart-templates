<h3>TM Megamenu</h3>

<p>Bu modül sayesinde kompleks menüler oluşturabilirsiniz. Modül; alt menüsüz tek öge, alt menülü öge ve çok satırlı ve çok sütunlu kutucuğa sahip alt menülü öge oluşturmanızı sağlar. Modül, aynı içerikle çeşitli konumlarda yer alabilir.
</p>

<h5>Genel bilgi</h5>

<ol class="index-list">
    <li>
        <p>Menü etkinleştirilmesi</p>

        <p>Megamenüyü etkinleştirmek için <strong>Bileşenler -> Modüller -> TM Megamenu</strong> sayfasına girip, <strong>Düzenle</strong> düğmesine basın. Yeni pencereden <strong>Etkinleştir</strong> seçeneğine tıklayın.
        </p>
    </li>
</ol>

