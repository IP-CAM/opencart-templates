<h3>TemplateMonster Pinterest</h3>

<p>El módulo TemplateMonster Pinterest permite mostrar la galería Pinterest en su tienda. </p>

<figure class="img-polaroid">
    <img src="img/tm-pinterest-view.jpg" alt=""/>
</figure>

<p>Cuando se instala el módulo, se puede configurar en la página de configuración del módulo.</p>

<h5>Parámetros:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>el nombre del módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Pinterest Page URL</dt>
            <dd>ruta completa a la página que muestra la galería;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>el ancho del bloque (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>el largo del bloque (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>estado del módulo (activado/desactivado).</dd>
        </dl>
    </li>
</ol>

