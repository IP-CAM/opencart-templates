<h3>TM Parallax</h3>

<p>Este módulo permite que você crie áreas com efeito de parallax nas páginas. O módulo possibilita mostrar imagens, vídeo e outros módulos. O módulo suporta um número ilimitade de camadas, as quais podem ser movidas em direções diferentes e a velocidades diferentes.</p>

<figure class="img-polaroid">
    <img src="img/tm_parallax.png" alt=""/>
</figure>

<h5>Configuração do módulo TM Parallax:</h5>
<h6>Configurações gerais:</h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>nome do módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>situação do módulo (ativo/inativo);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Image</dt>
            <dd>a imagem de parallax de fundo (principal);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>largura da imagem de fundo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>altura da imagem de fundo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Blur</dt>
            <dd>desfoque na imagem de fundo caso a resolução da imagem seja muito baixa para exibir na área em alta definição do parallax;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Direction</dt>
            <dd>direção do efeito parallax;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Speed</dt>
            <dd>velocidade do efeito parallax;</dd>
        </dl>
    </li>
</ol>

<h6>Configurações adicionais do módulo:</h6>
<ol class="index-list">
    <li>
        <p>Para criar uma nova camada você precisa clicar em <strong>"Add Layer" (Incluir Camada)</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/tm_parallax_edit-1.png" alt=""/>
        </figure>

        <p>Após inlcuir a camada preencha as configurações dela:</p>

        <h5>Configuração de camada:</h5>

        <ol class="index-list">
            <li>
                <dl class="inline-term">
                    <dt>Type</dt>
                    <dd>tipo de camada;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Description</dt>
                    <dd>descrição da camada;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Image</dt>
                    <dd>imagem da camada;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Width</dt>
                    <dd>largura da imagem da camada;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Height</dt>
                    <dd>altura da imagem da camada;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Speed</dt>
                    <dd>velocidade da camada;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Fade</dt>
                    <dd>a camada vai se tornar visível gradualmente de transparência total até opacidade total dependendo da posição de rolagem da página;
                    </dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Blur</dt>
                    <dd>a imagem de fundo vai desfocar no caso da resolução (dimensões) seja muito baixo para exibir na área em alta-definição do parallax. É aplicável apenas em tipos de camada "Media";
                    </dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Direction</dt>
                    <dd>direção do movimento da camada;</dd>
                </dl>
            </li>
        </ol>
    </li>
    <li>
        <p>Para exibir o módulo na camada, você precisa clicar em <strong>"Add Module" (Incluir Módulo)</strong> na camada desejada.</p>
        <figure class="img-polaroid">
            <img src="img/tm_parallax_edit-2.png" alt=""/>
        </figure>
        <p>Após clicar em <strong>"Add Module"</strong>, o campo <strong>"Added Module name" (Nome do módulo a adicionar)</strong> ficará disponível, você pode escolher o módulo aqui, para mostrar na camada atual.</p>
    </li>
</ol>

