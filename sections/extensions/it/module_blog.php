<h4>TM Blog Articles</h4>

<p>Questo modulo permette di visualizzare gli articoli del blog per categorie.</p>

<figure class="img-polaroid">
    <img src="img/tm-blog-articles-view.jpg" alt="">
</figure>

<p>Dopo aver installato il modulo è possibile configurarlo nella pagina delle impostazioni del modulo.</p>

<h5>Impostazioni:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Lo Stato</dt>
            <dd> stato del modulo (abilitato\disabilitato).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Nome Modulo</dt>
            <dd> il nome del modulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limite Articolo</dt>
            <dd> numero di articoli da visualizzare;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Readmore Mostra pulsante</dt>
            <dd> abilita/disabilita Read more pulsante;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostra Data</dt>
            <dd> abilita/disabilita data;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostra Autore</dt>
            <dd> abilita/disabilita articolo autore;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Visualizza Commenti</dt>
            <dd> abilita/disabilita il numero di commenti per ogni articolo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostra Immagine</dt>
            <dd> abilita/disabilita immagine articolo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Larghezza Immagine</dt>
            <dd> larghezza dell'immagine;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Altezza Immagine</dt>
            <dd> altezza dell'immagine;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Descrizione Simbolo Limite</dt>
            <dd> numero di simboli nella descrizione articolo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Categoria</dt>
            <dd>  è possibile impostare la categoria da trarre per articoli da. C'è anche la possibilità di visualizzare gli articoli più popolari o articoli aggiunti di recente. 
            </dd>
        </dl>
    </li>
</ol>

