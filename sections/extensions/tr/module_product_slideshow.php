<h3>TM Product Slideshow</h3>

<p>This module allows to display selected products with the carousel view.</p>
<figure class="img-polaroid">
    <img src="img/tm-product-slideshow-view.jpg" alt=""/>
</figure>

<p>When the module is installed, you can configure it on the module configuration page.</p>

<h5>Settings:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>the name of the module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Products</dt>
            <dd>selection of the product list;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd>the number of displayed products;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>product image width;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>product image height;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>module status (enabled/disabled).</dd>
        </dl>
    </li>
</ol>
