<h2 class="item1">Vorbereitung</h2>

<h6>Bevor Sie anfangen, Ihre OpenCart Website zu installieren, müssen Sie alles gründlich vorbereiten. </h6>

<h4>Software</h4>

<p>Damit die OpenCart Vorlage korrekt funktioniert, empfehlen wir Ihnen alle erforderlichen Anwendungen herunterzuladen.
    Die Liste der erforderlichen Software befindet sich auf der Vorschauseite Ihrer Vorlage. Anforderungen an
    verschiedene Vorlage können sich unterscheiden, deswegen nennen wir nur allgemeine Anforderungen:</p>

<ol class="index-list">
    <li> Sie benötigen das Programm Adobe Photoshop. Es wird bei der Bearbeitung von Quelldateien .PSD benutzt. Außerdem
        brauchen Sie dieses Programm, wenn Sie entweder das Grafikdesign oder Bilder Ihrer Vorlage ändern möchten.
    </li>
    <li>Um den Quellcode Ihrer Vorlagendateien zu bearbeiten, sollen Sie den Code-Editor wie, zum Beispiel, Adobe
        Dreamweaver, Notepad++, Sublime Text usw. nutzen.
    </li>
    <li>Um Dateien aufs Hosting hochzuladen, brauchen Sie den FTP-Manager wie Total Commander, FileZilla, CuteFTP usw.
    </li>
    <li>Nutzen Sie die folgenden Anleitungen, um Archive mit Hilfe von <a
            href="/help/extract-archived-files-windows-os.html" target="_blank"> WinZip(PC) </a> und <a
            href="/help/extract-archived-files-mac-os.html" target="_blank"> StuffitExpander(MAC)</a> zu entpacken.
    </li>
</ol>

<h4>Hosting</h4>

<p>OpenCart basiert auf solche Anwendungen wie PHP/MySQL, aus diesem Grund müssen Sie den Hosting-Server
    vorbereiten. </p>

<p>Wenn Sie einen aktiveren Server haben, müssen Sie dann überprüfen, ob er den Systemanforderungen <a
        href="http://docs.opencart.com/system/server-requirements" target="_blank"> entspricht </a> und ob Ihre OpenCart
    Website auf ihm korrekt funktionieren wird. </p>

<p>Sie können OpenCart auch auf Ihrem Computer mit Hilfe von einem lokalen Server installieren. Um einen lokalen
    Hosting-Server zu erstellen, müssen Sie die Software wie <b>WAMP</b>, <b>AppServ</b>, <b>MAMP</b> usw. nutzen. Jede
    von ihnen wird wie ein Programm, das OpenCart unterstützt, installiert. </p>

<p>Sie können die folgenden Anleitungen nutzen, um den lokalen Server einzustellen:</p>

