<h3>TemplateMonster Instagram</h3>

<p>This module displays the Instagram social network pictures including the number of likes in a list.</p>
<figure class="img-polaroid">
  <img src="img/tm-instagram-view.jpg" alt=""/>
</figure>

<p>When the module is installed, you can configure it on the module configuration page.</p>
<h4>How to manage TemplateMonster Instagram module</h4>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/> </figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/> </figure>
</p>
<p>Find the<strong> TemplateMonster Instagram</strong> section. Press the <strong>edit</strong> button to configure the existing module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
  <figure class="img-polaroid"> <img src="img/instagram-1.jpg" alt=""/> </figure>
</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid"> <img src="img/instagram-14.jpg" alt=""/> </figure>
<h5>Available settings:</h5>
<ol class="marked-list">
  <li><strong>Module Name</strong> — the name of the module;</li>
  <li>
  <div align="justify"><strong>Select Get</strong> — how to get the images. There are 2 values available (user and tagged), which means either to get the images of the exact user or using hashtag;</div>
  </li>
  <li>
  <div align="justify"><strong>Entry TagName</strong> — tag that is used to display the images (it is used if you previously selected tagged in the &quot;Select get&quot; field);</div>
  </li>
  <li><strong>Entry Client ID</strong> — your API ID at instagram.com (it is used if you previously selected tagged in the &quot;Select get&quot; field); </li>
  <li><strong>Entry User ID</strong> — user ID (it is used if you previously selected user in the &quot;Select get&quot; field);</li>
  <li><strong>Entry accessToken </strong>— token to display custom image (it is used if you previously selected user in the &quot;Select get&quot; field); </li>
  <li><strong>Entry limit</strong> — limit of entries;</li>
  <li><strong>Status </strong>— module status (enabled/disabled). </li>
  </ol>

