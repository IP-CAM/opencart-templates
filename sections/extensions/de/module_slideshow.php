<h3>TemplateMonster Slideshow</h3>

<p>Das jeweilige Modul ermöglicht es den Camera Slider auf Ihrer Website anzuzeigen. </p>

<p>Nachdem Sie das Modul installiert haben, können Sie es auf der Konfigurationsseite einstellen.</p>


<h5>Einstellungen:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>der Modulname;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Banner</dt>
            <dd>wählen Sie ein Banner aus der Liste, die Sie vorher
                <a href='#banner_create'>erstellt haben</a>
                ;
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>die Bildbreite für das Modul;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>die Bildhöhe für das Modul;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>der Modulstatus (aktviert/deaktiviert).</dd>
        </dl>
    </li>
</ol>

