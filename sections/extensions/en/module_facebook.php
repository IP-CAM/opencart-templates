<h3>TM Facebook Box</h3>

<p>The module allows to display Facebook like box that shows the fans who like your website and brand.</p>

<figure class="img-polaroid">
    <img src="img/tm-facebook-view.jpg" alt=""/>
</figure>

<p>When the module is installed, you can configure it on the module settings page.</p>

<h5>Settings:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>the name of the module;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Facebook Page URL</dt>
            <dd>Facebook page link;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Color Scheme</dt>
            <dd>color scheme (light, dark);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Show Faces</dt>
            <dd>show users faces (yes, no);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Show Stream</dt>
            <dd>show recent posts (yes, no);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Show Header</dt>
            <dd>show block titles (yes, no);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Show Border</dt>
            <dd>show block border (yes, no);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>block width (px);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>block height (px);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>module status (enabled/disabled).</dd>
        </dl>
    </li>
</ol>

