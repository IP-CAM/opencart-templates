    <img src="img/edit-module_1.jpg" alt=""/>
</figure>


<p>In order to add the module to a certain page, you need to go to the list of pages. It can be done in 2 ways:</p>
<ol class="index-list">
    <li>
        <p>In the side menu navigate to <strong>System -> Design -> Layout</strong>;</p>

        <figure class="img-polaroid">
            <img src="img/page-selecting-way.jpg" alt=""/>
        </figure>
    </li>
    <li>
        <p>In the nodule section click the link <strong>here</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/page-selecting-way-2.jpg" alt=""/>
        </figure>
    </li>
</ol>


<p>In the page list select the page where you want to place the module and click
    <strong>Edit</strong></p>

<figure class="img-polaroid">
    <img src="img/list-of-pages.jpg" alt=""/>
</figure>


<p>You can see a list of the modules and module positions that are already added to the page</p>


<p>The following module positions are available (respectively to the display order):</p>

<ol class="index-list">
    <li>Header Top (after the Header section);</li>
    <li>Column-left (left side panel);</li>
    <li>Column-right (right side panel);</li>
    <li>Content-top;</li>
    <li>Content-bottom;</li>
    <li>Footer.</li>
</ol>

<p>The <strong>Sort Order</strong> field shows the display order of the modules assigned to the same position.
    In order to add a module to a page, click <strong>Add Module</strong>. Select the required module and module
    position and click <strong>Save</strong>. The module installation is completed. </p>

