</figure>

<h5>Impostazioni:</h5>
<p>Impostazioni principali del modulo sono le seguenti:</p>

<ol class="index-list">
    <li>
        <dl class="inline-term">
                <dt>Modulo Nome</dt>
                <dd>modulo titolo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Stato</dt>
            <dd>stato modulo (abilitato/disabilitato);</dd>
        </dl>
    </li>
    
        
    
    <li>
        <dl class="inline-term">
            <dt>Data</dt>
            <dd>Data in yyyy-mm-dd hh:mm:ss formato (e.g.: 2012-12-12 00:00:00);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Tipo</dt>
            <dd>Tipo di contatore (standard/radiale);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Descrizione</dt>
            <dd>didascalia timer (descrizione);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Titolo su Giorni</dt>
            <dd>contatore giorni titolo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Titolo su Ore</dt>
            <dd>Titolo contatore ore;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Titolo su Minuti</dt>
            <dd>titolo contatore minuti;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Titolo su Secondi</dt>
            <dd>titolo contatore dei secondi;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostra giorni</dt>
            <dd>se per mostrare/nascondere giorni contatore;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostra ore</dt>
            <dd>se per mostrare/nascondere ore contatore;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostra minuti</dt>
            <dd>se per mostrare/nascondere minuti contatore;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mostra secondi</dt>
            <dd>se per mostrare/nascondere secondi contatore;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
