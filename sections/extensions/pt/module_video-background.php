<h3>TemplateMonster Video Background</h3>

<p>Este módulo permite incluir um bloco com vídeo de fundo (ou enviado do seu site ou compartilhado no YouTube) .</p>

<p>Após instalado, o módulo pode ser alterado na página de configurações de módulo .</p>

<h5>Configurações:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>o nome do módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>situação do módulo (ativo/inativo)</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Video Source</dt>
            <dd>origem do vídeo. Você tem duas opções:</dd>
        </dl>
        <ul class="marked-list">
            <li>
                <dl class="inline-term">
                    <dt>local</dt>
                    <dd>carregar do seu site;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>youtube</dt>
                    <dd>carregar do YouTube.</dd>
                </dl>
            </li>
        </ul>
    </li>
    <li>
        <dl class="inline-term">
            <dt>YouTube url</dt>
            <dd>endereço do vídeo no YouTube;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mute</dt>
            <dd>desativa o volumo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile</dt>
            <dd>ativa/desativa vídeo para dispositivos móveis. SE 'Yes', o vídeo será mostrado em dispositivos móveis;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Start Video</dt>
            <dd>o momento exato em que o vídeo começa;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile Image</dt>
            <dd>imagem que será mostrada em dispositivos móveis no lugar do vídeo desativado;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile Image Width</dt>
            <dd>largura da imagem de fundo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile Image Height</dt>
            <dd>altura da imagem de fundo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>marcação html do conteúdo do vídeo.</dd>
        </dl>
    </li>
</ol><p>Se escolher
    <strong>Video Source - Local Video</strong>, defina uma imagem (banner) em formato ".jpg", isso será usado por dispositivos móveis. A pasta que contém a imagem de fundo também deve conter 3 arquivos de vídeo com o mesmo nome da imagem nos seguintes formatos:
</p>

<ul class="marked-list">
    <li>*.ogv</li>

    <li>*.mp4</li>

    <li>*.webm</li>
</ul>


