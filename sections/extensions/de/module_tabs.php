<h3>TemplateMonster Module Tabs</h3>

<p>Das jeweilige Modul zeigt 4 Hauptmodule (Bestsellers, Featured, Specials, Latest) als Tabs an.</p>

<figure class="img-polaroid">
    <img src="img/tm-moduletabs-view.jpg" alt=""/>
</figure>

<p>Nachdem Sie das Modul installiert haben, können Sie es auf der Konfigurationsseite einstellen.</p>

<h5>Einstellungen:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>der Modulname;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Specials</dt>
            <dd>aktiviert/deaktiviert die Darstellung vom Specials Modul;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Bestsellers</dt>
            <dd>aktiviert/deaktiviert die Darstellung vom Bestsellers Modul;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Latest</dt>
            <dd>aktiviert/deaktiviert die Darstellung vom Latest Modul;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Featured</dt>
            <dd>aktiviert/deaktiviert die Darstellung vom Featured Modul;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Products</dt>
            <dd>die Auswahl der Produktliste für den Tab Featured;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd>legt die Beschränkung auf die Anzahl der angezeigten Produkten fest;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>die Breite des Produktbildes;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>die Höhe des Produktbildes;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>der Modulstatus (aktviert/deaktiviert).</dd>
        </dl>
    </li>
</ol>
