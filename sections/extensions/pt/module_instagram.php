<h3>TemplateMonster Instagram</h3>

<p>Este módulo exibe fotos do Instagram incluindo o número de curtidas em uma lista.</p>

<figure class="img-polaroid">
    <img src="img/tm-instagram-view.jpg" alt=""/>
</figure>

<p>Ao instalar o módulo, você pode alterar suas configurações na página de configurações do módulo.</p>

<h5>Configurações:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>o nome do módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Select Get</dt>
            <dd>como buscar imagens. Há duas formas (user ou tagged), o que significa que as imagens de um usuário só ou
                usando uma hashtag;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry TagName</dt>
            <dd>hashtag usada para mostrar imagens (é usada se você escolher tagged no campo "Select get");</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry ClientID</dt>
            <dd>a API ID do instagram.com (é usada se você escolher tagged no campo "Select get");</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry UserId</dt>
            <dd>ID do usuário (é usada se você escolher user no campo "Select get");</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry accessToken</dt>
            <dd>token usado para mostrar imagem personalizada (é usada se você escolher user no campo "Select get");
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry limit</dt>
            <dd>limite de fotos;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>situação do módulo (ativado/desativado).</dd>
        </dl>
    </li>
</ol>
