<h3>TM Layout Builder</h3>

<figure class="img-polaroid">
  <img src="img/tm-layout-bilder-edit.jpg" alt=""/>
</figure>
<p align="justify">Модуль предназначен для быстрого, простого и удобного создания различных вариантов главной страницы магазина.</p>

<h4>Как управлять модулем TemplateMonster  Layout Builder</h4>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong> Модули / Расширения </strong>(Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите раздел<strong> TemplateMonster Layout Builder</strong>. Нажав кнопку &quot;редактировать&quot; (edit), вы перейдете к окну настроек существующего модуля.</p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/layout-4.jpg" alt=""/></figure>
</p>
<p>Вы перейдете к следующему окну настроек:</p>
<figure class="img-polaroid"> <img src="img/layout-5.jpg" alt=""/></figure>
<h5>Базовые настройки модуля:</h5>
<ol class="index-list">
	<li>
			<strong>Status</strong>
			 — cтатус модуля (вкл./выкл.);
		
  </li>

	<li>
			<strong>Module Name</strong>
			 — название модуля;
		
</li>

	<li>
			<strong>Module Class</strong>
			 — класс модуля;
		
</li>
	<li>
			<strong>Module ID</strong>
			 —  ID модуля.
		
		
  </li>
</ol>
<p>Для того чтобы создать новый макет, нажмите на кнопку <strong>New Layout View</strong>.</p>
<p align="justify">В модуле доступно несколько кнопок. Эти кнопки упрощают процесс редактирования. Давайте рассмотрим их более детально:</p>

<ol class="marked-list">
  <li>
    <p>Нажмите на кнопку <strong>Добавить колонку</strong> (Add Column). Нажав на эту кнопку, вы сможете добавить ряды. В этой настройке доступно несколько разделов:</p>
    
    
    <ul class="marked-list">
      <li class="marked-list">
        <p align="justify">Добавив колонку, вставьте строку. Для того чтобы добавить строки, нажмите на кнопку <strong>Добавить строку</strong> (Add Row). Количество колонок в ряду не ограничено, Вы можете добавить   столько колонок, сколько Вам нужно. Ширину колонок можно задать сразу   после добавления их с помощью перетягивания правой границы колонки.</p>
      </li>
      <li>
        <p>Кнопка <strong>Удалить</strong> позволяет удалить ненужные колонки.</p>
      </li>
      <li>
        <p align="justify">Нажатие на кнопку <strong>Редактировать</strong> (Edit) открывает   всплывающее окно с замечательным инструментом настройки, который   позволяет изменить стиль колонки/строки. Он позволяет задать фон и   изменить значения полей блоков</p>
      </li>
      <li class="marled-list">
        <div align="justify">Добавив колонки, заполните их содержимым (модулями). Для того чтобы добавить модуль, нажмите на кнопку <strong>Добавить виджет</strong> (Add Widget) и выберите нужный виджет из выпадающего списка. Вы также можете изменять настройки модуля, нажав кнопку &quot;Редактировать&quot; в блоке соответствующего модуля.</div>
      </li>
    </ul>
  </li>
  <li>
    <p>Используйте кнопку <strong>Удалить</strong> (Delete), для того чтобы удалить колонку, которая вам не нужна.</p>
  </li>
  <li>
    <p>Кнопка <strong>Редактировать</strong> (Edit) позволяет настроить стиль колонки. Мы упомянули это ранее.</p>
  </li>
  <li>
    <p align="justify"><strong>Дублировать</strong> (Duplicate) – это отличный инструмент, который позволяет дублировать колонки, не копируя их вручную.</p>
  </li>
  <li>
    <p>Настройка <strong>Сортировать</strong> (Sort) позволяет изменить порядок колонок.</p>
  </li>
</ol>
<p align="justify">По завершению настройки всех столбцов, колонок и модулей необходимо сохранить модуль билдера и вывести его в одной из позиций главной страницы магазина.</p>