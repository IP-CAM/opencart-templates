    </li>
    <li>
        <dl class="inline-term">
            <dt>Giorni cerchio di colore</dt>
            <dd>colore dei giorni timer cerchio interno;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Ore cerchio di colore</dt>
            <dd>colore delle ore timer cerchio interno;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Minuti cerchio di colore</dt>
            <dd>colore del verbale del timer cerchio interno;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Secondi cerchio di colore</dt>
