<h3>TM Single Category Products</h3>

<p>Questo modulo permette di visualizzare 4 moduli principali (Più Venduti, In Primo Piano, Specials, Ultimi) con i prodotti provenienti da certa categoria in vista scheda.</p>

<figure class="img-polaroid">
    <img src="img/tm-single-category-product-view.jpg" alt="">
</figure>

<p>Dopo aver installato il modulo è possibile configurarlo nella pagina delle impostazioni del modulo.</p>

<h5>Impostazioni:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Nome Modulo</dt>
            <dd> il nome del modulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Categoria</dt>
            <dd>permette di selezionare la categoria di prodotto esistente;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Tabs</dt>
            <dd> attiva/disattiva visualizzazione a schede. Se disattivato, vi è la possibilità di selezionare il modulo tipo di vista;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Tipo Prodotto</dt>
            <dd>permette di selezionare il tipo di modulo. Disponibile quando l'opzione Tabs è disabilitato.</dd>
        </dl>

        I seguenti valori sono disponibili:

        <ul class="marked-list">
            <li>Speciali;</li>
            <li>I più venduti;</li>
            <li>In primo piano;</li>
            <li>Più recente.</li>
        </ul>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Speciali</dt>
            <dd> abilita/disabilita modulo Speciali;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>I più venduti</dt>
            <dd> abilita/disabilita modulo I più venduti;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>In primo piano</dt>
            <dd> abilita/disabilita modulo In primo piano;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Più recente</dt>
            <dd> abilita/disabilita modulo Ultimi;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Prodotti In primo piano</dt>
            <dd> permette di selezionare lista dei prodotti per il modulo di In primo piano. Disponibile modulo quando in vetrina è attivato o l'opzione Tipo di prodotto è impostata in vetrina;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limite</dt>
            <dd> il numero di prodotti da visualizzare;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Larghezza</dt>
            <dd> larghezza dell'immagine del prodotto</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Altezza</dt>
            <dd> altezza dell'immagine del prodotto;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Lo Stato</dt>
            <dd> stato modulo (abilitato/disabilitato).</dd>
        </dl>
    </li>
</ol>

