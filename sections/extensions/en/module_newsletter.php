<h3>TemplateMonster Newsletter</h3>

<p>This module allows you to add newsletter block with the subscription form.</p>
<figure class="img-polaroid">
    <img src="img/tm-newsletter-view.jpg" alt=""/>
</figure>

<p>When the module is installed, you can configure it on the module configuration page.</p>
<h4>How to manage TemplateMonster Newsletter module</h4>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p>Find the<strong> TemplateMonster Newsletter</strong> section. Press <strong>edit</strong> button to configure the existing module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
  <figure class="img-polaroid"> <img src="img/news-15.jpg" alt=""/></figure>
</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid"><img src="img/news-18.jpg" alt=""/></figure>
<h5>Available settings:</h5>
<ol class="marked-list">
  <li><strong>Module Name</strong> — the name of the module;</li>
  <li>
    <div align="justify"><strong>Description </strong>— text that will be seen just before filling in the subscription form; </div>
  </li>
  <li><strong>Status </strong>— module status (enabled/disabled). </li>
</ol>
<h4>How to add a new TemplateMonster Newsletter module</h4>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">Find the<strong> TemplateMonster Newsletter</strong><strong> </strong>section. Press<strong> Add New </strong>button to create a new module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
  <figure class="img-polaroid"> <img src="img/news-16.jpg" alt=""/></figure>
</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid"> <img src="img/news-18.jpg" alt=""/></figure>
<p>Fill in the required fields:</p>
<ol class="marked-list">
  <li><strong>Module Name</strong> — the name of the module;</li>
  <li>
    <div align="justify"><strong>Description </strong>— text that will be seen just before filling in the subscription form; </div>
  </li>
  <li><strong>Status </strong>— module status (enabled/disabled). </li>
</ol>
<h4>How to assign the new  TemplateMonster Newsletter module to the Home Page (via Layout Builder)</h4>
<p align="justify">Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p>Find the <strong>Template Monster Layout Builder</strong> section. Navigate to the Home Page module  and press the edit button.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-7.jpg" alt=""/></figure>
</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>You will be brought to the settings window:</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-8.jpg" alt=""/></figure>
</p>
<p>Chose the position where you want to place the <strong>New  TemplateMonster Newsletter</strong> module. You can add row or column, if you want.</p>

<p>Press <strong>Add Widget</strong> button.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-11.jpg" alt=""/></figure>
</p>
<p> In the pop-up window choose the<strong> TemplateMonster Newsletter</strong> module to add. </p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
  <figure class="img-polaroid"> <img src="img/news-23.jpg" alt=""/></figure>
</p>
<p>We have added the <strong>New  TemplateMonster Newsletter</strong> module.</p>
<p>
  <figure class="img-polaroid"> <img src="img/news-24.jpg" alt=""/></figure>
</p>
<p>Scroll the page up and click the <strong>Save</strong> button at the top right of the screen to apply the changes:
  <figure class="img-polaroid"> <img src="img/edit-parallax-14.jpg" alt=""/></figure>
</p>
<p align="justify" class="alert alert-info">You can use such method to assign any module type to the home page.</p>
<h4>How to assign the new TemplateMonster Newsletter module to any layout (via Layouts)</h4>
<p>Navigate to <strong>Design</strong> &gt; <strong>Layouts</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-15.jpg" alt=""/></figure>
</p>
<p align="justify">Choose the page (layouts) you want the TemplateMonster Newsletter module place on and press the edit button in the corresponding field in the action column.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-17.jpg" alt=""/></figure>
</p>
<p>Move to the <strong>Choose the position of the modules</strong> section.</p>
<p>Select the module position you want to place the module.</p>
<p>Find the required module  in the dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/news-20.jpg" alt=""/></figure>
</p>
<p>Press the &quot;Add Module&quot; button.</p>
<p>
  <figure class="img-polaroid"> <img src="img/news-21.jpg" alt=""/></figure>
</p>
<p>You will see the module was successfully added.</p>
<p>
  <figure class="img-polaroid"> <img src="img/news-22.jpg" alt=""/></figure>
</p>
<p>Now you can remove and edit the module from here. Simply press the appropriate button.</p>
<p>Scroll the page up and click the <strong>Save</strong> button at the top right of the screen to apply the changes:
  <figure class="img-polaroid"> <img src="img/edit-parallax-21.jpg" alt=""/></figure>
</p>
<p align="justify" class="alert alert-info">You can use such method to assign any module type to any  page (layouts) of your online-store.</p>
<h4> How to set up newsletter</h4>
<ol>
    <li>
      <p>To create a newsletter for your customers, log into the admin panel and navigate to <strong> Marketing &gt; Mail</strong>.</p>
  </li>
      <figure class="img-polaroid"> <img src="img/news-25.jpg" alt=""/></figure>
  <li>
<p><strong>Fill in the fields</strong> with the necessary data and click <strong>Send</strong>.</p>
  <figure class="img-polaroid"> <img src="img/news-26.jpg" alt=""/></figure>
      <ul class="list">
        <li class="first">
          <p><strong>From</strong> – by default, your store email is the sender.</p>
        </li>
        <li>
          <p><strong>To</strong> – by default, <strong>All Newsletter Subscribers</strong> are selected, you can change the group to send the email from the dropdown.</p>
        </li>
        <li>
          <p><strong>Subject</strong> – this is a required field where you need to type the title for your newsletter.</p>
        </li>
        <li>
          <p><strong>Message</em></strong> – this is also a required field, type your message text here.</p>
        </li>
    </ul>
  </li>
   <li>Click <strong>Send</strong> button.
    <figure class="img-polaroid"> <img src="img/news-27.jpg" alt=""/></figure>
</ol>