<h2 class="item1"> Introducción</h2>

<h5>Gracias por la compra de la plantilla de OpenCart. Esta documentación se compone de varias partes y describe todo el
    proceso de creación y administración de la tienda de OpenCart desde cero. Hemos hecho todo lo posible para que este
    manual sea lo más claro y fácil de seguir.</h5>


    <h3>¿Qué es OpenCart?</h3>

    <p>OpenCart - es una aplicación e-commerce Open Source. OpenCart proporciona una fácil personalización de su tienda
        online. La comunidad de OpenCart incluye un gran número de usuarios tanto los desarrolladores web con una gran
        experiencia que buscan una interfaz fácil de usar, igual a los comerciantes que comienzan su negocio online por
        primera vez. OpenCart tiene una gran cantidad de características que le permiten controlar su tienda. Con las
        herramientas de OpenCart usted puede facilmente descubrir el potencial de su tienda.
        <a href="http://docs.OpenCart.com" target="_blank">Más detalles</a>
    </p>



