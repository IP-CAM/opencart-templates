<h3>TemplateMonster Product Builder</h3>

<p align="justify"><strong>Product builder</strong> is useful shopping tool, it allows potential customers to &quot;build&quot; an ideal version of a product by combining different options. </p>
<figure class="img-polaroid">
    <img src="img/product-builder.png" alt=""/>
</figure>
<h4>How to add the new option</h4>
<p>Navigate to <strong>Categories</strong> &gt;<strong> Options</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/product-builder-1.jpg" alt=""/></figure>
</p>
<p>Press the <strong>Add New</strong> button.</p>
<p>
  <figure class="img-polaroid"> <img src="img/product-builder-2.jpg" alt=""/></figure>
</p>

<p>You will be brought to the settings window:</p>
<figure class="img-polaroid"><img src="img/product-builder-3.png" alt=""/></figure>
<h5>Available settings:</h5>
<p>The<strong> Product Builder</strong> supports multilanguage, so you can configure it for each shop language separately.</p>
<ol class="marked-list">
  <li><strong>Option Name</strong> — assign the  name to the option;</li>
  <li><strong>Type</strong> — choose the option type. </li>
  <li><strong>Sort Order</strong> —  set the display order;</li>
  <li><strong>Option Value Name</strong> — enter the option value name;</li>
  <li><strong>Image</strong> — upload an image for the value;</li>
  <li><strong>Sort order</strong> — set the display order for the value.</li>
</ol>
<p>You are able to create the new option value pressing the <strong>Add Option Value</strong>.</p>
<p>
  <figure class="img-polaroid"><img src="img/product-builder-4.jpg" alt=""/></figure>
</p>
<p>Delete the option value by pressing the <strong>Remove </strong>button.</p>
 <figure class="img-polaroid"><img src="img/product-builder-5.jpg" alt=""/></figure>
<p><strong>Options for product builder type:</strong></p>
<figure class="img-polaroid">
    <img src="img/product-builder.png" alt=""/>
</figure>
<p><strong>List with quantity options type:</strong></p>
<figure class="img-polaroid">
  <img src="img/product-builder-20.jpg" alt=""/>
</figure>
<p>When complete, tab<strong> Save</strong>.</p>
<h4>How to add Product Builder to the product</h4>
<p>Navigate to <strong>Catalog</strong> &gt;<strong> Products</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/product-builder-6.jpg" alt=""/></figure>
</p>
<p>Find the previously created product you want to add the<strong> Product Builder </strong>to.</p>
<p>
  <figure class="img-polaroid"> <img src="img/product-builder-7.jpg" alt=""/></figure>
</p>
<p align="justify">Find the<strong> TemplateMonster Newsletter</strong><strong> </strong>section. Press<strong> Add New </strong>button to create a new module.</p>
<p align="justify">Press<strong> edit</strong> button.</p>
<p>Navigate to the <strong>Option </strong>tap. Enter the name of the option you want to add to the product in the Option field. </p>
<p>
  <figure class="img-polaroid"> <img src="img/product-builder-8.jpg" alt=""/></figure>
</p>
<p>Press the <strong>Add Option Value</strong> button.</p>
<p>Select the<strong> Option value</strong> from the option dropdown.</p>
  <p>
    <figure class="img-polaroid"> <img src="img/product-builder-9.jpg" alt=""/></figure>
  </p>
  <p>Choose the image for the option value. Double-click on the image area and press the <strong>edit </strong>button.</p>
  <p>
    <figure class="img-polaroid"> <img src="img/product-builder-10.jpg" alt=""/></figure>
  </p>
<p>Set the default option value quantity and subtrack status.</p>
  <p>
    <figure class="img-polaroid"> <img src="img/product-builder-11.jpg" alt=""/></figure>
  </p>
  <p>Enter the price change value. Set the points and the weight option.</p>
   <p>
    <figure class="img-polaroid"> <img src="img/product-builder-12.jpg" alt=""/></figure>
  </p>
   <p>Delete the option value pressing the<strong> remove</strong> button.</p>
    <p>
      <figure class="img-polaroid"><img src="img/product-builder-5.jpg" alt=""/></figure>
    </p>
    <p>When complete, tab<strong> Save</strong>.</p>
  
