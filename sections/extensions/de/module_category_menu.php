<h3>TemplateMonster Category Menu </h3>

<p>Das jeweilige Modul ermöglicht es Kategorien in der Seitenleiste, Unterkategorien der
    1. und 2. Ordnung und die Bilder der Unterkategorien der 1. Ordnung
    anzuzeigen.</p>

<p>Nachdem Sie das Modul installiert haben, können Sie es auf der Konfigurationsseite
    einstellen.</p>

