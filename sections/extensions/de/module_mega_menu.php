<h3>TM Megamenu</h3>

<p>Dieses Modul ermöglicht es Ihnen, komplexe Menüs zu erstellen. Das Modul bietet die Möglichkeit, ein einzelnes Element (ohne Untermenüs ) anzuzeigen, ein Element mit einem Untermenü und ein Element mit dem mehrreihigen und mehrspaltigen Untermenü Block. Dieses Modul kann in den verschiedenen Positionen angezeigt werden, aber mit dem gleichen Inhalt.
</p>

<h5>Allgemeine Information</h5>

<ol class="index-list">
    <li>
        <p>Menü-Aktivierung</p>

        <p>Um Megamenü zu aktivieren gehen Sie zur <strong>Extensions -> Modules -> TM Megamenu</strong> Seite, drücken Sie den <strong>Edit</strong> Knopf neben dem gewünschten Element und klicken Sie die <strong>Enabled</strong> Option im neuen Fenster.
        </p>
    </li>
</ol>

