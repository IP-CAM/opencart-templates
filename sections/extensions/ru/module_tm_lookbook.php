<h5>Связь товаров с картинкой коллекции, добавление области выделения товара:</h5>
<p>Для того чтобы добавить товар в коллекцию, выберите его в поле Products:</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_product.jpg" alt=""/>
</figure>

<p>Для того чтобы отметить выбранные товары на картинке, нажмите кнопку "Add Image Map":</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_image_map.jpg" alt=""/>
</figure>


<p>После нажатия кнопки "Add Image Map" в нижней части экрана появится окно, в котором есть возможность создавать области выделения на картинке, и прикреплять товары к этим областям.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_image_map_object.jpg" alt=""/>
</figure>

<p>Создаваемые области выделения могут быть трех разных форм: квадратной, круглой и сложной (полигональной) формы.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures.jpg" alt=""/>
</figure>

<p>Для создания области выделения на картинке нажмите на кнопку, название которой соответствует необходимой Вам форме выделения.</p>


<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_add.jpg" alt=""/>
</figure>

<p>После выбора формы нанесите ее на картинку. Нажмите левой кнопкой мыши в необходимом месте изображения, нарисуйте область.</p>

<p class="alert alert-warning">Обратите внимание: Полигональное выделение всегда должно быть замкнутым, сделайте первую и последнюю точку выделения совпадающими друг с другом.</p>

<p>После нанесения на картинку области выделения, выберите товар, который изображен в выделенной области. Для этого нажмите на кнопку "Edit".</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_edit.jpg" alt=""/>
</figure>

<p>После этого дважды нажмите на область и в выпадающем окне выберите необходимый товар, нажмите на кнопку "Save".</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_save.jpg" alt=""/>
</figure>

<p>После того, как были выделены все области и отмечены все товары, нажмите на кнопку Save.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_save.jpg" alt=""/>
</figure>

<p>Сохраните настройки модуля.</p>