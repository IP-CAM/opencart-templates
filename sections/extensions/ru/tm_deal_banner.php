<h4 align="justify">Как управлять модулем TemplateMonster Deal Banner
module</h4>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong>Модули / Расширения</strong> (Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите раздел <strong>TemplateMonster Deal Banner</strong>. Нажав кнопку &quot;редактировать&quot; (edit), вы перейдете к окну настроек существующего модуля. </p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/deal-2.jpg" alt=""/></figure>
</p>
<p> Вы увидите следующие окно настроек:</p>
<figure class="img-polaroid">
  <img src="img/deal-3.jpg" alt=""/>
</figure>
<h5>Доступные параметры настроек модуля:</h5>
<ul class="marked-list">
  <li> <strong>Module Name</strong> — название модуля;</li>
  <li><strong>Status</strong> —статус модуля (включен/отключен);</li>
  <li><strong>Date</strong> — установите дату в формате  yyyy-mm-dd hh:mm:ss. Напр.: 2016-01-01 00:00:00&quot;;</li>
  <li>
    <div align="justify"><strong>Image</strong> — выберите изображение для баннера.  Для этого нажните на иконку изображения, а затем кнопку &quot;редактировать&quot;; </div>
  </li>
  <li><strong>Image Width</strong> —    ширина баннера;</li>
  <li><strong>Image Height</strong> —     высота беннера;</li>
  <li>
    <div align="justify"><strong>Description</strong> — укажите текст, который будет отображаться на баннере. Добавьте ссылку на любой продукт или категорию. Для этого нажмите кнопку &quot;edit&quot; на панеле инструментов редактора;</div>
  </li>
  <li>
    <div align="justify"><strong>Title on Days/Hours/Minutes/Seconds</strong> — введите текст, который будет отображаться на баннере под областью <strong>Days/Hours/Minutes/Seconds</strong> обратного отчета.</div>
  </li>
</ul>
