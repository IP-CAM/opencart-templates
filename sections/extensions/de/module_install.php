<p>Nachdem Sie das Modul installiert haben, müssen Sie es auch einzustellen. Zwar können
    sich die Einstellungen abhängig von dem Modul unterscheiden, aber es gibt ein
    Pflichfeld <strong>Status</strong> - das jeweilige Feld
    aktiviert/deaktiviert unser Modul.</p>
<figure class="img-polaroid">
    <img src="img/edit-module_1.jpg" alt=""/>
</figure>


<p>Um das Modul zur Seite hinzuzufügen, sollen Sie erst die Liste von Seiten finden. Es
    gibt zwei Möglichkeiten dafür:</p>
<ol class="index-list">
    <li>
        <p>In der Seitenleiste finden Sie den Bereich <strong>System -> Design -> Layout
                (System -> Design -> Layout)</strong>;</p>


        <figure class="img-polaroid">
            <img src="img/page-selecting-way.jpg" alt=""/>
        </figure>
    </li>
    <li>
        <p>Indem Sie sich in dem Modulbereich befinden, drücken Sie auf den Link <strong>here</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/page-selecting-way-2.jpg" alt=""/>
        </figure>
    </li>
</ol>


<p>Wählen Sie eine Seite aus der Liste aus, zu der Sie das Modul hinzufügen möchten.
    Neben ihr drücken Sie auf den Button <strong>Bearbeiten (Edit)</strong>
</p>

<figure class="img-polaroid">
    <img src="img/list-of-pages.jpg" alt=""/>
</figure>


<p>Hier finden Sie eine Liste der Module und ihrer Positionen, die schon auf der Seite
    sind </p>


<p>Es sind die folgenden Positionen verfügbar (nach der Anzeigereihenfolge):</p>
<ol class="index-list">
    <li>Header Top (nach der Sektion Header);</li>
    <li>Column-left (linke Seitenleiste);</li>
    <li>Column-right (rechte Seitenleiste);</li>
    <li>Content-top;</li>
    <li>Content-bottom;</li>
    <li>Footer.</li>
</ol>
<p>Das Feld <strong>Sortierreihenfolge (Sort Order)</strong> zeigt die
    Anzeigereihenfolge von Modulen auf der Seite, die sich in einer Positione befinden.
    Um das Modul zur Seite hinzuzufügen, drücken Sie auf den Button <strong>Modul
        hinzufügen (Add Module)</strong>. In der Zeile, die Sie gerade sehen, wählen Sie
    das gewünschte Modul und seine Position auf der Seite aus, drücken Sie auf den
    Button <strong>Speichern (Save)</strong>. Die Modulinstallation ist abgeschlossen.
</p>

