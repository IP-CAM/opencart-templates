<h2 class="item1">Preparazione</h2>

<h6>Prima di iniziare la configurazione del tuo sito OpenCart è importante assicurarsi di avere tutto l'occorrente. </h6>

<h4>Software per l'editing</h4>

<p>Per lavorare da subito nella massima comodità con il template OpenCart, ti consigliamo di scaricare tutte le applicazioni necessarie. Puoi vedere un elenco del software necessario nella pagina del demo live del template. Le necessità possono variare a seconda del template, quindi qui ti daremo solo qualche indicazione generale:</p>

<ol class="index-list">
    <li> Potrebbe essere necessario l'applicazione Adobe Photoshop. Questo programma serve per modificare i file di origine .PSD ed è sicuramente necessario se desideri modificare la grafica e le immagini del template. </li>

    <li>Per modificare i file codice del template, sarà necessario un editor di codice come Adobe Dreamweaver, Notepad++, Sublime Text, ecc.</li>

    <li>Per caricare i file sul server, potrebbe essere necessario un gestore FTP come Total Commander, FileZilla, CuteFTP, ecc. </li>

    <li>Per estrarre i file della guida si può utilizzare
        <a href="/help/extract-archived-files-windows-os.html" target="_blank"> WinZip(PC)</a>
        o
        <a href="/help/extract-archived-files-mac-os.html" target="_blank"> StuffitExpander(MAC)</a>
        .
    </li>
</ol>

<h4>Hosting</h4>

<p>Dato che il motore OpenCart è un'applicazione basata su PHP/MySQL, sarà necessario preparare un ambiente host. </p>

<p>Se hai già un hosting live, assicurati che questo risponda ai
    <a href="http://docs.OpenCart.com/requirements" target="_blank">requisiti di sistema OpenCart</a>
   e che sia pronto all'utilizzo con i Siti Web OpenCart.
</p>

<p>In alternativa, puoi eseguire OpenCart localmente sul tuo computer utilizzando il server locale. Per creare un server hosting locale, utilizza applicazioni localhost, come
    <b>WAMP</b>, <b>AppServ</b>,
    <b>MAMP</b>, ecc. Ognuno di questi programmi è facile da installare e potrà essere utilizzato per eseguire OpenCart.
</p>

