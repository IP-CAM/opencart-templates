<h3>TM Lookbook</h3>

<p>The lookbook es un conjunto de fotos, donde se puede marcar cualquier parte de la imagen. Este módulo sirve para la visualización de los productos de una o varias marcas.</p>
<p>Los módulos tienen dos modos de visualización para que se muestre en la posición de la tienda: en forma de un Banner (1) o en forma de una Colección (2).</p>


