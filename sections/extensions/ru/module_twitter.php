<h3>TemplateMonster Twitter Box</h3>

<p>Данный модуль отображает ленту постов из социальной сети Twitter. Он позволяет посетителям сайта быть в
    курсе всех последних обновлений и сообщений на Вашей странице в социальной сети Twitter.</p>

<figure class="img-polaroid">
    <img src="img/tm-twitter-view.jpg" alt=""/>
</figure>

<p>После установки модуля Вы можете настроить его на странице настроек модуля.</p>

<h5>Настройки:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>название модуля;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Twitter Page URL</dt>
            <dd>ссылка на Twitter аккаунт;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Widget ID</dt>
            <dd>Widget ID, который Вы можете получить
                <a href="https://twitter.com/settings/widgets" target="_blank">здесь</a>
                ;
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Tweet Limit</dt>
            <dd>количество твитов;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Color Scheme</dt>
            <dd>цветовая схема (светлая, темная);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>ширина блока в пикселях;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>высота блока в пикселях;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>статус модуля (включен/отключен).</dd>
        </dl>
    </li>
</ol>

