<h3>Blog</h3>

<p>The following extension allows you to manage posts and news connected to your store.</p>

<p>Main features:</p>

<ul class="marked-list">
 <li>creating articles;</li>
 <li>creating categories;</li>
 <li>creating authors;</li>
 <li>commenting articles and replying to the comments;</li>
 <li>viewing report;</li>
 <li>displaying products and articles related to the certain article;</li>
</ul>

<p>In order to change Blog global settings navigate to <strong>System > Settings</strong>. Select the store where you want to change the settings out of the list and click <strong>Edit</strong>.</p>

<h6> Blog settings can be seen in Blog section:</h6>
<ul class="marked-list">
 <li><strong>Status</strong> — blog status (enabled/disabled);</li>
 <li><strong>Limit</strong> — number of posts per page;</li>
 <li><strong>Description Symbol Limit </strong>— number of symbols in the article description on the main Blog page (not on single article!);</li>
 <li><strong>Comment Auto approval</strong> — enables/disables automatic comment approval by administrator;</li>
 <li><strong>Author Information</strong> — enables/disables information about the author on article page;</li>
 <li><strong>Related Article</strong> — enables/disables related articles;</li>
 <li><strong>Share with social site</strong> — enables/disables social share buttons.</li>
</ul>


