<h3>TM Instagram</h3>

<p>This module displays the Instagram social network pictures including the number of likes in a list.</p>
<figure class="img-polaroid">
    <img src="img/tm-instagram-view.jpg" alt=""/>
</figure>

<p>When the module is installed, you can configure it on the module configuration page.</p>

<h5>Settings:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>the name of the module;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Select Get</dt>
            <dd>how to get the images. There are 2 values available (user and tagged), which means either to get the
                images of the exact user or using hashtag;
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry TagName</dt>
            <dd>tag that is used to display the images (it is used if you previously selected tagged in the "Select get"
                field);
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry ClientID</dt>
            <dd>your API ID at instagram.com (it is used if you previously selected tagged in the "Select get" field);
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry UserId</dt>
            <dd>user ID (it is used if you previously selected user in the "Select get" field);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry accessToken</dt>
            <dd>token to display custom image (it is used if you previously selected user in the "Select get" field);
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry limit</dt>
            <dd>limit of entries;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>module status (enabled/disabled).</dd>
        </dl>
    </li>
</ol>
