<h3>TemplateMonster Facebook Box</h3>

<p>Данный модуль отображает Facebook like box - отображает лица и количество поклонников, кому естественно нравится Ваш
    сайт или бренд.</p>

<figure class="img-polaroid">
    <img src="img/tm-facebook-view.jpg" alt=""/>
</figure>

<p>После установки модуля Вы можете настроить его на странице настроек модуля.</p>

<h5>Настройки:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>название модуля;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Facebook Page URL</dt>
            <dd>ссылка на Facebook страницу;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Color Scheme</dt>
            <dd>цветовая схема (светлая, темная);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Show Faces</dt>
            <dd>показывать лица пользователей, которым понравилась страница (yes, no);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Show Stream</dt>
            <dd>показывать ленту последних постов (yes, no);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Show Header</dt>
            <dd>показывать заголовок блока (yes, no);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Show Border</dt>
            <dd>показывать границу блока (yes, no);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>ширина блока в пикселях;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>высота блока в пикселях;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>статус модуля (включен/отключен).</dd>
        </dl>
    </li>
</ol>

