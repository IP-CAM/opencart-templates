<h3>TM Product Slideshow</h3>

<p>Este módulo permite exibir os produtos em um slideshow. </p>

<figure class="img-polaroid">
    <img src="img/tm-product-slideshow-view.jpg" alt=""/>
</figure>

<p>Ao instalar o módulo, você pode alterar suas configurações na página de configurações do módulo.</p>

<h5>Configurações:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>o nome do módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Products</dt>
            <dd>seleção de produtos;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd>número de produtos;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>largura da imagem do produto;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>altura da imagem do produto;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>situação do módulo (ativado/desativado).</dd>
        </dl>
    </li>
</ol>
