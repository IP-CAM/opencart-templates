<h3>TM CountDown</h3>

<p>Este módulo le permite crear contadores de cuenta regresiva. Hay dos tipos disponibles, numéricos y radiales temporizadores.
</p>
<p>(Numeric) Numérico:</p>

<figure class="img-polaroid">
    <img src="img/tm_countdown.png" alt=""/>
</figure>

<p>(Radial) Radial:</p>

<figure class="img-polaroid">
    <img src="img/tm_countdown_circle.png" alt=""/>
</figure>

<p>Con el fin de cambiar el tipo de contadores radiales es necesario seleccionar la opción "Radial" en el campo "Tipo". En este caso, los ajustes adicionales para el temporizador radial estén disponibles:</p>
<figure class="img-polaroid">
    <img src="img/tm_countdown_type.png" alt=""/>
</figure>

