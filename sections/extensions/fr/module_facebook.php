<h3>TM Facebook Box</h3>

<p>Le module permet d'afficher Facebook like box qui montre les fans qui aiment votre site et votre marque.</p>

<figure class="img-polaroid">
    <img src="img/tm-facebook-view.jpg" alt=""/>
</figure>

<p>Une fois le module installé, vous pouvez le configurer sur la page des paramètres du module.</p>

<h5>Paramètres:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>le nom du module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Facebook Page URL</dt>
            <dd>le lien vers la page Facebook;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Color Scheme</dt>
            <dd>la palette de couleurs (clair, foncé);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Faces</dt>
            <dd>afficher les visages des utilisateurs (oui, non);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Stream</dt>
            <dd>afficher les articles récents (oui, non);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Header</dt>
            <dd>afficher les titres des blocs (oui, non);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Border</dt>
            <dd>afficher la bordure du bloc(oui, non);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>la largeur du bloc (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>la hauteur du bloc (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>le statut du module (activé/désactivé).</dd>
        </dl>
    </li>
</ol>

