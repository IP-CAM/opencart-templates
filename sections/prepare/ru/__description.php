<h4>Технические требования</h4>
<p align="justify">Система OpenCart имеет определенные технические требования, необходимые для осуществления эффективного управления интернет-магазином. Во-первых, должен быть создан веб-сервер, нужно это для того, чтобы магазин OpenCart  был доступен в Интернете. Приобрести доменное имя и воспользоваться услугами хостинга, вы можете без труда и по доступной цене.</p>
<p>Для корректной установки OpenCart на веб-сервер, следующие приложения должны быть активны:</p>
<ul>
  <li>Web Server (рекомендуется Apache)</li>
  <li>PHP 5.3+(1.5.x не менее 5.2+)</li>
  <li>Database (рекомендуется MySQLi)</li>
</ul>
<p><strong>Необходимые PHP библиотеки / модули</strong></p>
<ol class="marked-list">
  <li>Curl</li>
  <li>ZIP</li>
  <li>Zlib</li>
  <li>GD Library</li>
  <li>Mcrypt</li>
  <li>Mbstrings</li>
  <li>Xml</li>
</ol>
<p align="justify">Вышеуказанные дополнения PHP должны быть предоставлены практически всеми хостинг-провайдерами, в процессе установки OpenCart наличие добалнений будет проверяться.  В случае отсутствия каких либо дополнений, свяжитесь со своим хостинг-провайдером.</p>
