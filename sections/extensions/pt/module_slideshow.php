<h3>TemplateMonster Slideshow</h3>

<p>Este módulo lhe permite exibir um slideshow no seu site. </p>

<p>Ao instalar o módulo, você pode alterar suas configurações na página de configurações do módulo.</p>

<h5>Configurações:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>o nome do módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Banner</dt>
            <dd>deve ser escolhido na lista de
                <a href='#banner_create'>banners</a>
                criados anteriormente;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>largura da imagem do módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>altura da imagem do módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>situação do módulo (ativado/desativado).</dd>
        </dl>
    </li>
</ol>

