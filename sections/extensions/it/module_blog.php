<h3>Blog</h3>

<p>L'estensione seguente consente di gestire i messaggi e le notizie legate al tuo negozio.</p>

<p>Caratteristiche principali:</p>

<ul class="marked-list">
    <li>la creazione degli articoli;</li>
    <li>creazione di categorie;</li>
    <li>la creazione di autori;</li>
    <li>commentando gli articoli e rispondendo ai commenti;</li>
    <li>visualizzazione report;</li>
    <li>visualizzazione dei prodotti e articoli relativi al determinato articolo;</li>
</ul>

<p>Per modificare le impostazioni globali Blog navigare a <strong>System->Settings</strong>. Selezionare l'archivio in cui si desidera modificare le impostazioni fuori dalla lista e fare clic su <strong>Edit</strong>.</p>

<h6> Impostazioni del blog può essere visto in sezione Blog:</h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Lo Stato</dt>
            <dd> blog stato (abilitato\disabilitato);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limite</dt>
            <dd> numero di articoli per pagina;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Descrizione Simbolo Limite</dt>
            <dd> numero di simboli nella descrizione articolo sulla pagina del blog principale (non sul singolo articolo!);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Commento approvazione Automatica</dt>
            <dd> abilita/disabilita commento approvazione automatica dall'amministratore;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Autore Informazioni</dt>
            <dd> attiva/disattiva informazioni sull'autore sulla pagina dell'articolo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Articolo Correlato</dt>
            <dd> abilita/disabilita articoli correlati;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Condividi con il sito di social</dt>
            <dd> abilita/disabilita pulsanti di condivisione sociale;</dd>
        </dl>
    </li>
</ol>

