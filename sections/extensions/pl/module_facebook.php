<h3>TemplateMonster Facebook Box</h3>

<p>Moduł pozwala wyświetlać Facebook jako okno, pokazujące fanów, którzy lubią twoją witryne i markę </p>

<figure class="img-polaroid">
    <img src="img/tm-facebook-view.jpg" alt=""/>
</figure>

<p>Kiedy moduł został zainstalowany, możesz konfigurować go na stonie konfiguracji modułu. </p>

<h5>Ustawienia:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>nazwa modułu;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Facebook Page URL</dt>
            <dd>kolorystyka (jasna, ciemna);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Color Scheme</dt>
            <dd>kolorystyka (jasna, ciemna);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Show Faces</dt>
            <dd>pokazać twarze uytkownikw (tak, nie); </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Show Stream</dt>
            <dd>pokazać nowe posty (tak, nie);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Show Header</dt>
            <dd>pokazać tytuły bloków (tak, nie);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Show Border</dt>
            <dd>pokazać granice bloków (tak, nie);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>szerokość bloku (px);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>wysokość bloku (px);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>status modułu (włączony/wyłączony).</dd>
        </dl>
    </li>
</ol>

