<h3>Blog</h3>

<p>Dane rozszerzenie pozwala zarządzać postami i wiadomościami związanymi z Twoim sklepem.</p>

<p>Główne funkcjonalności:</p>

<ul class="marked-list">
    <li>tworzenie artykułów;</li>
    <li>tworzenie kategorii;</li>
    <li>tworzenie autorów;</li>
    <li>komentowanie artykułów i odpowiadanie na komentarze;</li>
    <li>oglądanie raportów;</li>
    <li>wyświetlanie produktów i artykułów związanych z pewnym artykułem;</li>
</ul>

<p>W celu zmiany ustawień globalnych Blogu, przejdź do menu <strong>System->Settings (Ustawienia)</strong>. Wybierz z listy sklep w którym chcesz zmieniać ustawienia i kliknij <strong> Edit (Edytuj) </strong>.</p>

<h6> Ustawienia Blogu można znaleźć w sekcji Blog:</h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd> status blogu(włączony/wyłączony);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd> liczba postów na stronie;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Liczba Symboli Opisu</dt>
            <dd> liczba symboli w opisie artykułu na głównej stronie Blogu (nie w jednym artykule!);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Auto zatwierdzenie komentarzy</dt>
            <dd> włącza/wyłącza automatyczne zatwierdzenie komentarzy przez administratora;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Informacja o Autorze</dt>
            <dd> włącza/wyłącza informacje o autorze na stronie artykułu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Podobny Artykuł</dt>
            <dd> włącza/wyłącza podobne artykuły;;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt> Udostępnij na Serwisie społecznościowym</dt>
            <dd>włącza/wyłącza przyciski udostępniania społecznego;;</dd>
        </dl>
    </li>
</ol>

