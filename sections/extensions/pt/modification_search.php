<h3>TM Live search</h3>

<p> TM Live search é uma modificação que te permite exibir produtos buscados sem recarregar a página. Para
    apagá-lo ou removê-lo, navegue para
    <strong>Extensions -> Modifications</strong>, escolha a modificação da lista, clique em
    <strong>Disable</strong> para desativar ou em <strong>Delete</strong> para apagar.</p>
