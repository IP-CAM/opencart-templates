<h3>TemplateMonster Facebook Box</h3>

<p>Bu modül, websitenizi ve markanızı beğenen fanlarınızın Facebook beğeni kutusunda gösterilmesini sağlar.</p>

<figure class="img-polaroid">
    <img src="img/tm-facebook-view.jpg" alt=""/>
</figure>

<p>Modül kurulduktan sonra modülün sayfasından tüm düzenlemeleri yapabilirsiniz.</p>

<h5>Settings:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>modülün ismi;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Facebook Page URL</dt>
            <dd>Facebook sayfasına bağlantı adresi;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Color Scheme</dt>
            <dd>renk şeması (açık, koyu);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Faces</dt>
            <dd>kullanıcıların yüzünü göster (evet, hayır);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Stream</dt>
            <dd>güncel yazıları göster (evet, hayır);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Header</dt>
            <dd>kutu başlığını göster (evet, hayır);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Border</dt>
            <dd>kutu kenarlığını göster (evet, hayır);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>kutu genişliği (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>kutu uzunluğu (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>modülün durumu (aktif/pasif).</dd>
        </dl>
    </li>
</ol>

