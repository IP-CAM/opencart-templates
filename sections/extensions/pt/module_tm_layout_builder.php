<h3>TM Layout Builder</h3>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-edit.jpg" alt=""/>
</figure>
<p>Este módulo serve para a criação simples, rápida e fácil de variantes diferentes da página principal da loja.</p>

<h5>Configurações básicas do Módulo:</h5>
<ol class="index-list">
	<li>
		<dl class="inline-term">
			<dt>Estado</dt>
			<dd>O estado do módulo (Ativado/Desativado);</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Nome do Módulo</dt>
			<dd>O nome do módulo;</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Classe do Módulo</dt>
			<dd>A classe do módulo;</dd>
		</dl>
	</li>
	<li>
		<dl class="inline-term">
			<dt>ID do módulo</dt>
			<dd>O ID do módulo.</dd>
		</dl>
	</li>
</ol>
<p>Para criar um layout novo, pressione o botão <strong>Visualizar Novo Layout</strong>, e depois, pressione o botão <strong>Adicionar Linha
		(1)</strong>. Para adicionar colunas, pressione o <strong>Adicionar
		Colunas (2)</strong>.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-column.jpg" alt=""/>
</figure>

<p>O número de colunas na linha é ilimitado, você pode adicionar quantas colunas desejar. A largura das colunas podem ser definidas logo apos a colocação delas, com a ajuda da função de Arrastar e soltar, arrastando a borda direita da coluna. </p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-drag-drop.jpg" alt=""/>
</figure>

<p>Apos adicionar as colunas, é necessário preenche-las com conteúdo (módulos). Para adicionar um módulo, pressione o botão <strong>Adicionar Widget</strong> e selecione o necessário do menu suspenso.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-widget.jpg" alt=""/>
</figure>

<p>Apos adicionar um módulo, é possível editá-lo, deleta-lo e</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change.jpg" alt=""/>
</figure>

<p> também move-lo para uma outra coluna.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change-position.jpg" alt=""/>
</figure>

<p>Apos ter configurado todas as linhas, colunas e módulos, salve o módulo do construtor e mostre-o em uma das posições na página principal da loja.</p>