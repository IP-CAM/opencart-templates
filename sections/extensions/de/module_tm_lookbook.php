<h5>Verbinden der Produkte mit Kollektionsbildern, Produktauswahlbereich hinzufügen:</h5>
<p>Um ein Produkt zur Kollektion hinzuzufügen, wählen Sie es im Produktfeld:</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_product.jpg" alt=""/>
</figure>

<p>Um das gewählte Produkt auf dem Bild zu markieren, drücken Sie die "Add Image Map" Taste:</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_image_map.jpg" alt=""/>
</figure>


<p>Nachdem Sie die "Add Image Map" Taste gedrückt haben, werden Sie unten ein Fenster sehen, es ermöglicht Auswahl Bereiche zu schaffen und Hinzufügen von Produkten zu solchen Bereichen.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_image_map_object.jpg" alt=""/>
</figure>

<p>Die Bereiche, die Sie erstellen, können 3 verschiede Formen haben: quadratisch, rund und komplex (polygonal).</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures.jpg" alt=""/>
</figure>

<p>Um ein Auswahl Bereich auf dem Bild zu erstellen, klicken Sie auf den entsprechenden Knopf des Auswahle Bereiches.</p>


<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_add.jpg" alt=""/>
</figure>

<p>Nachdem Sie die Form gewählt haben, platzieren Sie es auf dem Bild. Klicken Sie links auf den gewünschten Teil des Bildes und malen Sie den Bereich.</p>

<p class="alert alert-warning">Hinweis: Polygonale Auswahl immer muss geschlossen sein ,der erste und der letzte Punkt müssen zusammenfallen .</p>

<p>Nachdem Sie den Auswahl Bereich zum Bild hinzugefügt haben, wählen Sie das Produkt, dass in dem Bereich angezeigt werden soll. Um das zu tun, klicken sie auf den "Edit" Knopf.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_edit.jpg" alt=""/>
</figure>

<p>Dann doppelklicken Sie auf den Bereich und im Dropdown-Fenster, wählen Sie das gewünschte Produkt, drücken Sie die Schaltfläche "Save".</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_save.jpg" alt=""/>
</figure>

<p>Nachdem Sie alle Bereiche markiert haben und alle Produkte hinzugefügt wurden,drücken Sie den Save Knopf.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_save.jpg" alt=""/>
</figure>

<p>Speichern Sie die Modul Einstellungen.</p>