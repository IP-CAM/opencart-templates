<h3>Die Installation von Modulen</h3>

<p>Die Installation von Modulen wird in ein paar Schritten durchgeführt. Öffnen Sie das
    Admin Panel, indem Sie /admin zur Adresszeile www.yoursite.com/admin hinzufügen.
    Danach wählen Sie in der Seitenleiste den Tab <strong> Erweiterungen
        (Extensions)</strong> und in der DropDownListe <strong>Module (Modules)</strong>
    (Bild5) aus. In dem DropDownFenster sind alle installierten und fertigen zur
    Installation Module angezeigt. Wenn es neben dem Namen den Button <strong>Deinstallieren
        (Uninstall)</strong> gibt, bedeutet das, dass das Modul schon installiert wurde
    und wenn Sie auf den Button <strong>Bearbeiten (Edit)</strong> drücken, können Sie
    die Einstellungen dieses Moduls bearbeiten. Wenn Sie auf den Button <strong>Installieren
        (Install)</strong> drücken, installieren Sie dieses Modul. Wenn Sie das Modul
    entfernen möchten, sollen Sie dann auf den Button <strong>Deinstallieren
        (Uninstall)</strong> drücken, um das jeweilige Modul zu entfernen. </p>

<p class="alert alert-warning">Achten Sie darauf, dass beim Entfernen des Moduls alle
    seinen Einstellungen auch entfernt werden, die es nicht möglich ist,
    wiederherzustellen.</p>

