<h3>TM Megamenu</h3>

<p>Данный модуль предназначен для создания сложных меню. В модуле предусмотрена возможность отображения одного пункта
    (без подменю), пункта с подменю и пункта с подменю в виде многорядного и многоколоночного блока. Модуль может
    использоваться в разных позициях, но при этом будет отображать то же содержимое.
</p>

<h5>Общая информация</h5>

<ol class="index-list">
    <li>
        <p>Включение меню</p>

        <p>Для того, чтобы включить мега-меню, вам необходимо перейти на страницу <strong>Extensions -> Modules -> TM Megamenu</strong>, нажать напротив соответствующего пункта копку <strong>Edit</strong> и в открывшемся окне выбрать пункт <strong>Enabled</strong>.
        </p>
    </li>
</ol>

