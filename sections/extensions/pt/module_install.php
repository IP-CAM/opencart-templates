<h3>Instalação de módulos</h3>
<p>O processo de instalação de um módulo consiste de vários passos. Navegue para o painel administrativo (basta
    adicionar /admin na barra de navegação do browser - www.yoursite.com/admin). Vá para
    <strong>Extensions </strong> e no menu de lista escolha
    <strong>Modules</strong> Você verá uma lista de módulos instalados e prontos para instalação. Se tiver um
    botão
    <strong>Uninstall</strong> próximo ao módulo, significa que o módulo já está instalado e você pode modificar suas
    propriedades clicando em
    <strong>Edit</strong>. Se você clicar no botão
    <strong>Install</strong>, vai começar o processo de instalação. Para apagar o módulo, você deve clicar no botão
    <strong>Uninstall</strong>, para que o módulo seja desinstalado.</p>
<p class="alert alert-warning">Perceba que ao desinstalar um módulo você perde todas as configurações realizadas
    anteriormente. Esse processo não pode ser revertido.</p>
<figure class="img-polaroid">
    <img src="img/install_1.jpg" alt=""/>
</figure>


<p>Quando a instalação terminar, você precisa configurar o módulo. As configurações são diferentes para cada módulo, mas
    o campo
    <strong>Status</strong> é o mesmo para todos - esse campo lhe permite desativar o módulo.</p>
<figure class="img-polaroid">
    <img src="img/edit-module_1.jpg" alt=""/>
</figure>


<p>Para incluir o módulo em determinada página, você precisa ir até a lista de páginas. Isso pode ser feitos de duas
    maneiras: </p>
<ol class="index-list">
    <li>
        <p>No menu lateral navegue para <strong>System -> Design -> Layout</strong>;</p>

        <figure class="img-polaroid">
            <img src="img/page-selecting-way.jpg" alt=""/>
        </figure>
    </li>

    <li>
        <p>Na seção de módulos clique o link <strong>here</strong>.</p>


        <figure class="img-polaroid">
            <img src="img/page-selecting-way-2.jpg" alt=""/>
        </figure>
    </li>
</ol>

<p>Na lista de pagina escolha a página em que deseja inlcuir o módulo e clique em <strong>Edit</strong></p>
<figure class="img-polaroid">
    <img src="img/list-of-pages.jpg" alt=""/>
</figure>


<p>Você pode ver uma lista de módulos e da posição de módulos já disponíveis à página </p>


<p>As seguintes posições de módulo estão disponíveis (respectivamente em ordem de aparecimento):</p>
<ol class="index-list">
    <li>Header Top (após a seção do cabeçalho);</li>

    <li>Column-left (painel do lado esquerdo);</li>

    <li>Column-right (painel do lado direito);</li>

    <li>Content-top;</li>

    <li>Content-bottom;</li>

    <li>Footer.</li>
</ol><p>O campo
    <strong>Sort Order</strong> mostra a ordem de aparecimento dos módulos definidos para uma mesma posição. Para
    adicionar um módulo em uma página, clique em
    <strong>Add Module</strong>. Escolha o módulo obrigatório e clique em
    <strong>Save</strong>. A instalação do módulo está terminada. </p>

