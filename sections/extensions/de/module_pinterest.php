<h3>TM Pinterest</h3>

<p>TM Pinterest Modul ermöglicht es die Pinterest Galerie in Ihrem Online-Shop anzuzeigen. </p>
<figure class="img-polaroid">
    <img src="img/tm-pinterest-view.jpg" alt=""/>
</figure>

<p>Nachdem Sie das Modul installiert haben, können Sie es auf der Konfigurationsseite einstellen.</p>

<h5>Einstellungen:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>der Modulname;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Pinterest Page URL</dt>
            <dd>der vollständige Pfad zur Seite, von der dies angezeigt wird;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>die Blockbreite in Pixel;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>die blockhöhe in Pixel;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>der Modulstatus (aktiviert/deaktiviert).</dd>
        </dl>
    </li>
</ol>

