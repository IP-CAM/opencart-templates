<h3>TM Slideshow</h3>

<p>Ce module vous permet d'afficher Camera slideshow sur votre site. </p>

<p>Une fois le module installé, vous pouvez le configurer sur la page de configuration de module.</p>

<h5>Paramètres:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>le nom du module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Banner</dt>
            <dd>choisissez dans la liste des
                <a href='#banner_create'>bannières</a>
                précédemment créée;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>la largeur de l'image du module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>la hauteur de l'image du module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>le statut du module (activé/désactivé).</dd>
        </dl>
    </li>
</ol>

