<h5>Conexión de productos con la imagen de la colección, añadiendo área de selección de productos:</h5>
<p>Para agregar un producto a una colección, seleccionelo en el campo de los productos:</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_product.jpg" alt=""/>
</figure>

<p>Para marcar los productos seleccionados en la imagen, presione el botón "Add Image Map":</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_image_map.jpg" alt=""/>
</figure>


<p>Después de pulsar el botón "Add Image Map", en la parte inferior, verá una ventana, lo que permite la creación de áreas seleccionadas y la adición de productos a tales áreas.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_image_map_object.jpg" alt=""/>
</figure>

<p>Las áreas que se crean pueden ser de 3 formas diferentes: cuadrado, redondo, y complejo (poligonal).</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures.jpg" alt=""/>
</figure>

<p>Para crear un área de selección en la imagen, pulse el botón, el nombre que corresponde a la forma necesaria de área de selección.</p>


<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_add.jpg" alt=""/>
</figure>

<p>Después de elegir la forma, coloque en la imagen. Haga clic con la tecla izquierda en la parte necesaria de la imagen, dibuja la zona.</p>

<p class="alert alert-warning">Nota: la selección poligonal siempre debe estar cerrada, haga que el primer y el último punto coincidan.</p>

<p>Después de colocar el área de seleccionado en la imagen, elija el producto que quiere mostrar en el área seleccionado. Para ello, pulse el botón "Edit".</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_edit.jpg" alt=""/>
</figure>

<p>A continuación, haga doble clic en la zona y, en la ventana desplegable, seleccione el producto necesario, pulse el botón "Save".</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_save.jpg" alt=""/>
</figure>

<p>Después de marcar todas las áreas y marcar todos los productos, pulse el botón Save.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_save.jpg" alt=""/>
</figure>

<p>Guarde la configuración del módulo.</p>