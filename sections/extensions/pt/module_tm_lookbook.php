<h5>Conectando mercadorias com a imagem. Adicionar a área de seleção da mercadoria:</h5>
<p>Para adicionar uma mercadoria a Galeria, selecione-a no campo 'Mercadorias':</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_product.jpg" alt=""/>
</figure>

<p>Para marcar as mercadorias selecionadas na imagem, pressione o botão "Adicionar Mapa de Imagem":</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_image_map.jpg" alt=""/>
</figure>


<p>Depois de pressionar o botão "Adicionar Mapa de Imagem", na parte inferior, você verá uma janela que permite a criação de áreas de seleção e adição de mercadorias em tais áreas.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_image_map_object.jpg" alt=""/>
</figure>

<p>É possível criar em 3 formas diferentes: quadradas, redondas, e complexas (poligonais).</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures.jpg" alt=""/>
</figure>

<p>Para criar uma área de seleção sobre a imagem, pressione o botão e o nome que corresponde à forma necessária de área de seleção.</p>


<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_add.jpg" alt=""/>
</figure>

<p>Depois de escolher o tipo de forma, coloque-a sobre a imagem. Clique com o botão esquerdo na parte necessária da imagem e desenhe a área.</p>

<p class="alert alert-warning">Note: A seleção poligonal deve ser sempre fechada, faça com que o primeiro e o último coincidem.</p>

<p>Depois de adicionar a área de seleção na imagem, escolha a mercadoria que será exibida nesta área. Para fazer isso, pressione o botão "Editar".</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_edit.jpg" alt=""/>
</figure>

<p>Em seguida, clique duas vezes na área e na janela de deslizamento, selecione a mercadoria necessária e pressione o botão "Salvar"</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_save.jpg" alt=""/>
</figure>

<p>Depois de marcar todas as áreas e mapear todas as mercadorias, pressione o botão Salvar.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_save.jpg" alt=""/>
</figure>

<p>Salvar as configurações do módulo.</p>