<h3>TemplateMonster Currency</h3>
Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong>Модули / Расширения</strong> (Extensions).
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/> </figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/> </figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите модуль<strong> TemplateMonster Currency</strong>, нажмите кнопку &quot;редактировать&quot; и вы перейдете в окно настроек модуля.</p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>

  <figure class="img-polaroid"> <img src="img/сurrency-1.jpg" alt=""/></figure>
</p>
<p> Вы увидите следующие окно настроек:</p>
<figure class="img-polaroid"> <img src="img/сurrency-2.jpg" alt=""/></figure>
<p>Выберите <strong>Disable</strong> в раскрывающемся списки опции <strong>Status</strong>, если хотите выключить модуль.</p>
<p>
