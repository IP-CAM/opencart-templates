<h3>TM Cookie Policy</h3>

<p>Das jeweilige Modul zeigt den Benutzern, ob es Cookies (cookies) benutzt
    werden.
    Laut dem Gesetz über Cookies zeigt dieses Modul den Benutzern, ob Cookies verwendet
    werden.</p>

<p>Cookies sind kleine Textdateien, die Websites auf Computern der Benutzer erstellen.
    Normalerweise werden sie benutzt, um den Benutzer zu identifizieren. Außerdem werden
    sie benutzt, damit der Benutzer die Website bequem nutzen kann. Cookies sind ein
    fester Bestandteil mehrerer Funktionen auf der Website.</p>

<p>Zum Beispiel, mit Hilfe von Cookies werden diejenigen Produkte gespeichert, die Sie
    in dem Online-Shop zum Warenkorb hinzugefügt haben. Außerdem speichern sie
    Anmeldedaten oder stellen wertvolle Nutzungsstatistik und Informationen den
    Webseiteninhaber zur Verfügung.</p>

<p>Wenn Sie eine Website haben, werden Cookies höchstwahrscheinlich auf dieser Webseite
    benutzt. Ihre Hauptfunktion besteht darin, dass sie der Anzahl der Benutzer und ihre
    Interessen über spezielle Werkzeuge wie, zum Beispiel,
    Google Analytics folgen. Außerdem werden sie benutzt, um Anzeigen darzstellen. Wenn
    Sie einen Online-Shop schon haben, sind sie ein wesentlicher Bestandteil vom
    E-Commerce.</p>

<p>Am 26. Mai 2012 hat ICO Organisation einen Akt darüber veröffentlicht, dass
    "stillschweigende Zustimmung" nicht erlaubt wird. Aus diesem Grund müssen Sie
    unbedingt zeigen, wo auf der Website sowohl Cookies als auch Datenschutzrichtlinie
    sind.</p>

