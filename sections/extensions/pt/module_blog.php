<h3>Blog</h3>

<p>As seguintes extensões permitem que você administre posts e novidades relacionadas a sua loja.</p>

<p>Características principais:</p>

<ul class="marked-list">
    <li>criação de artigos;</li>
    <li>criação de categorias;</li>
    <li>criação de autores;</li>
    <li>comentários em artigos e respostas a comentários;</li>
    <li>exibir relatório;</li>
    <li>mostrar produtos e artigos relacionados a determinado artigo;</li>
</ul>

<p>Para mudar as definições globais do Blog, navegue para <strong>System->Settings</strong>. Escolha a loja da qual deseja alterar as definições da lista e clique em <strong>Edit</strong>.</p>

<h6> Definições do blog podem ser vistas na seção do blog:</h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd> situação do blog (ativo/inativo);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd> quantidade de posts por página;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description Symbol Limit</dt>
            <dd> quantidade de símbolos na descrição do artigo na página principal do blog (não na página de leitura do artigo!);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Comment Auto approval</dt>
            <dd> ativa/desativa a aprovação automática dos comentários pelo administrador;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Author Information</dt>
            <dd> ativa/desativa informações sobre o autor na página do artigo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Related Article</dt>
            <dd> ativa/desativa artigos relacionados;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Share with social site</dt>
            <dd> ativa/desativa botões de compartilhamento;</dd>
        </dl>
    </li>
</ol>

