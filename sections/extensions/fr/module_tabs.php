<h3>TM Module Tabs</h3>

<p>Ce module permet d'afficher quatre modules principaux (Bestsellers, Featured, Specials, Latest) sous forme
    d'onglets.</p>

<figure class="img-polaroid">
    <img src="img/tm-moduletabs-view.jpg" alt=""/>
</figure>

<p>Une fois le module installé, vous pouvez le configurer sur la page de configuration de module.</p>

<h5>Paramètres:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>le nom du module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Specials</dt>
            <dd>activer/désactiver le module Specials;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Bestsellers</dt>
            <dd>activer/désactiver le module Bestsellers;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Latest</dt>
            <dd>activer/désactiver le module Latest;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Featured</dt>
            <dd>activer/désactiver le module Featured;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Products</dt>
            <dd>choisir la liste des produits pour l'onglet Featured;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd>établir une limite de produits affichés;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>la largeur de l'image du produit;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>la hauteur de l'image du produit;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>le statut du module (activé/désactivé).</dd>
        </dl>
    </li>
</ol>

