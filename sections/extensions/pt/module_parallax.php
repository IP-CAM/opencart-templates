<h3>Parallax</h3>

<p>Parallax-scroll - uma técnica específica de rolagem usada em gráficos de computador que faz com que imagens no fundo
    rolem mais lentamente, dando a impressão de profundidade e o efeito de imersão em jogos bidimensionais. Este módulo
    permite adicionar efeito parallax no seu site sem precisar de conhecimento ou esforço. </p>

<p>Quando o módulo é instalado, você pode configurá-lo na página de definições do módulo. </p>

<p class="alert alert-info">A largura e a altura da imagem devem corresponder ao tamanho original (usado por padrão), do
    contrário o efeito pode não ficar dentro do esperado.</p>

