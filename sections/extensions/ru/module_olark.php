<h3><a href="//special.olark.com/cDqdW">Olark Live Chat</a></h3>

<p>Модуль Olark Live Chat для OpenCart.</p>

<figure class="img-polaroid">
    <img src="img/olark-view.jpg" alt=""/>
</figure>

<p>После установки модуля вы можете настроить его на странице настроек модуля. </p>
<p align="justify" class="alert alert-info">Для более детальной информации обращайтесь к <a href="https://www.olark.com/help/guide" target="_blank" rel="nofollow">официальному веб-сайту</a>.</p>

<h4 align="justify">Как управлять модулем Olark Live Chat</h4>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong> Модули / Расширения </strong>(Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите раздел <strong>Olark Live Chat</strong>. Нажав кнопку &quot;редактировать&quot; (edit), вы перейдете к окну настроек существующего модуля. </p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/olark-view-2.jpg" alt=""/></figure>
</p>
<p>Вы перейдете к следующему окну настроек:</p>
<figure class="img-polaroid">
  <img src="img/olark-view-3.jpg" alt=""/>
</figure>
<h5>Доступные параметры настроек модуля:</h5>
<ul class="marked-list">
  <li> <strong>Module Name</strong> — название модуля;</li>
  <li>
    <div align="justify"><strong>Your Site ID</strong> — идентификатор сайта, который после регистрации и заполнения всех необходимых данных будет указан на
                странице
      <a href='//www.olark.com/install' rel="nofollow" target="_blank"> https://www.olark.com/install</a>;</div>
  </li>
  <li> <strong>Status</strong> — статус модуля (включен/отключен).
    
  </li>
</ul>
<h4 align="justify">Как добавить новый модуль типа  Olark Live Chat</h4>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong> Модули / Расширения </strong>(Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите раздел <strong>Olark Live Chat</strong>. Нажмите кнопку &quot;<strong>Добавить</strong>&quot; и вы перейдете к процессу создания нового модуля этого типа.</p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/olark-view-4.jpg" alt=""/></figure>
</p>
<p>Перед собой вы увидите следующие окно настроек:</p>
<figure class="img-polaroid"> <img src="img/olark-view-3.jpg" alt=""/></figure>
<p>Заполните все необходимые поля настроек:</p>
<ul class="marked-list">
  <li> <strong>Module Name</strong> — укажите название модуля;</li>
  <li>
    <div align="justify"><strong>Your Site ID</strong> — укажите идентификатор сайта, который после регистрации и заполнения всех необходимых данных, будет указан на
                странице
      <a href='//www.olark.com/install' rel="nofollow" target="_blank"> https://www.olark.com/install</a>;</div>
  </li>
  <li> <strong>Status</strong> — определите статус модуля (включен/отключен).
    
  </li>
</ul>
<h4 align="justify">Как добавить новый модуль  Olark Live Chat на Home Page (через Layout Builder)</h4>
<p align="justify">Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong> Модули / Расширения </strong>(Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите раздел <strong>Template Monster Layout Builder</strong>. В соответствующей колонке <strong>напротив модуля Home Page,</strong>  нажмите кнопку &quot;<strong>Редактировать</strong>&quot;.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-7.jpg" alt=""/></figure>
</p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>Вы перейдете к следующему окну настроек:</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-8.jpg" alt=""/></figure>
</p>
<p align="justify">Определитесь с позицией для нового модуля <strong>New  Olark Live Chat</strong>. Помните о том, что вы можете редактировать расположение рядов и колонок в макете, если есть такая необходимость.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-9.jpg" alt=""/></figure>
</p>
<p>Нажмите кнопку <strong>Add Widget</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-11.jpg" alt=""/></figure>
</p>
<p> В появившемся окне выберите тот <strong>Olark Live Chat</strong> модуль, который хотите добавить на главную страницу сайта. </p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/olark-view-6.jpg" alt=""/></figure>
</p>
<p>Модуль  <strong>New  Olark Live Chat </strong> добавлен.</p>
<p>
  <figure class="img-polaroid"> <img src="img/olark-view-7.jpg" alt=""/></figure>
</p>
<p>Не забудьте сохранить настройки модуля, нажав кнопку &quot;<strong>Сохранить</strong>&quot; в правом верхнем углу:
<figure class="img-polaroid"> <img src="img/edit-parallax-14.jpg" alt=""/></figure>
</p>
<p align="justify" class="alert alert-info">Используйте данный способ для добавления модуля любого типа на главную страницу вашего сайта.</p>
<h4 align="justify">Как добавить новый модуль Olark Live Chat на  страницу (макет) вашего интернет-магазина через стандартную систему макетов</h4>
<p>Перейдите к разделу <strong>Дизайн</strong> (Design) &gt; <strong>Макеты </strong>(Layouts).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-15.jpg" alt=""/></figure>
</p>
<p align="justify">Выберите страницу (макет), на которую(-ый) вы хотите добавить новый New  Olark Live Chat модуль. Затем нажмите кнопку &quot;редактировать&quot; (edit)   напротив той (того) страницы (макета), где будет располагаться модуль.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-17.jpg" alt=""/></figure>
</p>
<p>Перейдите к секции &quot;<strong>Выберите расположение модулей</strong>&quot; (Choose the position of the modules).</p>
<p>Определитесь с позицией для нового модуля.</p>
<p>В выпадающем списке, расположенным под наименование позиции, найдите тот модуль, который хотите добавить.</p>
<p>
  <figure class="img-polaroid"> <img src="img/olark-view-8.jpg" alt=""/></figure>
</p>
<p>Нажмите кнопку &quot;<strong>Добавить</strong>&quot; (Add Module).</p>
<p>
  <figure class="img-polaroid"> <img src="img/olark-view-9.jpg" alt=""/></figure>
</p>
<p>После этого действия, будет добавлен новый модуль.</p>
<p>
  <figure class="img-polaroid"> <img src="img/olark-view-10.jpg" alt=""/></figure>
</p>
<p align="justify">В данном окне настроек у вас есть возможность удалять или редактировать добавленные модули, нажав соответствующую кнопку (&quot;Редактировать&quot; или &quot;Удалить&quot;).</p>
<p>Не забудьте сохранить настройки модуля, нажав кнопку &quot;<strong>Сохранить</strong>&quot; в правом верхнем углу:
<figure class="img-polaroid"> <img src="img/edit-parallax-21.jpg" alt=""/></figure>
</p>
<p align="justify" class="alert alert-info">Используйте данный способ для добавления модуля любого типа на любую страницу (макет) вашего интернет-магазина.</p>


