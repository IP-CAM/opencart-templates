<h3>Modül kurulumu</h3>
<p>Modül kurulumu bir kaç aşamadan oluşur. Yönetici panelinize girin (adres çubuğuna /admin şeklinde ekleme yapın -
    www.siteniz.com/admin). Go to
    <strong>Extensions</strong> (Bileşenler) kısmına girin ve açılır menüden
    <strong>Modüller</strong> i seçin Burada kurulmuş ve kurulmayı bekleyen modülleri göreceksiniz. Herhangi
    bir modülün yanında
    <strong>Uninstall</strong> (Kaldır) düğmesi görüyorsanız, modülün kurulmuş olduğunu ve isterseniz
    <strong>Edit</strong> (Düzenle) ye basarak o modülün ayarlarını yapabileceğiniz anlamına gelir.
    <strong>Install</strong> (Kur) a basarak, modülün kurulumunu başlatabilirsiniz. Modülü kaldırmak için
    <strong>Uninstall</strong> (Kaldır) düğmesine basmalısınız. Böylece o modülü tamamen kaldırabilirsiniz.</p>
<p class="alert alert-warning">Unutmayın ki modülü kaldırırsanız, o modül için daha önceden yapmış olduğunuz tüm ayarlar
    silinir ve geri alınamaz.</p>
<figure class="img-polaroid">
    <img src="img/install_1.jpg" alt=""/>
</figure>


<p>Kurulum bittiğinde, modül ayarlarını yapmanız gerekir. Ayarlar her modül için değişebilir fakat
    <strong>Status</strong> (Durum) alanı hepsinde vardır - modülün açılıp kapatıldığı alan.</p>
<figure class="img-polaroid">
    <img src="img/edit-module_1.jpg" alt=""/>
</figure>


<p>Modülü belli bir sayfaya eklemek için, sayfa listesine gitmeniz gerekir. Bunu 2 şekilde yapabilirsiniz: </p>
<ol class="index-list">
    <li>
        <p>Soldaki ana menüden <strong>System -> Design -> Layout</strong> bölümüne girerek;</p>

        <figure class="img-polaroid">
            <img src="img/page-selecting-way.jpg" alt=""/>
        </figure>
    </li>

    <li>
        <p>Modül sayfasındaki <strong>here</strong> bağlantısına tıklayarak.</p>

        <figure class="img-polaroid">
            <img src="img/page-selecting-way-2.jpg" alt=""/>
        </figure>
    </li>
</ol>

<p>Sayfa listesinden modülü koymak istediğiniz yeri seçin ve <strong>Edit</strong> e tıklayın </p>
<figure class="img-polaroid">
    <img src="img/list-of-pages.jpg" alt=""/>
</figure>


<p>Tüm modüllerin listesini ve sayfayanın neresine eklendiklerini görebilirsiniz. </p>


<p>Şu alanlara modül ekleyebilirsiniz (görünüm sırasına göre):</p>
<ol class="index-list">
    <li>Üst bölüm (Header kısmından sonra);</li>

    <li>Sol sütun (sol panele);</li>

    <li>Sağ sütun (sağ panele);</li>

    <li>İçeriğin üstüne;</li>

    <li>İçeriğin altına;</li>

    <li>Alt bölüm.</li>
</ol><p>
    <strong>Sort Order</strong> sıralama alanı, aynı konuma atanmış olan modülleri gösterir. Bir sayfaya modül eklemek
    için
    <strong>Add Module</strong> (Modül Ekle) ye basınız. İstediğiniz modülü ve konumunu seçip
    <strong>Save</strong> e basınız. Modül kurulumu tamamlandı.</p>

