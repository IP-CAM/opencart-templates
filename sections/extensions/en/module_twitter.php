<h3>TemplateMonster Twitter Box</h3>

<p align="justify">This module allows you to display posts from Twitter. It helps your site visitors to be aware of the latest updates and messages in Twitter social network.</p>

<figure class="img-polaroid">
    <img src="img/tm-twitter-view.jpg" alt=""/>
</figure>

<p>When the module is installed, you can configure it on the module configuration page.</p>
<h4>How to manage TemplateMonster Twitter Box module</h4>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p>Find the <strong>TemplateMonster Twitter Box</strong><strong> </strong>section. Press the <strong>edit</strong> button to configure the existing module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-twitter-edit-2.jpg" alt=""/></figure>
</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid">
  <img src="img/tm-twitter-edit-4.jpg" alt=""/>
</figure>
<h5>Available settings:</h5>

<ul class="marked-list">
  <li> <strong>Module Name</strong> — the name of the module;</li>
  <li><strong>Twitter Page URL</strong> — allows specifying custom Twitter Page URL;</li>
  <li><strong>Color Scheme</strong> — select the color scheme (light, dark);</li>
  <li><strong>Width</strong> — the pixel width of the widget;</li>
  <li><strong>Height</strong> — the pixel height of the widget; </li>
  <li><strong>Status</strong> — define the module status (enabled/disabled).</li>
</ul>

<h4>How to add a new TemplateMonster Twitter Box module</h4>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">Find the <strong>TemplateMonster Twitter Box </strong>section. Press the<strong> Add New </strong>button to create a new module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-twitter-edit-3.jpg" alt=""/></figure>
</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid"> <img src="img/tm-twitter-edit-4.jpg" alt=""/></figure>
<p>Fill in the required fields:</p>

<ul class="marked-list">
  <li><strong>Module Name</strong> — enter the name of the module;</li>
  <li><strong>Page URL</strong> — specify custom Twitter Page URL;</li>
  <li><strong>Color Scheme</strong> — select the color scheme (light, dark);</li>
  <li> <strong>Width</strong> — set the pixel width of the widget;</li>
  <li> <strong>Height</strong> — set the pixel height of the widget; </li>
  <li> <strong>Status</strong> — define the module status (enabled/disabled).</li>
</ul>
