<h3>TemplateMonster Lazy load</h3>

<p>Modification that allows you to load product images at the time they appear when
    scrolling the website. This extension allows you to speed up website performance. In
    order to change preloader image, you should replace the
    <strong>lazy_loader.gif</strong> file in the <strong>catalog/view/theme/themeXXX/image/</strong>folder.
</p>