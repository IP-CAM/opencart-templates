<h3>Installing the template over existing store (Instalacja szablonu w istniejącym sklepie)</h3>

<p>Jeśli już posiadasz gotowy sklep OpenCart i potrzebujesz tylko zmienić jego wygląd za pomocą tego motywu, skorzystaj
    z tej metody instalacji.</p>

<p class="alert alert-warning">Upewnij się, że twoja wersja OpenCart odpowiada wymaganiom szablonu. Wszystkie wersje
    OpenCart są dostępne na <a href=" http://www.OpenCart.com/index.php?route=download/download"
                               target="_blank" class="spec_link">oficjalnej witrynie OpenCart</a>.
</p>
<ol class="index-list">
    <li>Żeby zainstalować szablon OpenCart, trzeba przesłać pliki na serwer. Archiwum
        <strong>"themeXXX(update).zip"</strong> znajduje się w folderze OpenCart. Trzeba go rozpakować na swój komputer.
    </li>
    <li>Po tym prześlij rozpakowane pliki do katalogu głównego instalacji OpenCart. <br/> Możesz obejrzeć szczegółowe
        wideo instrukcje: <a href="/help/how-upload-files-server-2.html" target="_blank">jak przesłać pliki na
            serwer</a>. <br/>
        <span class="info-box">Pliki szablonu w żaden sposób nie wpłyną na zawartość twojego sklepu.</span>
    </li>
    <li>Otwórz swoją witrynę w dowolnej przeglądarce i w pasku adresu przeglądarki dodaj swoło /install na końcu URL
        adresu swojej witryny (
        <i>na przykład, http://your_domain_name/OpenCart/install</i>
        )
    </li>
    <li>Naciśnij przycisk <strong>Continue (Dalej)</strong>. To może trochę potrwać.</li>
    <li>Wejdź na panel administracyjny, przejdź do <strong>Extensions (Rozszerzenia) -&gt; Extension Installer
            (Instalator rozszerzeń) </strong> i prześlij archiwum plików <strong>themeXXX(extensions)</strong>. Kiedy
        wszystkie pliki zostaną przesłane, przejdź do <strong>Extensions (Rozszerzenia) -&gt; Modifications
            (Modyfikacje)</strong> i kliknij <strong>refresh (odśwież)</strong>w prawym górnym rogu ekranu;
    </li>
    <li>Na następnym etapie przejdź do <strong>System-&gt; Settings (Ustawienia)</strong>, dalej idź do zakładki Sklep i
        wybierz szablon <strong>themeXXX</strong> w polu Szablon.
    </li>
</ol>

