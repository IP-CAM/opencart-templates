<h3>TemplateMonster Newsletter Popup</h3>

<p>Данный модуль добавляет всплывающее окно с предложением подписаться на email рассылку при посещении Вашего сайта.
    Окно не выводится для авторизованных (вошедших в аккаунт) или уже подписавшихся на рассылку
    пользователей.</p>

<figure class="img-polaroid">
    <img src="img/tm-newsletter-popup-view.jpg" alt=""/>
</figure>

<p>После установки модуля, Вы можете настроить его на странице настроек модуля.</p>

<h5>Настройки:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>название модуля;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background Image</dt>
            <dd>фоновое изображение всплывающего окна;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background image width</dt>
            <dd>ширина фонового изображения;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background image height</dt>
            <dd>высота фонового изображения;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Cookie Storage Time</dt>
            <dd>время хранения кукис;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Heading Title</dt>
            <dd>заголовок в всплывающем окне;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>описание в всплывающем окне;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>статус модуля (включен/отключен).</dd>
        </dl>
    </li>
</ol>

