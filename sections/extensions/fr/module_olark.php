<h3>Olark Live Chat</h3>

<p>Le module Olark Live Chat pour OpenCart.</p>
<figure class="img-polaroid">
    <img src="img/olark-view.jpg" alt=""/>
</figure>

<p>Une fois le module installé, vous pouvez le configurer sur la page de configuration du module. </p>

<h5>Paramètres:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>le nom du module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Site id</dt>
            <dd>ID du site qui peut être trouvé sur la page de
                <a href='https://www.olark.com/install' rel="nofollow" target="_blank">https://www.olark.com/install</a>
                après avoir créé votre compte et rempli les champs requis;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>le statut du module (activé/désactivé).</dd>
        </dl>
    </li>
</ol>

