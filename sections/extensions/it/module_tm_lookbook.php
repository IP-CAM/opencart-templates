<h5>Collegamento prodotti l'immagine collezione, aggiungendo area di selezione del prodotto:</h5>
<p>Per aggiungere un prodotto ad un insieme, selezionarlo nel campo Prodotti:</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_product.jpg" alt=""/>
</figure>

<p>Per contrassegnare i prodotti selezionati sull'immagine, premere il pulsante "Add Mappa immagine":</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_image_map.jpg" alt=""/>
</figure>


<p>Dopo aver premuto il pulsante "Add Mappa immagine", in basso, vedrete una finestra, che permette di creare aree di selezione e l'aggiunta di prodotti a tali aree.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_image_map_object.jpg" alt=""/>
</figure>

<p>Le aree che si creano possono essere di 3 diverse forme: quadrato, rotondo, e complessi (poligonali).</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures.jpg" alt=""/>
</figure>

<p>Per creare un'area di selezione sopra l'immagine, premere il pulsante, il cui nome corrisponde alla forma necessaria di area di selezione.</p>


<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_add.jpg" alt=""/>
</figure>

<p>Dopo aver scelto la forma, posizionarlo sulla foto. Sinistro del mouse la parte necessaria di immagine, disegnare l'area.</p>

<p class="alert alert-warning">Nota: selezione poligonale deve essere sempre chiuso, fare il primo e l'ultimo punto coincidono.</p>

<p>Dopo aver posizionato l'area di selezione sull'immagine, scegliere il prodotto che viene visualizzato nell'area selezionata. Per fare questo, premere il pulsante "Modifica" ("Edit").</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_edit.jpg" alt=""/>
</figure>

<p>Quindi, fare doppio clic l'area e, nella finestra a discesa, selezionare il prodotto necessario, premere il pulsante "Salva" ("Salve").</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_save.jpg" alt=""/>
</figure>

<p>Dopo aver segnato tutti i settori e la mappatura di tutti i prodotti, premere il pulsante Salva.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_save.jpg" alt=""/>
</figure>

<p>Salva impostazioni del modulo.</p>