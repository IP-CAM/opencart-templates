<h3>TemplateMonster 360-view</h3>

<p align="justify"> <strong>TemplateMonster 360-view</strong> is the module that turns image frames into animations.  The aim of this module is to provide a 360 degree view of  a product. </p>
<figure class="img-polaroid"> <img src="img/5.gif" alt=""/> </figure>
<h4>How to manage TemplateMonster 360-view module</h4>
<p>Navigate to the <strong>Extensions</strong> &gt; <strong>Extensions </strong>section.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/> </figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/> </figure>
</p>
<p>Find the <strong>TemplateMonster 360-view</strong> section. Press the <strong>edit</strong> button to configure the existing module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid"> <img src="img/3d.jpg" alt=""/> </figure>
<h5>Available settings:</h5>
<ol class="marked-list">
  <li><strong>Module Name</strong> — enter the module name (not visible on the frontend);</li>
  <li><strong>Status</strong> — module status (enabled/disabled);</li>
  <li><strong>Image Width</strong> — set the  image width, it is  the actual display width (in px);</li>
  <li><strong>Image Height</strong> —  set the  image height, it is  the actual display height (in px);</li>
  <li><strong>Rotation Speed</strong> — you can change the speed of rotation. The default is 100. Make it spin <strong>faster</strong> by <strong>decreasing</strong> the number  or <strong>slow </strong>it down by <strong>increasing</strong> the number. There is an inverse relationship between<strong> Rotation Speed </strong>value and actual speed of rotation;</li>
  <li><strong>Select Your Product Image</strong> — upload images for the spin;</li>
  <li><strong>Autoplay </strong> — if enabled starts the animation on load.</li>
</ol>
<p>The <strong>TemplateMonster 360-view</strong> module supports multilanguage, so you can configure it for each shop language separately.</p>
<ol class="marked-list">
  <li><strong> Title, Description, Information</strong> — enter the informative texts you want to display within the module.</li>
</ol>
<p>When complete, tab<strong> Save</strong>.</p>
<h4><strong> Requirements for the image files:</strong></h4>
<ul class="marked-list">
  <li>Your images should have filenames which identify the sequence using numbers starting at 1 (e.g. 1.png, 2.png, 3.png etc).</li>
  <li>Use a <strong>separate</strong> folder for each image spin.</li>
  <li>All images should be the same size.</li>
  <li>The object should be on a transparent background.</li>
  <li> PNG image format is preferred.</li>
</ul>
