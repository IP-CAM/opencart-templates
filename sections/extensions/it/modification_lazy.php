<h3>TM Lazy load</h3>

<p>Modifica che consente di caricare immagini del prodotto nel momento in cui appaiono quando si scorre la pagina sul sito Web. L'estensione consente così di accelerare le prestazioni del Sito Web. Per modificare l'immagine precaricata è necessario sostituire il file
    <strong>lazy_loader.gif</strong> nella cartella <strong>catalog/view/theme/themeXXX/image/</strong>.</p>