<h3>TemplateMonster Twitter Box</h3>

<p>Bu modül, Twitter'daki gönderilerinizi göstermek için kullanılır. Böylelikle sitenize gelen ziyaretçiler,
    güncel Twitter mesajlarınızdan da haberdar olur.</p>
<figure class="img-polaroid">
    <img src="img/tm-twitter-view.jpg" alt=""/>
</figure>

<p>Modül kurulduktan sonra, tüm düzenlemeleri modül ayar sayfasından yapabilirsiniz.</p>

<figure class="img-polaroid">
    <img src="img/tm-twitter-edit.jpg" alt=""/>
</figure>

<h5>Ayarlar:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>modülün ismi;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Twitter Page URL</dt>
            <dd>Twitter hesabınızın bağlantı adresi;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Widget ID</dt>
            <dd>Kimliğinizi
                <a href="https://twitter.com/settings/widgets" target="_blank">bu adresten</a>
                edinebilirsiniz;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Tweet Limit</dt>
            <dd>gösterilecek tweet sayısı;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Color Scheme</dt>
            <dd>renk şeması (açık, koyu);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>kutu genişliği (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>kutu uzunluğu (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>modülün durumu (aktif/pasif).</dd>
        </dl>
    </li>
</ol>