<h3>TM CountDown</h3>

<p>Ce module vous permet de créer des compteurs à rebours. Il existe deux types de minuteries, numériques et radiales.
</p>
<p>Numérique:</p>

<figure class="img-polaroid">
    <img src="img/tm_countdown.png" alt=""/>
</figure>

<p>Radial:</p>

<figure class="img-polaroid">
    <img src="img/tm_countdown_circle.png" alt=""/>
</figure>

<p>Afin de choisir le compteur radial vous devez sélectionner "Radial" dans le champ "Type". Dans ce cas, des paramètres supplémentaires pour la minuterie radiale deviennent disponibles:</p>
<figure class="img-polaroid">
    <img src="img/tm_countdown_type.png" alt=""/>
</figure>

<h5>Réglages:</h5>
<p>Les paramètres primaires du module sont les suivantes:</p>

<ol class="index-list">
    <li>
        <dl class="inline-term">
                <dt>Nom du module (Module Name)</dt>
                <dd>le titre du module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Statut (Status)</dt>
            <dd>l'état du module (activé/désactivé);</dd>
        </dl>
    </li>
    
        
    
    <li>
        <dl class="inline-term">
            <dt>Date (Date)</dt>
            <dd>La date au format yyyy-mm-dd hh:mm:ss (par exemple: 2012-12-12 00:00:00);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Type (Type)</dt>
            <dd>Le type de compteur (standard/radial);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description (Description)</dt>
            <dd>la légende du compteur (description);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Titre Jours (Title on Days)</dt>
            <dd>le titre pour les jours du compteur;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Titre Heures (Title on Hours)</dt>
            <dd>le titre pour les heures du compteur;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Titre Minutes</dt>
            <dd>le titre pour les minutes du compteur;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Titre Secondes</dt>
            <dd>le titre pour les secondes du compteur;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Afficher les jours(Show days)</dt>
            <dd>si vous souhaitez afficher/masquer les jours;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Afficher les heures (Show hours)</dt>
            <dd>si vous souhaitez afficher/masquer les heures;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Afficher les minutes(Show minutes)</dt>
            <dd>si vous souhaitez afficher/masquer les minutes;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Afficher les secondes</dt>
            <dd>si vous souhaitez afficher/masquer les secondes;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Type d'animation (Animation Type)</dt>
            <dd>le type d'animation, le tic-tac ou une animation fluide;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Direction de l'animation (Direction of animation)</dt>
            <dd>la direction de l'animation (dans le sens ou dans le sens inverse des aiguilles d'une montre);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Couleur d'arrière-plan (Background circle color)</dt>
            <dd>la couleur du cercle intérieur;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Couleur du cercle de jours (Days circle color)</dt>
            <dd>la couleur du cercle intérieur de jours;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Couleur du cercle d'heures (Hours circle color)</dt>
            <dd>la couleur du cercle intérieur d'heures;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Couleur du cercle de minutes (Minutes circle color)</dt>
            <dd>la couleur du cercle intérieur de minutes;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Couleur du cercle de secondes (Seconds circle color)</dt>
            <dd>la couleur du cercle intérieur de secondes;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Largeur de cercle du premier plan (Foreground circle width)</dt>
            <dd>épaisseur du cercle extérieur du temporisateur;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Largeur de cercle de l'arrière-plan</dt>
            <dd>épaisseur du cercle intérieur du temporisateur;</dd>
        </dl>
    </li>
</ol>