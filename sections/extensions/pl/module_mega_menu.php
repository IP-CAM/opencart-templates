<h3>TM Megamenu</h3>

<p>Ten moduł umożliwia tworzenie kompleksowych menu. Moduł zapewnia możliwość wyświetlania pojedynczego elementu (bez podmenu), element z podmenu i element z wieluwierszowym i wielukolumnowym blokiem podmenu. Ten moduł może być wyświetlony w różnych pozycjach, lecz o tej samej treści.
</p>

<h5>Ogólna informacja</h5>

<ol class="index-list">
    <li>
        <p>Aktywacja menu</p>

        <p>W celu umożliwienia megamenu, należy przejść na stronę <strong>Rozszerzenia -> Moduły -> TM Megamenu (Extensions -> Modules -> TM Megamenu)</strong>, nacisnąć przycisk <strong>Edytuj (Edit)</strong> obok odpowiedniego elementu oraz w nowym oknie wybrać opcję <strong>Włącz (Enabled)</strong>.
        </p>
    </li>
</ol>

