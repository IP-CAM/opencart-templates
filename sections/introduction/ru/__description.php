<article id="whatisopencart">
    <h3>Что представляет собой OpenCart?</h3>

    <p>OpenCart - это бесплатная платформа электронной коммерции с открытым исходным кодом. OpenCart позволяет создать
        профессиональную и надежную основу для построения успешного онлайн магазина. Сообщество Opencart включает
        множество пользователей, начиная с опытных веб-разработчиков, ищущих удобный интерфейс, и заканчивая владельцами
        магазинов, впервые запускающими онлайн-бизнес. OpenCart предоставляет огромное количество функций, позволяющих
        полностью контролировать магазин. С инструментами OpenCart Вы легко раскроете потенциал магазина.
        <a href="http://docs.opencart.com" target="_blank">Подробнее</a>
    </p>
</article>

