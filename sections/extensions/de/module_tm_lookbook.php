<h3>TM Lookbook</h3>

<p>Das Lookbook ist eine Reihe von Fotos, wo Sie einen beliebigen Teil des Bildes markieren können. Dieses Modul dient dazu, die Produkte von einem oder von mehreren Marken anzuzeigen.</p>
<p>Die Module verfügt über zwei Anzeigemodus in der Shopposition : in Form eines Banners (1) oder in Form einer Kollektion (2).</p>


