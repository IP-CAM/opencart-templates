<h3>TemplateMonster Twitter Box</h3>

<p>Das jeweilige Modul zeigt die Beiträge aus dem sozialen Netzwerk Twitter an. Dank ihm sehen Ihre
    Webseitenbesucher die letzten Aktualisierungen auf Ihrer Seite bei Twitter.</p>

<figure class="img-polaroid">
    <img src="img/tm-twitter-view.jpg" alt=""/>
</figure>

<p>Nachdem Sie das Modul installiert haben, können Sie es auf der Konfigurationsseite einstellen.</p>

<h5>Einstellungen:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>der Modulname;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Twitter Page URL</dt>
            <dd>der Link zum Twitter Konto;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Widget ID</dt>
            <dd>Widget ID, die Sie hier
                <a href="https://twitter.com/settings/widgets" target="_blank">erhalten können</a>
                ;
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Tweet Limit</dt>
            <dd>die Anzahl der Tweets;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Color Scheme</dt>
            <dd>das Farbschema (hell, dunkel);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>die Blockbreite in Pixel;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>die blockhöhe in Pixel;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>der Modulstatus (aktiviert/deaktiviert).</dd>
        </dl>
    </li>
</ol>

