<h3>Parallax</h3>

<p align="justify">Параллакс-скроллинг — это специальная техника скроллинга в компьютерной графике, при которой изображения на заднем
    фоне двигаются медленнее контента, который находится на переднем фоне, создавая, таким образом, иллюзию глубины и
    эффект погружения в двухмерной видео-игре. Данный модуль добавит параллакс-эффект на сайт, не требуя каких-либо
    дополнительных навыков и усилий. </p>

<p align="justify" class="alert alert-info">Ширина и высота картинки должна соответствовать оригинальной (используемой по умолчанию),
    иначе эффект может не соответствовать вашим ожиданиям.</p>


<p>После установки модуля вы можете настроить его на странице настроек модуля.</p>
<h4>Как управлять модулем Parallax</h4>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong> Модули / Расширения </strong>(Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/> </figure>
</p>
<p>Выберите  <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/> </figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите раздел <strong>TemplateMonster Parallax</strong>. Нажав кнопку &quot;редактировать&quot; (edit), вы перейдете к окну настроек  существующего модуля. В данном случае это модуль <strong>Lookbook Parallax</strong>.</p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>Перед собой вы увидите следующие окно настроек:</p>
<figure class="img-polaroid"> <img src="img/edit-parallax-3.jpg" alt=""/> </figure>
<h5>Доступные параметры настроек модуля:</h5>
<ul class="marked-list">
  <li> <strong>Название модуля</strong> — название модуля; </li>
  <li> <strong>Ширина</strong> — ширина изображения; </li>
  <li> <strong>Высота</strong> — высота изображения; </li>
  <li> 
    <div align="justify"><strong>Размытие </strong> —  	вы можете активировать эффект размытия слишком маленьких изображений [относительно размера контейнера]; </div>
  </li>
  <li> 
    <div align="justify"><strong>Направление </strong> — укажите направления движения parallax. Normal — в противоположную сторону относительно движения прокрутки, Inverse - по направлению движения прокрутки; </div>
  </li>
  <li>
    <div align="justify"><strong>Скорость</strong> —  	задайте значение скорости эффекта parallax относительно скорости прокрутки. Значение должно быть в пределах от 0 до 2; </div>
  </li>
  <li> <strong>Статус</strong> — состояние модуля (включен/отключен).
    </dt>
  </li>
</ul>
<p align="justify" class="alert alert-info">Ширина и высота картинки должна соответствовать оригинальной (используемой по умолчанию), иначе эффект может не соответствовать вашим ожиданиям. </p>
<h5 align="justify">Как добавить новый   модуль типа Parallax</h5>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong> Модули / Расширения </strong>(Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите раздел <strong>TemplateMonster Parallax</strong>. Нажмите кнопку &quot;<strong>Добавить</strong>&quot; и вы перейдете к процессу создания нового модуля этого типа.</p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-4.jpg" alt=""/></figure>
</p>
<p>Перед собой вы увидите следующие окно настроек:</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-5.jpg" alt=""/></figure>
</p>
<p>Заполните все необходимые поля настроек:</p>
<ul class="marked-list">
