<h3>TemplateMonster Cookie Policy</h3>

<p>Ten moduł powiadamia użytkowników witryny o wykorzystaniu plików cookies (Img. 27).
    Zgodnie z Ustawą Plików Cookies należy dodać odpowiednie moduły do swojej witryny,
    które będą wyświetlać komunikat ostrzegawczy o plikach cookies, używanych na twojej
    witrynie.</p>

<p>Pliki cookies to małe pliki tekstowe, które witryny internetowe umieszczają na
    komputerach użytkowników. Są one zazwyczaj używane do identyfikacji poszczególnych
    użytkowników, żeby zapewnić im lepsze doświadczenie. Pliki cookies są kluczowym
    elementem wielu funkcji witryny, które bierzemy za pewnik.</p>

<p>Dla przykładu, to zazwyczaj pliki cookies pozwalają sklepom internetowym pamiętać,
    jakie przedmioty masz w koszyku. Wykorzystuje się ich także do utrzymania
    użytkownika zalogowanym w witrynie albo do dostarczania cennych informacji oraz
    statystyk używania witryny jej właścicielom.</p>

<p>Jeśli prowadzisz witrynę, ona prawie na pewno używa plików cookies. Ich najbardziej
    lubiana funkcja to monitorowanie liczby odwiedzających oraz ich zachowań za pomocą
    narzędzi takich jak Google Analytics. One również mogą być używane do wyświetlania
    odpowiednich reklam dla użytkowników lub - w przypadku sprzedaży online - do
    zasilania kluczowych części systemu zakupów online.</p>

