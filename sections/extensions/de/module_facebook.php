<h3>TM Facebook Box</h3>

<p>Das jeweilige Modul zeigt Facebook like box an. Es zeigt sowohl Gesichter als auch die Anzahl der Fans, denen Ihre
    Webseite oder Ihre Marke gefällt.</p>
<figure class="img-polaroid">
    <img src="img/tm-facebook-view.jpg" alt=""/>
</figure>

<p>Nachdem Sie das Modul installiert haben, können Sie es auf der Konfigurationsseite einstellen.</p>

<h5>Einstellungen:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>Modulname;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Facebook Page URL</dt>
            <dd>der Link zur Facebook Seite;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Color Scheme</dt>
            <dd>Farbschema (hell, dunkel );</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Show Faces</dt>
            <dd>die Gesichter von Benutzern, denen die Seite gefällt (yes, no) zeigen;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Show Stream</dt>
            <dd>die letzten Beiträge (yes, no) zeigen;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Show Header</dt>
            <dd>den Block-Titel (yes, no) zeigen;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Show Border</dt>
            <dd>die Blockgrenze (yes, no) zeigen;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>die Blockbreite in Pixel;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>die Blockhöhe in Pixel;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>der Modulstatus (aktviert/deaktiviert).</dd>
        </dl>
    </li>
</ol>
