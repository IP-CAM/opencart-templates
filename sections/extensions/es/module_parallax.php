<h3>TM Parallax</h3>

<p>Este módulo le permite crear zonas con efecto de parallax en las páginas. El módulo proporciona la capacidad de mostrar imágenes, vídeos y otros módulos. El módulo soporta un número ilimitado de las capas, cualquiera de los cuales puede moverse en diferentes direcciones y a diferentes velocidades.</p>

<figure class="img-polaroid">
    <img src="img/tm_parallax.png" alt=""/>
</figure>

<h5>Configuración del módulo TM Parallax:</h5>
<h6>Ajustes generales:</h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Nombre del módulo (Module Name)</dt>
            <dd>nombre del módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Estado (Status)</dt>
            <dd>estado del módulo (activado/desactivado);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Imagen (Image)</dt>
            <dd>imagen de fondo de paralaje (principal);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Anchura (Width)</dt>
            <dd>ancho de la imagen de fondo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Altura (Height)</dt>
            <dd>altura de la imagen de fondo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Difuminado (Blur)</dt>
            <dd>borrosidad de la imagen de fondo en el caso de que el tamaño (dimensiones) es demasiado bajo para la visualización de alta calidad en el área de paralaje;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Dirección (Direction)</dt>
            <dd>dirección de movimiento de efecto paralaje;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Velocidad (Speed)</dt>
            <dd>velocidad de movimiento de efecto paralaje;</dd>
        </dl>
    </li>
</ol>

<h6>Ajustes adicionales de módulos:</h6>
<ol class="index-list">
    <li>
        <p>Con el fin de crear una nueva capa que necesita hay que pulsar el botón <strong>(Add Layer) "Añadir capa"</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/tm_parallax_edit-1.png" alt=""/>
        </figure>

        <p>Después de añadir la capa especifique los siguientes ajustes:</p>

        <h5>Configuración de la capa:</h5>

        <ol class="index-list">
            <li>
                <dl class="inline-term">
                    <dt>Tipo (Type)</dt>
                    <dd>tipo de capa;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Descripción (Description)</dt>
                    <dd>descripción de la capa;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Imagen (Image)</dt>
                    <dd>imagen de la capa;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Anchura (Width)</dt>
                    <dd>anchura de la imagen de la capa;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Altura (Height)</dt>
                    <dd>altura de la imagen de capa;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Velocidad (Speed)</dt>
                    <dd>la velocidad de movimiento de capa;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Descolorarse (Fade)</dt>
                    <dd>la capa gradualmente será visible desde la total transparencia a la opacidad completa dependiendo de la posición de desplazamiento de capa;
                    </dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Difuminado (Blur)</dt>
                    <dd>borrosidad de la imagen de fondo en el caso de que el tamaño (dimensiones) es demasiado bajo para la visualización de alta calidad en el área de paralaje; Es aplicable únicamente a las capas de tipo "Media";
                    </dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Dirección (Direction)</dt>
                    <dd>dirección de movimiento de la capa;</dd>
                </dl>
            </li>
        </ol>
    </li>
    <li>
        <p>Con el fin de mostrar el módulo en la capa, hay que pulsar sobre el botón <strong>"Añadir Módulo" (Add Module)</strong> en la capa adecuada.</p>
        <figure class="img-polaroid">
            <img src="img/tm_parallax_edit-2.png" alt=""/>
        </figure>
        <p>Después de pulsar el botón <strong>"Añadir Módulo" (Add Module)</strong>, el campo <strong>"Nombre del Módulo añadido" (Added Module name)</strong> estará disponible, es necesario seleccionar el módulo aquí, para mostrarlo en la capa actual.</p>
    </li>
</ol>

