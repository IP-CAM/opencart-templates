<h4>TM Blog Articles (TM Artykuły Blogu)</h4>

<p>Dany moduł pozwala wyświetlać artukuły blogu zgodnie z kategoriami.</p>

<figure class="img-polaroid">
    <img src="img/tm-blog-articles-view.jpg" alt="">
</figure>

<p>Po zainstalowaniu modułu możesz go skonfigurować na stronie ustawień.</p>

<h5>Ustawienia:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd> status modułu (włączony/wyłączony).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Nazwa Modułu</dt>
            <dd>nazwa modułu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit Artykułów</dt>
            <dd> liczba artykułów do wyświetlenia;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Pokaz Przycisku Readmore (Pokaż więcej)</dt>
            <dd>włącza/wyłącza przycisk Read more (Pokaż więcej);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Pokaz Daty</dt>
            <dd>włącza/wyłącza datę;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Pokaz Autora</dt>
            <dd> włącza/wyłącza autora artykułu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Pokaz Komentarzy</dt>
            <dd> włącza/wyłącza liczbę komentarzy w artykule;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Pokaz Zdjęcia </dt>
            <dd> włącza/wyłącza zdjęcie artykułu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Szerokość Zdjęcia</dt>
            <dd> szerokość zdjęcia;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Wysokość Zdjęcia</dt>
            <dd> wysokość zdjęcia;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit symboli w opisie</dt>
            <dd> liczba symboli w opisie artykułu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Kategoria</dt>
            <dd>  możesz ustawić kategorię do ściągnięcia artykułów. Istnieje również  możliwość wyświetlenia najbardziej popularnych lub ostatnie dodanych artykułów. 
            </dd>
        </dl>
    </li>
</ol>

