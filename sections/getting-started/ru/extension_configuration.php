<h3>Установка на существующий магазин</h3>

<p>Следуйте данному типу установки, если у вас уже есть действующий сайт OpenCart и
    требуется всего лишь изменить его внешний вид, путем установки новой темы.</p>

<p class="alert alert-warning">Убедитесь, что Ваша версия движка OpenCart соответствует требованиям шаблона. Все версии
    OpenCart доступны на <a
        href=" http://www.opencart.com/index.php?route=download/download"
        target="_blank" class="spec_link"> официальном сайте OpenCart
    </a>.</p>
<br/>
<ol class="index-list">
    <li>Чтобы установить шаблон OpenCart, нужно загрузить его файлы на хостинг сервер. В
        папке OpenCart шаблона найдите архив <strong>
            &quot;themeXXX(update).zip&quot;</strong> разархивируйте его на свой
        компьютер.
    </li>
    <li>После этого загрузите разархивированные файлы в корневую директорию
        установленного OpenCart. <br/>
        Вы также можете воспользоваться детальной видео инструкцией,
        <a href="/help/ru/how-upload-files-server-2.html" target="_blank"> как загружать
            файлы на
            хостинг
            сервер
        </a>
        . <br/>
        <strong>Файлы шаблона никаким образом не повредят магазину;</strong>
    </li>
    <li>Затем откройте сайт в браузере и добавьте /install в конце адресной строки
        браузера (
        <i>например, http://your_domain_name/opencart/install</i>
        )
    </li>
    <li>В появившейся на экране странице нажмите кнопку <strong>Continue
            (Продолжить)</strong>.
        Обновление
        темы может занять некоторое время.
    </li>
    <li>Войдите в панель управления Вашим сайтом, перейдите в меню <strong>Расширения ->
            Установщик
            расширений
            (Extensions
            -> Extension
            Installer)</strong>
        и
        загрузите файлы из архива <strong>themeXXX(extensions)</strong>. После загрузки
        всех файлов архива, перейдите в меню <strong>Расширения -> Модификации
            (Extensions ->
            Modifications)</strong> и нажмите
        копку <strong>обновить(refresh)</strong> в правом верхнем углу экрана;
    </li>
    <li>Далее перейдите в настройки магазина <strong> Расширения -> Темы (Extensions ->
            Themes)</strong>, и во вкладке
        Магазин (Store) в поле Шаблон (Template) выберите тему -
        <strong>themeXXX</strong>.
    </li>
</ol>
