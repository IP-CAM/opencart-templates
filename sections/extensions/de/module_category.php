<h3>TM Category</h3>

<p>Das jeweilige Modul ermöglicht es sowohl Kategorien als ein Akkordeonmenü als auch
    alle Unterkategorien (Bild 13) anzuzeigen. Normalerweise wird es in der Seitenleiste
    benutzt. </p>

<p>Nachdem Sie das Modul installiert haben, können Sie es auf der Konfigurationsseite
    aktivieren und deaktivieren.</p>

