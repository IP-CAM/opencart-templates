<h3>TemplateMonster Google Map</h3>

<p>Ten moduł pozwala dodać na swoją witrynę mapę Google z nieograniczoną liczbą znaczników </p>

<p>Centrum mapy i jeden ze znaczników wyznacza się w ustawieniach sklepu: <strong>System-&gt;Settings
        (Ustawienia)</strong>, w zakładce <strong>General (Ogólne)</strong>, pole<strong>Geocode</strong>. Po kliknięciu
    na znacznik pojawi się wyskakujące okno z zakładką <strong>Adres</strong>.</p>

<figure class="img-polaroid">
    <img src="img/tm-googlemap-view.jpg" alt=""/>
</figure>

<p>Kiedy moduł został zainstalowany, możesz konfigurować go na stonie konfiguracji
    modułu.</p>

<h5>Ustawienia:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd> nazwa modułu;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Google Map API key</dt>
            <dd> klucz API do Google Map. Możesz znaleźć więcej szczegółów na
                <a target="_blank" href="https://developers.google.com/maps/documentation/javascript/tutorial#api_key">oficjalnej
                    witrynie</a>;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd> status modułu (włączony/wyłączony);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Map Type</dt>
            <dd> rodzaj mapy. Są dostępne 4 opcje:</dd>
        </dl>
        <ul class="marked-list">
            <li>
                <dl class="inline-term">
                    <dt>Roadmap</dt>
                    <dd> mapa drogowa, używana domyślnie;</dd>
                </dl>
            </li>

            <li>
                <dl class="inline-term">
                    <dt>Terrain (Landscape)</dt>
                    <dd> mapa topograficzna na podstawie informacji o terenie;</dd>
                </dl>
            </li>

            <li>
                <dl class="inline-term">
                    <dt>Hybrid</dt>
                    <dd> kombinacja podstawowej mapy i zdjęć satelitarnych;</dd>
                </dl>
            </li>

            <li>
                <dl class="inline-term">
                    <dt>Satellite</dt>
                    <dd> zdjęcia Google Earth, zrobione przez satelitę;</dd>
                </dl>
            </li>
        </ul>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Map Zoom</dt>
            <dd> wartość liczbowa. Początkowe skalowanie, używane do wyświetlania mapy. Wartość 0 odpowiada najniższemu
                poziomowi skalowania. Po zwiększeniu tej wartości mapa się powiększa;
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Sensor</dt>
            <dd> pokazuje, czy jest wykorzystywany czujnik, który określa lokalizację użytkowników.</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Map Width</dt>
            <dd> szerokość mapy. Trzeba określić jednostkę miary (% albo px);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Map Height</dt>
            <dd> wysokość mapy. Trzeba określić jednostkę miary (% albo px);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Map Styles</dt>
            <dd>ustawia styl mapy. Różnorodne style są dostępne na
                <a href='https://snazzymaps.com/' rel="nofollow" target="_blank">https://snazzymaps.com/</a>;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Disable UI</dt>
            <dd> domyślnie włącza/wyłącza interfejs użytkownika;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Scrollwheel</dt>
            <dd> włącza/wyłącza skalowanie mapy za pomocą kółka myszy;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Draggable</dt>
            <dd> włącza/wyłącza opcję przesuwania mapy;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Marker Image</dt>
            <dd> ustanawia obrazek znacznika. Jeśli pole pozostanie puste, będzie zastosowany domyślny znacznik;;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Active Marker Image</dt>
            <dd> ustanawia obrazek znacznika po klikaniu;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Marker Image Width</dt>
            <dd> ustanawia szerokość znacznikowego obrazka;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Marker Image Height</dt>
            <dd> ustanawia wysokość znacznikowego obrazka.</dd>
        </dl>
    </li>
</ol>

<p>Aby dodać znacznik, kliknij <strong>Add New (Dodaj nowy)</strong>. W polu <strong>Geocode</strong>
    określ koordynaty znacznika (szerokość i długość geograficzną - Przykład: 40.6700 ,
    -73.9400 ) i w polu <strong>Adres</strong> wprowadź informacje, które będą się
    pojawiać po klikaniu na znacznik. Potrzebne koordynaty można znaleźć na
    <a href='http://www.mapcoordinates.net' rel="nofollow" target="_blank">http://www.mapcoordinates.net</a>.
    Aby usunąć znacznik, kliknij <strong>Delete Marker (Usuń znacznik)</strong>.
</p>

