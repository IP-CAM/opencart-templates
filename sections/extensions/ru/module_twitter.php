  <li> <strong>Height</strong> — задайте значение высоты блока в пикселях; </li>
  <li> <strong>Status</strong> — укажите статус модуля (включен/отключен). </li>
</ul>

<h5>Как получить  Twitter widget ID</h5>
<p align="justify">Если Twitter виджет, который  пытаетесь активировать, запрашивает   Twitter widget ID, вам необходимо будет зарегистрировать новый виджет в системе Twitter, перейдя по <a href="https://twitter.com/settings/widgets" onclick="__gaTracker['send', 'event', 'outbound-article', 'https://twitter.com/settings/widgets', 'Twitter widget settings');">этой ссылке</a>.</p>
<p align="justify">Чтобы получить доступ к настройкам виджета Twitter, необходимо войти в учетную запись Twitter, которая будет выводиться в виджете на вашем сайте. Затем,  в правом верхнем углу, нажмите на кнопку с изображением шестеренки и выберите <strong>Settings</strong> и перейдите во вкладку <strong>Widgets</strong>, что расположена в самом низу боковой панели меню. </p>
<p align="justify">В появившемся окне, нажмите кнопку <strong>Create new</strong>.   Вы приступите к процессу создания [регистрации]. Далее, вам будут предложено настроить виджет, используя доступные параметры конфигурации. Здесь также  доступна функция предварительного просмотра этого виджета. Помните, вы всегда сможете вернуться   к настройкам параметров виджета.</p>
<p align="justify">Кнопка <strong>Create new</strong> будет заменена на кнопку <strong>Save changes</strong>. Теперь, когда  виджет добавлен в систему Twitter, вы можете получить его ID. В адресную строке браузера вы увидите адрес URL, который выглядит примерно так:</p>
<blockquote><p>https://twitter.com/settings/widgets/<strong>512253083833733120</strong>/edit?focus_textarea=1&amp;notice=WIDGET_CREATED</p> </blockquote>
<p> ID вашего виджета состоит из  18 цифр. В этом случае это — <strong>512253083833733120</strong>. Сохраняйте ID в тайне. </p>
<p align="justify">Скопируйте ID и внесите в соответствующее поле в настройках модуля. После того, как настройки будут сохранены, виджет активируется на вашем сайте.</p>
