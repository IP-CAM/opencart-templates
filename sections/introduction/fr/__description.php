<h2 class="item1"> Introduction</h2>

<h5>Merci d'avoir acheté un template OpenCart. Cette documentation est composée de plusieurs parties et décrit le
    processus entier de mise en place et d'administration d'une boutique OpenCart à partir de zéro. Nous avons fait de
    notre mieux pour rendre ce manuel aussi clair et facile à suivre que possible.</h5>


    <h3>Qu'est-ce qu'OpenCart?</h3>

    <p>OpenCart est une application e-commerce Open Source. OpenCart fournit une personnalisation facile de votre
        boutique
        en ligne. La communauté OpenCart comprend un certain nombre d'utilisateurs allant des développeurs web
        expérimentés
        à la recherche d'une interface conviviale aux commerçants lançant leur affaire en ligne pour la première fois.
        OpenCart fournit une grande quantité de fonctionnalités qui vous permettent de contrôler votre boutique. Grâce
        aux
        outils d'OpenCart vous pouvez facilement découvrir le potentiel de boutique.
        <a href="http://docs.OpenCart.com" target="_blank">Plus d'informations</a>
    </p>



