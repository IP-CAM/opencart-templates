<h3>Blog</h3>

<p>С помощью данного расширения, вы можете быстро и удобно управлять новостями и публикациями, связанными с вашим
    магазином.</p>

<p>Основные возможности:</p>

<ul class="marked-list">
    <li>создание статей;</li>
    <li>создание категорий;</li>
    <li>создание авторов;</li>
    <li>комментирование статей и ответа на комментарий;</li>
    <li>наличие отчета по просмотрам;</li>
    <li>вывод продуктов и других статей, которые относятся к данной статье;</li>
</ul>

<p>Для изменения глобальных настроек блога перейдите в раздел <strong>Система -> Настройки (System->Settings)</strong>. Выберите из
    списка магазин, в котором нужно изменить настройки и напротив него нажмите Редактировать <strong>(Edit)</strong>. </p>

<h6> Настройки блога находятся во вкладке Блог (Blog):</h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd> статус блога (включен\отключен);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd> количество постов, отображаемых на странице;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description Symbol Limit</dt>
            <dd> количество символов в описании статьи на странице блога (не статьи!);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Comment Auto approval</dt>
            <dd> включает/отключает автоматическое подтверждение комментариев администратором;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Author Information</dt>
            <dd> включает/отключает показ информации о авторе в статье;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Related Article</dt>
            <dd> включает/отключает показ статей, которые относятся к данной;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Share with social site</dt>
            <dd> включает/отключает показ социальных иконок “поделиться”;</dd>
        </dl>
    </li>
</ol>

