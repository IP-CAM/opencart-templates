<h3>TM Module Tabs</h3>

<p>Данный модуль выводит 4 основных модуля (Bestsellers, Featured, Specials, Latest) в виде вкладок.</p>
<figure class="img-polaroid">
    <img src="img/tm-moduletabs-view.jpg" alt=""/>
</figure>

<p>После установки модуля, Вы можете настроить его на странице настроек модуля.</p>

<h5>Настройки:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>название модуля;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Specials</dt>
            <dd>включает/отключает отображение модуля Specials;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Bestsellers</dt>
            <dd>включает/отключает отображение модуля Bestsellers;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Latest</dt>
            <dd>включает/отключает отображение модуля Latest;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Featured</dt>
            <dd>включает/отключает отображение модуля Featured;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Products</dt>
            <dd>выбор списка продуктов для вкладки Featured;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd>устанавливает ограничение на количество отображаемых продуктов;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>ширина изображения продукта;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>высота изображения продукта;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>статус модуля (включен/отключен).</dd>
        </dl>
    </li>
</ol>
