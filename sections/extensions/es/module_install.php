<h3>Module installation</h3>
<p>Module installation process includes several steps. Navigate to your administration panel (by adding /admin in the
    browser address bar - www.yoursite.com/admin). Go to <strong>Extensions </strong> and in the drop-down menu select
    <strong>Modules</strong>
    You will see the list of installed and ready-to-install modules. If you see the <strong>Uninstall</strong>
    button next to any module, it means that the module is already installed and you can modify module settings by
    clicking <strong>Edit</strong>. By clicking the <strong>Install</strong> button, you will start module installation
    process. In order to remove the module, you have to click the <strong>Uninstall</strong> button, so the module will
    be uninstalled. </p>
<p class="alert alert-warning">Note that uninstalling the module means all the settings added earlier will be erased and
    cannot be reverted back.</p>

