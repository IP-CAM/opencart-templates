<h3>Banner oluşturmak</h3>

<h4>TM Description for banners düzenlemesi</h4>

<p>Bu düzenlemeyle banner açıklaması ekleyebilirsiniz. Düzenleme seçeneğini kapatmak veya kaldırmak için <strong>Extensions
        -> Modifications</strong> kısmına girip, "TM Description for banners" bileşenini bulun ve
    <strong>Disable</strong> a veya <strong>Delete</strong> e basın.</p>

<p>Bazı modüllerde, banner seçme alanı vardır. Bunun için <strong>System -> Design -> Banner</strong> kısmına girin</p>

<p>Bu sayfada oluşturulmuş bannerlar yer alır. Yeni bir banner oluşturmak için <strong>Add New</strong> (Yeni Ekle) ye
    basın. Var olan bir bannerı düzenlemek için
    <strong>Edit</strong> (Düzenle) ye basın.</p>

<figure class="img-polaroid">
    <img src="img/banner-list.jpg" alt=""/>
</figure>

<p>Banner Name ve Status alanlarını doldurup <strong>Add Banner</strong> a tıklayın. Karşınıza doldurmanız gereken şu
    tablo çıkacak: </p>

<ol class="index-list">
    <li>Banner Name - bannerınız için isim;</li>
    <li>Description - bannerda gözükecek olan açıklama yazısı (html desteklemektedir);</li>
    <li>Link - bannera tıklanınca açılacak olan url adresi (örnek: index.php?route=product/product&amp;product_id=47),
        bu alan zorunlu değildir;</li>
    <li>Image - banner görseli;</li>
    <li>Banner order - birden fazla banner olabilir, bu alanın doldurulması zorunludur. </li>
</ol>

<p>Bannerı silmek için <strong>Remove</strong> a tıklayın. Banner oluşturulduktan sonra onu farklı modüller için
    kullanabilirsiniz.</p>

