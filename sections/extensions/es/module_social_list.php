<h3>TM Social List</h3>

<p>Este módulo muestra una lista de enlaces a las redes sociales populares. </p>

<p class="alert alert-danger"> Si instala la versión de "update", la lista de enlaces a las redes sociales no estarán
    disponibles. Para configurar correctamente el módulo, asegúrese de rellenar toda la información requerida.</p>

<p>Después de instalar el módulo, se puede configurar en la página de configuración del módulo. Si deja el
    campo en blanco, el elemento no se mostrará en la lista. </p>

