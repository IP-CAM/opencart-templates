<h3>TM Lookbook</h3>

<p>O lookbook é um conjunto de fotos, é possível marcar qualquer parte da imagem. Este módulo serve para exibir as mercadorias de uma ou várias marcas.</p>
<p>Este módulo possui duas formas de exibição para loja: em forma de um Banner (1) ou em forma de galeria (2).</p>


