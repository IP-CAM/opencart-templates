<h3>Parallax</h3>

<p>Parallax-scroll - Arkaplan görsellerini site içeriğinden daha yavaş şekilde göstermeye yarayan, özel bir kaydırma
    tekniğidir. Bu şekilde bir derinlik oluşturarak, iki boyutlu video oyunu gibi bir efekt meydana gelir. Bu modül
    sayesinde sitenize, hiç bir ekstra yetenek veya çaba harcamadan parallax-effect ekleyebilirsiniz.</p>

<p>Modül kurulduktan sonra tüm düzenlemeleri modülün kendi ayar sayfasından yapabilirsiniz</p>

<p class="alert alert-info"> Görsel genişliği ve uzunluğu orijinaliyle aynı olmalıdır yoksa efekt beklentinizi
    karşılamayacaktır.</p>

