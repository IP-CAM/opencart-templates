<h3>TemplateMonster Newsletter Popup</h3>

<p>Este módulo añade la ventana emergente con una oferta para suscribirse a boletines de noticias cuando alguien ve su
    sitio web. La ventana no se muestra para los usuarios autorizados (conectados) y para aquellos que ya han
    sido suscritos.</p>
<figure class="img-polaroid">
    <img src="img/tm-newsletter-popup-view.jpg" alt=""/>
</figure>

<p>Después de instalar el módulo, se puede configurar en la página de configuración del módulo.</p>

<h5>Parámetros:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>el nombre del módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background Image</dt>
            <dd>una imagen de fondo de la ventana pop-up;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background image width</dt>
            <dd>la anchura de la imagen de fondo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background image height</dt>
            <dd>la altura de la imagen de fondo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Cookie Storage Time</dt>
            <dd>el tiempo de almacenamiento de cookies;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Heading Title</dt>
            <dd>el título de la ventana pop-up;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>la descripción de la ventana pop-up;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>el estado del módulo (activado/desactivado).</dd>
        </dl>
    </li>
</ol>

