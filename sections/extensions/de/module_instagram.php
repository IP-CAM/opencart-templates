<h3>TemplateMonster Instagram</h3>

<p>Das jeweilige Modul zeigt Bilder von Instagram als Liste von Bildern mit der angezeigten Anzahl von Likes.</p>

<figure class="img-polaroid">
    <img src="img/tm-instagram-view.jpg" alt=""/>
</figure>

<p>Nachdem Sie das Modul installiert haben, können Sie es auf der Konfigurationsseite einstellen.</p>

<h5>Einstellungen:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>der Modulname;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Select Get</dt>
            <dd>die Auswahl, wie man Bilder erhält. Es sind 2 Werte verfügbar (user,tagged). Das bedeutet, dass man die
                Bilder entweder von einem bestimmten Benutzer erhält oder per Hashtag erhält;
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry TagName</dt>
            <dd>der Tag, mit dem die Bilder (es wird benutzt, wenn die Option "Select get" den Wert tagged hat)
                angezeigt wird;
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry ClientID</dt>
            <dd>Ihre API ID von instagram.com (es wird benutzt, wenn die Option "Select get" den Wert tagged hat);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry UserId</dt>
            <dd>eine eindeutige Benutzer-ID (es wird benutzt, wenn die Option "Select get" den Wert user hat);</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry accessToken</dt>
            <dd>ein Zugriffstoke, der benutzerdefinierte Bilder anzeigt (es wird benutzt, wenn die Option "Select get"
                den Wert user hat);
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry limit</dt>
            <dd>legt die Beschränkung auf die Anzahl der angezeigten Bilder fest;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>der Modulstatus (aktiviert/deaktiviert).</dd>
        </dl>
    </li>
</ol>
