<h3>TM Layout Builder</h3>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-edit.jpg" alt=""/>
</figure>
<p>Este módulo sirve para la creación rápida, simple y práctica de las diferentes variantes de la página principal de la tienda.</p>

<h5>Ajustes básicos del módulo:</h5>
<ol class="index-list">
	<li>
		<dl class="inline-term">
			<dt>Estado</dt>
			<dd>Estado del módulo (Activado/Desactivado);</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Nombre del módulo</dt>
			<dd>El nombre del módulo;</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Clase del módulo</dt>
			<dd>La clase del módulo;</dd>
		</dl>
	</li>
	<li>
		<dl class="inline-term">
			<dt>ID del Módulo</dt>
			<dd>ID del módulo.</dd>
		</dl>
	</li>
</ol>
<p>Para crear un nuevo diseño, pulse el botón <strong>Nueva Vista de Diseño</strong>, a continuación, pulse el botón <strong>Añadir Fila
		(1)</strong>. Después de añadir una fila, agregue las columnas. Para añadir columnas, pulse el botón <strong>Añadir
 columna (2)</strong>.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-column.jpg" alt=""/>
</figure>

<p>El número de columnas en la fila es ilimitado, y se puede añadir tantas columnas como sea necesario. El ancho de las columnas se puede ajustar inmediatamente después de la adición de ellos con la ayuda de la función de arrastrar y colocar arrastrando el borde de la columna derecha. </p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-drag-drop.jpg" alt=""/>
</figure>

<p>Después de añadir columnas, usted debe llenarlos de contenido (módulos). Para añadir un módulo, pulse el botón <strong>Añadir Widget</strong> y seleccione el que sea necesario en el menú desplegable.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-widget.jpg" alt=""/>
</figure>

<p>Después de añadir un módulo, puede modificarlo o borrarlo,</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change.jpg" alt=""/>
</figure>

<p>y también moverlo a otra columna.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change-position.jpg" alt=""/>
</figure>

<p>Después de configurar todas las filas, columnas y módulos, guarde el módulo Builder y muestralo en una de las posiciones en la página principal de la tienda.</p>