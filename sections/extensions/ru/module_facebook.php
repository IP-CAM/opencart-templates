<p align="justify">Поле <strong>Facebook App ID</strong> обязательно для заполнения. Вам потребуется  идентификатор  приложения Facebook, который можно получить на сайте: <a href="https://developers.facebook.com/" rel="nofollow" target="_blank">https://developers.facebook.com/</a>.</p>
<p align="justify">Следуйте этой простой инструкции и вы без труда сможете получить идентификатор для вашего приложения Facebook. </p>
<h5>Пошаговая инструкция</h5>
 <h5>1. Авторизуйтесь в системе Facebook</h5>
        <p align="justify">Перед тем, как перейти к созданию аккаунта разработчика приложений, войдите в ваш пользовательский Facebook аккаунт. Для этого пройдите по ссылке ниже:</p>
<a href="https://www.facebook.com/login.php">Авторизуйтесь в  Facebook</a>
<h5>2. Создайте аккаунт разработчика приложений</h5>
        <p align="justify">На этом этапе,  вам необходимо создать аккаунт разработчика приложений. </p>
        <p align="justify">Если у вас нет аккаунта разработчика на Facebook, преобразуйте ваш личный аккаунт Facebook в аккаунт разработчика на Facebook. Если у вас уже есть аккаунт разработчика на Facebook, пропустите этот шаг.</p>
        <p><a href="https://developers.facebook.com/async/onboarding/dialog/" rel="dialog">Создайте аккаунт разработчика приложений</a></p>
<h5>3. Добавьте приложение Facebook</h5>
        <p>Нажмите на <a href="https://developers.facebook.com/apps/"><strong>Apps</strong></a> в верхней части  навигации по странице и выберите <strong>Add a New App</strong>, или же просто перейдите по ссылке:</p>
<a href="https://developers.facebook.com/apps/async/create/platform-setup/dialog/" rel="dialog">Добавьте приложение Facebook</a>
<h5>4. Выберите платформу[ы] для приложения</h5>
        <p align="justify">Выберите платформу, которую  хотите добавить. Если ваше приложение разработано для нескольких платформ, вы сможете добавить их в свое приложение Facebook позже.</p>
<figure class="img-polaroid">
    <p><img src="img/facebook_feed-6.jpg" alt="">
</p></figure>
    <h5>5. Присвойте приложению имя </h5>
    <p align="justify">Задайте имя для вашего приложения и выберите <strong>Create New Facebook App ID</strong>. Затем, определите категорию  [класс] приложения. Если же вы создаете тестовую версию уже существующего проложения, выберите его [то приложение, которое вы хотите протестировать] в соответствующем списке.</p>
<figure class="img-polaroid">
    <p><img src="img/facebook_feed-7.jpg" alt="">
</p></figure>
    <h5>6. Упростите этап создания приложения</h5>
    <p align="justify">Окно мастера быстрого создания приложения поможет значительно ускорить этот процесс. А также, поможет настроить приложение Facebook для выбранной платформы. Позже вы всегда сможете вернуться в мастер, чтобы продолжить настройку приложения или добавить новую платформу для него.</p>

<h5>7.  Получите ID вашего приложения</h5>
    <p align="justify">На этом этапе вы получите ваш уникальный  ID для приложения. Этот ID понадобится вам всякий раз, когда вы будете использовать наши <a href="https://developers.facebook.com/docs/apis-and-sdks">SDK</a> или теги <a href="https://developers.facebook.com/docs/sharing/webmasters/">Open Graph для публикации</a>. Его   [ID  приложения] доступен в главном окне <a href="https://developers.facebook.com/apps/">  панели приложения</a>.</p>
<figure class="img-polaroid">
    <p><img src="img/facebook_feed-9.jpg" alt="">
</p></figure>
    <h5>8.  Защитите аpp secret  вашего приложения</h5>
    <p align="justify">Система Facebook автоматически генерирует app secret  для вашего приложения. </p>
<p align="justify">Секрет приложения — это пароль, используемый для проверки подлинности запросов вашего приложения к серверам Facebook.</p>
    <p align="justify">Не размещайте секрет приложения Facebook в разметке веб-страницы. Будьте осторожны, размещая файлы, содержащие секрет приложения, в хранилищах типа контроля версий [например, Git или Subversion].</p>
<h5>9. Контактный  Email</h5>
    <p align="justify">Для каждого приложения нужно настроить   email для  связи. Перейдите к главному окну<a href="https://developers.facebook.com/apps/"> панели приложения</a>, затем пройдите во вкладку <strong>Settings</strong>, которая расположена в боковом меню страницы. На открывшейся странице, в соответствующем поле, укажите действительный email для связи.</p>
<figure class="img-polaroid">
    <p><img src="img/facebook_feed-10.jpg" alt="">
</p></figure>
    <h5>10. Добавьте детальную информацию о приложении</h5>
    <p align="justify">Пройдите во вкладку  <strong>App Details</strong>, которая расположена в левом боковом меню. В окне этой вкладки вы сможете  просмотреть и, если есть такая необходимость, добавить дополнительную информацию о вашем приложении.</p>
<p align="justify">Если вы хотите, чтобы информация о вашем приложении размещалась в app-center, активируйте опцию  <strong>App   Center Listed Platform</strong>. Добавляйте к приложению иконки, рекламные банеры и видео. Make sure to follow the <a href="https://developers.facebook.com/docs/games/appcenter/guidelines">App Center Guidelines</a> to be approved for listing in App Center.</p>
<p align="justify">Если вы хотите, чтобы ваше приложение было представлено в Центре приложений, включите настройку <strong>App   Center Listed Platform</strong> [Указанные платформы Центра приложений] для полной или мобильной версии своего сайта. Добавьте значки, рекламные изображения и видео. Следуйте <a href="https://developers.facebook.com/docs/games/appcenter/guidelines">Рекомендациям Центра приложений</a>, чтобы ваше приложение было одобрено для размещения.</p>
<figure class="img-polaroid">
    <p><img src="img/facebook_feed-11.jpg" alt="">
</p></figure>
<h5>11. Отключение режима разработки</h5>
<p align="justify">Новые приложения Facebook по умолчанию существуют в режиме разработки. В этом режиме присутствуют ограничения для публикуемых действий и связанных объектов, [например, отмечать друзей можно только в Хрониках Facebook, принадлежащих разработчику приложения].</p>
<p align="justify">Когда приложение будет готово, вы сможете переключить его с режима разработки на режим публичной доступности. После этого оно сможет взаимодействовать с аккаунтами Facebook, не связанными с вашим приложением.</p>
