<h3>Catalog Page</h3>

<p align="justify">Страница со всеми продуктами и их детальным описанием. На странице выводится 6 продуктов (количество опционально) в левой колонке, в правой колонке выводится описание первого продукта. При доскроилвании до 2-го продукта в правую колонку подгружается информация о 2-м продукте. Так происходит пока клиент не доскролит до последнего (6-го) продукта, после этого можно подгрузить еще 6 продуктов нажав на кнопку 'Load  products' (это Infinity Scroll). </p>

