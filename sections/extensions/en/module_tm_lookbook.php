<h3>TM Lookbook</h3>

<p>The lookbook is a set of photos, where you can mark any part of the image. This module serves for displaying the products of one or of the several brands.</p>
<p>The modules has two display modes for being displayed in the store position: in form of a Banner (1) or in form of a Collection (2).</p>


