<h3>TM Instagram</h3>

<p>Questo modulo consente di inserire in un elenco immagini della rete Instagram, incluso il numero di mi piace.</p>
<figure class="img-polaroid">
    <img src="img/tm-instagram-view.jpg" alt=""/>
</figure>

<p>Dopo aver installato il modulo, sarà possibile configurarlo nell'apposita pagina di configurazione.</p>

<h5>Impostazioni:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>il nome del modulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Select Get</dt>
            <dd>come ottenere le immagini. Sono disponibili due valori (user e tagged) per ottenere le immagini,
                rispettivamente, da utente o da hashtag;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry TagName</dt>
            <dd>tag utilizzato per inserire le immagini (è il valore utilizzato se hai in precedenza selezionato tagged
                nel campo "Select");
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry ClientID</dt>
            <dd>l'ID API di instagram.com (è il valore utilizzato se hai in precedenza selezionato tagged nel campo
                "Select");
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry UserId</dt>
            <dd>ID utente (è il valore utilizzato se hai in precedenza selezionato tagged nel campo "Select");</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry accessToken</dt>
            <dd>token da visualizzare (è il valore utilizzato se hai in precedenza selezionato tagged nel campo
                "Select");
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry limit</dt>
            <dd>limite massimo di inserimenti;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>lo stato del modulo (abilitato/disabilitato).</dd>
        </dl>
    </li>
</ol>

