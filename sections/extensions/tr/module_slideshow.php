<h3>TemplateMonster Product Slideshow</h3>

<p>Bu modül sayesinde, seçeceğiniz ürünler karusel görünümüyle gösterilir. </p>

<p>Modül kurulduktan sonra, tüm düzenlemeleri modül ayar sayfasından yapabilirsiniz.</p>


<h5>Ayarlar:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>modülün ismi;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Products</dt>
            <dd>ürün listesinin oluşturulması;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd>gösterilecek olan ürün sayısı;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>ürün görselinin genişliği;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>ürün görselinin uzunluğu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>modülün durumu (aktif/pasif).</dd>
        </dl>
    </li>
</ol>

