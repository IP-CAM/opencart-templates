<h3>TemplateMonster Cookie Policy</h3>

<p>Ce module informe les visiteurs que ce site web utilise des cookies. Selon les termes de la loi relative
    aux cookies, vous devez ajouter des modules appropriés à votre site Web qui permettront d'afficher un message
    d'avertissement sur ​​les cookies utilisés sur votre site.</p>

<p>Les cookies sont de petits fichiers texte que les sites placent sur les ordinateurs des visiteurs. Ils sont
    généralement utilisés pour identifier les visiteurs et leur proposer la meilleure expérience. Les cookies sont une
    partie cruciale de plusieurs fonctions de base des sites web.</p>

<p>Par exemple, ce sont habituellement les cookies qui permettent aux boutiques en ligne de se souvenir des éléments que
    vous avez dans votre panier. Ils servent également à vous garder connecté à un site ou à fournir d'importantes
    statistiques et informations sur l'utilisation aux propriétaires de sites.</p>

<p>Si vous possédez un site web, il utilise quasiment à coup sûr des cookies. Leur but principal est probabement de
    suivre le nombre de visiteurs et leur comportement avec des outils comme Google Analytics. Ils peuvent également
    servir à afficher des publicités pertinentes aux visiteurs, ou - si vous vendez en ligne - à mettre en avant des
    éléments clés de votre système d'achat en ligne.</p>

<p>26th May 2012 - L'ICO a apporté un changement à la loi, le "consentement implicite" est maintenant autorisé tant que
    vous affichez clairement où se trouve votre politique de vie privée/cookies.</p>
