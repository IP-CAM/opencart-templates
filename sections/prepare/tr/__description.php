<h2 class="item1"> Hazırlık</h2>

<h6>OpenCart Web sitenizi kurmaya başlamadan önce, tüm hazırlıkları yapmış olduğunuza emin olunuz.</h6>

<h4>Yazılımı düzenlemek</h4>

<p>OpenCart şablonu üzerinde rahatça çalışmak için, gerekli olan tüm uygulamaları indirmenizi öneririz. Şablonun demo
    sayfasından gerekli olan uygulama listesini görebilirsiniz. Bunlar her şablon için değişiklik gösterebilir bu yüzden
    şimdi size genel olarak nelere ihtiyacınız olduğunu belirteceğiz:</p>

<ol class="index-list">
    <li>Adobe Photoshop uygulamasına ihtiyacınız olabilir. .PSD kaynak dosyalarını ve şablon içindeki grafik ve
        görselleri düzenlemek için kullanabilirsiniz.</li>

    <li>Şablonun kaynak dosyalarını düzenlemek için; Adobe Dreamweaver, Notepad++, Sublime Text vs. gibi bir kod
        editörüne ihtiyacınız var.</li>

    <li>Dosyaları hosting sunucunuza yüklemek için; Total Commander, FileZilla, CuteFTP vs. gibi bir FTP yöneticisine
        ihtiyacınız var.</li>

    <li>Ziplenmiş arşiv dosyalarını açmak için
        <a href="/help/extract-archived-files-windows-os.html" target="_blank"> WinZip(PC)</a>
        veya
        <a href="/help/extract-archived-files-mac-os.html" target="_blank"> StuffitExpander(MAC)</a>
        programlarına ihtiyacınız var.
    </li>
</ol>

<h4>Hosting</h4>

<p>OpenCart PHP/MySQL temelli bir uygulama olduğu için, kullandığınız hosting (barındırma) da buna uygun olmalıdır.</p>

<p>Kullanmakta olduğunuz bir hosting varsa,
    <a href="http://docs.OpenCart.com/system/server-requirements" target="_blank">OpenCart sistem gereksinimleri</a>
    ne uygun olduğuna ve OpenCart sitenizin kullanımı için hazır olduğuna emin olmalısınız.
</p>
<p>Alternatif olarak OpenCart'ı kendi bilgisayarınızda da barındırabilirsiniz. Bunun için bir yerel sunucu kurmalısınız
    ve
    <b>WAMP</b>, <b>AppServ</b>,
    <b>MAMP</b> vs. gibi bir program kullanmalısınız. Bunlardan birini kolaylıkla kurabilir ve OpenCart'ı
    çalıştırabilirsiniz.
</p>

