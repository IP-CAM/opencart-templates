<h2 class="item1">Preparação</h2>

<h6>Antes re prosseguir para a instalação do seu site OpenCart, certifique-se de estar totalmente preparado para esta tarefa. </h6>

<h4>Programas para edição</h4>

<p>Para se sentir confotável em trabalhar com um templateOpenCart, recomendamos que você baixe todos os programas necessários. Você pode ver uma lista de programas na página de demonstração do template. Eles variam entre templates, então vamos lhe mostrar o que é necessário na maioria dos casos:</p>

<ol class="index-list">
    <li> Você pode precisar do Adobe Photoshop. Ele é usado para editar arquivos .PSD e será necessários se quiser editar os gráficos e imagens do template. </li>

    <li>Para alterar os arquivos fontes do tema, você vai precisar de um editor de código, como Adobe Dreamweaver, Notepad++, Sublime Text, etc.</li>

    <li>Para enviar arquivos para o servidor de hospedagem, você vai precisar de um gerenciador de FTP tipo Total Commander, FileZilla, CuteFTP, etc. </li>

    <li>Siga o seguinte guia para abrir arquivos usando o
        <a href="/help/extract-archived-files-windows-os.html" target="_blank"> WinZip(PC)</a>
        ou
        <a href="/help/extract-archived-files-mac-os.html" target="_blank"> StuffitExpander(MAC)</a>
        .
    </li>
</ol>

<h4>Hospedagem</h4>

<p>Já que o programa do OpenCart é baseado em PHP/MySQL, você precisa preparar um ambiente de hospedagem com essas tecnologias.</p>

<p>Se você usa uma hospedagem pré-configurada, verifique se ela comporta os
    <a href="http://docs.OpenCart.com/requirements" target="_blank">requisitos de sistema OpenCart</a>
   e está pronto para ser utilizado para sites OpenCart.
</p>

<p>Outra alternativa é rodar o OpenCart em um servidor local no seu computador. Para criar um servidor local, use programas como
    <b>WAMP</b>, <b>AppServ</b>,
    <b>MAMP</b>, etc. Qualquer um desses pode ser instalado facilmente como qualquer programa e usado para rodar o OpenCart.
</p>

