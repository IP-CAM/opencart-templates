<h3>TM Video Background</h3>

<p>Bu modül sayesinde sitenize video arakaplanına sahip bir kutu ekleyebilirsiniz (kendi websitenize veya YouTube'a
    yüklediğiniz bir video olabilir).</p>

<p>Modül kurulduktan sonra, tüm düzenlemeleri modül ayar sayfasından yapabilirsiniz.</p>

<h5>Ayarlar:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>modülün ismi;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>modülün durumu (aktif/pasif)</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Video Source</dt>
            <dd>video kaynağı. 2 seçenek kullanılabilir:</dd>
        </dl>
        <ul class="marked-list">
            <li>
                <dl class="inline-term">
                    <dt>local</dt>
                    <dd>websitenizden yüklensin;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>youtube</dt>
                    <dd>YouTube aracılığıyla yüklensin.</dd>
                </dl>
            </li>
        </ul>
    </li>
    <li>
        <dl class="inline-term">
            <dt>YouTube url</dt>
            <dd>YouTube video adresi;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mute</dt>
            <dd>sessiz gösterim;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile</dt>
            <dd>videoyu mobil aygıtlardan göster/gösterme. 'Yes' seçeneği mobil aygıtlarda gösterilmesini sağlar;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Start Video</dt>
            <dd>videonun başlatılacağı zaman;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile Image</dt>
            <dd>mobil aygıtlarda oynatılmayacak olan videonun yerine gösterilecek görsel;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile Image Width</dt>
            <dd>arkaplan görselinin genişliği;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile Image Height</dt>
            <dd>arkaplan görselinin uzunluğu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>videonun üzerinde gösterilecek olan html tanımlama yazısı.</dd>
        </dl>
    </li>
</ol><p>Eğer
    <strong>Video Source - Local Video</strong> seçerseniz, mobil aygıtlarda gösterilmek üzere ".jpg" formatında bir
    görsel seçiniz. Arkaplan görselini barındıran klasörde ayrıca 3 adet aynı isimde video dosyası da olmalı. Videolar
    şu uzantılara sahip olabilir:
</p>

<ul class="marked-list">
    <li>*.ogv</li>

    <li>*.mp4</li>

    <li>*.webm</li>
</ul>

