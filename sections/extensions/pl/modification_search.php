<h3>TM Live search</h3>

<p>Modyfikacja TM Live search pozwala wyświetlać szukane produkty bez ponownego
    ładowania strony (Img. 44). Żeby wyłączyć ten moduł albo usunąć go, przejdź do
    <strong>Extensions (Rozszerzenia) -&gt; Modifications (Modyfikacje)</strong>,
    wybierz modyfikację z listy i kliknij <strong>Disable (Wyłącz)</strong> albo
    <strong>Delete (Usuń)</strong>.</p>

