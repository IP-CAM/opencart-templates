<h3>TemplateMonster Social List</h3>

<p>Ten moduł pozwala na wyświetlenie listy linków do popularnych sieci socjalnych. </p>

<p class="alert alert-danger"> W przypadku instalacji wersji motywu "update", przykładowa treść szablonu (lista linków
    do sieci socjalnych) nie będzie dostępna. Aby skonfigurować moduł poprawnie, należy wypełnić wymagane
    informacje.</p>

<p>Kiedy moduł został zainstalowany, możesz konfigurować go na stonie konfiguracji
    modułu. Jeśli pole pozostanie puste, element nie pojawi się w
    liście.</p>

