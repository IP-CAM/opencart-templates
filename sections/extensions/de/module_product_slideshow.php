<h3>TemplateMonster Product Slideshow</h3>

<p>Das jeweilige Modul zeigt ausgewählte Produkte als Karussell an. </p>
<figure class="img-polaroid">
    <img src="img/tm-product-slideshow-view.jpg" alt=""/>
</figure>

<p>Nachdem Sie das Modul installiert haben, können Sie es auf der Konfigurationsseite einstellen.</p>

<h5>Einstellungen:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>der Modulname;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Products</dt>
            <dd>die Auswahl der Produktliste ;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd>die Beschränkung auf die Anzahl der angezeigten Produkte;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>die Breite des Produktbildes;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>die Höhe des Produktbildes;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>der Modulstatus (aktiviert/deaktiviert).</dd>
        </dl>
    </li>
</ol>
