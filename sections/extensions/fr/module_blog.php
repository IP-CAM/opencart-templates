<h3>Blog</h3>

<p>L'extension suivante vous permet de gérer les articles et les nouvelles liées à votre boutique.</p>

<p>Caractéristiques principales:</p>

<ul class="marked-list">
    <li>la création d'articles;</li>
    <li>la création de catégories;</li>
    <li>la création d'auteurs;</li>
    <li>les commentaires d'articles et les réponses aux commentaires;</li>
    <li>voir le rapport;</li>
    <li>l'affichage de produits et d'articles liés à certain article;</li>
</ul>

<p>Pour changer les paramètres globaux de Blog allez à <strong>System->Settings</strong>. Sélectionnez la boutique où vous voulez changer les paramètres et cliquez sur <strong>Edit</strong>.</p>

<h6> Paramètres de blog peut être vu dans la section Blog:</h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Statut</dt>
            <dd> le statut de blog (activé\désactivé);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limite</dt>
            <dd> nombre d'articles par page;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limite de symboles dans une description</dt>
            <dd> nombre de symboles dans une description d'article sur la page principale de Blog (pas sur l'article unique!);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Approbation automatique des commentaires</dt>
            <dd> activer/désactiver une approbation automatique des commentaires par l'administrateur;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Renseignements sur l'auteure</dt>
            <dd> activer/désactiver les renseignements sur l'auteure sur le page d'article;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Articles recommandés</dt>
            <dd> activer/désactiver les articles recommandés;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Partager avec site social</dt>
            <dd> activer/désactiver les boutons de partage sociale;</dd>
        </dl>
    </li>
</ol>

