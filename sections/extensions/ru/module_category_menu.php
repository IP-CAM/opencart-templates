<h3>TemplateMonster Category Menu </h3>

<p>Данный модуль позволяет выводить категории в боковой панели и подкатегории 1-го и
    2-го порядка, включая изображения подкатегории 1-го порядка.</p>

<p>После установки модуля, Вы можете включить или выключить его на странице
    настроек модуля.</p>

