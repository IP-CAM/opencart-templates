<h3>TemplateMonster Social List</h3>

<p>Ce module permet d'afficher la liste des liens vers les réseaux sociaux populaires. </p>

<p class="alert alert-danger"> Si vous installez la version "update", la liste des liens vers les réseaux sociaux ne sera
    pas disponible. Pour configurer correctement le module, assurez-vous de remplir toutes les informations
    requises.</p>

<p>Une fois le module installé, vous pouvez le configurer sur la page de configuration de module. Si vous
    laissez le champ vide, l'élément ne sera pas affiché dans la liste. </p>

