<h3>Catalog Page</h3>

<p>Esta página contém todos os produtos e descrições detalhadas. Ela mostra seis produtos (opcionalmente) na coluna da esquerda, uma coluna na direita mostra a descrição do primeiro produto. Quando os visitantes rolarem para o segundo produto, a coluna da direita exibe informações sobre o segundo produto, e assim por diante, até o último (sexto) produto. Os próximos 6 produtos podem ser vistos pressionando o botão 'Mostrar Mais' (esta função se chama Infinite Scroll). </p>

