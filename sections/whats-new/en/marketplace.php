<h3>Marketplace</h3>

<p align="justify">OpenCart Marketplace is now available right in the admin panel so you can access all the themes and modules directly from the website backend. You can use the inbuilt search functionality to look up the paid or free themes and extensions and install them to your e-store.


</p>


<p align="justify">
All you need to do is to press the <b>API</b> Marketplace button and enter your <b>Username</b> and <b>Secret</b> to start purchasing and installing the components.   


</p>

