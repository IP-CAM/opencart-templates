<h3>TM ABC Customers</h3>

<p>Ten moduł służy do ustawiania rabatów dla różnych grup klientów. Gdy zamówienie zostanie złożone, kwota zamówienia jest przeliczana, z odsetkiem rabatu wyznaczonego dla określonej grupy klientów.</p>

