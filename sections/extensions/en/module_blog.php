  </li>
  <li>
  <p>Locate the needed comment, and press the <strong>Edit</strong> button in the right column: </p> </li>
  <figure class="img-polaroid">
 <img src="img/blog-global-edit-12.jpg" alt="">
</figure>
  
  <li>
  <p align="justify"> Here you can see two tabs: <strong>General</strong> and <strong>Comment</strong>. In the <strong>General</strong> tab you have the option to edit the <strong>Author Name</strong>, select the <strong>Article Name</strong> and enable or disable comment by setting its <strong>Status</strong> to 'Enabled' or 'Disabled'. Once you are done with your modifications, press the <strong>Save</strong> button above: </p>
  </li>
  <figure class="img-polaroid">
 <img src="img/blog-global-edit-13.jpg" alt="">
</figure>
  <li>
  <p align="justify">In the <strong>Comment </strong>tab you have the option to edit the <strong>Comment</strong> text itself. Also, you can add your reply by clicking the <strong>Add Comment Reply</strong> button below: </p></li>
  <figure class="img-polaroid">
 <img src="img/blog-global-edit-14.jpg" alt="">
</figure>
  
  <p align="justify"> You will be able to add the <strong>Author Name</strong>, your <strong>Reply</strong> and select the <strong>Status</strong> of the reply. Once you are done with your modifications, press the <strong>Save</strong> button.</p>
  <li>
  <p align="justify"> Except for editing the existing comments, you can also add a new one. Press the <strong>Insert</strong> button at the top under the <strong>Blog &gt; Article Comment</strong> section.</p>
  <figure class="img-polaroid">
 <img src="img/blog-global-edit-15.jpg" alt="">
</figure>
  
  <p align="justify">Complete all the required fields such as <strong>Author Name</strong>, <strong>Article Name</strong> and <strong>Comment</strong> and make sure that <strong>Status</strong> is set to 'Enabled' if you need a comment to be visible on the website. Press the <strong>Save</strong> button to save your changes.</p>
  </li>
  
  <li>
  <p align="justify"> In order to delete a comment, check the box in the left column for the respective comment, and press the <strong>Delete</strong> button at the top: </p>
