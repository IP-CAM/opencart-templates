<h5>Step-by-Step Guide</h5>
  <h5>1. Login to Facebook</h5>
  <p>Before creating a developer account, login in to your Facebook account.</p>
<a href="https://www.facebook.com/login.php">Login to Facebook</a>
  <h5>2. Developer Account</h5>
  <p align="justify">You'll need Facebook developer account to get started. If you don't have one, upgrade your personal Facebook account to a Facebook Developer account now. Skip this step if you already have a developer account.</p>
<a href="https://developers.facebook.com/async/onboarding/dialog/" rel="dialog">Create Developer Account</a>
<h5>3. Create new Facebook app</h5>
  <p>Choose <a href="https://developers.facebook.com/apps/"><strong>Apps</strong></a> in the header navigation and select <strong>Add a New App</strong> or use the button below.</p>
<a href="https://developers.facebook.com/apps/async/create/platform-setup/dialog/" rel="dialog">Create new Facebook App</a>
 <h5>4. Choose Platform</h5>
  <p align="justify">Choose the platform you want to add. If your app is on multiple platforms, you'll be able to add them to your Facebook app later.</p>
  <figure class="img-polaroid">
 <p><img src="img/facebook_feed-6.jpg" alt="">
</p></figure>
 <h5>5. Choose a Name</h5>
 <p align="justify">Choose a name for your app and select <strong>Create New Facebook App ID</strong>. Choose a <strong>category</strong> for your app. If you're creating a test version of an existing Facebook app, select the app you're testing.</p>
 <figure class="img-polaroid">
 <p><img src="img/facebook_feed-7.jpg" alt="">
</p></figure>
 <h5>6. Follow &quot;Quick Start&quot; Steps</h5>
 <p align="justify">The quick start wizard will help you to set up your Facebook app for the selected platform. You can come back to the quick start wizard at a later time to either continue your setup or add a new platform to your app.</p>
<h5>7. App ID</h5>
 <p align="justify">Your app will have a unique app ID. You will use this ID whenever you use one of our <a href="https://developers.facebook.com/docs/apis-and-sdks">SDKs</a> or <a href="https://developers.facebook.com/docs/sharing/webmasters/">Open Graph tags for sharing</a>. You can find your app ID in your <a href="https://developers.facebook.com/apps/">app's dashboard</a>.</p>
 <figure class="img-polaroid">
 <p><img src="img/facebook_feed-9.jpg" alt="">
</p></figure>
 <h5>8. Protect your App Secret</h5>
 <p align="justify">Facebook auto-generates an app secret associated with your application. The application secret authenticates requests made by your application to Facebook servers and should be treated like a password.</p>
<p align="justify">Do not output your Facebook application secret into your web page markup; be careful when committing files containing this value into source control software such as a public Git or Subversion repository.</p>
<h5>9. Contact Email</h5>
 <p align="justify">You need to add a contact email for each app. Go to your <a href="https://developers.facebook.com/apps/">app's dashboard</a>, select the <strong>Settings</strong> navigation item in the left sidebar and provide a valid contact email.</p>
 <figure class="img-polaroid">
 <p><img src="img/facebook_feed-10.jpg" alt="">
</p></figure>
 <h5>10. App Details</h5>
 <p>Select the App Details on the left sidebar to view and add additional information about your app.</p>
 <p align="justify">If you want your app to be listed in the apps center, turn on the App Center Listed Platform setting for your website or mobile site. Add icons, promotional images and videos. Make sure to follow the <a href="https://developers.facebook.com/docs/games/appcenter/guidelines">App Center Guidelines</a> to be approved for listing in App Center.</p>
 <figure class="img-polaroid">
 <p><img src="img/facebook_feed-11.jpg" alt="">
</p></figure>
<h5>11. Disable Development Mode</h5>
 <p align="justify">New Facebook apps exist in development mode by default. This limits published actions and related objects such as friend tagging to Facebook Timelines owned by a developer of the application. </p>
<p align="justify">Once your application is ready for public use you can switch from the development mode to public mode to allow your app to interact with Facebook accounts not associated with your app.</p>
 <figure class="img-polaroid">
 <p><img src="img/facebook_feed-12.jpg" alt="">
</p></figure>
