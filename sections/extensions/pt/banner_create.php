<h3>Como criar banners</h3>

<h4>TM Description para modificação de banners</h4>

<p>A modificação permite incluir uma descrição ao banner. Para desligar essa modificação ou retirá-la, navegue para
    <strong>Extensions -> Modifications</strong>, encontre a extensão "TM Description for banners" e clique em <strong>Disable</strong>
    para desativar ou <strong>Delete</strong> para apagar.</p>

<p>Alguns dos módulos contém um campo para escolher um banner. Navegue até a seção <strong>System -> Design ->
        Banner</strong></p>

<p>A página mostra a lista de banners criados. Para criar um novo banner, clique em <strong>Add New</strong>. Para
    editar um banner existente, clique em
    <strong>Edit</strong></p>

<figure class="img-polaroid">
    <img src="img/banner-list.jpg" alt=""/>
</figure>

<p>Especifique o nome do Banner, Status e clique em <strong>Add Banner</strong>. Você verá a tabela onde deve incluir os
    seguintes valores: </p>

<ol class="index-list">
    <li>Nome do Banner;</li>
    <li>Description - o texto que aparece no banner (suporta html);</li>
    <li>Link - especifica o link relative ao endereço do site (por exemplo,
        index.php?route=product/product&amp;product_id=47), esse campo não é obrigatório;</li>
    <li>Imagem;</li>
    <li>Ordem do Banner - pode haver mais de um banner, esse campo é obrigatório. </li>
</ol>
<p>Para apagar
    o banner, clique em <strong>Remove</strong>. Após o banner ser criado, você pode usá-lo em diferentes
    módulos.</p>

