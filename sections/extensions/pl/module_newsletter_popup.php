<h3>TM Newsletter Popup</h3>

<p>Ten moduł pozwala dodać wyskakujące okno z propozycją do zapisania się do
    newslettera, kiedy ktoś przegląda twoją witrynę (Img.36). Okno nie wyświetla się
    autoryzowanym (zalogowanym) i zapisanym do newslettera użytkownikom.</p>

<figure class="img-polaroid">
    <img src="img/tm-newsletter-popup-view.jpg" alt=""/>
</figure>

<p>Kiedy moduł został zainstalowany, możesz konfigurować go na stonie konfiguracji
    modułu</p>

<h5>Ustawienia:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>nazwa modułu;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Background Image</dt>
            <dd>ustawienie obrazka tła dla wyskakującego okna;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Background image width</dt>
            <dd>szerokość obrazka tła;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Background image height</dt>
            <dd>wysokość obrazka tła;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Cookie Storage Time</dt>
            <dd>czas przechowywania plików cookies;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Heading Title</dt>
            <dd>nagłówek wyskakującego okna;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>opis wyskakującego okna;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>status modułu (włączony/wyłączony).</dd>
        </dl>
    </li>
</ol>


