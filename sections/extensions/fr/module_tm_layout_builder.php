<h3>TM Layout Builder</h3>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-edit.jpg" alt=""/>
</figure>
<p>Ce module sert à la création rapide, simple et pratique des différentes variantes de la page principale du magasin.</p>

<h5>Réglages de base du module:</h5>
<ol class="index-list">
	<li>
		<dl class="inline-term">
			<dt>Etat</dt>
			<dd>Etat du module (Activé/Désactivé);</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Nom du Module</dt>
			<dd>Le nom du module;</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Classe de module</dt>
			<dd>La classe du module;</dd>
		</dl>
	</li>
	<li>
		<dl class="inline-term">
			<dt>ID du Module</dt>
			<dd>ID du module.</dd>
		</dl>
	</li>
</ol>
<p>Pour créer une nouvelle disposition, appuyez sur  le bouton <strong>New Layout View</strong>, puis appuyez sur le bouton <strong>Add Row
		(1)</strong> . Après avoir ajouté une ligne, ajoutez des colonnes. Pour ajouter des colonnes, appuyez sur le bouton <strong>Add
		Column (2)</strong>.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-column.jpg" alt=""/>
</figure>

<p>Le nombre de colonnes dans la ligne est illimité, et vous pouvez ajouter autant de colonnes que vous avez besoin. La largeur des colonnes peut être réglée juste après leur ajout à l'aide de la fonction glisser-déposer, en faisant glisser la bordure droite de la colonne.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-drag-drop.jpg" alt=""/>
</figure>

<p>Après l'ajout de colonnes, vous devez les remplir avec un contenu (modules). Pour ajouter un module, appuyez sur le bouton <strong>Add Widget</strong> et sélectionnez le module nécessaire dans la liste déroulante.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-widget.jpg" alt=""/>
</figure>

<p>Après avoir ajouté un module, vous pouvez le modifier ou supprimer,</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change.jpg" alt=""/>
</figure>

<p>et également le déplacer vers une autre colonne.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change-position.jpg" alt=""/>
</figure>

<p>Après la création de toutes les lignes, des colonnes et des modules, enregistrez le module Builder et l'affichez dans l'une des positions sur la page principale du magasin.</p>