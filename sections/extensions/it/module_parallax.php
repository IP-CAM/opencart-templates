<h3>TM Parallax</h3>

<p>Questo modulo permette di creare aree con effetto di parallasse sulle pagine. Il modulo fornisce la possibilità di visualizzare le immagini, video e altri moduli. Il modulo supporta il numero illimitato di strati, ognuno dei quali può muoversi in direzioni diverse e con velocità diverse.</p>

<figure class="img-polaroid">
    <img src="img/tm_parallax.png" alt=""/>
</figure>

<h5>TM Parallax modulo configurazione:</h5>
<h6>Impostazione generale:</h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Nome Modulo</dt>
            <dd>nome del modulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Stato</dt>
            <dd>stato del modulo (abilita/disabilita);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Immagine</dt>
            <dd>sfondo (principale) immagine di parallasse;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Larghezza</dt>
            <dd>larghezza dell'immagine di sfondo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Altezza</dt>
            <dd>altezza dell'immagine di sfondo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Blur</dt>
            <dd>immagine sfocatura dello sfondo nel caso in cui le sue dimensioni (le dimensioni) è troppo bassa per la visualizzazione di alta qualità nella zona di parallasse;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Direzione</dt>
            <dd>direzione del movimento effetto di parallasse;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Velocità</dt>
            <dd>velocità di movimento effetto di parallasse;</dd>
        </dl>
    </li>
</ol>

<h6>Impostazioni del modulo aggiuntive:</h6>
<ol class="index-list">
    <li>
        <p>Al fine di creare un nuovo livello è necessario premere su <strong>"Aggiungi Livello" ("Add Layer")</strong> pulsante.</p>

        <figure class="img-polaroid">
            <img src="img/tm_parallax_edit-1.png" alt=""/>
        </figure>

        <p>Dopo aver aggiunto lo strato specificare le seguenti impostazioni:</p>

        <h5>Configurazione strato:</h5>

        <ol class="index-list">
            <li>
                <dl class="inline-term">
                    <dt>Tipo</dt>
                    <dd>tipo di livello;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Descrizione</dt>
                    <dd>descrizione strato;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Immagine</dt>
                    <dd>immagine strato;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Larghezza</dt>
                    <dd>larghezza dell'immagine strato;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Altezza</dt>
                    <dd>altezza dell'immagine strato;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Velocità</dt>
                    <dd>velocità di movimento strato;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Dissolvenza</dt>
                    <dd>lo strato diventerà gradualmente visibile dalla piena trasparenza alla piena opacità a seconda della posizione di scorrimento strato;
                    </dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Blur</dt>
                    <dd>sfondo sfocatura delle immagini nel caso in cui la sua dimensione (le dimensioni) saranno troppo basse per la visualizzazione di alta qualità nella zona di parallasse. E applicabile solo ai livelli di testo "Media";
                    </dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>Direzione</dt>
                    <dd>direzione del movimento strato;</dd>
                </dl>
            </li>
        </ol>
    </li>
    <li>
        <p>Per visualizzare il modulo nello strato, è necessario premere sul <strong>"aggiungi Modulo" ("Add Module")</strong> pulsante nel livello appropriato.</p>
        <figure class="img-polaroid">
            <img src="img/tm_parallax_edit-2.png" alt=""/>
        </figure>
        <p>Dopo aver premuto il <strong>"Aggiungi Modulo" ("Add Module")</strong> pulsante, <strong>"Aggiungi nome Modulo" ("Added Module name")</strong> campo saranno disponibili, è necessario selezionare il modulo qui, per visualizzarlo nel livello corrente.</p>
    </li>
</ol>

