<h3>TemplateMonster Newsletter Popup</h3>

<p>Ce module permet d'ajouter la fenêtre pop-up avec une offre de souscription aux newsletters lorsque quelqu'un
    consulte votre site. La fenêtre ne s'affiche pas pour les utilisateurs autorisés (connectés) et pour ceux
    qui ont déjà souscrit.</p>
<figure class="img-polaroid">
    <img src="img/tm-newsletter-popup-view.jpg" alt=""/>
</figure>

<p>Une fois le module installé, vous pouvez le configurer sur la page de configuration de module.</p>

<h5>Paramètres:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>le nom du module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background Image</dt>
            <dd>une image d'arrière-plan de la fenêtre pop-up;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background image width</dt>
            <dd>la largeur de l'image d'arrière-plan;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background image height</dt>
            <dd>la hauteur de l'image d'arrière-plan;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Cookie Storage Time</dt>
            <dd>la durée de stockage des cookies;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Heading Title</dt>
            <dd>le titre de la fenêtre pop-up;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>la description de la fenêtre pop-up;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>le statut du module (activé/désactivé).</dd>
        </dl>
    </li>
</ol>
