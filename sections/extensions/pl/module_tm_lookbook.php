<h3>TM Lookbook</h3>

<p>Lookbook jest zestawem obrazków, gdzie można zaznaczyć dowolny fragment obrazku. Ten moduł służy do wyświetlania produktów jednej lub kilku marek.</p>
<p>Moduł posiada dwa tryby wyświetlania dla wyświetlania w pozycji sklepu: w postaci banera (1) oraz w postaci kolekcji (2).</p>


<ol class="index-list">
    <li>
