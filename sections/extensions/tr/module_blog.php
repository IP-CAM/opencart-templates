<h3>Blog</h3>

<p>Bu eklenti sayesinde maðazanýza baðlý olan yazý ve haberleri yönetebilirsiniz.</p>

<p>Ana özellikler:</p>

<ul class="marked-list">
    <li>makale oluþturmak;</li>
    <li>kategori oluþturmak;</li>
    <li>yazar oluþturmak;</li>
    <li>makaleleri yorumlamak ve yorumlarý yanýtlamak;</li>
