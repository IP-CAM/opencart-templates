<h3>TM Video Background</h3>

<p>Das jeweilige Modul zeigt den Block mit einem Video im Hintergrund, das entweder lokal oder mit mit Hilfe von YouTube
    hochgeladen wurde, an.</p>

<p>Nachdem Sie das Modul installiert haben, können Sie es auf der Konfigurationsseite einstellen.</p>

<h5>Einstellungen:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>der Modulname;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>der Modulstatus (aktiviert/deaktiviert).</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Video Source</dt>
            <dd>Videoquelle. Es sind 2 Werte verfügbar:</dd>
        </dl>

        <ul class="marked-list">
            <li>
                <dl class="inline-term">
                    <dt>local</dt>
                    <dd>das Video wird von Ihrer Website hochgeladen;</dd>
                </dl>
            </li>

            <li>
                <dl class="inline-term">
                    <dt>youtube</dt>
                    <dd>das Video wird von YouTube hochgeladen.</dd>
                </dl>
            </li>
        </ul>
    </li>
    <li>
        <dl class="inline-term">
            <dt>YouTube url</dt>
            <dd>der Link zum Video von Youtube;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Mute</dt>
            <dd>Aus Lautstärke;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Mobile</dt>
            <dd>aktiviert/deaktiviert die Darstellung vom Video auf mobilen Geräten. Wenn der Wert 'Yes' ausgewählt
                wurde, wird das Video auf den mobilen Geräten angezeigt;
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Start Video</dt>
            <dd>die Sekunde, ab die das Video abgespielt werden muss;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Mobile Image</dt>
            <dd>das Bild, dass anstatt des Hintergrundbildes angezeigt wird, wenn es auf mobilen Geräten deaktiviert
                wurde;
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Mobile Image Width</dt>
            <dd>die Breite des Hintergrundbildes;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Mobile Image Height</dt>
            <dd>die Höhe des Hintergrundbildes;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>html-Markup, das über dem Hintergrundvideo angezeigt wird.</dd>
        </dl>
    </li>
</ol><p>Wenn Sie
    <strong>Video Source - Local Video</strong> auswählen, legen Sie dann das Bild (Poster) im Format ".jpg" fest, durch
    das das Hintergrundvideo auf mobilen Geräten ersetzt wird. Damit das Modul korrekt funktioniert, müssen sich in dem
    Ordner mit dem Bild, das Sie festgelegt haben, 3 Videos mit den gleichen Namen in den folgenden Formaten befinden:
</p>
<ul class="marked-list">
    <li>*.ogv</li>

    <li>*.mp4</li>

    <li>*.webm</li>
</ul>

