<h3>TemplateMonster Color Switcher 

</h3>
<p align="justify">Это модуль позволяющий менять цветовые схемы  темы сайта. Модуль автоматически применяет цветовые схемы из папки color_schemes\css\ активной темы.</p>
<figure class="img-polaroid">
  <p><img src="img/switcher.jpg" alt=""/></p>
</figure>
<h4>Как управлять модулем TemplateMonster Color Switcher 
</h4>
<p>Для того чтобы изменить цветовую схему вашего шаблона, следуйте перечисленым ниже инструкциям:</p>
<ul class="marked-list">
  <li>В админ панеле вашего сайта перейдите к <strong>Srores</strong> &gt; <strong>StoreFlex </strong>(Ваш Магазин).</li>

  <p>
    <figure class="img-polaroid">
      <img src="img/switcher-4.jpg" alt=""/>
    </figure>
    
  </p>
<li>Нажмите кнопку модуля &quot;Color Switcher&quot;, расположеную слева:</li>
<p>
  <figure class="img-polaroid">
    <img src="img/switcher-2.jpg" alt=""/>
  </figure>
  
</p>
<li>Выберите нужную цветовую схему (цветовая схема вашего сайта изменится);</li>
<p>
  <figure class="img-polaroid">
    <img src="img/switcher-1.jpg" alt=""/>
  </figure>
  
</p>
<p>или же, воспользуйтесь вторым способ:</p>
<li>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong> Модули / Расширения </strong>(Extensions).</li>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<li>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</li>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<li>
  <div align="justify">В таблице с перечнем всех модулей, найдите модуль <strong>TemplateMonster  Color Switcher</strong>. Нажав кнопку &quot;редактировать&quot; (edit), вы перейдете к окну настроек данного модуля.</div>
</li>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/switcher-6.jpg" alt=""/></figure>
</p>
<li>Вы перейдете к следующему окну настроек:</li>
<figure class="img-polaroid"> <img src="img/switcher-7.jpg" alt=""/></figure>
<h5>Базовые настройки модуля:</h5>
<ol class="index-list">
	<li>
		 
			<strong>Status</strong>
			— статус модуля (вкл./выкл.);
		
</li>

	<li>
		 
			<strong>Color</strong>
			— выберите желаемую цветовую схему из списка.</li>
</ol>
<li>Не забудьте сохранить настройки модуля, нажав кнопку &quot;<strong>Сохранить</strong>&quot; в правом верхнем углу: </li>
  <figure class="img-polaroid"> <img src="img/edit-parallax-21.jpg" alt=""/></figure>
       </ul>