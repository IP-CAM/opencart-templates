<h3>TM CountDown</h3>

<p>Модуль предназначен для создания счетчиков обратного отсчета. Счетчики могут быть представлены в виде числового и
    кругового таймера.
</p>
<p>Числовой:</p>

<figure class="img-polaroid">
    <img src="img/tm_countdown.png" alt=""/>
</figure>

<p>Круговой:</p>

<figure class="img-polaroid">
    <img src="img/tm_countdown_circle.png" alt=""/>
</figure>

<p>Для того чтобы изменить тип счетчика с обычного на круговой, необходимо выбрать в поле “Type” опцию “Radial”. При
    этом появятся дополнительные настройки кругового таймера:</p>
<figure class="img-polaroid">
    <img src="img/tm_countdown_type.png" alt=""/>
</figure>

<h5>Настройки:</h5>
<p>Базовые настройки модуля включают в себя:</p>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>статус модуля (включен/отключен);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>название модуля;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Date</dt>
            <dd>Дата в формате гггг-мм-дд чч:мм:сс (Для примера: 2012-12-12 00:00:00);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Type</dt>
            <dd>Тип счетчиков (стандартный/круговой);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>заголовок (описание) таймера;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Title on Days</dt>
            <dd>заголовок для счетчика дней;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Title on Hours</dt>
            <dd>заголовок для счетчика часов;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Title on Minutes</dt>
            <dd>заголовок для счетчика минут;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Title on Seconds</dt>
            <dd>заголовок для счетчика секунд;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show days</dt>
            <dd>отображать или скрывать счетчик дней;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show hours</dt>
            <dd>отображать или скрывать счетчик часов;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show minutes</dt>
            <dd>отображать или скрывать счетчик минут;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show seconds</dt>
            <dd>отображать или скрывать счетчик секунд;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Animation Type </dt>
            <dd>тип анимации счетчика “тиканье” или плавная анимация;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Direction of animation</dt>
            <dd>направление анимации относительно часовой стрелки (по или против часовой стрелки);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background circle color</dt>
            <dd>цвет внутренней окружности таймера;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Days circle color</dt>
            <dd>цвет внутренней окружности таймера, выводящего значение дней;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Hours circle color</dt>
            <dd>цвет внутренней окружности таймера, выводящего значение часов;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Minutes circle color </dt>
            <dd>цвет внутренней окружности таймера, выводящего значение минут;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Seconds circle color</dt>
            <dd>цвет внутренней окружности таймера, выводящего значение секунд;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Foreground circle width</dt>
            <dd>толщина внешней окружности таймера;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background circle width</dt>
            <dd>ширина внутренней окружности таймера;</dd>
        </dl>
    </li>
</ol>

