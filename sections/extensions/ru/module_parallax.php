<h4 align="justify">Как добавить новый модуль  Parallax на  страницу вашего интернет-магазина через стандартную систему макетов</h4>
<p align="justify" class="alert alert-info">Перейдите к разделу <strong>Дизайн</strong> (Design) &gt; <strong>Макеты </strong>(Layouts).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-15.jpg" alt=""/></figure>
</p>
<p align="justify">Выберите страницу на которую вы хотите добавить новый  Parallax модуль. Затем нажмите кнопку &quot;редактировать&quot; (edit)  напротив той страницы, где будет располагаться модуль.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-17.jpg" alt=""/></figure>
</p>
<p>Перейдите к секции &quot;<strong>Выберите расположение модулей</strong>&quot; (Choose the position of the modules).</p>
<p>Определитесь с позицией для нового модуля.</p>
<p>В выпадающем списке, расположенным под наименование позиции, найдите тот модуль, который хотите добавить.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-18.jpg" alt=""/></figure>
</p>
<p>Нажмите кнопку   &quot;Добавить &quot; (Add Module).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-19.jpg" alt=""/></figure>
</p>
<p>После этого действия, будет добавлен новый модуль.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-20.jpg" alt=""/></figure>
</p>
<p align="justify">В данном окне настроек у вас есть возможность удалять или редактировать добавленный модули, нажав соответствующие кнопки (&quot;Редактировать&quot; или &quot;Удалить&quot;).</p>
<p>Не забудьте сохранить настройки модуля, нажав кнопку &quot;<strong>Сохранить</strong>&quot; в правом верхнем углу:
  <figure class="img-polaroid"> <img src="img/edit-parallax-21.jpg" alt=""/></figure>
</p>
<p align="justify" class="alert alert-info">Используйте данный способ для добавления модуля любого типа на любую страницу вашего интернет-магазина.</p>