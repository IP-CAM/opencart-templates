    <h5>What is Instagram Access Token – Brief Description</h5>
    <p align="justify">Authenticated requests need <strong>Instagram Access token</strong>.   Access Token is an opaque string that identifies a user, app, or page.   It can be used by the app to make graph API calls and is unique to each   user.</p>
    <blockquote>Instagram Access Token is essential for the usage of most Instagram based apps.</blockquote>
