<h3>TM Category</h3>

<p>Данный модуль позволяет выводить категории в виде аккордеон меню, также выводит все
    подкатегории. Как правило, используется в боковой панели. </p>

<p>После установки модуля Вы можете включить или выключить его на странице
    конфигураций.</p>

