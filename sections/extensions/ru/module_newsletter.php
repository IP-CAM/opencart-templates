<h4> Как настроить рассылку</h4>
<ol>
  <li> Для того, чтобы создать новую рассылку для ваших клиентов, перейдите к разделу<strong> Marketing &gt; Mail</strong>. </li>
      <figure class="img-polaroid"> <img src="img/news-25.jpg" alt=""/></figure>
  <li>
<p><strong>Заполните</strong> все необходимые поля.</p>
  <figure class="img-polaroid"> <img src="img/news-26.jpg" alt=""/></figure>
      <ul class="list">
        <li class="first">
          <p><strong>From</strong> (От кого) – по умолчанию будет указан  email адрес определенный при установке магазина.</p>
        </li>
        <li>
          <p align="justify"><strong>To </strong>(Кому) – по умолчанию – это <strong>All Newsletter Subscribers</strong> (все подписчики на рассылку). Вы можете изменить группу рассылки, выбрав нужную в выпадающем списке опции.</p>
        </li>
        <li>
          <p align="justify"><strong>Subject</strong> (Предмет сообщения) – поле является обязательным. Введите заголовок (предмет) вашего сообщения.        </p>
        </li>
        <li>
          <p><strong>Message</em></strong> (Текст сообщения) – обязательное поле. Введите текст сообщения.        </p>
        </li>
    </ul>
  </li>
   <li>Нажмите кнопку  <strong>Send</strong>.
     <figure class="img-polaroid"> <img src="img/news-27.jpg" alt=""/></figure>
</ol>
