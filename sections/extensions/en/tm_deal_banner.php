<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-9.jpg" alt=""/></figure>
</p>
<p>Press <strong>Add Widget</strong> button.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-11.jpg" alt=""/></figure>
