<h3>Installazione su negozi preesistenti</h3>

<p class="alert alert-info custom">Se non vuoi spendere il tempo per l'installazione del modello, non esitare a contattarci <span class="label label-warning opener" onclick="showChat()">ordina</span> servizio di installaione utilizzando il nostro live-chat.</p>

<p>Se hai già un negozio OpenCart pronto e desideri semplicemente cambiarne l'aspetto utilizzando il tema, utilizza questo metodo di installazione.</p>
<p class="alert alert-warning">Assicurati che la tua versione di OpenCart risponda ai requisiti del template. Tutte le
    versioni di OpenCart sono disponibili sul
    <a href=" http://www.OpenCart.com/index.php?route=download/download" target="_blank" class="spec_link">sito
        ufficiale di OpenCart</a>
    .
</p><br/>
<ol class="index-list">
    <li>Per installare un template OpenCart sarà necessario caricare i file sul server. L'archivio
        <strong> "themeXXX(update).zip"</strong> si trova nella cartella OpenCart. Estrarre i file dell'archivio sul
        proprio computer.
    </li>

    <li>Fatto ciò, caricare i file estratti sulla directory radice dell'installazione OpenCart.
        <br/> Qui, se vuoi, puoi vedere anche una guida video su come caricare i file sul server:
        <a href="/help/how-upload-files-server-2.html" target="_blank"> How to upload files to the server</a>
        . <br/>
        <strong>I file del template non influiranno in nessun modo sui contenuti del negozio.</strong></li>
    <li>Aprire il proprio sito Web con un browser qualunque e aggiungere /install alla fine dell'URL del sito nelle
        barra degli indirizzi (
        <i>ad esempio http://il_tuo_dominio/OpenCart/install</i>
        ).
    </li>

    <li>Fare clic sul pulsante <strong>Continue</strong>. Questa operazione potrà richiedere del tempo. </li>

    <li>Entra nel pannello di Amministrazione, vai su
        <strong>Extensions -> Extension Installer </strong> e carica i file dell'archivio
        <strong>themeXXX(extensions)</strong>. Quando tutti i file sono stati caricati, vai su
        <strong>Extensions -> Modifications</strong> e premi <strong>refresh</strong> in alto a destra dello schermo;
    </li>

    <li>Quindi vai su <strong>System->Settings</strong>, vai sulla scheda Store e seleziona il template
        <strong>themeXXX</strong> nel campo Template. </li>
</ol>
<p class="alert alert-info">Qui, se vuoi, puoi vedere anche una guida completa sull'installazione:
    <a target="_blank"
       href="/help/OpenCart-2-x-how-to-install-a-template-over-the-existing-store-using-update-package.html">OpenCart.
        How to install a template over the existing store using update package</a>
    .
</p>