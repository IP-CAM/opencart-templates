<h3>Parallasse</h3>

<p>Parallax-scroll - una specifica tecnica di scorrimento utilizzata nella grafica computer in cui le immagini di sfondo
    scorrono più lentamente rispetto ai contenuti veri e propri del sito, creando così un'illusione di profondità e un
    effetto di immersione, simile alla tecnica utilizzata in molti videogiochi a due dimensioni. Questo modulo consente
    di aggiungere un effetto parallasse al sito senza particolari sforzi. </p>

<p>Dopo aver installato il modulo, sarà possibile configurarlo nell'apposita pagina di configurazione.</p>

<p class="alert alert-info">Altezza e ampiezza dell'immagine dovrebbero corrispondere all'originale (impostazione
    predefinita) altrimenti l'effetto potrebbe non essere l'ideale.</p>

