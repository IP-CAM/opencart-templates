<h3>TemplateMonster Newsletter</h3>

<p>Данный модуль добавляет блок с формой подписки на email рассылку.</p>
<figure class="img-polaroid">
    <img src="img/tm-newsletter-view.jpg" alt=""/>
</figure>

<p>После установки модуля, Вы можете настроить его на странице настроек модуля.</p>
<h4>Как управлять модулем TemplateMonster Newsletter </h4>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong>Модули / Расширения</strong> (Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите раздел<strong> TemplateMonster Newsletter</strong>. Нажав кнопку &quot;редактировать&quot; (edit), вы перейдете к окну настроек существующего модуля. В данном случае это модуль  <strong>Newsletter</strong>.</p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/news-15.jpg" alt=""/></figure>
</p>
<p>Вы увидите следующие окно настроек:</p>
<figure class="img-polaroid"><img src="img/news-18.jpg" alt=""/></figure>
<h5>Доступные параметры настроек модуля:</h5>
<ol class="marked-list">
  <li><strong>Module Name</strong> — название модуля;</li>
  <li>
    <div align="justify"><strong>Description </strong>— текст, который будет добавлен перед формой подписки на email рассылку;</div>
  </li>
  <li><strong>Status </strong>— статус модуля (включен/отключен). </li>
</ol>
<h4>Как добавить новый модуль типа TemplateMonster Newsletter</h4>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong> Модули / Расширения </strong>(Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите раздел <strong> TemplateMonster Newsletter</strong><strong></strong>. Нажмите кнопку &quot;Добавить&quot; и вы перейдете к процессу создания нового модуля этого типа.</p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/news-16.jpg" alt=""/></figure>
</p>
<p>Перед собой вы увидите следующие окно настроек:</p>
<figure class="img-polaroid"> <img src="img/news-18.jpg" alt=""/></figure>
<p>Заполните все необходимые поля настроек:</p>
<ol class="marked-list">
  <li><strong>Module Name</strong> — укажите название модуля;</li>
  <li>
    <div align="justify"><strong>Description </strong>— укажите текст, который будет добавлен перед формой подписки на email рассылку; </div>
  </li>
  <li><strong>Status </strong>— определите статус модуля (включен/отключен). </li>
</ol>
