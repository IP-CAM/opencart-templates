<h3>TemplateMonster Instagram</h3>

<p>Ce module affiche les photos du réseau social Instagram y compris le nombre de "likes" sous forme de liste
    .</p>
<figure class="img-polaroid">
    <img src="img/tm-instagram-view.jpg" alt=""/>
</figure>

<p>Une fois le module installé, vous pouvez le configurer sur la page de configuration de module</p>

<h5>Paramètres:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>le nom du module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Select Get</dt>
            <dd>comment obtenir les images. Il y a 2 valeurs disponibles (user and tagged), vous pouvez obtenir les
                images de l'utilisateur ou utiliser le hashtag;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry TagName</dt>
            <dd>le tag qui est utilisé pour afficher les images (il est utilisé si vous avez précédemment sélectionné
                "tagged" dans le champ "Select get");
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry ClientID</dt>
            <dd>votre API ID sur instagram.com (il est utilisé si vous avez précédemment sélectionné "tagged" dans le
                champ "Select get");
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry UserId</dt>
            <dd>ID de l'utilisateur (il est utilisé si vous avez précédemment sélectionné "user" dans le champ "Select
                get");
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry accessToken</dt>
            <dd>un jeton pour afficher une image personnalisée (il est utilisé si vous avez précédemment sélectionné
                "user" dans le champ "Select get");
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry limit</dt>
            <dd>limite le nombre d'entries affichées;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>le statut du module (activé/désactivé).</dd>
        </dl>
    </li>
</ol>
