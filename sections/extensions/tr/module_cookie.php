<h3>TM Cookie Policy</h3>

<p>Bu modül, site ziyaretçilerini, çerez kullanımı için uyarır. Çerez Yasası gereğince, websitenizde çerez
    kullanıldığı uyarısını uygun modüllerle bildirmeniz gerekir.</p>

<p>Çerezler; websitelerin, ziyaretçilerin bilgisayarına yerleştirdiği küçük yazı dosyalarıdır. Belirli ziyaretçileri
    tanımlamak ve onlara daha özgün hizmetler sunabilmek için kullanılır. Çerezler, pek çok sitenin fonksiyonlarını
    yerine getirebilmesi için önemli bir gereksinimdir.</p>

<p>Örneğin, online mağaza sitelerinde yer alan sepetinizdeki ürünler çerezler tarafından kaydedilir. Ayrıca siteye
    kayıtlı kullanıcı olarak giriş yapabilmenizi veya website sahiplerinin kullanım istatistiklerini ve bilgilerini
    görmelerini de sağlar.</p>

<p>Eğer bir websiteniz varsa, büyük ihtimalle çerez kullanıyordur. Ziyaretçi sayısını kaydetme ve Google Analytics gibi
    ziyaretçi davranışlarını gözlemleme gibi fonksiyonları vardır. Ayrıca ziyaretçilere, ilgili reklamlar göstermek veya
    online mağazanız varsa mağaza sisteminizin kolayca kullanılmasını sağlarlar.</p>

<p>26 Mayıs 2012 tarihinde - ICO bu yasayla ilgili bir değişiklik yaptı. Artık, gizlilik politikası açıkça belirtildiği
    sürece çerezler kullanabiliyor.</p>

