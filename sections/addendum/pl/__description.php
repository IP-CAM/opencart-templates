<h4>Can't load Google Fonts: </h4>

<ol class="index-list">
    <li>Otwórz plik <strong>catalog\view\theme\themeXXX\stylesheet\stylesheet.css</strong>.</li>

    <li><p>Zlokalizuj linię: </p>
        <code>//fonts.googleapis.com/css?family=[---your_web_font_name---]</code>
    </li>

    <li><p>Zamień jej następującą: </p>
        <code>//fonts.useso.com/css?family=[---your_web_font_name---]</code>
    </li>
</ol>

<h4>Walidacja</h4>

<p class="alert alert-info">Zarówno język HTML, jak i style CSS, stosowane w naszych szablonach, są semantycznie
    poprawne i aktualne. Jednak niektóre błędy W3C nadal mogą się zdarzać. Dokonanie kodu stuprocentowo poprawnym według
    W3C eliminuje użycie nowoczesnych technologii witryny internetowej, takich jak funkcje CSS3 i język HTML5. Pragniemy
    dostarczać użytkownikom bogate doświadczenie z naszymi szablonami o wysokiej jakości, dlatego czasami musimy łamać
    niektóre zasady.</p>