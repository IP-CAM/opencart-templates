<h2 class="item1">Préparation</h2>

<h6>Avant de commencer à mettre en place votre site Web OpenCart, veuillez vous assurer d'être complètement préparé. </h6>

<h4>Logiciel d'édition</h4>

<p>Pour être à l'aise en travaillant avec un template OpenCart, nous recommandons de télécharger toutes les applications requises. Vous pouvez consulter la liste des logiciels requis sur la page de Démo Live du template. Les besoins peuvent varier selon le template, nous allons vous dire ici ce qui est requis en général:</p>

<ol class="index-list">
    <li>Vous pouvez avoir besoin de l'application Adobe Photoshop. Elle sert à modifier les fichiers source .PSD, ce qui est nécessaire si vous voulez modifier les images et graphiques du template. </li>

    <li>Pour modifier les fichiers de code source du template, il vous faut un éditeur de code comme Adobe Dreamweaver, Notepad++, Sublime Text, etc.</li>

    <li>Pour uploader des fichiers sur le serveur d'hébergement, vous pouvez avoir besoin d'un Gestionnaire FTP comme Total Commander, FileZilla, CuteFTP, etc. </li>

    <li>Veuillez consulter le guide suivant pour dézipper les archives, en utilisant
        <a href="/help/extract-archived-files-windows-os.html" target="_blank"> WinZip(PC)</a>
        et
        <a href="/help/extract-archived-files-mac-os.html" target="_blank"> StuffitExpander(MAC)</a>
        .
    </li>
</ol>

<h4>Hébergement</h4>

<p>Comme OpenCart est une application basée sur PHP/MySQL, vous devez préparer un environnement d'hébergement. </p>

<p>Si vous avez un hébergement, assurez-vous qu'il est conforme aux
    <a href="http://docs.OpenCart.com/system/server-requirements" target="_blank">Exigences du système OpenCart</a>
   et qu'il est prêt à être utilisé pour les sites OpenCart.
</p>
<p>Sinon, vous pouvez faire tourner OpenCart localement sur votre ordinateur en utilisant un serveur local. Pour créer un serveur d'hébergement local, veuillez utiliser les applications localhost telles que
    <b>WAMP</b>, <b>AppServ</b>,
    <b>MAMP</b>, etc. Chacune de ces applications peut être facilement installée comme tout autre programme et utilisée pour faire tourner OpenCart.
</p>

