<h3>TM CountDown</h3>

<p>Este módulo permite criar contadores regressivos. Existem dois tipos disponíveis, numérico e radial.
</p>
<p>Numérico:</p>

<figure class="img-polaroid">
    <img src="img/tm_countdown.png" alt=""/>
</figure>

<p>Radial:</p>

<figure class="img-polaroid">
    <img src="img/tm_countdown_circle.png" alt=""/>
</figure>

<p>Para mudar o tipo de contador para radial, você precisa escolher a opção "Radial" no cmapo "Type" (tipo). Neste caso, configurações adicionais para o contador radial tornam-se disponíveis:</p>
<figure class="img-polaroid">
    <img src="img/tm_countdown_type.png" alt=""/>
</figure>

<h5>Configurações:</h5>
<p>As configurações primárias do módulo são as seguintes:</p>

<ol class="index-list">
    <li>
        <dl class="inline-term">
                <dt>Module Name</dt>
                <dd>título do módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>situação do módulo (ativo/inativo);</dd>
        </dl>
    </li>
    
        
    
    <li>
        <dl class="inline-term">
            <dt>Date</dt>
            <dd>Data no formato aaaa-mm-dd hh:mm:ss format (exemplo: 2012-12-12 00:00:00);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Type</dt>
            <dd>Tipo de contador (padrão/radial);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>legenda/descrição do contador;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Title on Days</dt>
            <dd>título do contador de dias;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Title on Hours</dt>
            <dd>título no contador de horas;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Title on Minutes</dt>
            <dd>título no contador de minutos;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Title on Seconds</dt>
            <dd>título no contador de segundos;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show days</dt>
            <dd>se deve exibir/esconder o contador de dias;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show hours</dt>
            <dd>se deve exibir/esconder o contador de horas;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show minutes</dt>
            <dd>se deve exibir/esconder o contador de minutos;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show seconds</dt>
            <dd>se deve exibir/esconder o contador de segundos;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Animation Type </dt>
            <dd>tipo de animação do contador, em cortes secos ou com transição suave;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Direction of animation</dt>
            <dd>animação direta relativa ao sentido horário (no sentido ou contrário ao relógio);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background circle color</dt>
            <dd>cor do círculo interno do contador;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Days circle color</dt>
            <dd>cor do interior do círculo do contador de dias;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Hours circle color</dt>
            <dd>cor do interior do círculo do contador de horas;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Minutes circle color </dt>
            <dd>cor do interior do círculo do contador de minutos;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Seconds circle color</dt>
            <dd>cor do interior do círculo do contador de segundos;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Foreground circle width</dt>
            <dd>espessura do exterior do círculo do contador;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Background circle width</dt>
            <dd>largura interior do círculo do contador;</dd>
