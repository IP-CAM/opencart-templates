        <dl class="inline-term">
            <dt>Statut</dt>
            <dd> état du module (activé\désactivé).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Nom du module</dt>
            <dd> le nom du module;</dd>
        </dl>
    </li>
