<h3>TemplateMonster 360-view</h3>

<p align="justify"> <strong>TemplateMonster 360-view </strong>модуль  позволит быстро и легко создавать объемные изображения любого продукта. </p>
<figure class="img-polaroid"> <img src="img/5.gif" alt=""/> </figure>
<h4>Как управлять модулем  TemplateMonster 360-view</h4>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt;<strong> Модули / Расширения</strong> (Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/> </figure>
</p>
<p>Выберите <strong>Модули </strong>(Modules) в выпадающем списке фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/> </figure>
</p>
<p>В таблице с перечнем всех модулей, найдите раздел <strong>TemplateMonster 360-view</strong>. Нажав кнопку &quot;<strong>редактировать</strong>&quot; (edit), вы перейдете к окну настроек существующего модуля. </p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>Перед собой вы увидите следующие окно настроек:</p>
<figure class="img-polaroid"> <img src="img/3d.jpg" alt=""/> </figure>
<h5>Доступные параметры настроек модуля:</h5>
<ol class="marked-list">
  <li><strong>Module Name</strong> — введите название модуля (название НЕ будет отображаться на сайте);</li>
  <li><strong>Status</strong> — состояние модуля (включен/отключен);</li>
  <li><strong>Image Width</strong> — задайте ширину изображения;</li>
  <li><strong>Image Height</strong> —  задайте значение высоты изображения;</li>
  <li><strong>Rotation Speed</strong> — вы можете изменить скорость вращения. По умолчанию значение равно 100. Чем <strong>больше</strong> значение, тем <strong>меньше</strong> видимая скорость вращенияю и наоборот.</li>
  <li><strong>Select Your Product Image</strong> — загрузите изображения для анимации;</li>
  <li><strong>Autoplay </strong> — анимация начинается автоматически, если опция активирована.</li>
</ol>
<p> Обратите внимание: данный модуль поддерживает мультиязычность, что дает вам возможность настраивать его отдельно под все установленные в вашем магазине языки.</p>
<ol class="marked-list">
  <li><strong> Title, Description, Information</strong> — введите текс который хотите отображать в пределах данного модуля.</li>
</ol>
<p>Не забудьте сохранить настройки модуля, нажав кнопку &quot;<strong>Сохранить</strong>&quot; в правом верхнем углу.</p>
<h4><strong> Требования к изображениям:</strong></h4>
<ul class="marked-list">
  <li>Имена изображений должный соответствовать последовательности их отображения, начиная с 1. (напр.: 1.png, 2.png, 3.png etc).</li>
  <li>Изображения для каждой(-го) отдельной(-го) анимации (товара) должны храниться в отдельной папке.</li>
  <li>Изображения должны быть одинакового размера.</li>
  <li>Объект должен быть расположен на прозрачном  фоне (для наилучшего эффекта <strong>360-view</strong>).</li>
  <li> Предпочтительно использовать PNG изображения (другие форматы изображений допустимы).</li>
</ul>
