<h3>TM Layout Builder</h3>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-edit.jpg" alt=""/>
</figure>
<p>Ten moduł służy do szybkiego, prostego oraz poręcznego tworzenia różnych wariantów strony głównej sklepu.</p>

<h5>Podstawowe ustawienia modułu:</h5>
<ol class="index-list">
	<li>
		<dl class="inline-term">
			<dt>Status</dt>
			<dd>Status modułu (Włączony/Wyłączony);</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Nazwa modułu (Module name)</dt>
			<dd>Nazwa modułu;</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Klasa modułu (Module Class)</dt>
			<dd>Klasa modułu;</dd>
		</dl>
	</li>
	<li>
		<dl class="inline-term">
			<dt>ID modułu (Module ID)</dt>
			<dd>Identyfikator modułu.</dd>
		</dl>
	</li>
</ol>
<p>Aby utworzyć nowy układ, należy nacisnąć przycisk <strong>Widok nowego układu (New Layout View)</strong>, a następnie nacisnąć przycisk <strong>Dodaj wiersz (Add Row)
		(1)</strong>. Po dodaniu wiersza, należy dodać kolumny. Aby dodać kolumny, należy nacisnąć przycisk <strong>Dodaj kolumnę (Add
		Column) (2)</strong>.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-column.jpg" alt=""/>
</figure>

<p>Liczba kolumn w wierszu jest nieograniczona i będzie można dodawać dowolną liczbę kolumn. Szerokość kolumn może być ustawiona bezpośrednio po dodaniu ich za pomocą funkcji 'przeciągnij i upuść', przeciągając prawą krawędź kolumny. </p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-drag-drop.jpg" alt=""/>
</figure>

<p>Po dodaniu kolumny, należy wypełnić je zawartością (moduły). Aby dodać moduł, należy nacisnąć przycisk <strong>Dodaj widżet (Add Widget)</strong>, zatem wybrać potrzebny z rozwijanej listy.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-widget.jpg" alt=""/>
</figure>

<p>Po dodaniu modułu, można go edytować lub usunąć,</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change.jpg" alt=""/>
</figure>

<p>a także przenieść go do innej kolumny.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change-position.jpg" alt=""/>
</figure>

<p>Po przeprowadzeniu ustawień wszystkich wierszy, kolumn i modułów, należy zapisać moduł konstruktora i wyświetlić go w jednej z pozycji na stronie głównej sklepu.</p>