<h3>TemplateMonster Einzelkategorien Produkte</h3>

<p>Dieses Modul ermöglicht es Ihnen, 4 Hauptmodule (Angebote, Bestseller, Ausgewählte, Neuste) mit Produkten aus bestimmten Kategorien im Ansichttab anzuzeigen.</p>

<figure class="img-polaroid">
    <img src="img/tm-single-category-product-view.jpg" alt="">
</figure>

<p>Nachdem das Modul installiert ist, können Sie es auf der Moduleinstellungenseite konfigurieren.</p>

<h5>Einstellungen:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Modulname</dt>
            <dd> Name des Moduls;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Kategorie</dt>
            <dd>ermöglicht es, die vorhandene Produktkategorie zu wählen;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Tabs</dt>
            <dd> aktiviert/deaktiviert Tabansicht. Falls es deaktiviert ist, gibt es eine Option, um den Modul Ansichtstyp auswählen;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Produkttyp</dt>
            <dd>erlaubt es, Modultyp auswählen, wenn die Tabs-Option ist deaktiviert.</dd>
        </dl>

        Die folgenden Werte stehen zur Verfügung:

        <ul class="marked-list">
            <li>Angebote;</li>
            <li>Bestseller;</li>
            <li>Ausgewählte;</li>
            <li>das Neuste.</li>
        </ul>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Angebote</dt>
            <dd> aktiviert/deaktiviert Angebotenmodul;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Bestsellers</dt>
            <dd> aktiviert/deaktiviert Bestsellermodul;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Ausgewählte</dt>
            <dd> aktiviert/deaktiviert Ausgewähltenmodul;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>das Neuste</dt>
            <dd> aktiviert/deaktiviert den Neustenmodul;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Ausgewählte Produkte</dt>
            <dd> erlaubt es eine Produktliste für das Ausgewähltenmodul auszuwählen. Verfügbar, wenn Ausgewählte aktiviert ist oder der Optionsprodukttyp zum Ausgewählte gesetzt wird;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd> Anzahl der angezeigten Produkte;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Bildbreite</dt>
            <dd> Bildbreite;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Bildhöhe</dt>
            <dd> Bildhöhe;</dd>
        </dl>
    </li>
    <li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd> Modulstatus (aktiviert/deaktiviert).</dd>
        </dl>
    </li>
</ol>

