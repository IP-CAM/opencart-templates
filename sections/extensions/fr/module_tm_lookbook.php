<h5>La connexion des produits avec l'image de la collection, l'ajout de la zone de sélection de produits:</h5>
<p>Pour ajouter un produit à une collection, sélectionnez-le dans le champ produits:</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_product.jpg" alt=""/>
</figure>

<p>Pour marquer les produits sélectionnés de l'image, appuyez sur le bouton "Add Image Map":</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_image_map.jpg" alt=""/>
</figure>


<p>Après avoir appuyé sur le bouton "Add Image Map", au bas, vous verrez une fenêtre, qui permet de créer des zones de sélection et d'ajouter des produits à ces zones.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_image_map_object.jpg" alt=""/>
</figure>

<p>Les zones que vous créez peuvent être de 3 formes différentes: carré, rond, et complexe (polygonale).</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures.jpg" alt=""/>
</figure>

<p>Pour créer une zone de sélection sur l'image, appuyez sur le bouton, dont le nom correspond à la forme nécessaire de la zone de sélection.</p>


<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_add.jpg" alt=""/>
</figure>

<p>Après avoir choisi la forme, placez-le sur l'image. Cliquez sur le bouton gauche de la partie nécessaire de l'image, dessinez la zone.</p>

<p class="alert alert-warning">Remarque: la sélection polygonale doit toujours être fermé, il faut faire le premier et le dernier point coïncider.</p>

<p>Après avoir placé la zone de sélection sur l'image, choisissez le produit qui est affiché dans la zone sélectionnée. Pour ce faire, appuyez sur le bouton "Edit".</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_edit.jpg" alt=""/>
</figure>

<p>Ensuite, cliquez deux fois sur la zone et, dans la fenêtre déroulante, sélectionnez le produit nécessaire, appuyez sur le bouton "Save".</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_save.jpg" alt=""/>
</figure>

<p>Après avoir marqué toutes les zones et la cartographie de tous les produits, appuyez sur le bouton "Save".</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_save.jpg" alt=""/>
</figure>

<p>Enregistrez les paramètres du module.</p>