<h3>Установка модулей</h3>

<p align="justify">Установка модуля проходит в несколько шагов. Перейдите в панель администратора
    (добавив /admin в адресной строке — www.yoursite.com/admin).</p>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong> Модули / Расширения </strong>(Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p align="justify">Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">В таблице показаны все установленные и готовые к установке модули.
  Если напротив
  модуля есть кнопка <strong>Деинсталлировать (Uninstall)</strong> — это значит, что
  модуль уже установлен.</p>
  <p>
  <figure class="img-polaroid"> <img src="img/install-2.jpg" alt=""/></figure>
</p>
  
<p align="justify"> Нажав на <strong>Редактировать (Edit)</strong>, вы перейдете к 
  окну настроек данного модуля. </p>
  <p>
  <figure class="img-polaroid"> <img src="img/install-3.jpg" alt=""/></figure>
</p>
<p align="justify"> Нажав
  на <strong>Установить (Install)</strong>, Вы произведете установку модуля. </p>
<p>
  <figure class="img-polaroid"> <img src="img/install-4.jpg" alt=""/></figure>
</p>

<p align="justify">Удаление
  модуля происходит похожим образом: необходимо нажать кнопку <strong>Деинсталлировать
    (Uninstall)</strong>,
  после чего модуль будет удален. </p>

<p>
  <figure class="img-polaroid"> <img src="img/install-5.jpg" alt=""/></figure>
</p>


<p align="justify" class="alert alert-warning">Будьте внимательны,  при удалении модуля будут удалены и его настройки, которые
    восстановить будет невозможно.</p>
<p align="justify">После установки модуля необходимо его настроить. Настройки могут отличаться в
    зависимости от модуля, кроме обязательного поля <strong>Status</strong> —
    данное поле
    включает/выключает наш модуль.</p>
<figure class="img-polaroid">
    <img src="img/edit-module_1.jpg" alt=""/>
</figure>
<p>Для добавления модуля на страницу требуется перейти к списку страниц. </p>

        <p>В боковом меню перейдите в раздел <strong>Дизайн > Макет (Design > Layout)</strong>.</p>

  <figure class="img-polaroid">
         <img src="img/edit-parallax-15.jpg" alt=""/>   
        </figure>

<p>Выберите из списка страницу, на которую нужно добавить модуль, напротив неё нажмите
    <strong>Редактировать (Edit)</strong>.</p>
<figure class="img-polaroid">
    <img src="img/edit-parallax-17.jpg" alt=""/>
</figure>

<p>Далее показан список модулей, которые уже имеются на странице, и их позиции.</p>
<figure class="img-polaroid">
    <img src="img/lay.jpg" alt=""/>
</figure>


<p>Доступны следующие позиции (по порядку отображения):</p>
<ol class="index-list">
    <li>Header Top (после секции Header);</li>
    <li>Column-left (левая боковая панель);</li>
    <li>Column-right (правая боковая панель);</li>
    <li>Content-top;</li>
    <li>Content-bottom;</li>
    <li>Footer.</li>
</ol>

<p align="justify">Поле <strong>Порядок Сортировки (Sort Order)</strong> указывает порядок отображения
    модулей на странице, которые
    находятся в одной позиции.
    Для добавления модуля на страницу нажмите <strong>Добавить модуль (Add
        Module)</strong>. В появившейся
    строке выберите необходимый модуль и позицию на странице и нажмите <strong>Сохранить
        (Save)</strong>.
    Установка модуля на этом
    закончена. </p>