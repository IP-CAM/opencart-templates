<h3>TM Megamenu</h3>

<p>Este mpodulo permite que você crie menus complexos. O módulo te dá a habilidade de exibir um item apenas (sem submenus), um item com um submenu e um item com um bloco de submenu multi-linhas e multi-colunas. Este módulo pode ser exibido em diversas posições, mas com o mesmo conteúdo.
</p>

<h5>Informações gerais</h5>

<ol class="index-list">
    <li>
        <p>Ativação do Menu</p>

        <p>Para ativar o megamenu você deve navegar para a página <strong>Extensions -> Modules -> TM Megamenu</strong>, clique no botão <strong>Edit (Editar)</strong> próximo ao item desejado e escolha a opção <strong>Enabled (Ativado)</strong> na nova janela.
        </p>
    </li>
</ol>

