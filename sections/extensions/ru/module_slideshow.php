<h3>TemplateMonster Slideshow</h3>

<p>Данный модуль позволяет отобразить Camera слайдер на вашем сайте. </p>

<p>После установки модуля Вы можете настроить его на странице настроек модуля.</p>
<h4 align="justify">Как управлять модулем  TemplateMonster Slideshow</h4>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong> Модули / Расширения </strong>(Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите раздел <strong>TemplateMonster Slideshow</strong>. Нажав кнопку &quot;редактировать&quot; (edit), вы перейдете к окну настроек существующего модуля. </p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/slideshow-1.jpg" alt=""/></figure>
</p>
<p>Вы перейдете к следующему окну настроек:</p>
<figure class="img-polaroid">
  <img src="img/slideshow-3.jpg" alt=""/>
</figure>
<h5>Доступные параметры настроек модуля:</h5>
<ul class="marked-list">
  <li> <strong>Module Name</strong> — название модуля;</li>
  <li><strong> <strong>Status</strong> — </strong>статус модуля (включен/отключен); </li>
  <li><strong>Width</strong> — ширина блока в пикселях;
  
  </li>
  <li> <strong>Height</strong> — высота блока в пикселях; </li>
  <li><strong>Min Height </strong>— минимальная высота слайдера в пикселях;</li>
  <li><strong>Effect</strong> — эффект анимации для слайдов. Может быть &quot;slide&quot;, &quot;fade&quot;, &quot;cube&quot;, &quot;coverflow&quot;или &quot;flip&quot;;</li>
  <li> <strong>Speed</strong> — скорость переключения слайдов (в ms)(допустимое значение от 0 до 1);</li>
  <li>
    <div align="justify"><strong>Autoplay</strong> — значение задержки между сменой слайдов  (в ms). Опция автопрокрутки будет отключена в случае отсутствия  заданного значения;</div>
  </li>
  <li>
    <div align="justify"><strong>Keyboard control</strong> — опция навигации по слайдам с помощью  клавиатуры (клавиш-стрелок);</div>
  </li>
  <li><strong>Mousewheel control</strong> — опция навигации по слайдам с помощью колёсика мыши; </li>
  <li><strong>Mousewheel release on edges</strong> — опция позволяет продолжать прокрутку слайдов при крайней позиции слайдера  (в начеле или в конце, бесконечная прокрутка);</li>
  <li><strong>Next Button</strong> — опция отображения кнопки &quot;Вперед&quot; в слайдере;</li>
  <li><strong>Prev Button</strong> — опция отображения кнопки &quot;Назад&quot; в слайдере;</li>
  <li><strong>Pagination</strong> —  Опция отображения нумерации слайдов;</li>
  <li><strong>Scrollbar</strong> —опция отображения полосы прокрутки;</li>
  <li><strong>Loop</strong> — опция бесконечной автоматической прокрутки слайдов, от последнего к первому.</li>

  <h5>
    <p>Настройка слайдов</p></h5>
  <p>Добавьте новый слайд, нажав кнопку &quot;<strong>Add Slide</strong>&quot;.</p>
  <figure class="img-polaroid">
  <img src="img/slideshow-6.jpg" alt=""/>
</figure>
  <p>Или же удалите его при помощи кнопки  &quot;<strong>Remove Slide</strong>&quot;.</p><figure class="img-polaroid">
  <img src="img/slideshow-7.jpg" alt=""/>
</figure>
<li>
  <p><strong>Slide type </strong> — выберите вид слайда (с изображением или видео);</p></li>
<p><strong>Image:</strong></p>


  <li>
    <div align="justify"><strong>Image </strong> — выдерите файл изображения для слайда. Для этого нажмите кнопку  &quot;edit button&quot;, которая активируется при клике на эскиз изображения (см. изображение ниже);
    </div>
  </li>
  <figure class="img-polaroid">
   <img src="img/slideshow-13.jpg" alt=""/>
      
    
  </figure>
  <li><strong>Title</strong> — введите название слайда, которое будет отображаться на сайте вашего интернет-магазина;</li>
  <li> <strong>Description </strong>— введите дополнительную информацию о слайде, поле опции поддерживает html;
    
  </li>
  <li>
    <div align="justify"><strong>Link </strong>— добавьте пользовательскую ссылку на любую страницу вашего магазина или любой другой внешний ресурс (при клике на слайд).</div>
  </li>
  <figure class="img-polaroid">
    <img src="img/slideshow-4.jpg" alt=""/>
   
  </figure>

<p><strong>Video:</strong></p>

  <li>
    <div align="justify"><strong>Video loop</strong> —  если данная опция отключена,  то при нажатии пользователем кнопки запуска проигрывания, видео будет проигрываться только один раз;</div>
  </li>
  <li><strong>Video autoplay</strong> — активируйте функцию автоматического воспроизведения видео при загрузке веб-страницы;</li>
  <li><strong>Video playback rate</strong> — задайте скорость воспроизведения видео (допустимое значение от 0 до 1);</li>
  <li><strong>Video muted </strong>— активируйте опция беззвучного воспроизведения видео;</li>
  <li>
    <div align="justify"><strong>Video volume</strong> — настройте уровень громкости воспроизведения данного видео  (допустимое значение от 0 до 1);</div>
  </li>
  <li><strong>Image </strong> — активируйте функцию показа изображения вместо видео  для мобильной версии сайта;</li>
  <li><strong>Title</strong> — введите название слайда, которое будет отображаться на сайте вашего интернет-магазина;</li>
  <li> <strong>Description </strong>— введите дополнительную информацию о слайде, поле опции поддерживает html;</li>
  <li>
    <div align="justify"><strong>Link</strong> — добавьте пользовательскую ссылку на любую страницу вашего магазина или любой другой внешний ресурс (при клике на слайд).</div>
  </li>
  <figure class="img-polaroid">
  <img src="img/slideshow-5.jpg" alt=""/>
</figure>
</ul>
