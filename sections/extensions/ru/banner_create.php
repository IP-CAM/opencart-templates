<h3>Создание баннеров</h3>

<h4>Модификация TemplateMonster Description for banners</h4>

<p align="justify">Модификация предназначенная для добавления описания баннеров.
    Для удаления или отключения модификации зайдите в <strong>Расширения > Модификации
        (Extensions >
        Modifications)</strong>
    найдите в списке расширение "TemplateMonster Description for banners"  и нажмите кнопку <strong>Отключить
        (Disable)</strong>
    или <strong>Удалить (Delete)</strong>. </p>

<p align="justify">Настройки некоторых модулей содержат поле для выбора баннера, который будет
    использоваться. </p>
<p align="justify">Перейдите в раздел <strong> Дизайн > Баннера (Design > Banner)</strong>.</p>

<p align="justify">На данной странице отображается список созданных баннеров. </p>

<figure class="img-polaroid">
    <img src="img/banner.jpg" alt=""/>
</figure>

<p>Для создания нового
    баннера нажмите <strong>Добавить (Add New)</strong>.</p>
<figure class="img-polaroid">
  <img src="img/banner-1.jpg" alt=""/>
</figure>
<p> Для редактирования
    существующего баннера нажмите <strong>Редактировать (Edit)</strong>.</p>
    <figure class="img-polaroid">
    <img src="img/banner-2.jpg" alt=""/>
</figure>
<p>Далее укажите <strong>Banner Name</strong>, <strong>Status </strong>и нажмите <strong>Добавить баннер (Add
  Banner)</strong>. В появившейся
  таблице укажите: </p>

<ol class="index-list">
<li><strong>Banner Name</strong> (Название баннера);</li>
    <li><strong>Description</strong> (Описание) — отображаемый в баннере текст (поддерживается html-разметка);</li>
    <li>
      <div align="justify"><strong>Link</strong> (Ссылку) — укажите относительную ссылку. ссылку относительно адреса сайта
        (например,
        index.php?route=product/product&amp;product_id=47) данный параметр не является
        обязательным;
      </div>
    </li>
  <li><strong>Image</strong> (Изображение);</li>
    <li><strong>Banner order</strong> (Порядок отображения баннера) — так как баннер может быть не один, необходимо заполнять данное
        поле.
</li>
</ol>
