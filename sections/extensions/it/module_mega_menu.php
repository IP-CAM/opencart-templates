<h3>TM Megamenu</h3>

<p>Questo modulo permette di creare menu complessi. Il modulo fornisce la possibilità di visualizzare un singolo elemento (senza sottomenu), un elemento con un sottomenu e un elemento con il multi-riga e blocco sottomenu a più colonne. Questo modulo può essere visualizzabile in varie posizioni, ma con lo stesso contenuto.
</p>

<h5>Informazioni generali</h5>

<ol class="index-list">
    <li>
        <p>Attivazione menu</p>

        <p>Al fine di consentire mega menù si dovrebbe passare alla <strong>Extensioni -> Moduli -> TM Megamenu (Extensions -> Modules -> TM Megamenu)</strong> pagina, premere il <strong>Modifica (Edit)</strong> pulsante accanto alla voce appropriata e scegliere la <strong>Abilita (Enabled)</strong> opzione nella nuova finestra.
        </p>
    </li>
</ol>

