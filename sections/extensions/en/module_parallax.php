<h3>Parallax</h3>

<p>Parallax-scroll - a specific scrolling technique used in computer graphics that shows background images appearing
    slower than the site content itself, thereby creating the illusion of depth and the effect of immersion in a
    two-dimensional video game. This module allows to add a parallax-effect to your site with no additional skills and
    efforts. </p>

<p>When the module is installed, you can configure it on the module settings page</p>

<p class="alert alert-info">Image width and height should be corresponding to the original (used by default), otherwise
    the effect may not meet your expectations.</p>

