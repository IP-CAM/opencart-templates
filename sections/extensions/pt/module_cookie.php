<h3>TemplateMonster Cookie Policy</h3>

<p>Esse módulo notifica o visitante do seu site do uso de cookies. De acordo com as condições de Leis acerca
    de Cookie, você deve incluir módulos apropriados para o seu site para mostrar mensagens de aviso sobre os cookies
    usados no seu site.</p>

<p>Cookies são pequenos arquivos de texto que sites deixam no computador de visitantes. Eles são usados para identificar
    visitantes e melhorar a experiência desses usuários. Cookies são cruciais para muitas funções de sites que achamos
    triviais.</p>

<p>Por exemplo, normalmente são cookies que permitem a funcionalidade de um carrinho de compras. Eles também são
    responsáveis por mantê-lo logado em um site quando você volta para ele, além de ser valioso em medir estatísticas de
    uso para os donos de sites.</p>

<p>Se você tem um site, provavelmente usa cookies. Em geral para monitorar o número de visitantes e seu comportamento
    através do Google Analytics. Também pode ser usados para mostrar propagandas relevantes para seus visitantes, por
    exemplo.</p>

<p>26 de maio de 2012 - O ICO lançou uma mudança na política, "consentimento tácito" é agora permitido se você manter
    sua política de cookies visível.</p>

