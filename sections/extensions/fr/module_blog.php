            <dt>Affichage du bouton Lire la suite</dt>
            <dd> activer/désactiver le bouton Lire la suite;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Afficher la date</dt>
            <dd> activer/désactiver la date;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Afficher l'auteur</dt>
            <dd> activer/désactiver l'auteur d'article;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Afficher les commentaires</dt>
            <dd> activer/désactiver le nombre de commentaires;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Afficher une image</dt>
            <dd> activer/désactiver une image d'article;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Largeur de l'image</dt>
            <dd> largeur de l'image;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Hauteur de l'image</dt>
            <dd> hauteur de l'image;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limite de symboles dans une description</dt>
            <dd> nombre de symboles dans une description d'article;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Catégorie</dt>
            <dd>  vous pouvez définir la catégorie depuis laquelle tirer les articles. Il y a aussi la possibilité d'afficher des articles les plus populaires ou les articles ajoutés récemment. 
            </dd>
