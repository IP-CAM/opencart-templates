<h3>TemplateMonster Filter
</h3>
<p>Этот модуль позволяет клиентам фильтровать просматриваемые товары по их атрибутам.</p>
<figure class="img-polaroid">
    <img src="img/filter.jpg" alt=""/>
</figure>

<p align="justify">После установки модуля вы можете настроить его на странице настроек модуля.</p>
<h4 align="justify">Как управлять модулем TemplateMonster Filter</h4>
<p>Перейдите к разделу <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p>В таблице с перечнем всех модулей, найдите модуль <strong>TemplateMonster Filter</strong>. Нажав кнопку &quot;редактировать&quot; (edit), вы перейдете к окну его настроек. </p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/filter-2.jpg" alt=""/></figure>
</p>
<p>Вы увидите следующие окно настроек:</p>
<figure class="img-polaroid">
  <img src="img/filter-3.jpg" alt=""/>
</figure>
<h5>Базовые настройки модуля:</h5>
<ul class="marked-list">
  <li> <strong>Module Name</strong> — название модуля;</li>
  <li><strong>Title</strong> — заголовок модуля, который будет виден на фронтенде;  </li>


  <li><strong>Store</strong> —  выберите магазин, где ходите отображать данный модуль;</li>
  <li><strong>Status</strong> — статус модуля (включен/отключен);</li>
  <li>
    <div align="justify"><strong>Sort Order </strong>—  заданное значение в этом поля определяет позицию модуля на фронтенде. Значение 1 определяет расположение модуля перед модулем с порядковым номером 2, 3 и т.д.;</div>
  </li>
  <li><strong>Position</strong> —  	выберите позицию для модуля из списка готовых позиций  (Column Left\Fixed);</li>
  <li>
    <div align="justify"><strong>Layout </strong>— выберите макет, в котором ходите отображать данный модуль. Если макет не будет выбран, модуль будет доступен на всех макетах вашего магазина.                                              </div>
  </li>
</ul>
