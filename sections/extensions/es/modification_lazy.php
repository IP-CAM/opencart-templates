
    <h3>TM Lazy load</h3>

    <p>La modificación puede cargar imágenes de los productos cuando se desplaza a través del sitio web. Esta extensión permite optimizar el rendimiento de su sitio web. Para cambiar la imagen preloader, debe reemplazar el archivo<strong> lazy_loader.gif</strong> en el archivo
        <strong>catalog/view/theme/themeXXX/image/</strong>.</p>
