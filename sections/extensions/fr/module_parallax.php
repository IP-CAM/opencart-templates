<h3>Parallax</h3>

<p>Défilement parallaxe est une technique spéciale de défilement utilisée en infographie où les images d'arrière-plan se
    déplacent plus lentement que le contenu du site lui-même, créant ainsi une illusion de profondeur et l'effet de
    l'immersion dans un jeu vidéo en 2D. Ce module permet d'ajouter des effets parallax à votre site web sans aucun
    effort ni compétence supplémentaire. </p>

<p>Une fois le module installé, vous pouvez le configurer sur la page des paramètres du module.</p>

<p class="alert alert-info">La largeur et la hauteur de l'image doivent correspondre à l'original (utilisé par défaut),
    sinon l'effet peut ne pas répondre à vos attentes.</p>

<h5>Paramètres:</h5>

<ol class="index-list">
    <li>
