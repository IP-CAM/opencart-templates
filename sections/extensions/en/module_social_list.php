<h3>TemplateMonster Social List</h3>

<p>This module allows to display the list of the popular social network links. </p>

<p class="alert alert-danger"> In case you install "update" theme version, template sample content (the list of the social
    network links) will not be available. In order to configure the module properly, make sure to fill in the required
    information.</p>

<p>When the module is installed, you can configure it on the module configuration page. If you leave the field
    empty, the element will not show up in the list. </p>

