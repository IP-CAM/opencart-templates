<h3>Création de bannières</h3>

<h4>Modification de TM Description for banners</h4>

<p>La modification permet d'ajouter la description de bannière. Pour désactiver ou supprimer la modification, accédez à
    <strong>Extensions -> Modifications</strong>, trouvez une extension "TM Description for banners" et cliquez
    <strong>Disable</strong> ou <strong>Delete</strong>.</p>

<p>Certains modules comprennent un champ pour sélectionner une bannière. Accédez à la section
    <strong>System -> Design -> Banner</strong></p>


<p>La page affiche la liste des bannières créées. Pour créer une nouvelle bannière, cliquez
    <strong>Add New</strong>. Pour éditer la bannière existante, cliquez <strong>Edit</strong>.</p>

<figure class="img-polaroid">
    <img src="img/banner-list.jpg" alt=""/>
</figure>


<p>Indiquez Banner Name, Status et cliquez
    <strong>Add Banner</strong>. Vous verrez la table où vous devez saisir les valeurs suivantes: </p>

<ol class="index-list">
    <li>Banner Name;</li>

    <li>Description - le texte qui apparaît dans une bannière (il prend en charge le balisage html);</li>

    <li>Link - spécifiez le lien relatif à l'URL du site (par exemple, index.php?route=product/product&amp;product_id=47),
        ce champ n'est pas obligatoire;
    </li>

    <li>Image;</li>

    <li>Banner order - il peut y avoir plus qu'une seule bannière, le champ est obligatoire à remplir.</li>
</ol>

<p>Pour supprimer la bannière, cliquez
    <strong>Remove</strong>. Une fois que la bannière a été créée vous pouvez l'utiliser pour les différents modules.
</p>

