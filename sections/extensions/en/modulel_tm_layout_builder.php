<h3>TM Layout Builder</h3>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-edit.jpg" alt=""/>
</figure>
<p>This module serves for quick, simple and handy creation of different variants of the store main page.</p>

<h5>Module basic settings:</h5>
<ol class="index-list">
	<li>
		<dl class="inline-term">
			<dt>Status</dt>
			<dd>Module status (Enabled/Disabled);</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Module Name</dt>
			<dd>The name of the module;</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Module Class</dt>
			<dd>The class of the module;</dd>
		</dl>
	</li>
	<li>
		<dl class="inline-term">
			<dt>Module ID</dt>
			<dd>ID of the module.</dd>
		</dl>
	</li>
</ol>
<p>To create a new layout, press the <strong>New Layout View</strong> button, then, press the <strong>Add Row
		(1)</strong> button. After adding a row, add columns. To add columns, press the <strong>Add
		Column (2)</strong> button.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-column.jpg" alt=""/>
</figure>

<p>The number of columns in the row is unlimited, and you can add as many columns as you need. The width of the columns can be set right after adding them with the help of the Drag and drop function by dragging the right border of the column. </p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-drag-drop.jpg" alt=""/>
</figure>

<p>After adding columns, you should fill them with content (modules). To add a module, press the <strong>Add Widget</strong> button and select the needed one from the dropdown.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-widget.jpg" alt=""/>
</figure>

<p>After adding a module, you can edit or delete it,</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change.jpg" alt=""/>
</figure>

<p>and also move it to another column.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change-position.jpg" alt=""/>
</figure>

<p>After setting up all the rows, columns and modules, save the Builder module and display it in one of the positions on the store main page.</p>