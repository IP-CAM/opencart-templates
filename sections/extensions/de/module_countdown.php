<h3>TM CountDown</h3>

	<p>Dieses Modul ermöglicht es Ihnen, Countdown-Zähler zu erstellen. Es sind zwei Arten verfügbar, numerischer und radialer Timer.
	</p>
<p>Numerischer:</p>

<figure class="img-polaroid">
    <img src="img/tm_countdown.png" alt=""/>
</figure>

<p>Radialer:</p>

<figure class="img-polaroid">
    <img src="img/tm_countdown_circle.png" alt=""/>
</figure>

<p>Um Zählertyp zum Radialen zu wechseln sollten Sie die Option "Radial" im "Typ" Feld ändern. Dann werden zusätzliche Einstellungen für den radiale Timer zur Verfügung stehen:</p>
<figure class="img-polaroid">
    <img src="img/tm_countdown_type.png" alt=""/>
</figure>

