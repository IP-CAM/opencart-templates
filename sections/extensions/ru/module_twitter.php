<h3>TemplateMonster Twitter Box</h3>

<p align="justify">Данный модуль отображает ленту постов из социальной сети Twitter. Он позволяет посетителям сайта быть в
    курсе всех последних обновлений и сообщений на Вашей странице в социальной сети Twitter.</p>

<figure class="img-polaroid">
    <img src="img/tm-twitter-view.jpg" alt=""/>
</figure>

<p>После установки модуля Вы можете настроить его на странице настроек модуля.</p>

<h4>Как управлять модулем TemplateMonster Twitter Box</h4>
<p>Перейдите к разделу <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите раздел <strong>TemplateMonster Twitter Box</strong>. Нажав кнопку &quot;редактировать&quot; (edit), вы перейдете к окну настроек существующего модуля. В данном случае это модуль <strong>Twitter Box</strong>.</p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-twitter-edit-2.jpg" alt=""/></figure>
</p>
<p>Вы увидите следующие окно настроек:</p>
<figure class="img-polaroid"> <img src="img/tm-twitter-edit.jpg" alt=""/> </figure>
<h5>Доступные параметры настроек модуля:</h5>
<ul class="marked-list">
  <li> <strong>Module Name</strong> — название модуля;</li>
  <li><strong>Page URL</strong> —  в этом поле, задайте URL той Twitter  страницы, которую хотите добавить на ваш сайт;</li>
  <li>
    <div align="justify"><strong>Widget ID</strong> — Widget ID, который вы можете получить                  <a href="https://twitter.com/settings/widgets" target="_blank">здесь</a>; </div>
  </li>
  <li><strong>Tweet Limit</strong> —  количество твитов;</li>
  <li><strong>Color Scheme</strong> — ветовая схема (светлая, темная);</li>
  <li> <strong>Width</strong> — ширина блока в пикселях; </li>
  <li> <strong>Height</strong> — высота блока в пикселях; </li>
  <li> <strong>Status</strong> — статус модуля (включен/отключен). </li>
</ul>
