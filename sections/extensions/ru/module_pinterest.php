<h3>TemplateMonster Pinterest</h3>

<p><strong>TemplateMonster Pinterest </strong>модуль позволяет отобразить Pinterest галерею в любой странице вашего интернет-магазина. </p>

<figure class="img-polaroid">
    <img src="img/tm-pinterest-view.jpg" alt=""/>
</figure>

<p>После установки модуля вы можете настроить его на странице настроек модуля.</p>
<h4 align="justify">Как управлять модулем TemplateMonster Pinterest</h4>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong> Модули / Расширения </strong>(Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите раздел  <strong>TemplateMonster Pinterest</strong>. Нажав кнопку &quot;редактировать&quot; (edit), вы перейдете к окну настроек существующего модуля. В данном случае это модуль <strong>TemplateMonster Pinterest Contact</strong>.</p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-pinterest-edit-2.jpg" alt=""/></figure>
</p>
<p>Вы увидите следующие окно настроек:</p>
<figure class="img-polaroid">
  <img src="img/tm-pinterest-edit.jpg" alt=""/>
</figure>
<h5>Доступные параметры настроек модуля:</h5>
<ul class="marked-list">
  <li> <strong>Module Name</strong> — название модуля;</li>
  <li><strong>Pinterest Page URL </strong>— полный путь к странице, с которой будет выводиться лента;</li>
  <li> <strong>Width</strong> — ширина блока в пикселях;
    
  </li>
  <li> <strong>Height</strong> — высота блока в пикселях; </li>
  <li> <strong>Status</strong> — статус модуля (включен/отключен).
    
  </li>
</ul>
<h4 align="justify">Как добавить новый модуль типа TemplateMonster Pinterest</h4>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong> Модули / Расширения </strong>(Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите раздел <strong>TemplateMonster Pinterest</strong>. Нажмите кнопку &quot;<strong>Добавить</strong>&quot; и вы перейдете к процессу создания нового модуля этого типа.</p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-pinterest-edit-3.jpg" alt=""/></figure>
</p>
<p align="justify">Перед собой вы увидите следующие окно настроек:</p>
<figure class="img-polaroid"> <img src="img/tm-pinterest-edit-4.jpg" alt=""/></figure>
<p>Заполните все необходимые поля настроек:</p>
<ul class="marked-list">
  <li> <strong>Module Name</strong> — укажите название модуля;</li>
  <li><strong>Pinterest Page URL </strong>— в этом поле, задайте полный путь к странице, с которой будет выводиться лента;</li>
  <li> <strong>Width</strong> — задайте значение ширины блока в пикселях;
    
  </li>
  <li> <strong>Height</strong> — задайте значение высоты блока в пикселях; </li>
  <li> <strong>Status</strong> — укажите статус модуля (включен/отключен).
    
  </li>
</ul>
<h4 align="justify">Как добавить новый модуль   TemplateMonster Pinterest  на Home Page (через Layout Builder)</h4>
<p align="justify">Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong> Модули / Расширения </strong>(Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p>В таблице с перечнем всех модулей, найдите раздел <strong>Template Monster Layout Builder</strong>. В соответствующей колонке <strong>напротив модуля Home Page,</strong>  нажмите кнопку &quot;<strong>Редактировать</strong>&quot;.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-7.jpg" alt=""/></figure>
</p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>Вы перейдете к следующему окну настроек:</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-8.jpg" alt=""/></figure>
</p>
<p align="justify">Определитесь с позицией для нового модуля  <strong>New   Pinterest</strong>. Помните о том, что вы можете редактировать расположение рядов и колонок в макете, если есть такая необходимость.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-9.jpg" alt=""/></figure>
</p>
<p>Нажмите кнопку <strong>Add Widget</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-11.jpg" alt=""/></figure>
</p>
<p align="justify"> В появившемся окне выберите тот  <strong>TemplateMonster Pinterest </strong>модуль, который хотите добавить на главную страницу сайта. </p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-pinterest-edit-5.jpg" alt=""/></figure>
</p>
<p>Модуль <strong>New Pinterest</strong> добавлен.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-pinterest-edit-6.jpg" alt=""/></figure>
</p>
<p>Не забудьте сохранить настройки модуля, нажав кнопку &quot;<strong>Сохранить</strong>&quot; в правом верхнем углу:
<figure class="img-polaroid"> <img src="img/edit-parallax-14.jpg" alt=""/></figure>
</p>
<p align="justify" class="alert alert-info">Используйте данный способ для добавления модуля любого типа на главную страницу вашего сайта.</p>
<h4 align="justify">Как добавить новый модуль TemplateMonster Pinterest на страницу (на любой макет) вашего интернет-магазина через стандартную систему макетов</h4>
<p>Перейдите к разделу <strong>Дизайн</strong> (Design) &gt; <strong>Макеты </strong>(Layouts).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-15.jpg" alt=""/></figure>
</p>
<p align="justify">Выберите страницу (макет), на которую(-ый) вы хотите добавить новый  New  Pinterest модуль. Затем нажмите кнопку &quot;редактировать&quot; (edit)   напротив той (того) страницы (макета), где будет располагаться модуль.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-17.jpg" alt=""/></figure>
</p>
<p>Перейдите к секции &quot;<strong>Выберите расположение модулей</strong>&quot; (Choose the position of the modules).</p>
<p>Определитесь с позицией для нового модуля.</p>
<p>В выпадающем списке, расположенным под наименование позиции, найдите тот модуль, который хотите добавить.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-pinterest-edit-8.jpg" alt=""/></figure>
</p>
<p>Нажмите кнопку &quot;<strong>Добавить</strong>&quot; (Add Module).</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-pinterest-edit-9.jpg" alt=""/></figure>
</p>
<p>После этого действия, будет добавлен новый модуль.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-pinterest-edit-10.jpg" alt=""/></figure>
</p>
<p align="justify">В данном окне настроек у вас есть возможность удалять или редактировать добавленные модули, нажав соответствующую кнопку (&quot;Редактировать&quot; или &quot;Удалить&quot;).</p>
<p>Не забудьте сохранить настройки модуля, нажав кнопку &quot;<strong>Сохранить</strong>&quot; в правом верхнем углу:
  <figure class="img-polaroid"> <img src="img/edit-parallax-21.jpg" alt=""/></figure>
</p>
<p align="justify" class="alert alert-info">Используйте данный способ для добавления модуля любого типа на любую страницу (макет) вашего интернет-магазина.</p>
