<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-11.jpg" alt=""/></figure>
</p>
<p align="justify"> В появившемся окне выберите тот <strong>TemplateMonster Slideshow</strong> модуль, который хотите добавить на главную страницу сайта. </p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/slideshow-8.jpg" alt=""/></figure>
</p>
