<h3>TM Newsletter</h3>

<p>Das jeweilige Modul fügt den Block mit dem Anmeldungsformular für E-Mail-Newsletters hinzu.</p>
<figure class="img-polaroid">
    <img src="img/tm-newsletter-view.jpg" alt=""/>
</figure>

<p>Nachdem Sie das Modul installiert haben, können Sie es auf der Konfigurationsseite einstellen.</p>

<h5>Einstellungen:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>der Modulname;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>der Text, der vor dem Anmeldungsformular für E-Mail-Newsletters, angezeigt wird;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>der Modulstatus (aktviert/deaktiviert).</dd>
        </dl>
    </li>
</ol>

