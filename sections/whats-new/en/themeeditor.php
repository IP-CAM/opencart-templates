<h3>Theme editor</h3>

<p align="justify">Theme editor allows you to edit the OpenCart theme files directly from the admin panel.
</p>


<p align="justify">In order to reach the theme editor, you should navigate to the <b>Design</b> tab and choose a <b>Theme Editor</b> option.


</p>

<figure class="img-polaroid">
    <img src="img/themeeditor.jpg" alt=""/>
</figure>
<p>First, choose a template you want to edit on the left side of the editor and then modify the needed file with the help of the text editor on right.
In order to change the code of your template choose the required file and change the code. Press the ‘Save’ button after applying your edits in order to save the template in its current state, or click on ‘Reset’ to restore the default code.
Twig is used for editing, it’s a new language used by OpenCart. You can learn more about it at the following <a href="https://twig.symfony.com/doc/2.x/" target="_blank">page.</a>
</p>