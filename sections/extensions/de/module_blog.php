<h3>Blog</h3>

<p>Die folgende Erweiterung erlaubt es Ihnen, die Beiträge und Nachrichten von Ihrem Geschäft zu verwalten.</p>

<p>Haupteigenschaften:</p>

<ul class="marked-list">
    <li>Erstellen von Beiträgen;</li>
    <li>Erstellen von Kategorien;</li>
    <li>Erstellen von Autoren;</li>
    <li>Kommentieren von Artikeln und antworten der Kommentare;</li>
    <li>Ansehen des Berichts;</li>
    <li>Anzeigen von Produkten und Artikeln die mit einem bestimmten Artikel zusammen gehören;</li>
</ul>

<p>Um die Allgemeinen Einstellungen vom Blog zu ändern, navigieren Sie <strong>System->Settings</strong>. Wählen Sie den Laden, in dem Sie die Einstellungen ändern möchten,aus der Liste und klicken Sie <strong>Edit</strong>.</p>

<h6> Blog Einstellungen kann man in der Blog Sektion sehen :</h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd> Blogstatus (aktiviert\deaktiviert);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd> Anzahl der Beiträge pro Seite;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Grenzwert der Symbole in der Beschreibung </dt>
            <dd> Anzahl der Symbole in der Artikelbeschreibung auf der Hauptblog-Seite (nicht für einzelne Artikel!);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Kommentar Auto-Genehmigung</dt>
            <dd> aktiviert / deaktiviert die automatische Kommentar Zustimmung durch den Administrator;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Autor Information</dt>
            <dd> aktiviert / deaktiviert die Information über den Autor auf Artikelseite;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Vewandte Artikel</dt>
            <dd> aktiviert / deaktiviert verwandte Artikel;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mit sozialen Netzwerken teilen</dt>
            <dd> aktiviert / deaktiviert die social share Knöpfe;</dd>
        </dl>
    </li>
</ol>

