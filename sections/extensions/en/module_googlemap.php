<h3>TemplateMonster Google Map</h3>

<p>This module allows you to add a Google map with an unlimited number of markers to your website. </p>

<p>The center of the map and one of the markers (main) are defined in store settings —
    <strong>System > Settings</strong>, in the <strong>Store</strong> tab,
    <strong>Geocode</strong> field.</p>
<figure class="img-polaroid">
    <p><img src="img/m-googlemap-view-3.jpg" alt=""/>
    </p></figure>
    <p>Press &quot;edit&quot; button.</p>
    <figure class="img-polaroid">
    <img src="img/tm-googlemap-view-4.jpg" alt=""/>
</figure>

<p>Once you click on a marker, you will see the pop up window with the <strong>Address</strong> bar. </p>

<figure class="img-polaroid">
    <img src="img/tm-googlemap-view.jpg" alt=""/>
</figure>

<p>When the module is installed, you can configure it on the module configuration page.</p>
<h4 align="justify">How to manage TemplateMonster Google Map module</h4>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p>Find the <strong>TemplateMonster Google Map</strong> section. Press <strong>edit</strong> button to configure the existing module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-googlemap-view-5.jpg" alt=""/></figure>
</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid">
  <img src="img/tm-googlemap-edit_1.jpg" alt=""/>
</figure>
<h5>Available settings:</h5>

 <ul class="marked-list">
<li> <strong>Module Name</strong> — the name of the module;</li>
  <li><strong>Google Map API key</strong> — API key for Google Map. You can find more details at the
                <a target="_blank" href="https://developers.google.com/maps/documentation/javascript/tutorial#api_key">official
  website</a>; </li>
  <li><strong><strong>Status</strong> — </strong>define the module status (enabled/disabled); </li>
  <li>
    <div align="justify"><strong>Map Zoom</strong> — numerical value. Initial scaling used when displaying a map. A value of 0 corresponds to the lowest
                level of scaling. When increasing this value, map zooms in; </div>
  </li>
  <li><strong>Map Type</strong> — map type. There are 4 options available:   <ul class="marked-list">
  <li> <strong>Roadmap</strong> — road map, the one used by default;
    
  
          <li><strong>
           
            Terrain</strong> (Landscape)
          — topographical map based on terrain information; </li>
          <li>
           
          <strong>Hybrid</strong> — a combination of a basic map and satellite images; </li>
          <li>
            
          <strong>Satellite</strong> — Google Earth images taken by satellite; </li></ul>
  
  <li><strong>Sensor</strong> — indicates whether sensor that determines users location is used or not.</li>
  <li><strong>Map Width</strong> — the width of the map. You should specify unit of measurement (% or px);
    
  </li>
  <li><strong>Map Height</strong><strong> </strong>— the height of the map. You should specify unit of measurement (% or px); </li>
  <li><strong>Map Styles</strong> — sets the style of the map. The variety of styles is available at
   <a href='https://snazzymaps.com/' rel="nofollow" target="_blank">https://snazzymaps.com/</a>;</li>
   <div class="alert alert-info">
     <p>Copy the code and paste it into the<strong> Map <strong>Styles </strong></strong> option field:</p>
             <p>
               <figure class="img-polaroid">
                 <img src="img/map.jpg" alt="">
               </figure>
             </p>
          </div>
  <li><strong>Disable UI</strong> — enables/disables user interface by default;</li>
<li><strong>Scrollwheel </strong>— enables/disables map scaling with the help of mouse wheel;</li>
  <li><strong>Draggable</strong><strong> </strong>— enables/disables the option to move the map (Drag &amp; Move);</li>
  <li><strong>Marker Image</strong> — sets marker image. If you leave the field empty, it will apply default marker; </li>
<li><strong>Active Marker Image</strong> — sets marker image when clicking; </li>
  <li><strong>Marker Image Width</strong><strong> </strong>— sets marker image width;</li>
<li><strong>Marker Image Height</strong> — sets marker image height;</li></ul>

<h5>&nbsp;</h5>
<p align="justify">In order to add a marker, click <strong>Add New</strong>. In the
    <strong>Geocode</strong> field specify marker coordinates (latitude, longitude - Example: 40.6700 , -73.9400 ) and
    in the
    <strong>Address</strong> field enter the information that will show up when clicking the marker. Required
    coordinates can be found at
    <a href='http://www.mapcoordinates.net' rel="nofollow" target="_blank">http://www.mapcoordinates.net</a>
    . In order to remove the marker, click <strong>Delete Marker</strong>.
</p>
