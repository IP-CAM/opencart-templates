<h3>Language editor</h3>

<p align="justify">OpenCart has introduced not just a design configuration functionality but also an easy way to customize your store content. The new language editor will let you change any text on the website.


</p>
<p>Navigate to the Design tab and choose the Language Editor option in your site admin panel in order to use the language editor.
</p>

<p align="justify">
