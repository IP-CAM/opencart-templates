<h3>TM Instagram</h3>

<p>Bu modül, beğenilme sayısı da dahil olmak üzere Instagram fotoğraflarının görüntülenmesini sağlar.</p>
<figure class="img-polaroid">
    <img src="img/tm-instagram-view.jpg" alt=""/>
</figure>

<p>Modül kurulduktan sonra, tüm düzenlemeleri modül ayar sayfasından yapabilirsiniz.</p>
<h5>Ayarlar:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>mdoülün ismi;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Select Get</dt>
            <dd>görseller nasıl alınacak. 2 değer kullanılabilir (kullanıcı ve etiket), yani ya belli bir kullanıcının
                görsellerini veya belli bir hashtag görsellerini kullanabilirsiniz;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry TagName</dt>
            <dd>görsellerin görüntülenmesi için kullanılan etiket (bir önceki adımda 'Görseller etiketle alınacak'
                seçeneğini işaretlediyseniz geçerli olur);
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry ClientID</dt>
            <dd>instagram.com adresindeki uygulama kimliğiniz (bir önceki adımda 'Görseller etiketle alınacak'
                seçeneğini işaretlediyseniz geçerli olur);
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry UserId</dt>
            <dd>kullanıcı kimliği (bir önceki adımda 'Görseller kullanıcıya göre alınacak' seçeneğini işaretlediyseniz
                geçerli olur);
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry accessToken</dt>
            <dd>belli bir görselin görüntülenmesi için kullanılan sembol (bir önceki adımda 'Görseller kullanıcıya göre
                alınacak' seçeneğini işaretlediyseniz geçerli olur);
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Entry limit</dt>
            <dd>gösterilecek olan ögelerin limiti;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>modülün durumu (aktif/pasif).</dd>
        </dl>
    </li>
</ol>
