<h5>Connecting products with the collection image, adding product selection area:</h5>
<p>To add a product to a collection, select it in the Products field:</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_product.jpg" alt=""/>
</figure>

<p>To mark the selected products on the image, press the "Add Image Map" button:</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_image_map.jpg" alt=""/>
</figure>


<p>After pressing the "Add Image Map" button, at the bottom, you'll see a window, which allows creating selection areas and adding products to such areas.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_image_map_object.jpg" alt=""/>
</figure>

<p>The areas that you create can be of 3 different forms: square, round, and complex (polygonal).</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures.jpg" alt=""/>
</figure>

<p>To create a selection area on the picture, press the button, the name of which corresponds to the necessary form of selection area.</p>


<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_add.jpg" alt=""/>
</figure>

<p>After choosing the form, place it on the picture. Left-click the necessary part of the image, draw the area.</p>

<p class="alert alert-warning">Note: Polygonal selection should always be closed, make the first and the last point coincide.</p>

<p>After placing the selection area on the image, choose the product that is displayed in the selected area. To do this, press the "Edit" button.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_edit.jpg" alt=""/>
</figure>

<p>Then, double-click the area and, in the drop-down window, select the needed product, press the "Save" button.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_save.jpg" alt=""/>
</figure>

<p>After marking all the areas and mapping all the products, press the Save button.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_save.jpg" alt=""/>
</figure>

<p>Save module settings.</p>