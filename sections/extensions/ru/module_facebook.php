<h3>TemplateMonster Facebook Box</h3>

<p align="justify"><strong>TemplateMonster Facebook Box</strong> — это модуль, который позволит вывести страницу профиля Facebook на любую страницу вашего веб-сайта:</p>

<figure class="img-polaroid">
    <img src="img/tm-facebook-view.jpg" alt=""/>
</figure>

<p>После установки модуля Вы можете настроить его на странице настроек модуля.</p>
<h4>Как управлять модулем TemplateMonster Facebook Box</h4>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong>Модули / Расширения</strong> (Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите раздел<strong> TemplateMonster Facebook Box</strong>. Нажав кнопку &quot;редактировать&quot; (edit), вы перейдете к окну настроек  существующего модуля. В данном случае это модуль <strong>Facebook Box</strong>.</p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-facebook-view-1.jpg" alt=""/></figure>
</p>
<p> Вы увидите следующие окно настроек:</p>
<figure class="img-polaroid"> <img src="img/tm-facebook-view-2.jpg" alt=""/></figure>
