<p align="justify">
Шаблон Storeflex  — многофункциональный шаблон, внешний вид и тематику которого вы можете настраивать по своему выбору. Шаблон состоит из ряда скинов. 
Чтобы установить новый скин, разархивируйте выбранный архив и  скопируйте все содержимое папки  <strong>themeXXX-full_update</strong> в папку с фулпаком. </p>
<p align="justify" class="alert alert-danger">Установить новый  скин вы можете только поверх главной темы.</p>
<p align="justify">Для перехода на страницу установки, в адресной строке, после адреса вашего магазина, пропишите  &quot;/install&quot;(напр., http://your_domain_name/install).</p>

 <p> Вы перейдете на страницу установки. </p>
