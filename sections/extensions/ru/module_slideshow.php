<h3>TemplateMonster Slideshow</h3>

<p>Данный модуль позволяет отобразить Camera слайдер на Вашем сайте. </p>

<p>После установки модуля Вы можете настроить его на странице настроек модуля.</p>

<h5>Настройки:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>название модуля;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Banner</dt>
            <dd>выберите из списка созданных ранее
                <a href='#banner_create'>баннеров</a>
                ;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>ширина картинки для модуля;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>высота картинки для модуля;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>статус модуля (включен/отключен).</dd>
        </dl>
    </li>
</ol>

