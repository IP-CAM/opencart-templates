<h3>TemplateMonster Single Category Products</h3>

<p align="justify">Данный модуль выводит 4 основных модуля (Bestsellers, Featured, Specials, Latest) продуктов определенной категории в
    виде вкладок.</p>

<figure class="img-polaroid">
    <img src="img/tm-single-category-product-view.jpg" alt="">
</figure>

<p>После установки модуля вы можете настроить его на странице настроек модуля.</p>
<h4>Как управлять модулем  TemplateMonsterSingle Category Products</h4>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong> Модули / Расширения </strong>(Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите раздел<strong> TemplateMonster Single Category Products</strong>. Нажав кнопку &quot;редактировать&quot; (edit), вы перейдете к окну настроек существующего модуля.</p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-single-category-product-view-3.jpg" alt=""/></figure>
</p>
<p>Вы перейдете к следующему окну настроек:</p>
<figure class="img-polaroid"> <img src="img/tm-single-category-products-edit-1.jpg" alt=""/></figure>
<h5>Доступные параметры настроек модуля:</h5>
<ol class="index-list">
<li>
        
            <strong>Module Name</strong>
             —  название модуля;
        
    </li>
    <li>
        
            <strong>Category</strong>
             —  выбор существующей категории продуктов;
        
    </li>
    <li>
        
            <strong>Tabs</strong>
             —  включает/отключает вывод в виде вкладок. При отключенном варианте предлагается выбрать тип выводимого
                модуля;
            
        
    </li>
    <li>
        
            <strong>Product Type</strong>
             —  выбор типа выводимого модуля. Поле доступно при отключенной опции Tabs.
        

        Доступны следующие значения:

        <ul class="marked-list">
            <li>Specials;</li>
            <li>Bestsellers;</li>
            <li>Featured;</li>
            <li>Latest.</li>
        </ul>
    </li>
    <li>
        
            <strong>Specials</strong>
             —  включает/отключает отображение модуля Specials;
        
    </li>
    <li>
        
            <strong>Bestsellers</strong>
             —  включает/отключает отображение модуля Bestsellers;
        
    </li>
    <li>
        
            <strong>Featured</strong>
             —  включает/отключает отображение модуля Featured;
        
    </li>
    <li>
        
            <strong>Latest</strong>
             —  включает/отключает отображение модуля Latest;
        
    </li>
    <li>
        
            <strong>Featured Products</strong>
             —  выбор списка продуктов для модуля Featured. Поле доступно при включенной опции Featured или, если опция Product type равна Featured;
            
        
    </li>
    <li>
        
            <strong>Limit</strong>
             —  устанавливает ограничение на количество отображаемых продуктов;
        
    </li>
    <li>
        
            <strong>Width</strong>
             —  ширина изображения продукта;
        
    </li>
    <li>
        
            <strong>Height</strong>
             —  высота изображения продукта;
        
    </li>
    <li>
        
            <strong>Status</strong>
             —  статус модуля (включен/отключен).
        
    </li>
</ol>

