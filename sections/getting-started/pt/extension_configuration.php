<h3>Instalação sobre uma loja existente</h3>

<p>Caso você já tenha uma loja OpenCart pronta, você só deseja alterar a aparência dela com a ajuda do tema, siga este
    método de instalação.</p>
<p class="alert alert-warning">Certifique-se de que sua versão do OpenCart corresponde ao necessário para o template.
    Todas as versões do OpenCart estão disponíveis no
    <a href=" http://www.OpenCart.com/index.php?route=download/download" target="_blank" class="spec_link">site official
        do OpenCart</a>
    .
</p><br/>

<ol class="index-list">
    <li>Para instalar um template de OpenCart, você precisa enviar arquivos para o servidor. Você pode encontrar o
        arquivo
        <strong> &quot;themeXXX(update).zip&quot;</strong> na pasta do OpenCart, ele deve ser descompactado no seu
        computador.
    </li>

    <li>Depois, envie os arquivos decompactador para o diretório raíz da Instalação do OpenCart.
        <br/> Você pode dar uma olhada no vídeo com as instruções detalhadas em
        <a href="/help/how-upload-files-server-2.html" target="_blank">como enviar arquivos para o servidor</a>
        . <br/>
        <strong>os arquivos de template não vão mudar o conteúdo de sua loja de nenhuma maneira;</strong>
    </li>
    <li>Abra o seu site com qualquer navegador e inlcua /install no fim da URL do site na barra de endereço (
        <i>por exemplo, http://seu_dominio/OpenCart/install</i>
        )
    </li>

    <li>Clique no botão <strong>Continue</strong>. O processo pode demorar um pouco. </li>

    <li>Entre no painel administrativo e navegue para <strong>Extensions ->
            Extension
            Installer </strong> e envie os arquivos de
        <strong>themeXXX(extensions)</strong> . Quando todos os arquivos forem enviados, navegue para <strong>Extensions
            ->
            Modifications</strong> e clique no botão
        <strong>refresh</strong> no topo direito da tela; </li>

    <li>No proximo passo navegue para <strong>System->Settings</strong>, vá para a aba Store e escolha o template
        <strong>themeXXX</strong> no campo Template. </li>
</ol>
<p class="alert alert-info">Você também pode ler o guia detalhado de instalação:
    <a target="_blank"
