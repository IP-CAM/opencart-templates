<h3 align="justify">Установка шаблона на существующий магазин</h3>

<p align="justify">Следуйте данному типу установки, если у вас уже есть действующий сайт OpenCart и
    требуется всего лишь изменить его внешний вид, путем установки новой темы.</p>

<p align="justify" class="alert alert-warning">Убедитесь, что ваша версия движка OpenCart соответствует требованиям шаблона. Все версии
    OpenCart доступны на <a
        href=" http://www.opencart.com/index.php?route=download/download"
        target="_blank" class="spec_link"> официальном сайте OpenCart
    </a>.</p>
<br/>
<ol class="index-list">
    <li>
      <div align="justify">Чтобы установить шаблон OpenCart, нужно загрузить его файлы на хостинг сервер. В
        папке OpenCart шаблона найдите архив <strong>
            &quot;themeXXX(update).zip&quot;</strong> разархивируйте его на свой
        компьютер.
      </div>
    </li>
    <li>
      <div align="justify">После этого загрузите разархивированные файлы в корневую директорию
      установленного OpenCart. </p></p>
        Вы также можете воспользоваться детальной видео инструкцией,
        <a href="/help/ru/how-upload-files-server-2.html" target="_blank"> как загружать
            файлы на
            хостинг
            сервер
        </a>. </div>
    </li>
            <p align="justify" class="alert alert-danger">
        <strong>Файлы шаблона никаким образом не повредят магазину.</strong></p>
     
    <li>
      <div align="justify">Затем откройте сайт в браузере и добавьте /install в конце адресной строки
        браузера (<i>например, http://your_domain_name/opencart/install</i>
        )
      </div>
    </li>
    <li>
      <div align="justify">В появившейся на экране странице нажмите кнопку <strong>Continue
            (Продолжить)</strong>.
        Обновление
        темы может занять некоторое время.
      </div>
    </li>
    <li>
      <div align="justify">Войдите в панель управления вашим сайтом, перейдите в меню <strong>Расширения >
            Установщик
            расширений
            (Extensions            > Extension
            Installer)</strong>
        и
        загрузите файлы из архива <strong>themeXXX(extensions)</strong>. После загрузки
        всех файлов архива, перейдите в меню <strong>Расширения > Модификации
            (Extensions >
            Modifications)</strong> и нажмите
        копку <strong>обновить(refresh)</strong> в правом верхнем углу экрана;
      </div>
    </li>
    <li>
      <div align="justify">Далее перейдите в настройки магазина <strong>Система > Настройки (System >
            Settings)</strong>, и во вкладке
        Магазин (Store) в поле Шаблон (Template) выберите тему -
            <strong>themeXXX</strong>.
      </div>
    </li>
</ol>
