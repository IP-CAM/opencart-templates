                <a href='#banner_create'>bannières</a>
                créées précédemment;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>la largeur de l'image de module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>la hauteur de l'image de module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>le statut du module (activé/désactivé).</dd>
        </dl>
    </li>
</ol>

