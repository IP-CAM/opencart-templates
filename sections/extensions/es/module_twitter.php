<h3>TemplateMonster Twitter Box</h3>

<p>El módulo puede mostrar las posiciones de Twitter. Permite a los visitantes del sitio web para estar al
    tanto de las últimas actualizaciones y mensajes en la red social Twitter.</p>

<figure class="img-polaroid">
    <img src="img/tm-twitter-view.jpg" alt=""/>
</figure>

<p>Una vez instalado el módulo, se puede configurar en la página de configuración del módulo.</p>

<h5>Parámetros:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>el nombre del módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Twitter Page URL</dt>
            <dd>el enlace que lleva a una cuenta de Twitter;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Widget ID</dt>
            <dd>se puede obtener el ID del Widget
                <a href="https://twitter.com/settings/widgets" target="_blank">aquí;</a>
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Tweet Limit</dt>
            <dd>número de tweets;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Color Scheme</dt>
            <dd>la paleta de colores (claro, oscuro);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>el ancho del bloque (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>el largo del bloque (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>estado del módulo (activado/desactivado).</dd>
        </dl>
    </li>
</ol>

