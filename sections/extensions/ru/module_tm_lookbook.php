<h3>TM Lookbook</h3>

<p>Лукбук  представляет собой серию фотографий, на которых можно отметить маркером любую часть изображения. Этот модуль предназначен для демонстрации товаров одного или нескольких брендов.</p>
<p>Модуль имеет два варианта отображения при отображении в одной из позиций магазина: в виде Баннера (1) и в виде Коллекции (2).</p>


