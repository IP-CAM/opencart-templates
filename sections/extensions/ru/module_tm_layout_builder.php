<h3>TM Layout Builder</h3>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-edit.jpg" alt=""/>
</figure>
<p>Модуль предназначен для быстрого, простого и удобного создания различных вариантов главной страницы магазина.</p>

<h5>Базовые настройки модуля:</h5>
<ol class="index-list">
	<li>
		<dl class="inline-term">
			<dt>Status</dt>
			<dd>cтатус модуля (вкл./выкл.);</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Module Name</dt>
			<dd>название модуля;</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Module Class</dt>
			<dd>класс модуля;</dd>
		</dl>
	</li>
	<li>
		<dl class="inline-term">
			<dt>Module ID</dt>
			<dd>ID модуля.</dd>
		</dl>
	</li>
</ol>
<p>Чтобы создать новый макет, нажмите на кнопку <strong>Новый вид макета</strong> (New Layout View), и затем на кнопку <strong>Добавить ряд</strong> (Add Row
		(1)). После добавления ряда добавьте колонки. Чтобы добавить колонки, нажмите на кнопку <strong>Добавить колонку</strong> (Add
		Column (2)).</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-column.jpg" alt=""/>
</figure>

<p>Количество колонок в ряду не ограничено, и Вы можете добавить столько колонок, сколько Вам необходимо, при этом
	ширину колонок возможно настроить сразу же после их добавления при помощи функции "Drag and drop", просто потянув
	за правую границу колонки. </p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-drag-drop.jpg" alt=""/>
</figure>

<p>После добавления колонок, их необходимо заполнить содержимым (модулями). Для того чтобы добавить модуль, нажмите на
	кнопку <strong>Добавить виджет</strong> (Add Widget), и выберите нужный модуль из выпадающего окна со списком модулей.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-widget.jpg" alt=""/>
</figure>

<p>После добавления модуля его можно изменить или удалить,</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change.jpg" alt=""/>
</figure>

<p>а также переместить в другую колонку.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change-position.jpg" alt=""/>
</figure>

<p>По завершению настройки всех рядов, колонок и модулей, необходимо сохранить модуль TM Layout Builder и отобразить его в одной из позиций главной страницы магазина.</p>