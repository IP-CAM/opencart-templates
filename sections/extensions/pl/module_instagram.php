<h3>TemplateMonster Instagram</h3>

<p>Ten moduł wyświetla zdjęcia z sieci socjalnej Instagram, włącznie z liczbą "lajków" w
    formie listy </p>

<figure class="img-polaroid">
    <img src="img/tm-instagram-view.jpg" alt=""/>
</figure>

<p>Kiedy moduł został zainstalowany, możesz konfigurować go na stonie konfiguracji
    modułu </p>

<h5>Ustawienia:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd> nazwa modułu;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Select Get</dt>
            <dd> jak dostać się do obrazków. Istnieją 2 dostępne możliwości: uzyskać obrazki określonego użytkownika, jeśli ustawić "user", albo dostać je za pomocą hashtag, jeśli ustawić "tagged";</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry TagName</dt>
            <dd> tag, używany do wyświetlania obrazków (używa się go, jeśli wcześniej wybrano "tagged" w polu "Select get");</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry ClientID</dt>
            <dd> twój identyfikator API w instagram.com (używa się go, jeśli wcześniej wybrano "tagged" w polu "Select get");</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry UserId</dt>
            <dd> identyfikator użytkownika (używa się go, jeśli wcześniej wybrano "user" w polu "Select get");</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry accessToken</dt>
            <dd> Token do wyświetlania własnego obrazka (używa się go, jeśli wcześniej wybrano "user" w polu "Select get");</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Entry limit</dt>
            <dd> limit pozycji;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd> status modułu (włączony/wyłączony).</dd>
        </dl>
    </li>
</ol>

