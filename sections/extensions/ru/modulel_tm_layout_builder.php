<h3>TM Layout Builder</h3>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-edit.jpg" alt=""/>
</figure>
<p>Модуль предназначен для быстрого, простого и удобного создания различных вариантов главной страницы магазина.</p>

<h5>Базовые настройки модуля:</h5>
<ol class="index-list">
	<li>
		<dl class="inline-term">
			<dt>Status</dt>
			<dd>cтатус модуля (вкл./выкл.);</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Module Name</dt>
			<dd>yазвание модуля;</dd>
		</dl>
	</li>

	<li>
		<dl class="inline-term">
			<dt>Module Class</dt>
			<dd>класс модуля;</dd>
		</dl>
	</li>
	<li>
		<dl class="inline-term">
			<dt>Module ID</dt>
			<dd> ID модуля.</dd>
		</dl>
	</li>
</ol>
<p>Чтобы создать новый лейаут, нажмите на кнопку <strong>New Layout View</strong>, и затем на кнопку <strong>Add Row
		(1)</strong>. После добавления столбца добавьте колонки.Чтобы добавить колонки, нажмите на кнопку <strong>Add
		Column (2)</strong>.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-column.jpg" alt=""/>
</figure>

<p>Количество колонок в ряду не ограничивается, и Вы можете добавить столько колонок, сколько Вам необходимо, при этом,
	ширину колонок возможно настроить сразу же после их добавления при помощи Drag and drop функционала, просто потянув
	за правую границу колонки. </p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-drag-drop.jpg" alt=""/>
</figure>

<p>После добавления колонок, их необходимо заполнить контентом (модулями). Для того чтобы добавить модуль, нажмите на
	кнопку <strong>Add Widget</strong>, и выберите его из выпадающего окна со списком модулей.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-add-widget.jpg" alt=""/>
</figure>

<p>После добавления модуля его можно изменить или удалить,</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change.jpg" alt=""/>
</figure>

<p>а также переместить в другую колонку.</p>

<figure class="img-polaroid">
	<img src="img/tm-layout-bilder-change-position.jpg" alt=""/>
</figure>

<p>По завершению настройки всех столбцов, колонок и модулей необходимо сохранить модуль билдера и вывести его в одной из позиций главной страницы магазина.</p>