<h3>TemplateMonster Newsletter</h3>

<p>This module allows you to add newsletter block with the subscription form.</p>
<figure class="img-polaroid">
  <img src="img/tm-newsletter-view.jpg" alt=""/>
</figure>

<p>When the module is installed, you can configure it on the module configuration page.</p>
<h4>How to manage TemplateMonster Newsletter module</h4>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
 <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
 <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p>Find the<strong> TemplateMonster Newsletter</strong> section. Press the <strong>edit</strong> button to configure the existing module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
 <figure class="img-polaroid"> <img src="img/news-15.jpg" alt=""/></figure>
</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid"><img src="img/news-18.jpg" alt=""/></figure>
<h5>Available settings:</h5>
<ol class="marked-list">
 <li><strong>Module Name</strong> — the name of the module;</li>
 <li>
  <div align="justify"><strong>Description </strong>— text that will be seen just before filling in the subscription form; </div>
 </li>
 <li><strong>Status </strong>— module status (enabled/disabled). </li>
</ol>
<h4>How to add a new TemplateMonster Newsletter module</h4>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
 <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
 <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">Find the<strong> TemplateMonster Newsletter</strong><strong> </strong>section. Press the<strong> Add New </strong>button to create a new module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
 <figure class="img-polaroid"> <img src="img/news-16.jpg" alt=""/></figure>
</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid"> <img src="img/news-18.jpg" alt=""/></figure>
<p>Fill in the required fields:</p>
<ol class="marked-list">
 <li><strong>Module Name</strong> — the name of the module;</li>
 <li>
  <div align="justify"><strong>Description </strong>— text that will be seen just before filling in the subscription form; </div>
 </li>
 <li><strong>Status </strong>— module status (enabled/disabled).</li>
</ol>
<h4> How to set up newsletter</h4>
<ol>
  <li>
   <p>To create a newsletter for your customers, log into the admin panel and navigate to <strong> Marketing &gt; Mail</strong>.</p>
 </li>
   <figure class="img-polaroid"> <img src="img/news-25.jpg" alt=""/></figure>
 <li>
<p><strong>Fill in the fields</strong> with the necessary data and click <strong>Send</strong>.</p>
 <figure class="img-polaroid"> <img src="img/news-26.jpg" alt=""/></figure>
   <ul class="list">
    <li class="first">
     <p><strong>From</strong> – by default, your store email is the sender.</p>
    </li>
    <li>
     <p><strong>To</strong> – by default, <strong>All Newsletter Subscribers</strong> are selected, you can change the group to send the email from the dropdown.</p>
    </li>
    <li>
     <p><strong>Subject</strong> – this is a required field where you need to type the title for your newsletter.</p>
    </li>
    <li>
     <p><strong>Message</em></strong> – this is also a required field, type your message text here.</p>
    </li>
  </ul>
 </li>
  <li>Click <strong>Send</strong> button.
  <figure class="img-polaroid"> <img src="img/news-27.jpg" alt=""/></figure>
</ol>