<h5>Łączenie produktów z obrazkiem kolekcji, dodawanie obszaru wyboru produktu:</h5>
<p>Aby dodać produkt do kolekcji, wybierz go w polu Produkty:</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_product.jpg" alt=""/>
</figure>

<p>Aby zaznaczyć wybrane produkty na obrazku, należy nacisnąć przycisk "Dodaj mapę obrazku":</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_image_map.jpg" alt=""/>
</figure>


<p>Po naciśnięciu przycisku "Dodaj mapę obrazku", na dole pojawi się okienko, które pozwala na tworzenie obszarów wyboru oraz dodawanie produktów do takich obszarów.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_add_image_map_object.jpg" alt=""/>
</figure>

<p>Pod czas tworzenia obszarów, można wybrać jedną z 3 różnych postaci: kwadratowe, okrągłe i złożone (wielokątne).</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures.jpg" alt=""/>
</figure>

<p>Aby utworzyć obszar wyboru na obrazku, należy nacisnąć przycisk, którego nazwa odpowiada niezbędnej postaci obszarze wyboru.</p>


<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_add.jpg" alt=""/>
</figure>

<p>Po wyborze postaci, należy umieścić ją na obrazku. Należy kliknąć lewym przyciskiem niezbędną część obrazu, aby narysować obszar.</p>

<p class="alert alert-warning">Uwaga: Wielokątna postać wyboru powinna być zawsze zamknięta, aby pierwszy i ostatni punkt zbiegali się.</p>

<p>Po umieszczeniu obszaru wyboru na obrazku, należy wybrać produkt, który zostanie wyświetlany w wybranym obszarze. Aby to zrobić, należy nacisnąć przycisk "Edytuj".</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_edit.jpg" alt=""/>
</figure>

<p>Następnie należy dwukrotnie kliknąć obszar, w okienku rozwijanej listy wybrać potrzebny produkt, a zatem nacisnąć przycisk "Zapisz".</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_figures_save.jpg" alt=""/>
</figure>

<p>Po zaznaczeniu wszystkich obszarów i mapowaniu wszystkich produktów, należy nacisnąć przycisk Zapisz.</p>

<figure class="img-polaroid">
    <img src="img/edit-collection_product_save.jpg" alt=""/>
</figure>

<p>Należy zapisać ustawienia modułu.</p>