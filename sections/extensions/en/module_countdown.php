<h3>TM CountDown</h3>

<p>This module allows you to create countdown counters. There are two types available, numeric and radial timers.
</p>
<p>Numeric:</p>

<figure class="img-polaroid">
    <img src="img/tm_countdown.png" alt=""/>
</figure>

<p>Radial:</p>

<figure class="img-polaroid">
    <img src="img/tm_countdown_circle.png" alt=""/>
</figure>

<p>In order to change counter type to radial you need to select "Radial" option in the "Type" field. In this case additional settings for the radial timer become available:</p>
<figure class="img-polaroid">
    <img src="img/tm_countdown_type.png" alt=""/>
</figure>

<h5>Settings:</h5>
<p>Module primary settings are the following:</p>

