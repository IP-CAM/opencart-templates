<h3>TemplateMonster Newsletter</h3>

<p>Ce module permet d'ajouter le bloc avec un formulaire de souscription aux newsletters.</p>
<figure class="img-polaroid">
    <img src="img/tm-newsletter-view.jpg" alt=""/>
</figure>

<p>Une fois le module installé, vous pouvez le configurer sur la page de configuration de module.</p>

<h5>Paramètres:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>le nom du module;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>le texte que vous voyez juste avant de remplir le formulaire de souscription;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>le statut du module (activé/désactivé).</dd>
        </dl>
    </li>
</ol>

