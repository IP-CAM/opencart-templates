<h3>TemplateMonster Google Map</h3>

<p>Данный модуль позволяет добавлять Google карту с неограниченным количеством маркеров на ваш сайт. </p>

<p align="justify">Центр карты и один (главный) из маркеров определяются настройками магазина — <strong>Система >
        Настройки
        (System >
        Settings)</strong>, во вкладке <strong>Магазин (Store)</strong>, поле
    <strong>Геокод (Geocode)</strong>. </p>
<figure class="img-polaroid">
  <p><img src="img/m-googlemap-view-3.jpg" alt=""/>
    </p></figure>
    <p>Нажмите кнопку &quot;редактировать&quot; (edit), чтобы перейти в окно настроек.</p>
    <figure class="img-polaroid">
    <img src="img/tm-googlemap-view-4.jpg" alt=""/>
</figure>

<p align="justify">При нажатии на маркер, появляется окно с адресом указанным в той же вкладке в
  поле <strong>Адрес (Address)</strong>.</p>

<figure class="img-polaroid">
    <img src="img/tm-googlemap-view.jpg" alt=""/>
</figure>

<p>После установки модуля Вы можете настроить его на странице настроек модуля.</p>
<h4 align="justify">Как управлять модулем TemplateMonster Google Map </h4>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong> Модули / Расширения </strong>(Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите раздел <strong>TemplateMonster Google Map</strong>. Нажав кнопку &quot;редактировать&quot; (edit), вы перейдете к окну настроек существующего модуля. </p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/tm-googlemap-view-5.jpg" alt=""/></figure>
</p>
<p>Вы перейдете к следующему окну настроек:</p>
<figure class="img-polaroid">
  <img src="img/tm-googlemap-edit_1.jpg" alt=""/>
</figure>
<h5>Доступные параметры настроек модуля::</h5>
<h5>Настройки:</h5>
 <ul class="marked-list">
<li> <strong>Module Name</strong> — название модуля;</li>
  <li><strong>Google Map API key</strong> — API ключ для Google Map. Подробная информация доступна на                  <a target="_blank" href="https://developers.google.com/maps/documentation/javascript/tutorial#api_key"> официальном сайте</a>; </li>
  <li><strong><strong>Status</strong> — </strong>статус модуля (включен/отключен); </li>
  <li>
    <div align="justify"><strong>Map Zoom</strong> — числовое значение. Первоначальное масштабирование, используемое при отображении карты. Значение 0 соответствует наименьшему уровню масштабирования. При увеличении этого значения увеличивается масштабирование карты; </div>
  </li>
  <li><strong>Map Type</strong> — тип карты. Доступны 4 варианта:   
    <ul class="marked-list">
  <li> <strong>Roadmap</strong> — дорожная карта, используется по умолчанию;
    
  
          <li><strong>
           
            Terrain</strong> (Landscape)
          — топографическая карта, основанная на ландшафтных данных; </li>
          <li>
           
          <strong>Hybrid</strong> — комбинация обычной карты и снимков со спутника; </li>
          <li>
            
          <strong>Satellite</strong> —  снимки Google Планета Земля, сделанные со спутника; </li></ul>
  
  <li><strong>Sensor</strong> — указывает, используется или нет в приложении датчик определения местоположения пользователя;</li>
  <li><strong>Map Width</strong> — ширина карты. Необходимо указать единицы измерения (% или px);
    
  </li>
  <li><strong>Map Height</strong><strong> </strong>— высота карты. Необходимо указать единицы измерения (% или px); </li>
  <li><strong>Map Styles</strong> — задает стиль карты. Разнообразные стили доступны на                  <a href="https://snazzymaps.com/" rel="nofollow" target="_blank"> https://snazzymaps.com/</a> ; </li>
   <div class="alert alert-info">
     <p>Просто скопируйте код и внесите его в соответствующие поле:</p>
             <p>
               <figure class="img-polaroid">
                 <img src="img/map.jpg" alt="">
               </figure>
             </p>
          </div>
  <li><strong>Disable UI</strong> — включает/отключает пользовательский интерфейс по умолчанию;</li>
<li><strong>Scrollwheel </strong>—включает/отключает масштабирование карты при помощи колеса мыши;</li>
  <li><strong>Draggable</strong><strong> </strong>— включает/отключает возможность перемещения карты при захвате ее курсором мыши;</li>
  <li>
    <div align="justify"><strong>Marker Image</strong> — устанавливает изображение маркера. Если оставить поле пустым, будет отображен стандартный маркер;</div>
  </li>
<li><strong>Active Marker Image</strong> — устанавливает изображение маркера при нажатии (активный маркер); </li>
  <li><strong>Marker Image Width</strong><strong> </strong>— устанавливает ширину изображения маркера;</li>
<li><strong>Marker Image Height</strong> — устанавливает высоту изображения маркера;</li></ul>
<p align="justify">Для добавления маркера нажмите <strong>Add New</strong>. В поле
    <strong>Geocode</strong> укажите координаты маркера (широта, долгота — пример: 40.6700, -73.9400) и в поле
    <strong>Address</strong> укажите информацию, которая будет отображена при клике на маркер. Координаты необходимого
    места можно найти на
    <a href='http://www.mapcoordinates.net' rel="nofollow" target="_blank">
        http://www.mapcoordinates.net</a>. Для удаления маркера, соответственно, нажмите <strong>Delete Marker</strong> над необходимым маркером.
