<h4>TM Blog Articles</h4>

<p>Ce module permet d'afficher les articles de blog par catégories.</p>

<figure class="img-polaroid">
    <img src="img/tm-blog-articles-view.jpg" alt="">
</figure>

<p>Une fois le module installé, vous pouvez le configurer sur la page des paramètres du module.</p>

<h5>Réglages:</h5>

<ol class="index-list">
