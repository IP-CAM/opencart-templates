<h3>TemplateMonster Module Tabs</h3>

<p>Ten moduł pozwala na wyświetlenie 4 głównych modułów (Bestsellers (Bestsellery),
    Featured (Wyróżnione), Specials (Promocje), Latest (Najnowsze)) w zakładkach </p>

<figure class="img-polaroid">
    <img src="img/tm-moduletabs-view.jpg" alt=""/>
</figure>

<p>Kiedy moduł został zainstalowany, możesz konfigurować go na stonie konfiguracji
    modułu </p>

<h5>Ustawienia:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd> nazwa modułu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Specials</dt>
            <dd> włącza/wyłącza Promocje modułu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Bestsellers</dt>
            <dd> włącza/wyłącza Bestsellery modułu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Latest</dt>
            <dd>włącza/wyłącza Najnowsze modułu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Featured</dt>
            <dd> włącza/wyłącza Wyróżnione modułu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Products</dt>
            <dd> Wybór listy produktów do zakładki Wyróżnione;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Limit</dt>
            <dd> ustala limit liczby wyświetlanych produktów;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd> szerokość obrazka produktu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>wysokość obrazka produktu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd> status modułu (włączony/wyłączony).</dd>
        </dl>
    </li>
</ol>

