<h3>Parallax</h3>

<p>Parallax-scroll - El desplazamiento parallax es una técnica especial que se utiliza en los gráficos del ordenador
    cuando se desplaza la imagen de fondo se mueve más lento que el contenido del sitio web en sí, creando una ilusión
    de profundidad y el efecto de inmersión como video juego en 2D. Este módulo le permite añadir efectos de parallax a
    su sitio web sin ningún esfuerzo o habilidad adicional. </p>

<p>Una vez instalado el módulo, puede configurar la página de parámetros del módulo</p>

<p class="alert alert-info">La anchura y la altura de la imagen deben coincidir con el original (por defecto), de lo
    contrario el efecto puede no satisfacer sus expectativas.</p>

