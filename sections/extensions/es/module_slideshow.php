<h3>TemplateMonster Slideshow</h3>

<p>Este módulo le permite mostrar Camera slideshow en su página web. </p>

<p>Después de instalar el módulo, se puede configurar en la página de configuración del módulo.</p>

<h5>Parámetros:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>el nombre del módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Banner</dt>
            <dd>elegir de la lista de
                <a href='#banner_create'>banneres</a>
                previamente creada;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>la anchura de la imagen de módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>la altura de la imagen de módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>estado del módulo (activado/desactivado).</dd>
        </dl>
    </li>
</ol>

