<h3>TM ABC Customers</h3>

<p>Este módulo sirve para la creación de descuentos para diferentes grupos de clientes. La cantidad de la orden se calcula, cuando se realiza el pedido, con el porcentaje de descuento establecido para un grupo específico de clientes.</p>

<h4>Configuraciones de grupos de clientes:</h4>

