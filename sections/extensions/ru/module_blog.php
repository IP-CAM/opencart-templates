<h3>Blog</h3>

<p align="justify">С помощью данного расширения, вы можете быстро и удобно управлять новостями и публикациями, связанными с вашим
    магазином.</p>

<p>Основные возможности:</p>

<ul class="marked-list">
    <li>создание статей;</li>
    <li>создание категорий;</li>
    <li>создание авторов;</li>
    <li>комментирование статей и ответа на комментарий;</li>
    <li>наличие отчета по просмотрам;</li>
    <li>вывод продуктов и других статей, которые относятся к данной статье;</li>
</ul>

<p align="justify">Для изменения глобальных настроек блога перейдите в раздел <strong>Система > Настройки (System > Settings)</strong>. Выберите из
    списка магазин, в котором нужно изменить настройки и напротив него нажмите <strong>Редактировать</strong> <strong>(Edit)</strong>. </p>
<h6> Настройки блога находятся во вкладке Блог (Blog):</h6>
<ul class="marked-list">
  <li><strong>Status</strong> — статус блога (включен\отключен);</li>
    <li><strong>Limit</strong> — количество постов, отображаемых на странице;</li>
    <li><strong>Description Symbol Limit </strong>— количество символов в описании статьи на странице блога (не статьи!);</li>
    <li><strong>Comment Auto approval</strong> — включает/отключает автоматическое подтверждение комментариев администратором;</li>
    <li><strong>Author Information</strong> — включает/отключает показ информации об авторе на стратице основного текста статьи;</li>
    <li><strong>Related Article</strong> — включает/отключает показ  статей,  относящихся к данной (смежный статей);</li>
    <li><strong>Share with social site</strong> — включает/отключает показ социальных иконок “поделиться”.</li>
</ul>


