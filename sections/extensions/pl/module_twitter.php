<h3>TemplateMonster Twitter Box</h3>

p>Ten moduł pozwala wyświetlać posty z Twittera (Img.16). On pomaga użytkownikom twojej witryny znać najnowsze wiadomości z sieci socjalnej Twitter.</p>

<figure class="img-polaroid">
    <img src="img/tm-twitter-view.jpg" alt=""/>
</figure>

<p>Kiedy moduł został zainstalowany, możesz konfigurować go na stonie konfiguracji modułu. </p>

<h5>Ustawienia:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>nazwa modułu;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Twitter Page URL</dt>
            <dd>link do konta Twittera;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Widget ID</dt>
            <dd>możesz dostać identyfikator widżetu <a href="https://twitter.com/settings/widgets" target="_blank">tutaj</a>;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Tweet Limit</dt>
            <dd>liczba wpisów;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Color Scheme</dt>
            <dd>kolorystyka (jasna, ciemna);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>szerokość bloku (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>wysokość bloku (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>status modułu (włączony/wyłączony).</dd>
        </dl>
    </li>
</ol>

