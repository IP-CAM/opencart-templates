<h3>TM Video Background</h3>

<p>Este módulo añade un bloque con el vídeo de fondo (se puede subir un video desde su sitio web o de YouTube) .</p>

<p>Después de instalar el módulo, se puede configurar en la página de configuración del módulo.</p>

<h5>Parámetros:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>el nombre del módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>el estado del módulo (activado/desactivado)</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Video Source</dt>
            <dd>la fuente de vídeo. Usted tiene dos opciones:</dd>
        </dl>
        <ul class="marked-list">
            <li>
                <dl class="inline-term">
                    <dt>local</dt>
                    <dd>la carga de su sitio web;</dd>
                </dl>
            </li>
            <li>
                <dl class="inline-term">
                    <dt>youtube </dt>
                    <dd>la carga de YouTube.</dd>
                </dl>
            </li>
        </ul>
    </li>
    <li>
        <dl class="inline-term">
            <dt>YouTube url</dt>
            <dd>el enlace de vídeo de YouTube;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mute</dt>
            <dd>desactivar el volumen;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile</dt>
            <dd>activar/desactivar el video en un dispositivo móvil. Si pulsa 'Yes', el vídeo se mostrará en
                dispositivos móviles;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Start Video</dt>
            <dd>exactamente cuando el vídeo comienza;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile Image</dt>
            <dd>una imagen que aparece en lugar del vídeo de fondo si el vídeo está deshabilitado para dispositivos
                móviles;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile Image Width</dt>
            <dd>la anchura de la imagen de fondo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Mobile Image Height</dt>
            <dd>la altura de la imagen de fondo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>marcado-html que se mostrará antes que el vídeo.</dd>
        </dl>
    </li>
</ol>
<p>Si decide
    <strong>Video Source - Local Video</strong>, defina el formato ".jpg" para la imagen (banner) que se utilizará para
    los dispositivos móviles. La carpeta que contiene la imagen de fondo también debe contener tres archivos de vídeo
    con el mismo nombre y en los siguientes formatos:
</p>

<ul class="marked-list">
    <li>*.ogv</li>

    <li>*.mp4</li>

    <li>*.webm</li>
</ul>


