<h3>TemplateMonster Twitter Box</h3>

<p>Questo modulo consente di inserire post da Twitter. In questo modo i tuoi visitatori saranno tenuti al
    corrente di tutti gli ultimi aggiornamenti attraverso la rete Twitter.</p>
<figure class="img-polaroid">
    <img src="img/tm-twitter-view.jpg" alt=""/>
</figure>

<p>Dopo aver installato il modulo, sarà possibile configurarlo nell'apposita pagina di configurazione.</p>

<h5>Impostazioni:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>il nome del modulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Twitter Page URL</dt>
            <dd>link dell'account Twitter;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Widget ID</dt>
            <dd>
                <a href="https://twitter.com/settings/widgets" target="_blank">qui</a>
                è possibile ottenere il Widget ID;
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Tweet Limit</dt>
            <dd>numero di tweet;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Color Scheme</dt>
            <dd>lo schema colore (luminoso, scuro);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>ampiezza blocco (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>altezza blocco (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>lo stato del modulo (abilitato/disabilitato).</dd>
        </dl>
    </li>
</ol>

