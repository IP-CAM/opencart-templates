<h4>TM Blog Articles</h4>

<p>Este módulo permite exibir artigos do blog por categorias.</p>

<figure class="img-polaroid">
    <img src="img/tm-blog-articles-view.jpg" alt="">
</figure>

<p>Após instalado você pode configurá-lo na página de configurações do módulo.</p>

<h5>Configurações:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd> situação do módulo (ativo/inativo).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd> o nome do módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Article limit</dt>
            <dd> quantidade de artigos para exibir;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Readmore Button Show</dt>
            <dd> ativa/desativa o botão de Leia Mais;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Date Show</dt>
            <dd> ativa/desativa data;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Author Show</dt>
            <dd> ativa/desativa autor do artigo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Comments Show</dt>
            <dd> ativa/desativa a quantidade de comentários por artigo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Image Show</dt>
            <dd> ativa/desativa a imagem do artigo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Image Width</dt>
            <dd> largura da imagem;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Image Height</dt>
            <dd> altura da imagem;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description Symbol Limit</dt>
            <dd> quantidade de símbolos em uma descrição do artigo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Category</dt>
            <dd>  você pode definir a categoria de onde retirar os artigos. Também pode exibir os artigos mais populares incluídos recentemente. 
            </dd>
        </dl>
    </li>
</ol>

