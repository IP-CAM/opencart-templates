<h3> TM Footer Links</h3>
<h4 align="justify">How to manage TM Footer Links module</h4>
<p>Navigate to <strong>Extensions</strong> &gt; <strong>Extensions</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Choose <strong>Modules</strong> in the <strong>Extension type</strong> dropdown.</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p>Find the <strong>TM Footer Link</strong> section. Press the <strong>edit</strong> button to configure the existing module.</p>
<p align="justify" class="alert alert-info">Use <strong>Ctrl+F</strong> to simplify the search process.</p>
<p>
  <figure class="img-polaroid"> <img src="img/footer-2.jpg" alt=""/></figure>
</p>
<p>You will be brought to the settings window:</p>
<figure class="img-polaroid">
  <img src="img/footer-3.jpg" alt=""/>
</figure>
<ul class="marked-list">
  <li><strong>Module Name</strong> — the name of the module;</li>
  <li><strong>Links Group</strong> — choose a group of links you want to display in the footer;</li>
  <li> <strong>Status</strong> — define the module status (enabled/disabled). </li>
</ul>
<h5>Frontend:</h5>
<p>
  <figure class="img-polaroid">
    <img src="img/footer-4.jpg" alt=""/>
  </figure>
</p>
<ul class="marked-list">
  <li><strong>Module Name</strong> — TM Footer Links Information;</li>
  <li><strong>Links Group</strong> — Information;</li>
  <li> <strong>Status</strong> — enabled. </li>
</ul>
