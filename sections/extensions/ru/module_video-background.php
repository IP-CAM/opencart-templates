<h3>TM Video Background</h3>

<p>Данный модуль выводит блок с фоновым видео, загруженного либо локально, либо с помощью сервиса YouTube.</p>

<p>После установки модуля, Вы можете настроить его на странице настроек модуля.</p>

<h5>Настройки:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>название модуля;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>статус модуля (включен/отключен).</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Video Source</dt>
            <dd>источник загрузки видео. Доступны 2 значения:</dd>
        </dl>

        <ul class="marked-list">
            <li>
                <dl class="inline-term">
                    <dt>local</dt>
                    <dd>видео загружается с Вашего сайта;</dd>
                </dl>
            </li>

            <li>
                <dl class="inline-term">
                    <dt>youtube</dt>
                    <dd>видео загружается с сервиса YouTube.</dd>
                </dl>
            </li>
        </ul>
    </li>
    <li>
        <dl class="inline-term">
            <dt>YouTube url</dt>
            <dd>ссылка на видео сервиса Youtube;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Mute</dt>
            <dd>отключение громкости;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Mobile</dt>
            <dd>включает/отключает отображение видео на мобильных устройствах. При значении 'Yes' - видео будет
                отображаться на мобильных устройствах;
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Start Video</dt>
            <dd>секунда, с которой необходимо воспроизвести видео;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Mobile Image</dt>
            <dd>картинка, которая будет отображена вместо фонового видео, в случае его отключения на мобильных
                устройствах;
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Mobile Image Width</dt>
            <dd>ширина фоновой картинки;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Mobile Image Height</dt>
            <dd>высота фоновой картинки;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>html-разметка, которая будет отображена поверх фонового видео.</dd>
        </dl>
    </li>
</ol>
<p>При выборе
    <strong>Video Source - Local Video</strong> укажите картинку (постер) в формате ".jpg", на который будет заменено
    фоновое видео на мобильных устройствах. Для правильной работы модуля в папке с картинкой, которую Вы указали, должны
    находиться 3 видео с одинаковыми названиями в следующих форматах:
</p>
<ul class="marked-list">
    <li>*.ogv</li>

    <li>*.mp4</li>

    <li>*.webm</li>
</ul>

