<h3>TemplateMonster Newsletter</h3>

<p>Este módulo permite incluir um bloco para assinatura de lista de emails.</p>
<figure class="img-polaroid">
    <img src="img/tm-newsletter-view.jpg" alt=""/>
</figure>

<p>Ao instalar o módulo, você pode alterar suas configurações na página de configurações do módulo.</p>

<h5>Configurações:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>o nome do módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>texto que será visto antes do forumlário ser preenchido;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>situação do módulo (ativado/desativado).</dd>
        </dl>
    </li>
</ol>

