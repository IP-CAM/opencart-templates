<h3>TM Slideshow</h3>

<p>Ten moduł pozwala na wyświetlenie pokazu slajdów Camera na twojej witrynie. </p>

<p>Kiedy moduł jest zainstalowany, możesz konfigurować go na stonie konfiguracji modułu </p>

<h5>Ustawienia:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>nazwa modułu;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Banner</dt>
            <dd>ma być wybrany z listy wcześniej utworzonych <a href='#banner_create'>banerów</a>;
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>szerokość obrazka modułu;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>wysokość obrazka modułu;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>status modułu (włączony/wyłączony).</dd>
        </dl>
    </li>
</ol>

