<h3>TemplateMonster Newsletter Popup</h3>

<p align="justify">Данный модуль добавляет всплывающее окно с предложением подписаться на email рассылку при посещении вашего сайта.
    Окно не выводится для авторизованных (вошедших в аккаунт) или уже подписавшихся на рассылку
    пользователей.</p>

<figure class="img-polaroid">
    <img src="img/pop.jpg" alt=""/>
</figure>

<p>После установки модуля, Вы можете настроить его на странице настроек модуля.</p>
<h4>Как управлять модулем TemplateMonster Newsletter Popup</h4>
<p>Перейдите к разделу <strong>Модули / Расширения</strong> (Extensions) &gt; <strong>Модули / Расширения</strong> (Extensions).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-1.jpg" alt=""/></figure>
</p>
<p>Выберите <strong>Модули</strong> (Modules) в выпадающем списке  фильтра поиска <strong>по типу расширения</strong> (еxtension type).</p>
<p>
  <figure class="img-polaroid"> <img src="img/edit-parallax-2.jpg" alt=""/></figure>
</p>
<p align="justify">В таблице с перечнем всех модулей, найдите раздел <strong> TemplateMonster Newsletter Popup</strong>. Нажав кнопку &quot;редактировать&quot; (edit), вы перейдете к окну настроек существующего модуля. </p>
<p align="justify" class="alert alert-info">Используйте сочетание клавиш <strong>Ctrl+F</strong> для упрощения процесса поиска.</p>
<p>
  <figure class="img-polaroid"> <img src="img/pop-15.jpg" alt=""/></figure>
</p>
<p>Вы увидите следующие окно настроек:</p>
<figure class="img-polaroid"><img src="img/pop-18.jpg" alt=""/></figure>
<h5>Доступные параметры настроек модуля:</h5>
<ol class="marked-list">
  <li><strong>Module Name</strong> — название модуля;</li>
  <li><strong>Background Image</strong> — фоновое изображение всплывающего окна;</li>
  <li><strong>Background image width</strong> — ширина фонового изображения;</li>
  <li><strong> Background image height</strong> — высота фонового изображения; </li>
  <li><strong>Cookie Storage Time</strong> — опция позволяет определить время через которое окно повторно появится;</li>
  <li><strong>Heading Title</strong> — заголовок  отображающийся в окне модуля;</li>
  <li>
    <div align="justify"><strong>Description </strong>— описание отображающееся в окне модуля. Это текст, который поясняет назначение рассылки;</div>
  </li>
  <li><strong>Status </strong>—статус модуля (включен/отключен).</li>
</ol>
