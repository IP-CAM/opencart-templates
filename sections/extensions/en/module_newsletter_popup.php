<h3>TM Newsletter Popup</h3>

<p>This module allows to add pop-up window with an offer to subscribe to the email newsletter when someone is viewing
    your site. The window is not showing up for authorized (logged in) users and those who has already
    subscribed.</p>

<figure class="img-polaroid">
    <img src="img/tm-newsletter-popup-view.jpg" alt=""/>
</figure>

<p>When the module is installed, you can configure it on the module configuration page.</p>

<h5>Settings:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>the name of the module;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Background Image</dt>
            <dd>background image set for the popup;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Background image width</dt>
            <dd>background image width;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Background image height</dt>
            <dd>background image height;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Cookie Storage Time</dt>
            <dd>cookie storage time;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Heading Title</dt>
            <dd>popup heading;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>popup description;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>module status (enabled/disabled).</dd>
        </dl>
    </li>
</ol>


