<p align="justify">
Storeflex  is a multipurpose template with several skins. You can set the skin by yourself. 
In order to install desired skin you should unzip skin's package and copy all the content of <strong>themeXXX-full_update</strong> folder into the fullpackage folder. </p>
<p align="justify" class="alert alert-danger">You able to install new skin over the main theme only.</p>
<p align="justify">Open any browser and enter the OpenCart directory path in the browser address bar adding install at the end (for example http://your_domain_name/install). </p>
<p align="justify">You will see the OpenCart installation page.</p>
<figure class="img-polaroid">
  <img src="img/skin.jpg" alt=""/>   
</figure>

<p>Then  press "<strong>Continue</strong>". </p>


<p align="justify">After the installation process is finished, please navigate to <strong>admin panel &gt; </strong> <strong>Extensions</strong> &gt; <strong>Modifications</strong>.</p>
<figure class="img-polaroid">
  <img src="img/skin-1.jpg" alt=""/>   
</figure>
<p align="justify"> Click the<strong> Refresh </strong>sign at the upper right corner of the page.</p>
<p>
  <figure class="img-polaroid">
    <img src="img/skin-2.jpg" alt=""/>   
  </figure>
  </p>
<p align="justify">Go to the root shop folder and delete old files generated in <strong>image</strong> > <strong>cache </strong>> DELETE ALL EXCEPT INDEX.HTML. </p>
<p>Delete all except INDEX.HTML from the <strong>system</strong> &gt; <strong>storage</strong> &gt; <strong>cache</strong> folder.</p>
<p><strong>Refresh</strong> your website to   see changes.</p>
