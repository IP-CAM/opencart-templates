<h3>Установка модулей</h3>

<p>Установка модуля проходит в несколько шагов. Перейдите в панель администратора
    (добавив /admin в адресной строке - www.yoursite.com/admin). Далее, в боковой панели
    выберите вкладку <strong> Расширения (Extensions)</strong> и в выпадающем списке
    <strong>Модули (Modules)</strong>
    . В открывшемся окне показаны все установленные и готовые к установке модули.
    Если напротив названия
    модуля есть кнопка <strong>Деинсталлировать (Uninstall)</strong> - это значит, что
    модуль уже установлен и, нажав на <strong>Редактировать (Edit)</strong>, Вы можете
    редактировать настройки данного модуля. В противном случае, нажав
    на <strong>Установить (Install)</strong>, Вы произведете установку модуля. Удаление
    модуля происходит похожим образом: необходимо нажать кнопку <strong>Деинсталлировать
        (Uninstall)</strong>,
    после чего модуль будет удален. </p>

<p class="alert alert-warning">Будьте внимательны, так как при удалении модуля будут удалены его настройки, которые
    восстановить будет невозможно.</p>

