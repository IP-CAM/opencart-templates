<h3>TM Lazy load</h3>

<p>Модификация, которая загружает картинки продуктов в момент, когда они попадают в
    область видимости при прокрутке сайта. Данное расширение используется для ускорения
    работы сайта. Для смены картинки прелоадера, замените картинку <strong>lazy_loader.gif</strong>
    в
    папке <strong>catalog/view/theme/themeXXX/image/.</strong></p>