<h3>TM Newsletter Popup</h3>

<p>Das jeweilige Modul fügt das DropDownFenster hinzu, wo es angeboten wird, E-Mail-Newsletters zu abonnieren, sobald
    der Besucher Ihre Website (Bild 36) öffnet. Das Fenster wird autorisierten Benutzern (die eingeloggt sind) oder
    denjenigen, die schon E-Mail-Newsletters abonniert haben, nicht angezeigt.</p>
<figure class="img-polaroid">
    <img src="img/tm-newsletter-popup-view.jpg" alt=""/>
</figure>

<p>Nachdem Sie das Modul installiert haben, können Sie es auf der Konfigurationsseite einstellen.</p>

<h5>Einstellungen:</h5>
<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>der Modulname;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Background Image</dt>
            <dd>das Hintergrundbild in dem DropDownFenster;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Background image width</dt>
            <dd>die Breite des Hintergrundbildes;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Background image height</dt>
            <dd>die höhe des Hintergrundbildes;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Cookie Storage Time</dt>
            <dd>die Speicherzeit des Cookies;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Heading Title</dt>
            <dd>der Titel n dem DroDownFenster;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>die Beschreibung in dem DropDownFenster;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>der Modulstatus (aktviert/deaktiviert).</dd>
        </dl>
    </li>
</ol>

