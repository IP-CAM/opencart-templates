<h3>TemplateMonster Newsletter</h3>

<p>Moduł ten pozwala dodać blok newslettera z formularzem subskrypcji.</p>

<figure class="img-polaroid">
    <img src="img/tm-newsletter-view.jpg" alt=""/>
</figure>

<p>Kiedy moduł został zainstalowany, możesz konfigurować go na stonie konfiguracji
    modułu </p>

<h5>Ustawienia:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>nazwa modułu;</dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Description</dt>
            <dd>tekst, który będzie widoczny przed wypełnieniem formularzu zapisu; </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>status modułu (włączony/wyłączony).</dd>
        </dl>
    </li>
</ol>

