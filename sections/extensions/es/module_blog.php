<h3>Blog</h3>

<p>La siguiente extensión le permite administrar los mensajes y noticias relacionados con su tienda.</p>

<p>Características principales:</p>

<ul class="marked-list">
    <li>la creación de artículos;</li>
    <li>la creación de categorías;</li>
    <li>la creación de autores;</li>
    <li>comentar artículos y respuesta a los comentarios;</li>
    <li>vista del informe;</li>
    <li>mostrando productos y los artículos relacionados con el artículo determinado;</li>
</ul>

<p>Con el fin de cambiar Blog global settings vaya a <strong>System->Settings</strong>. Seleccione la tienda en la que desea cambiar la configuración de la lista y haga clic en <strong>Edit</strong>.</p>

<h6> Ajustes de blog se pueden ver en la sección Blog:</h6>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Estatus</dt>
            <dd> estado del blog (activado\desactivado);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Límite</dt>
            <dd> número de artículos por página;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Descripción Límite de Símbolo</dt>
            <dd> número de símbolos en la descripción del artículo en la página principal Blog (no en un solo artículo!);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Comentario Auto aprobación</dt>
            <dd> activa/desactiva comentario de aprobación automática por el administrador;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Información del Autor</dt>
            <dd> activa/desactiva la información sobre el autor en la página del artículo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Artículo Relacionado</dt>
            <dd> activa/desactiva artículos relacionados;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Compartir con página web social</dt>
            <dd> activa/desactiva botones sociales;</dd>
        </dl>
    </li>
</ol>

