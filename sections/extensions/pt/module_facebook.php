<h3>TM Facebook Box</h3>

<p>Este módulo permite exibir uma caixa de curtidas do Facebook que mostra os fans que curtiram sua marca e seu site.</p>

<figure class="img-polaroid">
    <img src="img/tm-facebook-view.jpg" alt=""/>
</figure>

<p>Após instalado, você pode configurar o módulo na página de configurações dele.</p>

<h5>Configurações:</h5>

<ol class="index-list">
    <li>
        <dl class="inline-term">
            <dt>Module Name</dt>
            <dd>o nome do módulo;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Facebook Page URL</dt>
            <dd>link da página do Facebook;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Color Scheme</dt>
            <dd>esquema de cores (claro, escuro);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Faces</dt>
            <dd>mostrar fotos dos usuários (sim, não);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Stream</dt>
            <dd>mostrar publicações recentes (sim, não);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Header</dt>
            <dd>mostrar título do bloco (sim, não);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Show Border</dt>
            <dd>mostrar borda do bloco (sim, não);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Width</dt>
            <dd>largura do bloco (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Height</dt>
            <dd>altura do bloco (px);</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Status</dt>
            <dd>situação do módulo (ativado/desativado).</dd>
        </dl>
    </li>
</ol>

